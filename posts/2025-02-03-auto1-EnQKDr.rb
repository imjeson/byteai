基尔·斯塔默在担任反对党领袖期间，在被警告可能受到俄罗斯黑客攻击后，停止使用个人电子邮件账户。

---
据《泰晤士报》报道，这一事件发生在2022年，即俄罗斯入侵乌克兰之后不久。据书籍《加油》中提到，斯塔默的团队首次得知该事件是因为他私人办公室的负责人吉尔·卡瑟伯森发送了一封消息，指示其他工作人员不要再向该电子邮件地址发送消息。 

斯塔默办公室的工作人员在国家网络安全中心的警告下，了解到了该账户可能被黑客侵入。警告称，可能已经窃取了敏感信息，尽管没有被公开。《泰晤士报》指出，之前的电子邮件地址“危险明显”，且缺乏双因素身份验证的安全备份，后者要求在尝试从不明设备登录时进行单独身份检查。斯塔默的新账户已解决了这些问题。

在斯塔默安全漏洞的疑似发生时，一些来自前MI6主任理查德·迪尔拉夫爵士及其他人士的电子邮件也在一次显然是俄罗斯黑客群体的攻击中被发布。当时，谷歌表示，这些电子邮件的发布似乎是由它称之为Coldriver的俄罗斯集团造成的。黑客公布了迪尔拉夫及其他人的电子邮件，声称其详细说明了一群支持脱欧的阴谋者的工作。然而，大部分的电子邮件，日期从2018年到2019年，展现了对当时首相特蕾莎·梅愿意向欧盟妥协感到沮丧的脱欧支持者的选择。

