---

Criminal IP，AI SPERA开发的人工智能驱动威胁情报平台，宣布将在2025年欧盟最大的网络安全会议Infosecurity Europe上首次亮相。![图片](https://ai-techpark.com/wp-content/uploads/Criminal-IP.jpg){ width=60% }

---
该活动将于2025年6月3日至5日在伦敦ExCeL举行，Criminal IP将在B131展位与全球安全专业人士进行交流。
Infosecurity Europe是该地区最具声望的网络安全事件，吸引了来自欧洲的超过18,000名安全专业人员和350多家公司。作为一个共享领先网络安全技术和见解的全球平台，该活动汇聚了从业者、企业和政策制定者。Criminal IP将利用其在150多个国家的不断扩大的用户基础，加速进入欧洲市场。
Criminal IP已经与40多家全球网络安全公司建立了合作伙伴关系，包括思科（Cisco）、Fortinet和VirusTotal。在展会上，该平台将重点展示其两项核心解决方案：Criminal IP和Criminal IP ASM。Criminal IP通过高精度的开源情报（OSINT）分析，提供全球域名和IP地址的实时网络威胁情报，帮助安全团队检测和响应诸如零日漏洞和C2基础设施等高级威胁。
Criminal IP ASM，一种攻击面管理解决方案，能够自动发现和监控暴露的数字资产，同时在早期识别潜在风险。通过结合这些解决方案，组织可以有效整合威胁情报和ASM，构建全面的主动安全运营。访问B131展位的访客可以体验现场演示，提前安排一对一会议，并获得现场独家赠品。
在6月5日，Criminal IP还将参与题为“增强安全自动化：在SOAR和SIEM集成中利用威胁情报”的演讲，由AI SPERA首席执行官Byungtak Kang主讲。该环节将探讨将Criminal IP的威胁情报与SOAR和SIEM平台相结合，如何提高检测准确性、加快响应速度并实现安全运营的全面自动化。用例将包括恶意软件检测、内部威胁识别、基于异常的监控，以及外部资产保护、攻击面减少、黑客群体跟踪和威胁波动分析。
AI SPERA首席执行官Byungtak Kang表示：“Infosecurity Europe 2025为Criminal IP展示威胁情报和ASM在实际安全运营中可实现的切实价值提供了重要机会。随着网络威胁的持续演变，我们将继续致力于帮助组织通过持续创新构建主动、自动和集成的防御。”

