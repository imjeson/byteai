新的平台更新解锁了端到端的运营可视性，以加速自动化并消除效率低下
Apromore，一家全球领先的流程挖掘和人工智能驱动的业务流程优化公司，今天宣布其流程智能平台的更新，现在具备完全集成的原生任务挖掘功能。![图片](https://ai-techpark.com/wp-content/uploads/Apromore-Unveils.jpg){ width=60% }

---
通过统一用户任务级和流程级的洞察，Apromore使组织能够简化操作，消除低效率，并自动化复杂的工作流程。
“将任务挖掘集成到Apromore平台中，代表了全方位流程智能的重大进步，” Apromore的联合创始人兼首席产品官Marlon Dumas表示。“通过将员工级数字互动数据与企业级流程关联起来，我们为组织提供了前所未有的洞察力，帮助他们识别和自动化合适的任务，为员工提供更好的工具，并持续改进工作方式。”
全方位流程智能：运营可视性的新的标准
在当今动态的商业环境中，持续改进的起点是可视性。许多组织仍然缺乏对桌面级工作实际执行方式的洞察——低效率、重工和合规差距通常在这里产生。Apromore的集成任务挖掘弥补了这一盲点，通过在单一平台中统一任务及流程数据。通过安全捕获匿名的用户互动，例如点击、键入和应用内活动，Apromore揭示了隐藏的摩擦，量化生产力损失，并揭示改进机会。这些洞察使团队能够迭代优化运营，降低风险，并以可衡量、合规和可持续的方式加速数字化转型。
关键能力

- 受控代理任务挖掘：Apromore数字代理以可配置的间隔捕获用户工作站上定向的屏幕和字段级互动。数据被处理、分阶段、审核并导入到Apromore平台，以揭示用户日常工作，监控绩效和合规，暴露系统日志中通常隐藏的手动任务。
- 隐私优先，安全强化架构：Apromore采用企业级安全性，确保匿名数据捕获，保护个人身份信息（PII）的机密性，获得用户同意，并遵守全球隐私标准，如GDPR。
- 面向行动的洞察和自动化支持：通过应用内指导、KPI监控和一个支持模拟及自动化机会发现的人工智能助手，提供可操作的建议。

Apromore 10.3的新功能
除了现在具备完全集成的原生任务挖掘，Apromore 10.3版本的发布还包括其他更新，如：

- 助手增强：用自然语言提出“如果”问题来模拟流程变更，并获得详细的、由人工智能生成的改进建议。
- 精简仪表板：通过集中式界面访问实时的运营绩效指标和可视化，具备简化的拖放自定义和轻松共享，提高各部门的可访问性和协作性。
- 合规中心增强：基于流程和现在的任务及用户互动数据的自动控制测试，允许更大的监督、审计准备和合规性。

