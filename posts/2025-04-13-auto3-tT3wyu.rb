title: 'Hypertec Cloud收购5C数据中心，成立5C集团'
date: 2025-04-14
author: ByteAILab

---

在一项反映人工智能与数据中心基础设施快速融合的里程碑式举动中，领先的AI云平台Hypertec Cloud收购了5C数据中心，这是一家在美国拥有超过2吉瓦（GW）容量路线图的顶级共置和数据中心提供商。![图片](https://ai-techpark.com/wp-content/uploads/5C-Group.jpg){ width=60% }

---
此项战略收购标志着Hypertec Cloud从母公司Hypertec Group的剥离，以及5C集团的成立——一个专注于大规模构建下一代AI基础设施的新独立实体。

乔纳森·阿胡特（Jonathan Ahdoot）被任命为新成立的5C集团的CEO，该集团将Hypertec Cloud的高性能云计算能力与5C数据中心在美国的广泛覆盖相结合。两者共同组成了北美最大的、最灵活的AI数字基础设施提供商之一，旨在构建将推动行业未来的下一代AI工厂。

这项收购受到三个关键战略目标的推动：
1. 提供全面的AI数字基础设施解决方案：通过结合Hypertec Cloud与5C数据中心，5C集团将提供集成的数据中心和AI基础设施，旨在满足AI前沿实验室、AI原生企业和大规模企业客户的需求。
2. 在受限市场中加速部署速度：随着AI部署需求的增长，而大规模数据中心容量依然有限，联合平台将使超大AI集群的市场投放速度加快。集成的计算与数据中心设计将显著缩短部署时间，并实现基础设施的同步部署。
3. 促进性能与成本优化：借助Hypertec在硬件和基础设施设计、多种服务支持及可持续工程方面的多年经验，新实体将采用先进冷却技术——如直接芯片液冷和浸没冷却，以支持更高的机架密度和节能。

乔纳森·阿胡特（Jonathan Ahdoot）表示：“当前AI基础设施面临的最大挑战是对齐——在AI计算需求与物理托管环境之间。我们通过将两者结合在一起解决了这一问题。”他补充道：“通过这项收购，我们结合了对性能优化的AI计算基础设施的深厚专业知识，以及对大型世界级数据中心校园的设计和运营。这样的组合使我们能够加速部署，提高密度，并为未来数年内最大、最苛刻的AI用户提供无与伦比的效率。”

Hypertec集团的首席执行官西蒙·阿胡特（Simon Ahdoot）表示：“Hypertec Cloud的剥离标志着Hypertec集团的一个重要时刻。”他还指出：“通过收购5C数据中心，新成立的5C集团有望提供为明天行业的规模、速度和复杂性设计的AI数字基础设施。作为合作伙伴，我们将继续推动计算、存储和数据中心技术的发展，为AI创新的未来提供真正的价值。”

5C集团在美国的AI优化数据中心校园的投资组合包括超过600兆瓦（MW）的容量——这是其总计2吉瓦（GW）容量的一部分——将在未来6到18个月内为新老AI数据中心共置和计算客户提供部署。这一平台是专门针对AI工作负载的极端密度、电力和冷却需求而设计，为未来AI创新奠定了基础。

