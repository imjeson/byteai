北京时间9月10日凌晨，苹果正式发布了iPhone 16，这是苹果第一款真正意义上的 AI iPhone。Apple Intelligence采用“端侧大模型+云端大模型”的方式，将为用户带来更丰富的智能体验。

---
而这仅仅是端侧智能的开始，未来我们可以想象，一个由大模型带来的移动智能生态正在缓缓打开。

![图片1](http://www.jesonc.com/FhQqtP98sQr9_1DdaNIRA_Ve8kx9)

**太长不看版：**

**1.技术：端侧模型短期能力有限，端云结合是长期状态**
- 手机大模型的瓶颈排序：
  - 电池容量和发热
  - 芯片计算速度
  - 现有架构下，8G+内存是最低要求
  - 内存读写速度可能需要进一步技术突破
- ~1B量级模型能力有限，性能提升空间不乐观
- 手机端侧模型有实际价值 -&gt; ~10B模型塞到手机里 -&gt; 估计3~4年
- 云+端混合将是长期主流
  - 端侧模型 + 云上模型 的配合能力将是核心技术点之一
  - 从用户价值看，端侧模型并不是必要路径
  - 端侧模型存在合理性是1) 降低推理成本，2) 响应速度更快  3）更好保护隐私

