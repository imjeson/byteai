对不起，ZuckBot：你已经失败了'
date: 2025-05-07
author: ByteAILab

---

在过去的几年里，马克·扎克伯格一直在进行一项非常昂贵的实验。如果他留长头发，更新衣橱，是否能让他看起来更具亲和力？如果他开始练习混合武术，去猎野猪，并告诉像乔·罗根这样的男子气概相关播客主持人，企业需要更多的“男性能量”，那些血气方刚的美国男性会不会对他心存敬意？借助一小队造型师、私人教练和公关顾问，Zuck是否能够将自己从一个不讨喜的书呆子转变为一个“alpha bro”？

在短暂的时间里，这一切似乎有了肯定的答案。扎克的新发型让这位亿万富翁看起来少了几分像是三块乐高人仔穿着外套的感觉，更像是一个成年男性。他的金链子和时尚新衣激起了“Zuck复兴”的兴奋讨论。2023年，当全世界最自我意识缺乏的人——埃隆·马斯克——向他发起了笼斗挑战，这位Meta亿万富翁也迎来了公关方面的幸运。随之而来的则是大批报道，讲述扎克其实是个技术高超的运动员，会在战斗中轻松击败马斯克，使约950个孩子失去了父亲。

这一切的意思是：你尽力了，马克！你努力想要让我们相信你是一个超级随和的兄弟，而不是一个邪恶的科技霸主。可我想现在可以结束这个实验了。无论ZuckBot进行多少系统更新，他似乎都无法逃避“令人毛骨悚然的谷底”。他身上总有一种“令人不安的自动机器人”的气息。

扎克对此心知肚明，而这让他感到难过。这位亿万富翁最近在Theo Von的播客上发言（Von是来自路易斯安那州的极受欢迎的喜剧演员和播客主持人，他在选举前采访过唐纳德·特朗普，特朗普在胜利演讲中提到过他），他感叹道：“人们已经在网上称我为机器人20年了。”如果扎克希望得到安慰，那他并没有得到。“有时候，”Von沉思道，“是的，你看起来就像一个可能在YouTube上观看如何做人的视频的人。”而这样的时刻，几乎是时时存在。

扎克在Von播客上的表现确实没有进一步让他显得人性化。他在其中一段谈话中（这位开着凯迪拉克的亿万富翁）沉思：“你希望你的车的马力能够和你的直升机一样多。”谈话还涉及到扎克在夏威夷价值2.7亿美元的末日避难所，他将其描述为“更像是一个地下储存设施，而非掩体”。那么他到底在储存什么呢？不是Nespresso胶囊。他告诉Von，他一般“只在假期时休闲饮用咖啡”，而且通常更喜欢“直接体验现实”。

当扎克忙于“直接体验现实”时，他似乎希望我们其他人生活在一个他控制并从中获利的反乌托邦数字气泡中。在最近的另一个播客采访中，扎克伯格表示，“普通美国人”的朋友少于三个，但却“渴望有更多的朋友”。他声称，Meta将满足这一需求，并解决孤独危机，推出AI聊天机器人。实际上，根据《华尔街日报》的最新调查，扎克伯格热切推出这些数字伙伴，以至于机器人与未成年用户进行性讨论的保护措施极少。Meta表示他们已经解决了这个问题，但这又是扎克伯格“快速行动，打破现象”这一长期行为的又一例证。

不过，存在一线希望的是，Meta也许会找到自己被解散的下场。扎克伯格亲吻总统的膝盖（他称赞总统是一个“坏蛋”），并最近在华盛顿特区购买了一座2300万美元的丑陋豪宅，以便他更方便地游说特朗普。Facebook实施了对共和党有利的政策，而扎克伯格的家庭盈利慈善机构“陈·扎克伯格倡议”也在努力剔除所有多样性、平等与包容（DEI）的努力，以迎合特朗普。

尽管如此，这位亿万富翁仍未能逃避一场生死攸关的反垄断审判。美国联邦贸易委员会辩称，Meta通过收购Instagram和WhatsApp建立了非法垄断，旨在扭转这些交易——这将对Meta的利润造成巨大打击。

然而，似乎坏人不再倒霉了，因此我并不指望扎克伯格会有任何堕落。更有可能的情况是，扎克说服特朗普停止此案，然后他在地下掩体中举杯庆祝，与五个最亲密的AI机器人朋友一起享用一杯特别的咖啡，庆祝他的反派角色圆满完成。阿尔瓦·马哈达维是《卫报》的专栏作家。

