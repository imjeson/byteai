---

公司扩大业务足迹，标志着对创新、人才和客户成功的战略投资  
GuidePoint Security，作为一家网络安全解决方案领导者，帮助组织做出更明智的决策并降低风险，今天宣布在弗吉尼亚州雷斯顿的1900 Reston Metro Plaza开设新的全球总部。![图片](https://ai-techpark.com/wp-content/uploads/GuidePoint-1.jpg){ width=60% }

---
这个新地点标志着公司发展的一个关键里程碑和持续扩张，代表着今年第三次办公室开设，并强调了GuidePoint在快速增长、扩展服务组合和深化网络安全市场领导地位方面的迅速发展。  
新总部在北弗吉尼亚技术走廊的雷斯顿车站中占据战略位置，旨在支持公司下一个增长阶段。这一现代化、灵活的空间设计旨在促进协作、加强客户参与并促进社区联系。周围是行业领先的企业邻居，该地点提供便捷的优质餐饮、酒店和交通便利设施，并与关键联邦机构、技术合作伙伴及企业客户紧密相邻。  
“这个总部不仅仅是一个新地址——它是GuidePoint Security下一个篇章的发射台，”GuidePoint Security首席执行官兼董事长迈克尔·沃尔克表示。“我们的势头直接反映了客户对我们的信任、团队的专业知识以及我们坚定不移的决心，帮助组织提升网络安全水平。我们希望打造一个体现我们文化、赋能团队并为我们在公共和私人部门的长期成功打下基础的空间。”  
雷斯顿设施的开设是在公司实现创纪录增长的背景下。过去一年中，GuidePoint Security取得了以下成就：  

- 实现双位数的收入增长  
- 在俄亥俄州诺伍德和德克萨斯州弗里斯科新增两个办公室  
- 在云安全、身份和访问管理、人工智能/机器学习风险等方面推出新服务，并扩大其“按需服务”产品组合  
- 员工人数超过1,000人  
- 在销售、交付和运营等领域通过战略招聘增强领导团队  

“作为一家根植于北弗吉尼亚的公司，我们很自豪能够在这个地区加深我们的根基。”沃尔克补充道。“雷斯顿的人才丰富、创新驱动的环境为未来奠定了完美的基础。”  
GuidePoint Security成立于2011年，旨在解决企业和政府组织面临的广泛复杂网络安全挑战。在一支经过严格审查的高认证安全专家团队的支持下，公司结合深厚的行业专业知识，评估风险、优化资源并推荐量身定制的安全解决方案。自成立以来，GuidePoint Security已成为4,200多家组织的可信顾问，其中包括40%的《财富》500强企业和超过一半的美国内阁级机构。  
新的雷斯顿办公室将容纳不断增长的员工队伍，并支持在产品创新、服务卓越和社区支持方面的持续投资。有关更多信息，请访问guidepointsecurity.com。

