大宇制药（共同首席执行官 朴成洙和李昌宰）和大宇疗法（首席执行官 姜博基）宣布，其专有的替格列肽（semaglutide）微针贴片在一项人类药代动力学初步研究中，实现了与注射制剂相比的生物利用度超过80%。![图片](https://ai-techpark.com/wp-content/uploads/Daewoong.jpg){ width=60% }

---
该研究是基于健康志愿者使用大宇疗法的CLOPAM®（闭合包装气压微针）药物输送平台的替格列肽微针贴片。这项研究直接比较了替格列肽皮下注射与微针贴片的给药效率。

在该试验中，70名健康成人接受了一剂替格列肽微针贴片或皮下注射。随后测量了血浆浓度，并与在相同条件下的替格列肽皮下注射结果进行了比较。在调整到等效剂量后，微针贴片显示出与注射相比生物利用度超过80%。这一结果是已知的同种活性成分的微针贴片中最高的，远超目前微针贴片的约30%的生物利用度。此外，与替格列肽口服片相比，微针贴片的生物利用度高出约160倍。

该贴片能够维持一周的治疗血浆水平，支持了单个高负荷贴片每周给药一次的潜力。其暴露曲线与注射治疗等效，加强了基于贴片的肥胖治疗的商业前景。

### 技术是肥胖市场的决定性因素
根据Grand View Research的数据显示，2024年全球肥胖治疗市场的价值为159亿美元，预计到2030年将增长至605亿美元。目前，GLP-1药物类的治疗和安全优势有限，专家们预计，提升生物利用度和患者便利性的给药技术将成为未来市场领导者的决定性因素。微针贴片因其解决了当前疗法中的两个关键缺陷而备受关注：注射的疼痛感和口服制剂的低给药效率。

尽管替格列肽口服片已在市场上，但其生物利用度极低，并需要复杂的用药方案，例如在用药前禁食、限制水的摄入以及避免与其他药物共同使用，这可能降低慢性病患者的依从性。而每周一次的微针贴片提供了一种更简单、无针的选择，可以被更广泛的患者群体使用，包括那些避免注射的人群。

### 大宇微针技术创新引领全球市场
这一结果得益于大宇疗法的专利CLOPAM®微针平台技术，这是一种设计用于精确、皮肤基础药物输送的溶解型微针技术。该平台利用加压干燥和密封包装来提高药物的均匀性和稳定性，从而降低污染风险并实现准确给药。CLOPAM®目前在全球拥有52项专利申请，并正在成为大宇的领先技术之一。

与其他需要冷链存储的替格列肽注射产品不同，大宇的贴片在室温下保持稳定，这可以减少分销成本。此外，它们还帮助减少来自注射器和针头的医疗垃圾，与医疗保健的可持续发展目标相一致。

基于这些结果，大宇疗法计划通过技术授权、共同开发和外部授权来拓展与全球市场的合作，以准备商业化。“微针贴片不仅要达到高生物利用度，还要具备足够的药物负载以产生治疗效果。”大宇疗法首席执行官姜博基表示。“在这项研究中，我们将高剂量的替格列肽融入单个贴片，展示了一次周应用的潜力。这克服了以往微针方法的关键限制，为全球技术转让和商业化奠定了良好的基础。”

“微针制剂长期以来承诺提供更好的依从性，但高剂量的输送一直是一个挑战。”大宇制药首席执行官朴成洙表示。“这项工作首次克服了这一障碍。我们计划将微针技术扩展至多种生物制剂，领导全球平台市场。”

