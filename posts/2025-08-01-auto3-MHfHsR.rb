GFT最新创新——AI构建AI——使金融公司在几天内而非几周内启动技术系统
全球数字转型公司GFT近日宣布，正在推动美国和加拿大金融机构的云计算和AI数字转型项目，推出了其全新的AI驱动软件开发产品Wynxx。![图片](https://ai-techpark.com/wp-content/uploads/GFTs-Wynxx.jpg){ width=60% }

---
随着金融机构现代化其遗留系统，包括向云迁移和部署全组织的新AI用例，他们正在寻找在降低成本的同时保持质量的途径。GFT的Wynxx使这一目标得以实现，将云和AI能力的上线时间缩短了多达95%。这一专有AI工具已经使一家全球顶级投资银行将一个云部署开发任务的时间从一个多月缩短至仅一天。

金融机构希望利用AI驱动的效率，但不到40%的机构甚至还没有迈出第一步：全面进入云环境。这主要是由于对大多数金融机构而言，数字转型项目，例如向云迁移，都是一项复杂的工作，需消耗大量开发资源。因此，这些金融机构在寻求利用AI的力量时面临挑战——AI通常依赖于集中的云生态系统——以通过降低运营成本和提升客户体验来获得竞争优势。

在这种背景下，GFT将Wynxx带到美国和加拿大，帮助金融机构减少大型数字转型项目所需的开发者时间和资源。该新产品使金融机构能够显著提高关键数字转型项目的效率，包括：

从分散的遗留系统迁移到全面的云环境。GFT与领先的云服务提供商（包括AWS和Google）合作，为每个金融机构提供符合其独特需求的云环境。由于云部署通常需要数月甚至数年的时间完成，机构通常选择缓慢而渐进地迁移到云。而现在，通过Wynxx，GFT能够更快地提供其以20年声誉而闻名的领先云转型服务，从而使组织能够花更多时间创新利用云基础设施的新AI解决方案。

跟上最新的自动化进展。近年来，大多数金融机构在工作流程中已使用某种形式的自动化技术——例如防欺诈、反洗钱等，但这些技术会随着时间的推移而不断改进。更新较旧的自动化工具却面临挑战，尤其是在代码文档出现缺口（解释代码如何工作的文档）时。借助Wynxx，GFT现在可以将文档所需的时间减少超过90%，同时确保最高质量，以减少新更新的复杂性。

将AI注入手动、容易出错的任务，以扩大产品和增加收入。近年来，私人信贷等金融产品的需求迅速增长。但大多数公司无法在不影响准确性和随后收入的情况下，按需扩展其手动信用风险分析。同时，构建一个具有公司所有内部风险控制和参数的专有AI信用分析系统，通常因所需的开发者时间而被排除在外。现在，Wynxx使GFT能够快速构建与金融公司现有系统交互的定制软件解决方案，同时考虑其内部风险偏好和控制。

“在过去的35年以上时间里，GFT建立并维护了为金融服务提供行业所需的有效、定制配置技术的声誉，以便于他们发展业务、提供增强的客户体验并简化运营，”GFT北美、英国和亚太地区区域首席执行官克里斯托弗·奥尔蒂斯表示。“现在，通过Wynxx，我们能够在更短的时间内提供客户期望的同样优质服务。我们期待与客户携手解决他们最紧迫的业务挑战，现有的时间表是之前无法实现的，以促进创新的新途径。”

Wynxx的推出是GFT计划在2025年前成为一家完全以AI为中心公司的最新一步，在该计划中，AI不仅是公司内部运营的核心，也是为客户提供的服务的核心。
