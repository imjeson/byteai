Scamnetic是一家致力于使用高级AI技术保护个人和企业免受数字威胁的公司，今天宣布获得1300万美元的A轮融资。Roo Capital领投，此轮融资还得到了1st and Main Growth Partners、SaaS Ventures和Riptide Ventures的参与。A轮融资使Scamnetic的总投资达到1600万美元。    
Scamnetic将利用这笔资金支持持续增长，增加市场营销、销售和客户支持功能，并加快其产品规划，以响应全球日益增长的需求。Scamnetic的客户将受益于增强的核心功能、更强大的全球覆盖以及客户生命周期中更深入的支持。    
“我们一直相信，对消费者最大的威胁不仅仅是欺诈——而是信任的侵蚀。这笔融资使我们能够加快我们的使命：在一个日益受到诈骗困扰的世界中恢复信心。我们感谢我们的合作伙伴继续与我们合作，共同应对这一关键斗争，因为诈骗已成为全球报告最多的犯罪行为——这突显了未来巨大的市场机会。” Scamnetic首席运营官John Evans说道。    
“诈骗是当今消费者和企业所面临的最大问题之一。” Roo Capital的合伙人Spencer Cecola表示。“随着AI的崛起，诈骗变得更加复杂，问题以惊人的速度蔓延。Scamnetic正在应对这一紧迫挑战，推出了一个真正有效的产品。世界需要用AI来对抗AI，我们很高兴通过资本和实际支持来帮助他们快速扩张，并保持领先。”    
Scamnetic提供市场上唯一的整体诈骗检测解决方案，打击所有当前的诈骗。Scamnetic包括：    

- **Scan&ScoreTM**：自动扫描电子邮件、短信、社交媒体直信、网站、Whatsapp、Telegram等消息应用，以及二维码、实体邮件和图像，以了解来自任何传入通信的风险。    
- **IDeveryoneTM**：使用先进但易于使用的技术验证任何对方的身份，使用户在进行支付或发送敏感信息之前，确切知道与谁进行互动。这应对约会诈骗、Facebook营销诈骗、加密货币诈骗、土地投资诈骗等。    
- **Scam InterventionTM**：Scamnetic设有专家热线，全力以赴为诈骗受害者恢复资产和内心的平静。Scamnetic提供24/7支持，帮助可能正在经历诈骗的消费者，引导受害者走向恢复，帮助他们重新控制自己的数字生活。    
- **Scam EducationTM**：提供最新的诈骗保护技巧和资源。    

除了诈骗受害者所经历的痛苦，诈骗的经济成本也是惊人的。例如：    

- 根据美国联邦贸易委员会，2024年美国消费者报告因诈骗损失超过125亿美元，比2023年的100亿美元增长了25%。    
- 根据美国联邦贸易委员会，2024年美国消费者报告因以短信开头的诈骗损失达4.7亿美元。    
- 根据纳斯达克2024年全球金融犯罪报告，2023年，诈骗案件和银行诈骗计划的全球预测损失达4856亿美元。    
- 根据联邦调查局2023年互联网犯罪报告，2023年美国消费者诈骗损失同比增长22%。    
- 根据全球反诈骗联盟2024年全球诈骗现状报告，国际诈骗者在12个月内盗取了超过1万亿美元。    

