title: 'Scope Technologies 任命 Ted Carefoot 为首席执行官'
date: 2025-06-08
author: ByteAILab

---

Scope Technologies Corp. (CSE: SCPE) (OTCQB: SCPCF) (FSE: VN8) (“Scope Technologies” 或 “公司”) 今日宣布任命 Ted Carefoot 为首席执行官，接替 James Young，后者将继续以顾问身份支持公司。![图片](https://ai-techpark.com/wp-content/uploads/Scope-Tec.jpg){ width=60% }

---
 
Carefoot 是一位经验丰富的科技领袖，在软件开发、网络安全、人工智能和风险管理领域拥有超过二十年的经验，他最近担任 Scope 的产品副总裁。他的职业生涯包括在艺电和迪士尼在线工作室加拿大全国担任领导职务，拥有开发大规模安全解决方案和监管框架的深厚专长。Carefoot 还拥有治理、风险与合规（GRC）和综合数据隐私的认证，并因其在风险管理框架（RMF）实施方面的专业知识而受到认可——这是 Scope 在扩展其后量子安全产品时的关键能力。

“随着 Scope Technologies 进入下一个增长阶段，我们致力于在量子安全创新和合规方面保持领先，" James Young 说道。“Ted 的领导力、行业经验和在风险管理及监管标准方面的深厚专业知识使他成为引领 Scope 走入未来的理想人选。我对他能够扩展公司和实现我们的使命充满信心。”

在 Carefoot 的领导下，Scope Technologies 将继续专注于为企业和政府客户提供量子抗性加密与安全解决方案，帮助组织应对日益增长的后量子密码学（PQC）标准监管要求。

“在这个关键时刻，我很荣幸能够担任这个角色，”Ted Carefoot 说道。“随着量子计算威胁成为现实，企业和政府必须立即采取行动来保护他们的数据。我期待领导 Scope 的优秀团队，帮助组织使他们的安全基础设施抵御这些新兴威胁。”

Scope Technologies 还宣布 Sean Prescott 已辞去公司董事职务。公司感谢他作为董事所做的贡献。Prescott 将继续担任 Scope Technologies 的首席技术官。

限制性股票单位和股票期权的授予
Scope Technologies 还授予了总计 950,000 股的股票期权，行使价格为每股 $0.385，有效期至授予之日起五年。这些股票期权需遵循董事会设定的归属要求。此外，Scope Technologies 向 Carefoot 先生授予了 1,500,000 股限制性股票单位（“RSU”）。这些RSU 的归属如下：250,000 股 RSU 在授予日起一年内归属，500,000 股 RSU 在授予日起两年内归属，750,000 股 RSU 在授予日起三年内归属。

