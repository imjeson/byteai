title: '苹果因英国政府要求移除高级数据保护工具'
date: 2025-02-22
author: ByteAILab

---

苹果采取了前所未有的步骤，在英国客户中移除了其最强大的数据安全工具，因为政府要求获取用户数据的“后门”访问权限。

---
英国用户将无法再使用这一高级数据保护（ADP）工具，该工具使用端到端加密，仅允许帐户持有者查看他们存储在iCloud云存储服务中的照片或文档等项目。苹果表示对不能再为英国客户提供这一安全功能“深感失望”，因为英国政府要求有权查看用户的加密数据，而目前即便是苹果也无法访问这些数据。

在周五下午3点，新的用户将无法访问ADP工具，而现有用户将在稍后需要禁用该安全功能。消息服务如iMessage和FaceTime将继续默认保持端到端加密。苹果表示：“我们对ADP所提供的保护在英国不再可用感到十分失望，因为数据泄露和其他客户隐私威胁的持续上升。增强云存储的安全性和端到端加密比以往任何时候都更为紧迫。苹果始终致力于为我们的用户提供最高级别的个人数据安全，我们希望未来能够在英国做到这一点。正如我们多次所言，我们从未为任何产品或服务构建过后门或主密钥，我们永远也不会。”

萨塞克斯大学的艾伦·伍德沃德（Alan Woodward）表示，苹果此举是“一项相当非凡的发展”。这位网络安全教授说：“英国政府认为他们可以告诉苹果该怎么做，非常幼稚。”他指出，苹果发出的信息是“你不能为了敌人削弱加密，而不削弱朋友的加密”，而政府所能达到的结果是使其应用在英国用户中变得不那么安全，却对情报行动没有任何好处。

一位网络安全专家彼得·索默（Peter Sommer）表示，技术专家在过去30年里尝试开发“万无一失的后门”却未果。“英国内政部应该集中精力寻找有针对性的，而不是大批量的加密违规措施”，因为这可以确保“获得的令状是合理的，并让无辜者保留他们的隐私，”他说。

内政部发言人表示：“我们不对操作事务发表评论，包括确认或否定任何此类通知的存在。”

