<p class="ne-empty-p"><br/></p>

<p>税收和收费管理领域，可通过生成式AI开发一个虚拟助手，可自动分析纳税申报，并通过比对历史数据、检测异常模式来识别可疑活动。一旦AI检测到可能的欺诈行为，可自动标记案件供人工复核。</p>

<p class="ne-empty-p"><br/></p>

<p>也可以将生成式AI集成在客服中，开发一个AI虚拟税务顾问，为民众提供7*24的自助咨询服务，可解答有关税率、抵扣、退税和合规性等问题，并根据最新的税法变动进行更新。</p>

<p class="ne-empty-p"><br/></p>

<p><strong>加州税务和收费机构主管Nick Maduros表示</strong>，2023年我们机构管理着 40 多个项目，并接到了超过66万个企业电话。希望通过部署生成式AI来监听这些电话，并实时提取州税法的关键信息，让工作人员能够更快地回答问题，而不必自己查找各种信息。</p>

