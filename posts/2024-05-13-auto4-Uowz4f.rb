<p class="ne-empty-p"><br/></p>

<p>早在2023年9月6日，加州州长Gavin Newsom便下达行政令，要求各州政务机构研究生成式AI的潜在威胁以及适合的应用场景。</p>

<p class="ne-empty-p"><br/></p>

<p>Gavin认为，<strong>生成式AI的出现可以与互联网相媲美，这是一种可极大提升工作效率的变革性工具</strong>。但我们要深度了解它的各种潜在安全问题，才能进行实际业务应用。</p>

<p class="ne-empty-p"><br/></p>

<p>随着ChatGPT等生成式AI工具在安全方面的防护栏越来越多，加州终于决定在4个政务繁重的部门开始测试。</p>

<p class="ne-empty-p"><br/></p>

<p><strong>以交通运输部门为例：</strong>交通运输部门可以使用集成了生成式AI（例如，GPT-4V）的监控系统，可实时分析交通数据，通过摄像头监控交通流量和行为模式，识别潜在的危险行为或交通拥堵情况。当AI发现潜在的风险时，可以立即通知附近的司机减轻交通压力。</p>

<p class="ne-empty-p"><br/></p>

<p>还可以通过生成式AI技术，对公交车、火车等公共交通工具的运行状态进行实时跟踪和调度。系统可以通过预测乘客流量和车辆位置来优化运行计划和服务安排。</p>

<p class="ne-empty-p"><br/></p>

