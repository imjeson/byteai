<p class="ne-empty-p"><br/></p>

<p><strong>加州交通主管Toks Omishakin表示</strong>，平均每天有12名加州人死在公路上，我们需要利用一切可用的工具来结束公路安全隐患，到2050年实现交通事故零死亡和零重伤的目标。</p>

<p class="ne-empty-p"><br/></p>

<p>用生成式AI监管交通部门的安全性是非常重要的一步。Gavin Newsom州长这一决定对改善所有加州人的生活起到了关键的推动作用。</p>

