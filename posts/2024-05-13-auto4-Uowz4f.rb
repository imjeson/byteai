<p class="ne-empty-p"><br/></p>

<p>5月11日，美联社消息，美国加州政府正在测试ChatGPT等生成式AI，应用在税收和收费管理部、交通部、公共卫生部以及卫生与公众服务部4大部门。</p>

<p class="ne-empty-p"><br/></p>

<p>测试时间6个月，<strong>为其提供技术支持的一共有5家公司，分别是OpenAI、Anthropic、谷歌、Meta和ServiceNow</strong>，测试环境将在多家云服务平台中进行，费用只需每家支付1美元。

---
</p>

<p class="ne-empty-p"><br/></p>

<p>测试期间，州政府会根据实际业务的应用情况，要求对生成式AI工具进行调整，例如，特定业务领域的数据微调等，以帮助政务实现更好的工作效率和节省时间。</p>

<p class="ne-empty-p"><br/></p>

