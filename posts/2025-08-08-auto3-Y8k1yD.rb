Curve Dental®，领先的全功能云端牙科实践管理软件提供商，今天宣布发布 Curve FLO AI，这是一个深度嵌入的人工智能能力套件，旨在提升生产力并恢复牙科的“人性化”一面。![图片](https://ai-techpark.com/wp-content/uploads/Curve.jpg){ width=60% }

---
FLO AI 原生集成于 Curve的平台中，无需依赖脱节的第三方 AI 工具，能够在护理最重要的时刻，即患者护理时，提供智能支持。

这一里程碑巩固了 Curve Dental 在 AI 驱动的牙科创新前沿的地位，为牙科团队提供了一个完全统一的平台，帮助他们更快工作，更智能记录，并更深切关注患者。

“通过 Curve FLO AI，智能被嵌入到牙科实践管理工作流程的每一个步骤中，这样患者护理才能始终处于中心位置，”Curve Dental 总裁 Jana Macon 表示。“我们的做法不是将 AI 层叠在上面，而是把它建立在 Curve 的 DNA 中，因为真正以人为本的护理始于无缝的技术。”

AI 按你工作的方式运作
Curve FLO AI 针对单一地点和多地点诊所的牙医、卫生员和办公室团队设计，将实用智能引入牙科工作流程的每个部分——减少繁琐的工作，简化临床和行政任务，帮助团队专注于患者。

FLO AI 是 Curve AI 路线图上的最新产品里程碑，强大的工具通过平台原生交付，包括：

- Eligibility+: 自动化保险验证，消除手动工作，通过实时数据直接同步到工作流程中。实践将每位患者的验证时间从 15-30 分钟缩短至仅几秒，并提高了同日治疗的接受率，提升幅度高达 150%。
- Ask CurveAI: 无需登记支持工单即可立即获取软件问题的答案。Ask CurveAI 是一个 24/7 的“个人 Curve 专家”，提供入职、培训和日常指导。

Curve FLO AI 还支持多个集成功能，以提升效率，例如：

- 由 Mango Voice 提供的 AI 通话摘要：自动记录患者通话并将其链接到记录中，以便更快的日程安排和跟进。
- 由 Bola AI 提供的语音笔记：用自然语言口述临床笔记并实时转换为 SOAP 格式的记录。
- 由 Pearl 提供的 AI 牙科 X 射线诊断：获取针对影像的二次校对，AI 可标记放射学发现以供审核。

这些创新共同反映出一个清晰的愿景：构建一个智能生态系统，减少摩擦，增强临床关注，提高患者连接。

即将推出：直观的 AI 牙科助手
作为 Curve Dental 每年 2000 万美元投资其解决方案的一部分，公司正在积极开发 AI 牙科助手，以支持临床和前台团队。这个强大的新功能将基于 Curve 现有的 AI 工具，提供更深入的支持，带来更无缝的患者体验。

开发的重点是能够支持牙科实践整个患者旅程的功能，包括：

- 适用于无手操作的环境监听，实现无缝记录。
- 自动化的临床摘要和文档。
- AI 辅助的图表管理与治疗计划。

这些功能将标志着 Curve 长期 AI 战略的下一重大步骤，旨在改善结果并转变牙科团队的运营方式。

“我们今年彻底重新规划了产品路线图，以优先考虑正在彻底改造现代牙医及其团队实践的 AI 开发，”Macon 补充说。“要想‘更聪明，而非更辛苦’。”

一个平台，多个更智能的时刻
除了助手之外，Curve 更广泛的 AI 愿景还包括旨在优化收入表现和减少行政复杂性的未来功能——这些领域 Curve 的深厚数据优势已经在为基础打下基础。

“这是我们在 Curve 的一大优势——我们拥有多年的数据。我们正在训练我们的模型处理其他所有诸如索赔、拒绝和减缓支付的一些细微差别，以便牙科团队可以少花时间进行故障排除，更多时间提供护理，”Macon 说。“我们专注于使牙医及其团队的生活更轻松的 AI，帮助他们提升业务和患者护理。”

通过 FLO AI，Curve 并不仅仅是在添加自动化；而是创造了一个更智能、更适应的系统，扎根于真实的实践数据。与依赖外部插件或 AI 附加组件的平台不同，Curve 利用其一体化模型的力量，训练出不断学习和改进的 AI。结果是：更快的索赔、更少的错误和更健康的底线。

