---
Axenya是一家专注于企业医疗智能协调的健康科技公司，宣布完成1200万美元（约合6500万巴西雷亚尔）的A轮融资，由Canary主导，Indicator Capital共同领导，Zentynel参与。此次投资将推动公司的商业扩展和技术平台的提升，重点在人工智能、数据互操作性和连接生物标志物方面。

与传统的健康保险代理商仅仅中介健康计划不同，Axenya采取了整合的方法：通过先进的技术基础设施和数据智能，使公司、员工、保险公司和医疗专业人员相互连接。其解决方案建立在两个主要支柱上：LifeVault，一个组织临床、运营和行政信息的互操作性数据湖；以及Axenya IQ，一个由人工智能和生成性人工智能驱动的预测引擎，能够预测风险、建议干预措施并优化护理流程。

Axenya创始人兼首席执行官Mariano Garcia-Valiño指出，公司真正的差异化在于前沿技术与能够根本性转变系统的商业模式的结合。“在医疗保健领域，拥有一个出色的产品还不够——你需要一个能够重新塑造系统的商业模式。这就是Axenya的独特之处。”

客户报告的结果包括：医疗通胀低于市场平均水平的50%，损失率低于全国平均水平1500个基点，整体医疗费用年比年减少22%，高风险患者的费用减少40%，每个程序或咨询的成本降低50%——即使在同一时期，预约数量增加13%。

Canary的普通合伙人Kristian Huber强调，Axenya交付可测量影响的能力是投资的关键：“Axenya以其创新的方法和经过验证的成果给我们留下了深刻印象，既降低了公司及其员工的年度健康计划成本急剧增加，同时改善了危重病患者的护理。”

拉丁美洲领先深度科技基金的联合创始人Thomas Bittar指出：“我们相信Axenya正在构建所需的技术基础设施，以改变巴西的企业健康管理。Indicator的角色是通过整合数据、设备和预测智能来加速这一转变——这些是重塑人口健康在该国最具挑战性和服务不足市场的关键要素。”

Axenya成立于2020年，之前在早期轮次中已筹集680万美元，参与者包括Patria High Growth、Big_Bets、Zentynel等。这笔新的融资主要将用于商业扩展、巩固增长渠道和推进技术路线图。

