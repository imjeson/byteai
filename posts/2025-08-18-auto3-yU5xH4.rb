分级服务提供灵活的网络应用和API保护，帮助组织整合、简化和扩展安全性
LevelBlue，一家领先的托管安全服务、战略咨询和威胁情报提供商，今天宣布推出其托管的网络应用和API保护（WAAP）安全服务，旨在提供自适应、始终在线的保护，从而减少网络应用和API安全的风险和运营开销。![图片](https://ai-techpark.com/wp-content/uploads/LevelBlue-3.jpg){ width=60% }

---
该服务由Akamai行业领先的应用和API保护技术提供支持，将下一代网络应用防火墙（WAF）、分布式拒绝服务（DDoS）缓解、机器人保护和基础API安全能力与LevelBlue专门的WAAP运营团队相结合。

全球范围内，组织正在快速扩展其应用和API的使用，以推动数字优先策略。根据企业战略集团的数据显示，预计两年内每个组织的网络应用数量将从145个增长到200个以上。同时，使用API的应用占比超过一半的组织数量将从32%上升到80%。安全团队面临的一些最关键挑战包括：

- 发现应用和API部署
- 扩展足够的保护措施
- 迅速识别和缓解攻击
- 确保安全不会影响性能

此外，许多组织还面临员工限制和专业知识缺口的问题。随着环境的日益复杂，中型市场组织中有一半表示，保护网络应用和API比两年前更加困难，因此许多人正在寻求外部帮助，以及简化和整合的解决方案。LevelBlue托管WAAP直接解决了这些需求，提供可衡量的安全成果，同时简化运营。

“如今，令人惊讶的是，许多组织依赖于多个并非专为网络应用和API安全设计的工具，从而导致复杂性、孤岛和成本上升，”LevelBlue总裁Sundhar Annamalai表示。“LevelBlue提供了一个替代方案：经过验证的服务，可以整合和简化保护，同时提供可预测的投资。通过将LevelBlue的运营专业知识与Akamai的成熟技术相结合，组织可以领先于不断演变的威胁，为关键数字功能创建网络韧性。”

LevelBlue托管WAAP分为两个层级，基本和高级，让组织可以灵活选择最符合其需求的服务级别。核心优势包括：

- 24/7 WAAP专家支持：获得一个完全运作的专家团队，提供全天候支持、监控和咨询，以改善WAAP保护。
- 发现和保护关键资产：自动识别和分类网络应用和API—优先考虑那些暴露或处理敏感数据的应用，并根据需要扩展保护。
- AI驱动的威胁防御：结合基于AI的检测和全球威胁情报，及时发现异常，适应新的攻击向量，保持领先于威胁。
- 流线型安全管理：消除手动调整，采用专家主导的自动化策略管理，提高效率，减少误报，并与现代DevOps工作流对接。

“仅在2024年，Akamai就记录了超过3110亿次网络应用攻击。随着AI的加速发展，威胁更难以识别，安全性也变得更加难以控制，”Akamai应用安全组合的高级副总裁兼总经理Rupesh Chokshi表示。“Akamai与LevelBlue的合作为客户提供了一支值得信赖、可靠的团队，结合了行业领先的技术和全球最大托管安全服务提供商之一的深厚运营专业知识。这是一个强大的组合，提供可以加速组织实现韧性保护和合规的灵活解决方案。”

