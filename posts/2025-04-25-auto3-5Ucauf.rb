Xona，关键基础设施安全访问的领先供应商，今天宣布其在拉丁美洲的战略扩张。![图片](https://ai-techpark.com/wp-content/uploads/Xona.jpg){ width=60% }

---
通过新的客户、地区合作伙伴以及专门的销售、市场和客户成功团队，Xona现在在墨西哥、巴西、哥伦比亚、秘鲁、巴拿马、智利和阿根廷提供安全、简便和可靠的关键系统访问控制。

拉丁美洲的电力、水、石油和天然气、制造、运输及其他工业部门正在经历快速的数字化转型，这使得安全操作访问变得比以往任何时候都更加重要。Xona的零信任、无客户端平台旨在保护关键基础设施资产免受勒索软件、内部威胁和不安全远程连接的侵害，同时简化IT和OT团队的部署和合规流程。

“随着拉丁美洲的关键基础设施变得更加互联，基于身份的安全访问的需求从未像现在这样迫切，” Xona首席执行官比尔·摩尔（Bill Moore）表示。“我们在该地区的扩张体现了我们持续致力于帮助运营商消除不安全端点、降低网络风险并确保运营弹性，而无需干扰工作流程。”

Xona的平台使组织能够为OT、IT和XIoT系统提供基于角色、身份和时间的访问权限，而不暴露资产于临时或未管理的设备。其独特的脱机访问架构在用户与关键基础设施之间创建了一个虚拟的气隙，确保符合IEC 62443和NERC CIP等标准。

随着新的区域客户——例如一家巴西能源公司——已经上线，并且渠道势头强劲，Xona正与拉丁美洲领先的网络安全和工业自动化提供商合作，提供本地化解决方案和支持。

“拉丁美洲的关键基础设施组织面临着愈发严重的网络威胁，尤其是在OT环境中远程访问和第三方连接变得更加普遍，” DICOFRA国际扩展总监赫苏斯·桑切斯·佩纳菲尔（Jesus Sanchez Peñafiel）表示。“传统工具显然并不是为这些高风险用例而设计的。这就是为什么我们很高兴与Xona合作，其平台专为在不干扰操作的情况下保护对关键系统的访问而构建。我们一起帮助客户降低风险、满足合规要求，并现代化他们的访问策略。我们期待着与Xona的长期成功合作，将这些强大的能力带给整个地区的组织。”

