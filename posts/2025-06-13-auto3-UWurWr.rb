供应链和数字化转型专家为平台顾问委员会带来全球制造业专业知识
FourKites®，全球领先的人工智能驱动的供应链转型公司，今天宣布米其林集团前高级副总裁帕斯卡尔·扎米特（Pascal Zammit）已加入其战略顾问委员会。![图片](https://ai-techpark.com/wp-content/uploads/Pascal.jpg){ width=60% }

---
扎米特在全球供应链运营和数字化转型方面拥有丰富的经验，他将在FourKites的顾问委员会中提供有关产品创新和市场拓展的战略指导。
在米其林的卓越职业生涯中，扎米特领导全球供应链组织，并主导连接移动业务单元的推出。他的成就包括转型米其林在欧洲的供应链，实施包括人工智能、物联网和区块链在内的先进技术，并建立全面的全球S&OP/IBP流程。
“工业供应链必须演变——从反应性系统变为更具预测性、更自主的操作，同时也要与供应商和客户更紧密集成，让运营团队更易于理解，这样才能交付对业务竞争力至关重要的客户和员工体验，”扎米特说道。“FourKites开发了一个端到端的人工智能平台，能够主动协调复杂的操作，仅在必要时进行人类干预。这代表了供应链管理的未来：可被前线运营团队和接近客户和供应商的系统使用和理解，能够快速高效地感知、分析和提出决策。根据每个业务的具体供应链绩效要求，必要时还能自主执行。”
FourKites的战略顾问委员会汇聚了数十年来在供应链领域实践经验丰富的优秀高管。该委员会体现了FourKites致力于理解供应链领导者面临的实际挑战，确保公司的解决方案满足真实的市场需求。
“帕斯卡尔在复杂组织中推动全球转型的经验，使他对在多个市场和产品线运营的制造商所面临的挑战有独特的洞见，”FourKites的创始人兼首席执行官马修·埃伦吉卡尔（Mathew Elenjickal）表示。“他在米其林实施先进技术并取得可衡量的业务成果的成功历史，使他成为我们继续演化平台的理想顾问。帕斯卡尔的战略愿景与我们将供应链从反应监控系统转变为可以感知、分析和执行决策的自主操作的使命完全契合。”
扎米特的任命恰逢FourKites从实时可视化发展到提供行业唯一的智能控制塔（Intelligent Control Tower™）。该平台结合了供应链网络数据、数字双胞胎和数字工作队的AI代理，能够自主行动，以防止扰动发生并协调复杂的供应链操作。
在加入米其林之前，扎米特于1997年在凯捷（Cap Gemini）担任顾问，完成了在法国INSA里昂的研究生学习。从2021年起，他担任SaaS解决方案提供商和咨询公司的高级顾问，并在巴黎潘提昂-阿萨大学担任供应链与物流的副教授。
扎米特将在FourKites峰会上分享他在通过数字工作者实现运营卓越的经验，他的会议主题为“数字工作者的优势：通过AI扩展卓越”，并将借鉴他转型米其林全球供应链运营的经验，展示如何消除跨国复杂运营中的操作差异。为期两天的会议将于2025年8月26日至27日在芝加哥举行，并汇聚供应链领导者，进行有关AI驱动转型的实践学习——可在 https://www.fourkites.com/event/summit-2025/?utm_source=businesswire&utm_medium=press&utm_campaign=summit-2025 注册参加。

