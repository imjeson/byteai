Phison Electronics (8299TT)，一家领先的NAND控制器和NAND存储解决方案创新者，今天在2025年台北电脑展（COMPUTEX 2025）上展示了一系列突破性技术。![图片](https://ai-techpark.com/wp-content/uploads/Phison-Unveils.jpg){ width=60% }

---
今年的主题为“连接AI”，Phison将六大主要趋势——生成式AI、先进计算、可持续性、下一代连接、智能应用和创新——融入强大的解决方案中，以推动AI的部署和未来的数字基础设施。

在“与Phison加速创新”的主题下，Phison在台北南港展览中心1号馆4楼MO419a展位展出了其最新的开发产品，涵盖企业SSD、AI部署平台、先进SSD控制器和PCIe高速信号IC——展示了Phison在加速各行业AI转型中的关键角色。

Pascari X200Z：为AI时代打造的企业SSD  
Phison的Pascari X系列旨在满足现代企业工作负载的性能和耐久性需求，从AI加速到关键任务存储基础设施。作为这一强大系列的扩展，Phison推出了其迄今为止最具写入强度和性能优化的企业SSD——Pascari X200Z。该旗舰企业SSD专为生成式AI和实时分析的高写入耐久需求而设计，具备PCIe Gen5 NVMe接口，具有近SCM延迟和高达60 DWPD的耐用性。X200Z适用于AI训练工作负载、事务数据库和高频数据日志记录，提供无与伦比的可扩展性和持续写入性能，设立了企业存储解决方案的新基准。

aiDAPTIVGPT：用于私有AI部署的全功能推理工具包  
随着越来越多的企业采用私有训练的大型语言模型（LLM），Phison推出了aiDAPTIVGPT——一款为安全的本地环境量身定制的全面AI推理工具包。该工具包基于获得COMPUTEX最佳选择奖的专利aiDAPTIV+平台，支持对话AI、语音服务、代码生成、网络搜索和数据分析等生成任务。

针对中小企业、教育工作者和公共机构，该综合解决方案简化了AI推理，同时保护数据隐私。Phison的aiDAPTIVGPT在本地设置中实现了云般的性能，突显了公司在边缘AI计算和私有LLM部署方面的领导地位。

aiDAPTIVCache AI150EJ：针对AI边缘和机器人系统优化的GPU内存扩展  
Phison还将展示其下一代aiDAPTIVCache AI150EJ，这是一种专为AI边缘系统设计的解决方案。借助Phison专有的aiDAPTIVLink软件层，它通过优化首次标记时间（TTFT）并增加处理的标记数量，显著增强边缘推理性能。

E28 SSD控制器：全球首款内置AI计算能力的SSD控制器  
采用台积电6nm工艺制造的革命性E28 PCIe 5.0 SSD控制器，成为全球首款集成AI处理的控制器。E28通过加速AI模型更新和整体系统性能，增强了SSD的智能。

E28实现了突破性的性能，随机读写达到2,600K/3,000K IOPS（输入输出操作每秒）——比同类产品高出10%以上，巩固了Phison在高性能存储领域的领导地位。尽管拥有这样的卓越吞吐量，E28的功耗却比竞争对手的6nm控制器低了最多15%，展示了Phison在芯片设计和功率效率方面的优势。

整体而言，E28实现了数据带宽和功耗之间的理想平衡，其性能功耗比比同类竞争产品高出15-30%——定义了高性能SSD的功率优化新标准。

值得一提的是，E28成功进入初版设计的量产阶段，获得“首款成功”的地位。这一里程碑突显了Phison强大的集成电路设计专业能力和丰富的生产经验，间接表明竞争对手在产品延迟和稳定性方面面临的挑战。

此外，E28还获得COMPUTEX 2025金奖，因其创新性、实用性和市场潜力而受到认可，巩固了其作为未来旗舰SSD的核心控制器的地位。

E31T：移动平台的无DRAM PCIe Gen5控制器  
Phison将展示E31T，一款为超薄笔记本电脑和手持游戏设备设计的无DRAM PCIe 5.0 SSD控制器。支持紧凑的M.2 2230和2242外形因素，E31T在高性能、低功耗和空间效率之间达成理想平衡——非常适合移动计算。

下一代连接：全球领先的PCIe信号IC  
继续在高速数据传输领域保持主导地位，Phison推出了一整套PCIe信号IC解决方案。亮点包括：

全球首款通过CXL 2.0认证的PCIe 5.0重定时器  
拥有超过50%全球市场份额的PCIe 5.0重传器  
行业首款PCIe 6.0重传器  
与客户共同开发的即将推出的PCIe 6.0重定时器、重传器、SerDes PHY和PCIe光纤平台

“AI与存储的融合正在迅速重塑世界。在2025年COMPUTEX上，我们展示了推动全球数字化转型的引擎。从全球首款AI驱动的SSD控制器E28和高端企业SSD Pascari X200Z到民主化私有AI部署的aiDAPTIVGPT工具包——每一项创新都源于多年的研发和前瞻性的愿景，”Phison Electronics首席执行官兼创始人K.S. Pua表示。“AI的采用不应仅限于科技巨头。真正的竞争力在于为中小企业、教育机构和公共部门提供负担得起的、快速的和安全的AI解决方案。Phison将继续秉承‘与Phison加速创新’的精神，提供高性能、能效解决方案，推动下一波AI驱动的创新。”

