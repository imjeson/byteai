Rocket Doctor在阿尔伯塔省的患者数量同比增长112%，在2024年接诊超过36,000名患者，医师采纳率增长218%。
在加利福尼亚州和加拿大推出了新合作伙伴关系，包括与中央加利福尼亚健康联盟的合作，这是一个约450,000成员的管理医疗计划，以及与Melanoma Canada的全国性组织的合作。
医学教育套件在明尼苏达大学投入使用，提供可扩展的AI驱动临床培训，潜在节省成本达到40%。
Rocket Doctor被美国远程医疗协会评选为顶尖临床创新者，并受邀在哈佛医学院和AARP的AgeTech Accelerator上发言。

Treatment.com AI Inc. (CSE: TRUE, OTC: TREIF, Frankfurt: 939)（“Treatment”）及其全资数字健康平台及市场Rocket Doctor很高兴地分享在完成收购后的一系列成就。现在以统一的愿景运作，这两家机构在产品部署、患者增长、全国合作伙伴关系和公众认可方面均取得了一定的进展。
本季度开始时，Treatment完成了对Rocket Doctor的收购，标志着公司使命的一个重要里程碑，旨在将自有的AI知识引擎与现实世界的虚拟护理交付结合起来。这一统一产品的推出为智能、更快速和更安全的护理创造了潜力，得益于医学全球图书馆（GLM）、AI分诊工具及Rocket Doctor可扩展的专有电子病历和数字健康平台，所有这些都由在职医师设计并为其服务。
收购完成后，Rocket Doctor在阿尔伯塔省的患者数量同比增长112%，过去12个月接诊了36,779名患者。截至目前，已有超过71,000名阿尔伯塔省居民通过该平台获得护理，其中35%居住在25,000人以下的社区，凸显了该平台在服务不足地区的重要作用。医生的采纳率也增长了218%，从去年的22名活跃医生增至今年的70名，使Rocket Doctor能够迅速扩大规模以应对不断增长的需求。
与此同时，Treatment.com AI推动其对下一代临床教育的承诺，推出了新一代医学教育套件（MES）。该平台在明尼苏达大学医学院投入使用，配备了新的AI模拟患者，对240名三年级学生在其OSCE考试中进行评估，允许在规模上进行虚拟和现场测试。根据医学院副院长Claudio Violato的估计，该解决方案有潜力将考试管理成本降低约40%，同时减少教职员工的工作负担。
Rocket Doctor还在美国和加拿大获得了新的合作伙伴关系。在加利福尼亚，该公司与中央加利福尼亚健康联盟推出了一项虚拟护理计划，此医疗计划为约450,000名成员提供服务。该计划使患者能够通过Rocket Doctor的医师网络获得急性、初级和专科护理。
在加拿大，Rocket Doctor与Melanoma Canada合作，拓展在安大略省、阿尔伯塔省和不列颠哥伦比亚省对高风险皮肤癌患者的随访护理，支持在该国最常见的癌症类别中实现更早的干预和持续的护理。
因其创新和临床影响，Rocket Doctor在NEXUS2025会议上被美国远程医疗协会评选为顶尖临床创新者。该奖项突显了Rocket Doctor由医师主导的虚拟护理模式及其在规模上提供高质量护理的能力。
Rocket Doctor还被选中参加数个高端加速器和思想领袖项目。该公司进入了加州大学伯克利分校的健康引擎加速器，这是一个具有竞争力的非稀释性健康科技创始人计划。Rocket Doctor的首席执行官兼创始人William Cherniak博士受邀在哈佛医学院发言，与医学生和商学院学生分享有关构建访问性AI支持虚拟护理平台的见解。他还参加了2025 AARP AgeTech Accelerator，展示了Rocket Doctor在支持老年人、医疗补助和医疗保险人群方面的工作。
该公司的影响力在全国范围内进一步增强。Hamza博士和Cherniak博士受邀在加拿大证券交易所敲响开盘钟，并随后在CSE的官方播客上共同亮相，讨论人工智能和北美虚拟护理的未来。Cherniak博士还在CSE峰会上讲述了负责任投资的演讲，直接与投资者互动，探讨医疗保健交付模式的演变。
在这一季度的势头下，Cherniak博士与其他领先医师共同撰写了一篇名为《初级护理患者对数字技术的使用和偏好中的社会人口统计变异》的同行评审研究论文。该研究为患者行为和数字工具在改善健康公平方面的作用提供了宝贵的见解。
“过去几个月对Rocket Doctor和Treatment.com AI来说都是变革性的，”William Cherniak博士表示。“我们达成了共同愿景，正在多个领域执行——从临床培训到专科护理，同时帮助患者和提供者重新想象现代护理的样子。”
“我们为迄今取得的成就感到自豪，”Treatment.com AI的首席执行官Essam Hamza博士表示。“我们将继续专注于结合医师主导的护理、经过验证的AI工具和强大的合作伙伴关系，以改善医疗服务交付。”

