医疗保健初创企业已经开始探索如何实施人工智能以扩大其患者群体，追求利润，从而使投资者受益。越来越多的美国人正在利用人工智能来诊断他们的健康问题，因为医疗专业人员之间采用人工智能工具已经成为规范。目前，推动这些改变游戏规则的技术开发者正在克服监管障碍，朝着其解决方案在现实世界中实施的目标迈进。包括Avant Technologies, Inc. (OTCQB: AVAI)、Agenus Inc. (NASDAQ: AGEN)、Clover Health Investments, Corp. (NASDAQ: CLOV)、Zimmer Biomet Holdings, Inc. (NYSE: ZBH) 和Monogram Technologies Inc. (NASDAQ: MGRM) 在内的市场参与者正在逐步接近重要的里程碑。

目前，医疗保健领域的人工智能行业预计到2030年将超过1100亿美元。不过，根据埃森哲的报告，人工智能在2035年前可以为医疗保健领域解锁额外的4610亿美元价值——在一个已经预计超过2.26万亿美元的行业之上。

Avant Technologies, Inc. (OTCQB: AVAI)及其合资伙伴Ainnova Tech正在继续提升其人工智能驱动的诊断平台的知名度，Ainnova首席执行官Vinicio Vargas最近在罗氏（Roche）“Macular Spectacular”眼科学大会上发言，该大会在哥伦比亚卡塔赫纳举行。此次活动汇集了来自整个拉丁美洲的视觉健康领域的领军人物，讨论如何通过人工智能创新来改善患者的结果。Vargas同时也是Ai-nova Acquisition Corp.(AAC) 的董事会成员，向与会者展示了Vision AI作为一种变革性工具，旨在扩大早期糖尿病视网膜病变筛查的覆盖面，并减少该地区可预防的失明案例。

Vargas还强调了与罗氏和预付健康计划提供商Salud 360于2024年第四季度达成的合作协议，该协议在高风险糖尿病患者中试点Vision AI；如果成功，该项目将在美国、加拿大和欧洲通过Ai-nova Acquisition Corp. (AAC)推广，AAC持有Ainnova技术组合的全球权利。

这一势头建立在美国市场最近的一个监管突破之上。Avant和Ainnova最近完成了一项关于Vision AI的关键预提交会议，这是他们针对糖尿病视网膜病变和其他视网膜疾病的旗舰诊断平台。“我们对这一下一阶段感到非常兴奋，”Ainnova首席执行官以及Ai-nova Acquisition Corp.的董事会成员Vinicio Vargas表示。“我们准备在美国的初级护理诊所收集数据，进行一项简单而严格的研究——将我们的人工智能基础的视网膜筛查与三位视网膜专家的读数进行比较。”

7月15日的会议标志着获取Vision AI 510(k)批准的一步关键进展。FDA的会议为研究设计、地点选择和执行策略提供了明确反馈，使Ainnova及其临床试验合作伙伴Fortrea能够最终确定美国境内试验的准备工作。“这个里程碑不仅让我们更接近在全球最大的医疗保健市场中验证我们的平台，而且为即将推出的新型自动化视网膜相机铺平了道路，”Vargas补充道。“我们相信这将成为一个游戏改变者——使糖尿病视网膜筛查更迅速、更易获取，并可从几乎任何护理点提供。”

在美国的监管工作不断推进时，该公司已在拉丁美洲推出了一项开创性的慢性护理模型。该项目目前已通过Grupo Dökka的Fischel和La Bomba药房连锁店上线，提供免费的步入式视网膜风险评估——提供实时的人工智能结果，并将高风险患者与诊所和专家直接连接。

超过30%的糖尿病患者发展为糖尿病视网膜病变，后者是可预防失明的主要原因。Vision AI无需现场眼科医生即可提供早期、低成本的筛查。该模型得到了药房、保险公司和制药合作伙伴的支持，展示了在医疗保健价值链中真正的吸引力。

Avant在这些努力中的角色不断扩大。通过与Ainnova共同成立并构建的Ai-nova Acquisition Corp.，Avant保持着对Ainnova平台的全球许可权，预计将从美国和国际商业化中受益。拉丁美洲的试点项目已经开始产生势头，而美国的试验将解锁更大的市场。

该公司还在准备一个独立的企业，以容纳潜在的糖尿病治疗候选药物——将领导、数据和知识产权整合到一个精简的结构中，用于诊断和治疗。

展望未来，Vision AI可能成为更广泛疾病检测的前线工具。Ainnova未来的路线图包括为乡村诊所提供一个云连接的视网膜相机以及旨在通过视网膜或血液生物标志物识别阿尔茨海默症、心血管疾病和其他慢性病的新模块。

结构化简化也可能在前方不远。在尚未绑定的意向书（LOI）中，Avant曾宣布希望收购Ainnova Tech的100%股份——可能将所有知识产权、领导力和商业权利整合到一个公共实体下。这样的举措将为投资者提供对这一快速发展的技术堆栈的纯粹投资机会，同时加深两家公司之间的运营协调。

