Agenus Inc. (NASDAQ: AGEN) 与人工智能生物技术公司Noetik合作，开发针对其癌症免疫疗法组合BOT/BAL的预测生物标志物。“在Agenus，我们致力于通过科学创新和下一代免疫疗法来转变癌症护理，”Agenus的主席兼首席执行官Garo Armen博士表示。“与Noetik的合作使我们能够利用尖端的人工智能，更好地理解患者生物学，并更精准地定制治疗。”

通过使用Noetik的先进“虚拟细胞”模型，该模型在大量肿瘤数据集上进行了训练，此次合作旨在识别哪些患者最可能从治疗中受益。这可以改善临床试验结果，加速个性化癌症护理的人工智能驱动洞察。

Clover Health Investments, Corp. (NASDAQ: CLOV) 最近在新泽西州与IPC的iCare+网络启动了一项新的药房试点项目。该计划使当地药房能够使用人工智能工具和实时数据来帮助老年人管理药物并减少住院次数。“这不仅仅是一个新计划，而是一种全新的支持新泽西州成员的模式，”Clover Health的Medicare Advantage首席执行官Jamie Reynoso表示。“通过与已经深入我们社区的药剂师合作，我们可以提供更好的护理，提供出色的顾客体验，并支持那些让新泽西更强大的独立企业。”

这是Clover向提供更个性化、社区导向的医疗保健以及支持独立药房的战略一部分。

Zimmer Biomet Holdings, Inc. (NYSE: ZBH) 已与Monogram Technologies Inc. (NASDAQ: MGRM) 达成一项最终协议，以约1.77亿美元的现金收购Monogram，并根据里程碑支付额外的每股高达12.37美元。“Monogram的技术是一个重大突破，展示了我们致力于成为外科机器人和导航领域中最具创新精神和广泛性的公司，”Zimmer Biomet的董事长、总裁兼首席执行官Ivan Tornos表示。“交易完成后，我们以客户为中心的产品组合将包含最全面和灵活的技术生态系统，支持各种外科医生的不同偏好——现在和未来。凭借Monogram的专有技术，Zimmer Biomet有望成为第一家提供全自动能力并重新定义护理标准和未来骨科手术的公司。”

收购将Monogram的半自动和全自动骨科机器人添加到Zimmer Biomet的ROSA®平台，旨在创建业内最全面的机器人套件。Monogram获得FDA批准的膝关节置换机器人预计将在2027年推出，完全自动化版本也在开发中。

“自成立以来，我们一直专注于推进骨科机器人技术，旨在安全、高效和准确地支持总膝关节置换手术的外科医生，”Monogram首席执行官Benjamin Sexson表示。“我们很高兴能够将我们的技术加入Zimmer Biomet领先的外科机器人、导航解决方案和可信赖的植入物组合中，并受益于他们深厚的行业专业知识和全球规模。”

Zimmer Biomet预计该交易将在2028年具有增值，支持在高需求的外科机器人市场中的长期增长。

