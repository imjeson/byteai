大模型正在掀起一场“诸神之战”，然而不少企业都被卡在关键一步，那就是落地。

为了能够帮助更多的企业了解大模型技术，将其应用到实际工作中，由msup举办的的2024年A2M人工智能创新峰会将于6月28-29日在上海明捷万丽酒店盛大举行。本届峰会，组委会邀请到了10位拥有专业选题视角的业界知名技术公司技术大咖担任联席主席，参与前期技术方向的把关、议题的打磨等工作，从多个维度复盘落地时间过程，梳理案例成功要点。

此外，今年A2M峰会特别增加了开幕式的分享，致力于帮助更多AI从业者了解2024年的技术方向及未来趋势。内容也涵盖AIGC、大模型、架构演进等话题，在重点关注AI技术的同时，也深度涉及数据科学和互联网架构的关键领域。

涵盖多元热门内容
汇聚AI优质实践案例
通过与66位一线AI技术大咖沟通，可以更好的了解行业创新者和先驱如何应用人工智能和机器学习。

对标学习，探索新锐公司
前沿技术和工程文化
为了促进大模型人工智能技术在不同行业间的交流，A2M组委会组织了一场会中研学，前往bilibili和阅文集团进行深度交流。这是A2M大会首次举办的线下研学活动，本次将与参会者共话人工智能发展新趋势、分享行业洞见、探索前沿技术和工程文化，帮助大家更好的融合多元化的行业经验、展望大模型的技术应用。

成为AGI时代的创新者
