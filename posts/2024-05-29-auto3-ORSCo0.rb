Marsh McLennan Agency的2024年商业保险趋势报告基于超过500份主要来自年收入在1000万至4900万美元之间、员工人数低于250的企业的回应。该报告提供了关于中市场企业面临的顶级风险关注点以及应对最重大威胁的准备情况的洞察。
当被问及他们极度担心或非常担心的风险对业务构成威胁时，75%的受访者表示担心网络安全和数据隐私风险，部分原因是不断增加对第三方数据管理供应商的依赖。经济风险排名第二，63%的受访者指出，这主要是由持续的通货膨胀和较高的利率引起，紧随其后的是监管风险，例如严格的工人分类规则和生物识别信息隐私法（BIPA）法规，被61%的受访者报告。
尽管美国中市场企业仅占全国企业总数的5%，但却占据了美国总营业额的33%。
Marsh McLennan Agency的商业保险总裁兼全国商业保险负责人Denise Perlman表示：“中市场企业正在一个日益复杂且不稳定的商业环境中运营，需要更积极的风险识别和缓解方法。” “尽管面临挑战，我们的研究结果显示，企业正在更加关注业务的脆弱性，并寻求确保适当的保险覆盖以保护他们的利润。”
根据报告，64%的受访者表示他们在2023年进行了对其保险覆盖范围的彻底审查；较2022年的14%大幅增加。
报告列出的前五个关注点包括劳动力风险（57%），例如由美国职业安全与卫生管理局引入的对人体工程学、防坠落和保护的增强关注，以及灾难性风险（56%），主要由极端天气事件频率和严重性上升所驱动。
