根据Marsh McLennan Agency今天发布的一份新报告，美国中市场商业领导者认为网络、经济和法规风险是2024年对其运营构成最大威胁。![图片](https://ai-techpark.com/wp-content/uploads/2024/05/Marsh-960x540.jpg){ width=60% }

