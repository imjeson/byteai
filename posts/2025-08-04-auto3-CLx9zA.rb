---

YY集团控股有限公司（NASDAQ: YYGH）("YY集团"、"YYGH"或"公司"）是全球领先的按需劳动力解决方案和综合设施管理（IFM）公司，今天宣布在其核心服务领域推出机器人集成举措。![图片](https://ai-techpark.com/wp-content/uploads/YY-Group.jpg){ width=60% }

---
该计划旨在提高服务交付效率，增强员工支持，并帮助客户在不断增长的需求下实现更高的一致性和效率。

机器人集成跨关键行业
第一阶段的推广重点在于高影响力领域，在这些领域中，自动化能够带来服务质量、速度和可靠性上的即时收益。在酒店业，机器人送餐员将支持宴会团队处理食物配送和活动物流，确保在高需求活动期间的及时协调。自主清洁机器人将提供全天候的商业空间卫生服务，通过AI指导实现最佳覆盖。智能安全巡逻机器人将支持常规监控和报告，使现场团队能够专注于优先事务。在外立面和高空清洁方面，机器人系统将增强在难以到达区域的安全性和精确度，减少风险，同时使员工能够专注于更高价值的任务。

机器人将灵活部署于各项服务线，根据每个客户的需求量身定制，运营模式范围从按需使用到资本拥有。所有部署都由YY集团的内部技术团队支持，并可根据需要与客户侧平台进行集成。

我们的机器人解决方案旨在支持而不是取代人类团队。从设置到日常运营，机器人与工作流程无缝集成，使员工能够专注于更高价值的任务。客户可以根据自己的节奏进行扩展，而不会对现有运营造成干扰。

“随着服务期望的发展和人力资源动态的变化，机器人技术为提升一致性、安全性和可扩展性提供了及时的机会，”YY集团控股有限公司首席执行官Mike Fu表示。“这一举措标志着我们在向客户提供更智能、技术驱动的解决方案旅程中的自然演变，同时保持对未来运营需求的前瞻性。”

YY集团正在与新加坡和马来西亚的部分客户合作，试点机器人部署，初步扩展重点集中在东南亚，并计划进行全球扩展。这些试点与公司成为数字化IFM和劳动力解决方案提供商的长期战略相一致。

通过将自动化嵌入服务模型，YY集团旨在提高资产效率、提升服务成果、加强客户保留，并在其网络中增长经常性收入。

