公司已开始实施其购买$1000万美元$XRP的先前宣布的计划
Hyperscale Data, Inc. (NYSE American: GPUS)，一家多元化控股公司（“Hyperscale Data”或“公司”），今天宣布将从2025年8月12日星期二开始发布其$XRP收购活动的每周更新。![图片](https://ai-techpark.com/wp-content/uploads/Hyperscale-Data.jpg){ width=60% }

---
今年早些时候，公司宣布其意向收购最高达$1000万美元的$XRP。公司确认已开始推进该战略。

这一举措是Hyperscale Data更广泛战略的一部分，旨在增强其数字资产投资运营的透明度，并强调其对$XRP累积的有序、长期方法的承诺。每周报告将总结前一周的所有数字资产活动，并反映公司资产负债表上的累计持有情况。公司认为，$XRP作为跨境价值转移和新兴金融基础设施的可扩展且高效的数字资产，具有显著的潜力。

“我们相信人工智能和能源的长期未来——在数据中心领域、数字资产领域，以及未来金融的需求，”Hyperscale Data执行董事长Milton “Todd” Ault III表示。“我们将$XRP视为不断发展的全球金融生态系统中的基础资产。我们的目标是建立一个反映不仅是计算基础设施，而且是全球金融整体未来的资产负债表。”

为了进一步与长期战略举措保持一致，公司正在评估其$XRP持有的36个月锁定期，这取决于内部里程碑和整体市场情况。此外，董事会正在积极考虑在有利的市场条件下扩大先前宣布的$1000万美元$XRP计划，这取决于可用融资。

