英国政府公布了一项广泛的行动计划，旨在提升该国在人工智能（AI）开发和部署中的角色。

---
这份由科技投资者马特·克利福德撰写的报告包含50条建议，得到了基尔·斯塔默的支持，他表示这项技术将给“我们的国家带来巨大的变化”。以下是该计划的关键要点：

1. 主权AI驱动
政府希望确保英国能够生产自己的AI模型——支撑如ChatGPT聊天机器人等工具的技术。为此，计划建议到2030年，公共控制下的AI计算能力增加20倍。这将包括在待定地点建设一台全新的“超级计算机”。其次是数据，这也是AI的核心燃料。如果将AI广义地描述为执行通常需要人类智能的任务的计算系统，那么它们确实需要数据——从学术论文到经典小说，再到转录文本——以“学习”如何执行这些任务。该计划指出，政府应创建一个国家数据图书馆，其中包含国家控制的数据，供研究人员和公司使用。至少五个“高影响力”数据集将被提供，不过计划提到隐私、伦理和数据保护的考量必须得到重视。首相在周一暗示，国家医疗服务体系（NHS）所控制的大量健康数据将是这个图书馆的候选者。该计划还提议创建一个文化数据图书馆，戏称为“英国媒体资产训练数据集”。这个库可以存放来自BBC、英国图书馆、自然历史博物馆和国家档案馆等机构的数据。计划指出，这个数据集将“版权清晰”，并明确政府应从中获利，将其称作“商业提案”。

2. 鼓励私营部门
政府将创建“AI增长区”。简化的规划程序将帮助这些地区建设数据中心——AI技术的中枢神经系统，同时加快为这些基础设施提供“清洁”能源。位于牛津郡的库尔汉姆科学中心（Culham Science Centre），即英国原子能机构的总部，被选中作为增长区的可能试点。招募顶尖AI人才也在计划中。该计划建议设立一个猎头单位，从海外引入顶尖人才，服务于英国的AI安全研究院和国有实验室——同时也为英国公司服务。它还建议改革工作签证，以帮助科技初创企业吸引非英国人才。计划还呼吁为“领军行业”解决方案，以推动AI在银行和创意产业等关键领域的采纳。政府应帮助工作人员适应AI的新技能和新工作。同时，该计划还希望创立“国家冠军”AI公司，提出创建一个名为“英国主权AI”的新机构，以支持企业家并帮助创造下一个Google DeepMind——一个由英国创立的AI研究世界领导者。

3. 将AI嵌入公共部门
计划呼吁政府“迅速试点”AI驱动的服务，表示这将改善公民体验和结果，同时提升生产力——这是经济表现的一个衡量标准。最佳试点方案将在全英国推广。政府提到的公共部门AI应用例子包括帮助教师备课以及使用AI增强的道路监控摄像头发现路面坑洼。这一推动背后的逻辑是改善公共服务，同时通过自动化任务节省开支，这隐含着可能会对公共部门造成裁员威胁。

4. AI与能源
AI是一种能耗密集的技术，因为它依赖耗电的数据中心，这意味着在提升英国推动这一技术的角色时会有明确的环境成本。根据国际能源署的数据，到2026年，数据中心的全球电力消费可能会达到与日本当前能源消费的水平。行动计划提到AI增长区使用“清洁能源”，政府周一宣布将成立一个新的AI能源委员会以实现这一目标。该机构将由科技主管彼得·凯尔和能源主管埃德·米利班共同主持。委员会将寻求加速对数据中心的可再生能源投资以及小型核反应堆的开发。去年，微软、亚马逊和谷歌等AI巨头均宣布达成使用核能的协议。

5. AI与版权
计划建议改变英国的版权制度，这是对他人作品未经许可使用的法律保护。它表示，英国在文本和数据挖掘的框架应当“至少与欧盟的竞争性相当”。这意味着允许AI公司使用受版权保护的材料，前提是他们给予版权所有者选择退出抓取过程的能力。对于强有力的版权保护倡导者来说，这被视为对创意专业人士和出版商权利的严重侵犯。英国作曲家及版权辩论中的重要声音埃德·牛顿-雷克斯表示，计划对版权改革的支持是对英国创意产业的“重大打击”。

