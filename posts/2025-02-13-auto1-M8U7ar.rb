斯嘉丽·约翰逊在金恩·韦斯特关于最近反犹言论的深度伪造视频在网上疯传后，警告人们AI的“紧迫危险”。

---
这段视频呈现了超过十多个名人的AI生成版本，包括约翰逊、大卫·施威默、杰瑞·宋飞、德雷克、亚当·桑德勒、斯蒂芬·斯皮尔伯格和米拉·库尼斯。视频开头是约翰逊的深度伪造形象，穿着印有中指伸出、戴有大卫之星和金恩名字的T恤。视频背景是《哈瓦·那吉拉》（Hava Nagila），这是一首通常在庆祝文化活动上演奏的犹太民谣，最后以口号“够了，参与反对反犹太主义的斗争”结束。

其他出现在视频中的明星还包括萨莎·拜伦·科恩、杰克·布莱克、娜塔莉·波特曼、亚当·莱维尼、本·斯蒂勒和莱尼·克拉维茨。约翰逊在一份声明中表示：“我通过家人和朋友得知，一个以我形象生成的AI视频正在网上传播并获得关注，内容是对抗一种反犹太主义观点。”“我是一名没有任何忍受反犹太主义或任何形式仇恨言论的犹太女性。但我也坚信，AI所乘法增加的仇恨言论比任何一个人承担的责任更具威胁。我们必须指出AI的误用， 不论其信息如何， 否则我们会失去对现实的掌控。”

金恩·韦斯特目前被称为Ye，他在X上多次自称“纳粹”并赞扬希特勒，之后注销了他的账户。他还在超级碗期间出现在一则广告中，指引人们前往他的网站，而该网站只销售一种产品——印有纳粹标志的T恤。由于违反政策，该网站后来被Shopify关闭，福克斯电视台首席执行官杰克·阿伯内西也在给员工的通知中谴责了这则广告。

约翰逊一直是反对未经同意使用AI的名人之一。去年，她对OpenAI提出了法律行动的威胁，因为该公司在其ChatGPT产品中使用了类似她声音的模型——在这之前的九个月，她表示她拒绝了该公司的请求，为其AI系统配音。OpenAI最终在广泛反响后，从ChatGPT中移除了这一备受推广的语音选项。

约翰逊表示，她曾是AI的“非常公开的受害者”，但事实是，AI的威胁影响着我们每个人。她补充说：“关于AI的1,000英尺的波浪正在袭来，而一些进步国家（不包括美国）已经以负责任的方式作出了回应。美国政府在通过立法保护所有公民免受AI紧迫危险方面却陷入瘫痪，这令人感到恐惧。”这位演员敦促当选官员采取立法行动反对AI滥用，称这一问题是“影响人类未来的两党问题”。

她的言论正值英国广告标准局表示，涉及名人的假广告仍然是网上最常见的诈骗广告类型。该视频是由Ori Bejerano创作的，他在Instagram简介中自称为生成式AI专家。他的原始帖子包含了一条说明，指出内容是“通过AI数字生成或更改，以看起来真实”。

