Black Kite，第三方网络风险情报的领导者，今天宣布推出基于AI的网络评估，这是一种自动化解决方案，用于简化第三方网络风险评估。通过其以自动化为导向的方法，Black Kite正在重新定义企业如何评估其供应商生态系统中的风险，以做出明智的决策并增强其供应链的网络弹性。

“管理网络生态系统风险是复杂的，而企业往往面临着不适应当今环境的网络评估过程的进一步挑战，”Black Kite首席产品官Chuck Schauber表示。“简而言之，传统方法存在问题，问卷冗长、等待时间长、流程手动化。安全和业务领导者需要一种以智能驱动的方法。我们通过提供以自动化为主、以问卷为辅的方法来解决这些挑战，从而节省时间，减少摩擦，实现更智能、更快速的决策。”

这款专为企业赋能的产品旨在消除手动工作，将评估时间从数月压缩至数分钟，并提供更准确、智能驱动的洞察。Black Kite解析供应商文档，利用信任中心数据，并将所有信息映射到行业框架。如果仍然存在差距，团队可以选择发送针对性问卷。此外，基于AI的网络评估直接集成到Black Kite平台中，将传统的繁琐和不一致的评估工作流转变为自动化、可审计和可扩展的流程。

关键特性和好处包括：

- 在每个步骤上的自动化：利用现成的情报和文档立即启动网络评估，利用AI读取、提取和总结文档，消除手动审核任务，并通过自动化文档与控制的映射和响应生成来加快风险工作流。
- 准确的、风险驱动的洞察：分析由从业人员撰写的强大、审计质量的文档以提取可信数据，使用AI和嵌入式智能将技术发现映射到评估控制，呈现可操作和可验证的风险，而不仅仅是供应商自我报告的风险，并利用Open FAIR™量化供应商风险以支持业务决策。
- 协作与跟踪：通过The Bridge™工作区与内部团队和供应商共享发现，建立持久的工作区以集中存储文档、发现和沟通，并监控变化以便审计准备。

凭借这一新产品，Black Kite颠覆了传统模型，通过从一开始就以自动化为起点，利用AI来简化和扩展供应商风险评估。现在，评估以智能为起点，而不是电子表格。

“作为一家医疗保健组织，评估第三方网络风险时速度和准确性至关重要。Black Kite的基于AI的网络评估使我们能够快速评估文档，获得可操作的洞察，并在一个平台上查看全面的第三方风险概况，”堪萨斯大学医疗系统的高级网络安全分析师Jennifer Blackburn表示。

Black Kite的AI驱动网络评估作为一个组件提供，包括评估、扩展和监控。

