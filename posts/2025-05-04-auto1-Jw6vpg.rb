title: '更胜一切：人工智能如何使人类变得无关紧要'
date: 2025-05-05
author: ByteAILab

---

现在，大多数大型人工智能实验室都有一个团队，专门研究叛逆AI如何逃避监督，或在没有人类的情况下秘密合作。

---
但我们失去对文明控制的一个更加平凡的方式是：我们可能会简单地变得无关紧要。这并不需要任何隐藏的阴谋——如果人工智能和机器人技术持续进步，这就是默认的结果。那么，怎么会出现这种情况呢？人工智能开发人员正沿着轨道不断构建人类几乎在每一个角色中都能被更好的替代品取代的工具：不仅是在经济上作为工人和决策者，还在文化上作为艺术家和创作者，甚至在社交上作为朋友和恋爱伴侣。当人工智能能够做我们所做的一切，而且做得更好时，人类将会有何处可去？

关于人工智能当前能力的讨论有时听起来就像是营销炒作，虽然其中确实存在一些。但是从长远来看，改进的潜力是巨大的。您可能会相信总会有一些独特的人性特质是人工智能无法复制的。我在人工智能研究中工作了20年，从基本推理到解决复杂科学问题，目睹了它的进步。曾被视为独特人类范畴的能力，如处理模糊性或使用抽象类比，现在都能轻易处理。虽然可能会有延迟，但我们应该假设人工智能将继续在各个领域取得进展。

这些人工智能不会仅仅帮助人类——它们将在无数细微的方面悄然接管，最初是因为它们更便宜，最终是因为它们的表现真的超过了我们的最佳表现者。一旦它们足够可靠，它们就会成为几乎所有重要任务的唯一负责任选择，从法律裁决、金融规划到医疗决策。

我们很难合理化花双倍的费用聘用一个表现还不如半个AI的治疗师、律师或教师。

在就业方面，最容易想象未来的样子：您会听到朋友失业，并在寻找新工作时遇到麻烦。公司将在期待明年更优秀的AI工作者的同时冻结招聘。您自己工作的更多部分将包括接受可靠、迷人、乐于助人的AI助手的建议。您将有更多时间考虑大局，但发现与您超 knowledgeable的AI助手进行对话。这个助手将补充您计划中的空白，提供相关的数字和先例，并建议改进。最终，您只需问它：“接下来我应该做什么？”无论您是否失去工作，您会发现自己的意见变得可有可无。

在工作之外，情况不会有任何不同。甚至一些人工智能研究员也惊讶于，第一批具备一般推理能力的模型——ChatGPT和Claude的前驱——竟然能具备机智、耐心、细腻和优雅的社交技能。但现在很清楚，机器也能像其他一样学习社交技巧。人们已经拥有AI浪漫伴侣，AI医生在床边举止上的评价一贯优于人类医生。

当每个人都可以获取无尽的个性化爱与指导支持时，生活将会怎样？您的家人和朋友将比以往更沉迷于屏幕。当他们和您交谈时，会跟您讲述他们的在线伙伴所说的有趣而令人印象深刻的事情。

也许您会对他人对新伴侣的偏好感到不快——在这种情况下，您可能会向每天的AI助手寻求建议。这位可靠的顾问将巧妙地帮助您应对面临的问题，并帮助您进行与家人谈论艰难问题的练习。在经历这些相对疲惫的互动后，参与者可能会通过与各自的AI知己交谈来放松。也许我们会同意，在这种向虚拟伴侣转变的过程中，某些东西已经失去了，尽管我们开始发现原始的人际联系与之相比变得更加令人厌烦和乏味。

那么，这一切都是反乌托邦吗？但我们难道不能简单地选择不这样使用人工智能，更偏爱人类顾问和人作的商品和服务？问题是，在许多领域，甚至很难察觉到人工智能的替代，而我们所注意到的部分大多数会看起来是显著的改进。即使在今天，AI生成的内容也越来越难以与人类创作的工作区分开。很难合理地为一个表现还不如一半AI的治疗师、律师或教师花费双倍费用。选择缓慢而昂贵的人类的组织必然会被选择快速、便宜、可靠的人工智能的组织所超越。

我们能否依靠政府在这些问题出现时加以解决？不幸的是，他们也将拥有依赖人工智能的动机。政治家和公务员也会问他们的虚拟助手：“我该怎么做？”发现将人类参与决策会导致延迟、误解和争吵。

政治理论家有时谈论“资源诅咒”，即拥有丰富自然资源的国家最终变得更加专制和腐败——沙特阿拉伯和刚果民主共和国便是很好的例子。这个观点是有价值的资源使国家对公民的依赖减少，这反过来又使国家更容易完全无视公民。这也可能发生在人工智能这个有效的“自然资源”的情况下。为什么还要投资教育和医疗保健，因为人力资本的回报率更低？

最令人不安的可能性是，这一切在我们看来可能是合情合理的。数十万已经爱上了当前原始状态下的AI伴侣，这些AI将提供超有说服力、迷人、成熟和幽默的理由，为什么我们日益减弱的相关性实际上是进步。人工智能权利将被视为下一个重大公民权利事业。持有“人类优先”立场的群体将被描绘成历史的错误一方。

最终，在没有人预计或选择的情况下，我们所有人可能会发现自己在拼命保持金钱、影响力，甚至相关性。这个新世界在许多方面可能会更加友善和人道：人工智能将处理令人厌烦的任务，提供根本更好的商品和服务，如医疗和娱乐。但是人类将成为增长的拖累，如果我们的民主权利开始减弱，我们将无力保护它们。

这些技术的开发人员难道没有更好的计划？令人震惊的答案是没有。Anthropic的首席执行官达里奥·阿莫德伊和Open AI的首席执行官萨姆·奥特曼都同意，一旦人类劳动不再具有竞争力，我们将需要彻底重组我们的经济体系。但还没有人明确知道这将是什么样子。那些承认可能发生根本改变的人大部分都在致力于解决来自人工智能的误用或秘密勾结的更直接威胁。而许多经济学家，例如诺贝尔奖获得者约瑟夫·斯蒂格利茨，已经发出了警告：人工智能可能将人类工资压至零，但许多经济学家似乎不愿考虑它可能永远是一种对人类劳动的补充。

我们该如何避免逐渐的失势？第一步是谈论它。记者、学者和其他思想者在这个庞大的话题上奇怪地保持沉默。我个人发现很难清晰思考它。说“我害怕未来，因为我无法竞争”听起来很软弱和屈辱。说“你应该担心，因为你将变得无关紧要”听起来不满。说“你的孩子们可能会继承一个没有他们位置的世界”听起来绝望。

人们容易直接跳跃到诸如“我总会有特殊的优势”或“我是谁，能阻碍进步？”的拒绝。这一明显的想法是根本不去构建通用人工智能。虽然减缓其发展在很大程度上是可行的，但是试图长期全球停止发展可能需要近乎专制的监控和控制，或者全球协调来拆除大部分计算机芯片制造行业。采取这一道路的主要因果关系是，政府可能禁止私人AI开发，但仍可能为军事和警务目的开发它，从而延迟我们的无关紧要，但在此之前已经剥夺了我们的权力。

如果我们开始看到危机的迹象，我们需要及时介入，至少减缓进程。

如果我们无法停止人工智能的发展，至少有四件事仍然可以帮到我们。首先，我们应当尝试追踪人工智能在经济和政府中的应用和影响。我们需要知道人工智能何时正在取代人类经济活动，尤其是在大规模用于游说和宣传时。Anthropic最近的经济指数是首次尝试，但仍有很多工作要做。

其次，我们需要对前沿人工智能实验室和部署进行一些监督和监管，以防技术在我们还在理解正在发生什么的情况下，积累过多的影响力。目前，我们依赖自愿努力，无法协调停止自主人工智能控制大量资源或聚集力量。如果我们开始看到危机的征兆，我们需要能够及时介入，从而减缓进程，尤其是在一些人和群体获得利益对整个社会产生损害的情况下。

第三，我们可以利用人工智能增强个人的组织和自我倡导能力。人工智能支持的预测、监督、计划和谈判为设计和实施更值得信赖的机构提供了可能性，如果我们能够在仍然拥有影响力的时候建立它们。例如，有条件的预测市场和人工智能支持的预测可以清晰地表明，在不同政策下世界的发展方向，有助于解决某些问题，比如“如果这项政策成为法律，平均人类工资在三年内会如何变化？”对人工智能支持的民主机制进行实验将让我们原型出在快速变化的世界中所需的更具回应能力的治理模型。

最后，如果我们希望构建强大的人工智能而不被边缘化，我们面临的是一个巨大的任务：学习如何引导我们的文明，而不仅仅是让政治体系根据面临的各种压力演变。这种随意应对在过去是可以接受的，因为无论如何人类都被需要。在缺乏这一保障的情况下，我们将漂泊不定"，除非我们明白权力、竞争和增长是如何运作的。"人工智能对齐"的技术领域旨在确保机器与我们目标的一致性，其范围需要扩大到包括政府、机构和整个社会。这一新兴的领域，通常称为“生态系统对齐”，可以借鉴经济学、历史和博弈论，帮助我们理解我们可以合理期待的未来类型，以及如何为之努力。

我们越能清晰地看到我们所走的方向，而且越能进行良好的协调，我们就越有可能创造一个人类依旧相关的未来——不是作为人工智能的竞争者，而是作为它们的受益者和管理者。就目前而言，我们正急于打造自己的替代品。

