        title: 'OnDefend增强BlindSPOT以实现实时威胁响应测试'
        date: 2025-06-03
        author: ByteAILab

---

        为了在渗透测试之间提供安全领导者所需的操作保证和可视化，OnDefend，这一主动网络安全控制验证的领导者，宣布在其漏洞和攻击模拟（BAS）平台BlindSPOT™中推出两项新功能：威胁检测验证和威胁响应验证。![图片](https://ai-techpark.com/wp-content/uploads/OnDefend.jpg){ width=60% }

---

        
        这些新功能使组织能够持续测试其检测工具（如端点检测和响应（EDR）、扩展检测和响应（XDR）以及安全信息和事件管理（SIEM））以及其内部安全运营中心（SOC）团队和托管检测和响应（MDR）提供商在现实场景中的表现是否符合预期。
        
        BlindSPOT模拟映射至MITRE ATT&CK框架，并测量平均检测时间（MTTD）、平均响应时间（MTTR）和供应商服务级别协议（SLA）的遵守情况。该平台还包括实时警报监控、升级路径验证和即时故障通知——帮助团队在攻击者之前捕捉到故障。
        
        “安全领导者告诉我们，他们不仅想要通过合规性测试——他们想要证据证明其投资正在保护他们，”OnDefend的首席执行官兼联合创始人Chris Freedman表示。“BlindSPOT现在为他们提供了检测工具和响应团队按预期提供服务的持续验证。”
        
        这些新功能补充了渗透测试，后者仍然对于识别周边漏洞至关重要。虽然渗透测试揭示了攻击者是否能突破，但BlindSPOT验证了接下来会发生什么——警报是否触发以及团队是否响应。
        
        Freedman表示：“渗透测试就像检查您家中的窗户和门锁。但它并不能告诉您，如果警报触发，响应团队是否会被通知。”  “BlindSPOT持续测试检测和响应能力，以证明您的组织得到了保护。如果您的家庭安全提供商每天都这样做，您晚上睡得会不会更安心？”
        
        为了减少复杂性，OnDefend还提供BlindSPOT作为全托管服务——无需购买软件、构建红/紫队或管理持续的模拟内容，即可实现BAS级别的结果。
        
        无论作为SaaS平台自我管理，还是作为服务提供，BlindSPOT都提供可衡量的保证，确保检测和响应的投资正在按预期工作，并带来有意义的投资回报。

