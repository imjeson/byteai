title: 'Vianai与SAP合作整合生成AI工具'
date: 2025-05-25
author: ByteAILab

---

这次合作建立在SAP Joule助手、SAP Analytics Cloud的“只需询问”功能和Vianai的hila的优势之上，旨在从根本上改进企业数据的分析。![图片](https://ai-techpark.com/wp-content/uploads/Vianai.jpg){ width=60% }

---
Vianai Systems, Inc. 今天在佛罗里达州奥兰多的SAP Sapphire大会上宣布，将与SAP合作，将SAP Joule助手、SAP Analytics Cloud的“只需询问”功能和Vianai的hila的最佳生成AI能力带给全球客户，利用作为基础的SAP Analytics Cloud，这是SAP商业数据云解决方案的一部分。

结合Joule和“只需询问”加上hila将允许一个自然语言界面，支持跨所有结构化数据的生成AI——包括SAP Analytics Cloud中的数据立方体和分析模型、第三方数据及其他原始的、事务性的非聚合数据。

SAP SE的首席技术官兼首席AI官Philipp Herzig博士表示：“SAP致力于推进企业级人工智能，以创造实际业务影响。我们与Vianai的合作反映了我们推动会话分析边界的努力——提升决策精度，扩展SAP的AI能力在企业及其之外的拓展。”

Vianai开发了先进的反幻觉和深度分析技术，专注于从原始结构化数据中获取高度准确的结果，同时保持响应性和低成本。

Vianai的首席执行官兼创始人Vishal Sikka博士表示：“我们很高兴与SAP合作，将我们的hila产品与Joule、‘只需询问’功能以及SAP Analytics Cloud的基础结合起来。这一突破性合作承诺将可靠、可扩展的生成AI置于全球企业用户的手中，使他们能够通过流畅地与数据互动实现巨大的价值。”

这一倡议验证了利用AI、大语言模型和智能技术在商业数据分析中变革性解决方案的巨大市场机会。此次合作将通过使商业用户能够利用他们的SAP投资，直接与他们的结构化数据资产进行准确快速的交互，从而帮助解锁SAP和Vianai解决方案的潜力，同时保持安全性和低成本。生成AI驱动的分析代表了数据驱动决策的新纪元，赋予全球企业利用数据的增长、优化和创新力量，前所未有的简单性和信任，体现了技术作为强大人类放大器的共同愿景。


