标题为“Chartis市场观点：ESG和气候风险调查”，分析了全球金融机构如何将ESG和气候风险因素整合到其风险管理和投资决策流程中。
该调查收集了来自77家ESG和气候风险从业者的见解，这些从业者代表着总资产从10亿美元到5000亿美元不等的金融机构，这些金融机构位于亚太地区、北美、欧洲和中东北非地区。发现超过72%的金融机构计划在ESG技术上投入50万美元或更多。
大多数公司每季度审查其ESG战略，年均花费25万美元至50万美元，其中北美和欧洲的机构可能超过50万美元。
在ESG方面，52%的受访者表示，监管合规是首要挑战，其次是风险评估（48%）。对于气候风险，挑战包括满足监管压力测试期望（67%）、准确的温室气体会计（56%）以及将气候风险在产品线中进行操作性整合（50%）。
BCT Digital的CEO Jaya Vaidhyanathan表示，“我们很高兴提供这一调查，揭示了ESG和气候风险领域的趋势、挑战和优先事项，回答了技术市场的发展、人口对风险规划的影响以及公司战略议程的主要驱动因素等问题。ESG和气候风险报告标准存在不一致性；不同国家可能有自己的框架。这种差异使跨国公司难以保持一致的报告。我们准备应对ESG和气候风险市场的不断增长需求，并根据这一调查，对于解决这两个领域的交集充满信心。”

