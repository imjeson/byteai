Global ESG and Climate Risk Spending Trends Unveiled in Survey that Highlights Regulatory Compliance as Top Challenge

BCT Digital, 一家全球数字转型公司，提供金融科技、监管科技和可持续科技解决方案，在与Chartis Research合作的基础上，公布了他们广泛的ESG和气候风险调查结果。![图片](https://ai-techpark.com/wp-content/uploads/2024/07/Over-72-960x540.jpg){ width=60% }

