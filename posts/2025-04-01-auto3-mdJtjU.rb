作为数字身份验证、合规和欺诈预防的领先人工智能（AI）提供商，Socure今天宣布，其Socure For Government（SocureGov）平台已获得联邦风险与授权管理计划（FedRAMP®）的“中等”授权。

美国政府问责办公室（GAO）在2025年的高风险系列更新中强调，通过减少对税收、补助金、应急救助和医疗保险等关键项目的不当付款和欺诈，可能节省数十亿美元，同时提高政府的效率和效果。SocureGov利用人工智能帮助机构解决导致身份欺诈和不当付款率上升的诸多挑战，同时在规模上提供安全和服务。

通过SocureGov，各机构将通过一个单一平台获取先进的数字身份验证和欺诈预防能力，以实现对美国公众简单、无缝和安全的验证流程。该平台的核心是应用人工智能和机器学习技术，实时分析和关联个人数字身份的每一个方面，准确验证身份和银行账户，同时在欺诈发生之前检测和预防欺诈。

这一里程碑凸显了Socure在促进公共部门采用准确数字身份验证以打击欺诈并保障联邦、州和地方信息安全方面的持续承诺。Socure目前已被34个州机构和3个联邦机构使用，阻止每年数百亿美元的欺诈损失，同时不影响公众获得依赖的政府福利和服务的机会与包容性。

“我们正站在身份与欺诈预防的一个决定性时刻——一个不再容许自满的转折点。 adversaries正在进步，而我们也是如此。随着Socure获得FedRAMP授权，联邦机构现在可以利用最先进的身份验证技术来对抗这些对手，保护我们的国家，消除纳税人的欺诈损失。现在是采取行动的时候——因为欺诈预防不仅是必要的，更是一项责任，”Socure公共部门负责人Jordan Burris说道。

Socure的平台为公共部门机构提供：

- 更高的自动批准率——主流人群达到99%，对于难以识别的人群（包括Z世代、千禧一代和新移民）最高可达94%，而传统提供商的验证率仅为65%-75%。
- 更有效的欺诈预防，在最具风险的5%用户中捕捉99%的第三方身份欺诈，而传统提供商仅能阻止40%-55%的第三方身份欺诈攻击。
- 通过全自动身份验证和欺诈预防，减少人工审核和不必要的视频裁判成本，其响应时间仅为毫秒，而传统提供商可能需要几周的时间来验证身份或要求个人面对面验证。
- 一个完全自动化的文档验证预测分析平台，处理约1%的人口不能自动验证的身份验证，而传统提供商则要求25%-35%的人口经历繁琐的验证过程。

随着越来越多的政府服务转向在线，并采纳前瞻性的方法来打击身份欺诈，Socure准备将其来自私营部门的最佳分析和技术带入帮助构建未来的在线政府项目中。

FedRAMP®消除了联邦机构在采用创新云解决方案前进行冗长的安全审查和授权程序的负担。Socure还于2024年1月被列入州风险与授权管理（即GovRAMP）项目的授权产品清单，展示了其满足GovRAMP合规核心监管及行业定义要求的能力。该产品还被列入Kantara信任状态列表，作为批准的IAL 2组件服务。

