NordVPN，一家领先的网络安全公司，与全球技术领导者华硕展开合作，将全球最受欢迎的VPN服务集成到华硕网络产品中。![图片](https://ai-techpark.com/wp-content/uploads/2024/07/ASUS-presents-960x540.jpg){ width=60% }

---
华硕路由器将是市场上第一款集成NordVPN服务的产品，提升互联网用户的网络安全和隐私保护。

“无论用户身在何处，使用何种设备，隐私和安全始终是我们最关注的事项。我们希望通过让客户能够轻松在他们的路由器上设置VPN服务来增进我们对构建更美好互联网的贡献。与华硕的合作使我们能够提升每个人的安全性 - 无论是使用便携式网络设备的旅行客户，还是视频游戏爱好者，” NordVPN产品战略负责人Vykintas Maknickas 表示。

“华硕致力于为用户提供更安全、更多样化的互联网体验。我们很高兴与NordVPN合作，将VPN服务集成到我们的网络产品中。设备内的各种VPN配置选项为NordVPN的灵活部署提供了可能，创造私密、安全、高速的互联网浏览体验，” 华硕企业副总裁、网络与无线设备业务部总经理邓天龙表示。

华硕RT-AX57 Go 是首批集成NordVPN服务的路由器。它具有三种连接模式，非常适合旅行、工作和家庭使用。其紧凑大小确保便携性。而华硕GT-AX11000 Pro 则专为希望保持多个设备连接并获得出色游戏性能的玩家而设计。这两款路由器均配备内置的AiProtection和NordVPN。后者的服务可以通过自助页面输入订阅凭据轻松设置。

虽然这些路由器是首批集成NordVPN服务的产品，但两家公司的目标是继续合作，并逐步使所有华硕网络设备的用户通过NordVPN服务提升在线隐私和安全性。

