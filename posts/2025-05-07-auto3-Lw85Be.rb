全新的整体应用安全解决方案帮助网络安全领导者和从业者证明和提升他们的网络能力。  
Immersive，作为以人为本的网络安全领导者，今日宣布推出应用安全范围练习，扩展其应用安全解决方案，使其超越动手实验，帮助网络安全领导者和从业者证明和提升他们的能力，作为整体网络准备计划的一部分。该新产品为工程、应用安全和DevSecOps团队提供范围练习，以在工作流程中嵌入安全性，减少摩擦，并在大规模上激发安全开发实践。

企业面临越来越大的压力，要求改善应用安全，但传统的开发者培训未能满足现代软件开发的速度和复杂性。没有相关的、持续的和可测量的培训，工程和应用安全领导者难以证明或提升安全编码实践，或评估他们的团队防止漏洞的准备情况。根据世界经济论坛的报告，去年90%的组织遭遇了安全漏洞，而大多数都将其归因于网络安全技能差距。Immersive应用安全范围练习旨在解决这些技能差距。

### Immersive应用安全范围练习的工作原理

应用安全范围练习模拟真实的工作流程，融入真实的软件开发生命周期。团队协作对实时应用程序中的漏洞进行分类、分配、修复和验证。通过真实、引人入胜且持续的培训，Immersive帮助工程VP和应用安全负责人消除开发安全技能的障碍并在其团队中大规模推动采用。

这些动手实验提供实用的编码挑战，暴露真实的漏洞，而范围练习则模拟真实的补救场景，促进在高压环境下的协作。团队和个人不仅在代码层面进行练习，还跨越整个软件开发生命周期。

“我们提供世界上首个可重复、实际的应用安全练习体验，模拟工程团队如何共同识别和修复漏洞。”Immersive的应用安全首席_subject matter expert_ Chris Wood表示。“我们正在解决工程VP和应用安全负责人的挑战，提供一种有意义的方式来改变开发人员的心态，从将安全视为障碍转变为将其视为软件质量和发布速度的推动者，同时支持可预测性和组织韧性。”

### Immersive应用安全范围练习的好处

该新解决方案提供了一种实用的、基于团队的方法来进行应用安全培训。通过打破DevSecOps团队之间的壁垒，这提升了整体网络韧性，并允许组织将应用安全转变为战略性业务促进者。

应用安全范围练习解决方案追踪个人和团队的熟练程度，提供有关完成情况、尝试、准确性和常见失败点的详细见解—这支持开发团队安全技能的提升。此外，它测量开发人员识别和修复漏洞的速度，使领导者能够确定交叉功能应用安全能力中的优势和劣势，以推动整个软件开发生命周期内的实际安全改进。

扩展产品的关键好处包括：

- **提高生产力：**通过将安全嵌入日常工作流程，加快安全代码交付，减少由于后期漏洞发现和补救所带来的延迟。
- **优化培训投资回报：**通过数据驱动的见解支持持续增长，构建对复杂威胁的实际准备。
- **降低成本：**避免昂贵的返工和高级人才招聘，使高级开发人员能够专注于更具战略性的项目。
- **改善合规性：**通过实际演练确保团队遵守行业标准，满足安全编码要求，降低风险。
- **统一安全性：**通过打破DevSecOps团队之间的壁垒，提升整体网络韧性。

