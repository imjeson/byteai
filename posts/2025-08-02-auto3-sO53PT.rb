title: 'Datasite任命Matthew Steinhilber为总法律顾问'
date: 2025-08-03
author: ByteAILab

---

Datasite®，全球领先的AI驱动的工作流程协作和自动化解决方案提供商，今日宣布Matthew Steinhilber被任命为总法律顾问，以进一步增强Datasite在技术、法律与商业战略日益交织的快速发展环境中的地位。![图片](https://ai-techpark.com/wp-content/uploads/Datasite-Appoints.jpg){ width=60% }

---

在这个职位上，Matt将负责Datasite的全球法律事务，包括商业、诉讼、知识产权和公司事务，直接向Datasite的总统兼首席执行官Rusty Wiley报告。
“我们很高兴欢迎Matt加入我们的领导团队，”Wiley说。“他带来了丰富的法律专业知识和经过验证的战略顾问经验，这在我们继续增长并应对日益复杂的监管环境时将是无价的。我相信他的指导将帮助我们继续保持最高的诚信、透明度和问责标准。”
Matt拥有超过20年的法律专业知识和领导经验。他曾在多个组织担任领导角色，包括担任ACA Group的总法律顾问，最近还在Blueflame AI™担任总法律顾问，这是一家为金融服务公司提供智能代理AI解决方案的领先供应商，而Datasite最近收购了该公司。
Matt拥有穆伦堡学院的环境科学学士学位和马里兰大学法学院的法学博士学位。

