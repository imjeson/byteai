        Atera，集成AI代理的全能IT管理平台，今天宣布推出IT Autopilot，这是首个真正的自主IT解决方案，旨在根本改变IT专业人员的工作方式。![图片](https://ai-techpark.com/wp-content/uploads/Atera.jpg){ width=60% }

---


        在Atera的AI Copilot取得巨大成功之后，该产品使IT团队每位技术人员每周节省11-13个小时的工作时间，IT Autopilot进一步迈出了关键一步，消除了在人力干预下解决密码重置、重启和其他单调、耗时请求的需要。IT Autopilot始终在线，始终可用，能够处理高达40%的IT工作负载，使服务水平协议（SLA）的响应时间低至0.1秒，平均解决时间仅为15分钟。

        对于在不断提升工作效率的压力下的IT技术人员来说，IT Autopilot提供了立即的、切实的解脱。通过卸载最乏味和重复的任务，IT Autopilot有效地充当了一个Tier 1工程师，使IT团队能够将注意力从低价值、重复性任务转移到高影响力的问题解决、项目工作和创新。

        这一创新在当前IT专业人员面临的关键时期推出。随着工单数量激增、员工短缺，以及组织对“以更少做更多”的压力，这种情况已成常态。Atera与Censuswide合作的最新调查数据显示，85%的IT专业人员认为，如果将Tier 1工单从他们的工作中移除，四天工作周是可能的。

        “IT专业人员是现代组织中最重要但又负担沉重的成员之一，”Atera的联合创始人兼首席执行官Gil Pekelman表示。“借助IT Autopilot，我们不仅在自动化任务，还在让技术人员重新获得他们的时间、焦点和工作生活平衡。这是IT新时代的开始：在这个时代，自主代理将成为IT技术人员蓬勃发展的重要支持系统。”

        Atera的IT Autopilot作为IT团队的自然延伸，在严格预配置的防护措施下，与最终用户自主沟通并解决问题——确保隐私、合规，并严格遵守组织的界限。当需要时，它会智能地升级超出其范围或需获得批准的问题。

        IT Autopilot的关键特性包括：

        - 无需技术人员干预即可端到端解决密码重置和重启等Tier 1工单
        - 在严格的预配置防护下与最终用户直接互动
        - 通过自动化低层次、高频率的问题节省每周工作时间
        - 更快的工单解决，提高了组织的效率，提升员工体验
        - 真正的自助IT，使员工和客户在不等待人力支持的情况下自行解决问题

        虽然许多供应商用AI术语重新命名现有的自动化工具，Atera的IT Autopilot则作为一个真正的自主AI系统独树一帜。IT Autopilot符合定义自主AI系统的严格标准，如记忆之前的行动、规划、应用接口、多代理协作、经验学习和完全自主。

        “IT Autopilot不仅仅是一个新功能，而是一个范式转变，”Atera的联合创始人兼首席技术官Oshri Moyal表示。“尽管行业充斥着AI的噱头和半成品，IT Autopilot则带来了真正的自主IT。我们构建了一个能够自主计划、行动、学习和改进的系统，就像一名熟练的技术人员。这不仅是自动化，而是自主。”

