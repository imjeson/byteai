伊隆·马斯克最近对英国政治的干预令人震惊。我发现他呼吁进行英国公共调查是相当引人注目的。作为一名媒体律师，我想到了2011年为调查电话窃听丑闻而成立的莱文森调查，调查媒体与政治之间的关系。莱文森发现，“证据清楚地表明，在过去的30-35年，甚至更久，英国国家政府和官方反对党的政党与媒体之间的关系过于密切，而这种关系不符合公众利益”。他发现这种联系造成了利益冲突的感知（在两个方向上：媒体对政治家的问责责任以及政治家对媒体影响等问题的监管和媒体政策的责任）。他建议采取措施来提高对这些关系的透明度。

