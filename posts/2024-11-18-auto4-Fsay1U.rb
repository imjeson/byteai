2018 年，扎克伯格在美国国会听证会上接受了 2 天共 10 小时的质询；2023 年初，TikTok CEO 周受资也参与了类似的听证会。

两次听证会结束后，各种梗图在互联网弥漫开来，核心围绕一个观点：白宫政客们，是真玩不明白社交媒体，怎么能问出如此基础的问题，怎么能对科技公司如此无知？

白宫似乎不懂硅谷，马斯克如今却成了二者的交叉点。

---


马斯克向来特立独行，在深蓝的加州硅谷，他在今年旗帜鲜明且真金白银地支持特朗普，阵仗堪比专业政客，《纽约时报》笑称马斯克是特朗普的「第一哥们」，如今特朗普重回白宫，马斯克也将要迎接他的新身份：部长。

11 月 12 日，马斯克已被特朗普任命与维韦克·拉马斯瓦米一起领导新成立的「政府效率部」，负责确定新的预算削减方案。

该部门此前并不存在，成立目的是「摧毁政府官僚主义」，很难不联想到马斯克收购 Twitter 后，大刀阔斧裁掉 X 85% 的员工的举措。

不用联想，这部门就是马斯克提议设立的，此前马斯克呼吁特朗普当选后，要减少联邦政府的浪费支出，成立「政府效率部」，自己愿意为此效力。

请愿网站，口号是「Make Musk Advisor On AI」｜图源：ARI

在政府效率部部长后，马斯克可能还要迎接他第二个官职。

几天前，非营利机构美国人工智能倡导团体「负责任创新美国」（ARI）发起了一份公开请愿书，呼吁特朗普任命马斯克为他的人工智能特别顾问，ARI 在请愿书中写道：

马斯克完全有能力保护美国在人工智能技术方面的领先地位，同时确保其安全应用。

请愿书中写到「马斯克是人工智能进步的先驱，也是持续为人工智能潜在风险敲响警钟的人，他知道要让美国在人工智能方面领先，需要做些什么。」

正逢周末，马斯克的 xAI 放大招，不仅曝光了最新一轮 60 亿美元的融资，xAI 估值已超 500 亿美元。似乎这个 AI 顾问，非马斯克莫属了。

而「马部长」，会不会取代宿敌 Sam Altman，成为美国乃至全球 AI 第一人？

