title: 'Hypertec Cloud收购5C数据中心，成立5C集团'
date: 2025-04-13
author: ByteAILab

---

在一个反映人工智能与数据中心基础设施快速融合的里程碑事件中，领先的人工智能云平台Hypertec Cloud收购了5C数据中心，这是一家拥有超过2吉瓦（GW）路线路径容量的顶级共置和数据中心提供商。![图片](https://ai-techpark.com/wp-content/uploads/5C-Group.jpg){ width=60% }

---
此次战略收购标志着Hypertec Cloud从母公司Hypertec Group的分拆，以及5C集团的成立——这是一个专注于大规模构建下一代人工智能基础设施的新独立实体。

Jonathan Ahdoot被任命为新成立的5C集团的首席执行官，该集团将Hypertec Cloud的高性能云计算能力与5C数据中心在美国的广泛足迹相结合。两者共同形成了北美最大的、灵活的人工智能数字基础设施提供商之一，旨在构建将推动行业未来的下一代人工智能工厂。

此次收购基于三个关键战略目标：
1. 提供综合的人工智能数字基础设施解决方案：通过结合Hypertec Cloud和5C数据中心，5C集团将提供集成的数据中心和人工智能基础设施，满足人工智能前沿实验室、人工智能原生企业和大型企业客户的需求。
2. 在受限市场中加速部署速度：随着人工智能部署需求的增长和大规模数据中心容量的限制，合并的平台将能够加快超大规模人工智能集群的上市时间。集成的计算和数据中心设计将显著缩短部署时间，并实现协调的基础设施推出。
3. 驱动性能和成本优化：借助Hypertec在硬件和基础设施设计、白手套支持和可持续工程方面的数十年经验，新实体将整合先进的冷却技术——如直接到芯片的液体冷却和浸没冷却——以支持更高的机架密度和节能。

“当今人工智能基础设施面临的最大挑战是对齐——在人工智能计算需求与物理托管环境之间的对齐。我们通过将这两个方面整合在一起解决了这个问题，”5C集团首席执行官Jonathan Ahdoot说道。“通过此次收购，我们将性能优化的人工智能计算基础设施的深厚专业知识与大规模、世界级数据中心校园的设计与操作结合在一起。这种组合使我们能够加快部署，进一步推动密度，并为最庞大、最苛刻的人工智能用户提供无与伦比的效率。”

“Hypertec Cloud的分拆标志着Hypertec Group的一个重要时刻，”Hypertec Group首席执行官Simon Ahdoot补充道。随着对5C数据中心的收购，新成立的5C集团有望提供为明天的行业设计的人工智能数字基础设施，满足规模、速度和复杂性的要求。作为合作伙伴，我们将继续推动计算、存储和数据中心技术的发展，提供真正的价值，以推动人工智能创新的未来。”

5C集团在美国的人工智能优化数据中心校园组合包括超过600兆瓦（MW）的容量——这是其总2吉瓦（GW）容量的一部分，预计在未来6到18个月内将为新的和现有的人工智能数据中心共置与计算客户提供可用。该平台专为满足人工智能工作负载的极端密度、电力和冷却需求而构建，为未来的人工智能创新奠定了基础。

