而所有答案都不止于开发团队。今天，BlueSky IT Partners宣布推出其人工智能治理加速器：一个指导性的快速启动程序，旨在帮助财富50强到1000强的CIO、CISO和CSO评估、实施和操作人工智能项目，而不将其业务置于风险之中。

“有一种正确的方式，也有一种冒险的方式来实施人工智能，”BlueSky IT Partners首席执行官梅格·图普斯(Meg Toups)说。“好消息是：同时快速和智能地推进是可能的。你不必花费一年的时间来开始，但确实需要评估和了解风险及结果，以便安全且大规模地部署。”

根据波士顿咨询公司和麻省理工学院斯隆管理评论的一项2024年研究，74%的公司在实现和扩展人工智能的价值方面面临困难，完全部署后70%的项目几乎没有产生影响。这一趋势得到了行业分析师高德纳(Gartner)的支持，后者报道称，真正准备好的公司不到10%。

快速行动。优先治理。
图普斯表示，成功的秘诀在于清晰的治理、强大的基础设施基础和跨职能的一致性。这些工作并不简单。IDC的一项新报告显示，88%的人工智能试点项目未能进入生产阶段。BlueSky的人工智能治理加速器消除了这些风险——提供了一种战略性的方法来评估准备情况、识别内部和外部风险，并制定人工智能成功的计划。

该指导流程由了解当前人工智能法律的高级技术战略家领导，并与利益相关者提供多个互动会议，以确保隐私和合规与业务结果相一致。

2024年Zogby Analytics的一项研究发现，使用人工智能的公司中超过95%没有治理框架。该研究对美国、英国和德国的大型公司中600多名CEO、CIO和CTO进行了调查。

“我们的客户需要的不仅是一个平台或工具。尽管我们也提供这些。他们需要了解他们不知道的东西以及最佳实践，”图普斯补充道。“我们不会只是提供一个框架然后说，‘祝好运’。这是你旅程的起点。我们会在执行的过程中与你一起，确保你的成功。”

该程序的初始版本特别针对需要迅速和安全地从探索到执行的中型到大型企业组织。人工智能治理加速器包括：

- 快速的360°人工智能准备和风险评估
- 技术生态系统审查（人工智能用例、数据模型、隐私暴露）
- 与NIST、CISA和全球合规标准一致的治理框架
- 与业务和技术对齐的利益相关者参与路线图
- 精心挑选的潜在人工智能合作伙伴和平台列表
- 适用于C级高管或董事会演示的执行级简报。

“要在人工智能领域取得成功，你必须做的不仅仅是创新，”图普斯说。“你还必须在推动业务的同时保护其安全。合适的合作伙伴理解这一点——并且不会让困难减慢你的步伐。与人工智能相关的、以及我们所有的工作中，我们看到竞争对手往往忽视的东西。”


