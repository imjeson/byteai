图灵奖得主 Yann LeCun 也赶来围观，不过他并不看好这个模型，并表示**人工智能**的炒作已经变成了讽刺。

![图片](https://mmbiz.qpic.cn/sz_mmbiz_png/KmXPKA19gWibOrSA4rRUsPAQe533w3ib6Xsz4wp9n1bA2v08V6Rql8QgYPB6w5yXllDxQoqA2srbIWWx0AXribAKA/640?wx_fmt=png&from=appmsg)

LeCun 之所以这样说，是因为有人问 gpt2-chatbot「一个农夫带着一只羊和一只山羊站在河的左岸，旁边有一条船。船只能装下一个人和两个动物。农夫如何带着羊和山羊到达河的右岸，并尽可能减少旅行次数？」

对于这个问题，正常来说只需一趟就可以了，但 gpt2-chatbot 把答案弄得非常复杂，还分了 5 步完成。

![图片](https://mmbiz.qpic.cn/sz_mmbiz_png/KmXPKA19gWibOrSA4rRUsPAQe533w3ib6XUh6yOM34UmSxeLuq2N9CKTTk2Lj7yiaWWjhW0KuQr8eJ8dlWWdmFs5g/640?wx_fmt=png&from=appmsg)

<sup>图源：https://twitter.com/ylecun/status/1785100806695325804</sup>

可以说，这些测试结果让我们看到了 gpt2-chatbot 在画图、数学、**逻辑**推理等任务上的不俗能力，同时也会出现将简单问题复杂化的情况。但要说它就是 GPT-4 或 GPT-5，有人还是不同意。

![图片](https://mmbiz.qpic.cn/sz_mmbiz_png/KmXPKA19gWibOrSA4rRUsPAQe533w3ib6XKKGIfo1IeJE8TUVQzU47wriaT1gwYJgXuo0AZvqUqianDKb9B7rQT2Yw/640?wx_fmt=png&from=appmsg)

因此，有人猜测 gpt2-chatbot 实际上还是此前的 **GPT-2**，但是使用现代助理数据集进行了微调。在这种情况下，这意味着 OpenAI 最初的预训练效果仍然令人惊叹，并在几年后仍比其他模型更好。

