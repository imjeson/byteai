该节目强调智能手机使用的病态影响很有说服力。然而，有一点缺失：成年人同样被这种疾病感染。没有人应该使用智能手机。它们使那些本该与孩子自然交流的人分心，从而影响年轻人的智力发展。真正的聪明对话即将消亡。只有真正年长的人会注意到这一点，因为只有他们记得。

——蒂姆·沃森（近94岁） 伍斯特

你们关于夜总会禁止手机的文章（摆脱一切：曼彻斯特俱乐部加入禁止手机摄像的趋势，12月14日）让我想知道这是否可以扩展到艺术展览。我最近参观了伦敦国家美术馆的梵高展览。展览人满为患，我根本无法站远处欣赏画作，这种情况被大量人们举起手机拍照或在画前自拍所加剧。也许可以提供无手机的预约时段？

——卡罗尔·格雷 斯坦斯特德·蒙特菲彻，埃塞克斯

