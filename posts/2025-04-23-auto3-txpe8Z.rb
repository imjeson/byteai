PartnerLINK社区为合作伙伴体验提供更多福利和个性化定制
TD SYNNEX (NYSE:SNX) 今日宣布了一项针对其在北美的合作伙伴社区的新专门创新结构，旨在通过有意的连接加强、赋能并推动合作伙伴的增长。![图片](https://ai-techpark.com/wp-content/uploads/TD-SYNNEX.jpg){ width=60% }

---
随着市场的发展，合作伙伴的需求快速变化，这种新的定制化方法能够根据合作伙伴的个体需求和目标，提供量身定制的福利和支持。

PartnerLINK根据合作伙伴的专业领域或地理重点，将成员对齐到新成立的社区中，以促进更强的同行关系。启动时提供的初始社区包括：

PartnerLINK Ascend - 专注于帮助客户利用AI驱动的先进基础设施、安全性、云服务和分析解决方案进行数字化转型的美国合作伙伴。
PartnerLINK Advantage - 旨在改善各行业效率、可靠性和可持续性的美国合作伙伴，专注于推进基础设施现代化。
PartnerLINK Canada - 专注于所有行业和技术的加拿大合作伙伴。
PartnerLINK Public Sector - 针对任何向公共部门销售的PartnerLINK成员的增强选项。

这一新结构建立在以往TD SYNNEX合作伙伴社区（如CommunitySolv）成功和历史的基础上，专注于使合作伙伴在所有技术类别中始终保持未来准备。福利将得到扩展，并针对每个社区提供，提供更大的专业培训、网络和商业构建机会的获取。社区成员还将能够访问早期采用机会、独家激励和专门的支持团队。

“通过社区促进客户增长是我们业务的一个重要组成部分，我们希望以支持我们未来的合作伙伴及其快速变化的商业模式的方法来延续这一传统，”TD SYNNEX收入与客户成功执行副总裁Gary Palenbaum表示。“这种新结构将鼓励更直接的客户参与跨所有渠道，并为合作伙伴提供量身定制、有意义的福利，无论他们在旅程的哪个阶段。”

PartnerLINK将由北美社区高级副总裁Kristi Kirby领导，她致力于合作伙伴的成功，并在合作伙伴参与方面拥有丰富的销售和赋能经验。这一新方法旨在通过增强的福利和工具支持合作伙伴的增长和发展，使他们获得更大的竞争优势。每个社区的福利将围绕四个核心原则进行定制：引领、创新、网络和知识。一个关键的福利是一个新的专门在线中央会员中心，社区成员可全天候进行互动。

现有的TD SYNNEX社区咨询委员会成员对此新方法提供了意见和反馈。美国委员会成员兼纽约市Abacus Group Fintech MSP首席执行官Anthony J. D’Ambrosi分享道：“社区参与的影响对我和我的团队在动态金融市场上导航、投资新技术和扩展新地区的过程至关重要。TD SYNNEX和合作伙伴社区在每一个步骤中都支持和赋能我们。我相信，随着PartnerLINK所提供的新福利的发展，社区结构的演变将为寻求盈利增长的合作伙伴解锁更大的价值。”

加拿大委员会成员兼Oxygen Technologies总裁Brian Oleksiuk补充道：“随着市场的快速变化，来自同行和合作伙伴的观点变得比以往任何时候都重要。获得针对加拿大市场量身定制的扩展PartnerLINK福利，进一步增强了我们参与和融入TD SYNNEX社区所获得的整体价值。”

PartnerLINK对所有北美的TD SYNNEX合作伙伴开放。如欲了解有关新社区及相应福利的更多信息，或参与PartnerLINK，请访问 [TD SYNNEX PartnerLINK](https://www.tdsynnex.com/na/us/PartnerLINK/)。

