title: 'Akamai与P3M合作为英国政府和国防提供零信任ICAM解决方案'
date: 2025-05-07
author: ByteAILab

---

Akamai Technologies（纳斯达克：AKAM），这家支持和保护在线业务的网络安全与云计算公司，和英国一家被政府和关键国家基础设施信赖的网络韧性和转型咨询公司P3M Works，今天宣布了一项战略合作伙伴关系，旨在为英国政府和国防供应链提供零信任身份、凭证和访问管理（ICAM）解决方案。![图片](https://ai-techpark.com/wp-content/uploads/P3M-Works.jpg){ width=60% }

---
该合作将结合Akamai在美国国防部（DoD）网络中成功部署的零信任安全能力与P3M对英国政府需求的深刻理解，以提供强大的供应链安全框架。双方将共同应对保护对敏感系统和数据的第三方访问的日益增长的需求。

“P3M Works与英国政府和国家安全的既有关系，加上Akamai已验证的ICAM技术，创造了一种有力的解决方案，弥补了国防供应链中的一个关键安全漏洞。” P3M Works的创始人Jack Marley表示，“此合作使我们能够实施基于政策的控制，强制执行最小权限访问，同时在复杂的多方合作中提供必要的灵活性。我们正在为需要在每个访问决策中确保、快速和信任的组织带来下一代技术解决方案。”

该合作将结合Akamai在美国国防部多个网络中成功部署的零信任安全能力与P3M对英国政府要求的深刻理解，以提供强大的供应链安全框架。具体而言，重点将放在Akamai的零信任联合ICAM解决方案上，这些解决方案已成功应用于美国陆军。这些解决方案使与供应商、承包商和盟友的安全协作成为可能，而不会暴露底层网络或扩大攻击面。

“基于我们在手机关键系统安全方面的二十年成功经验，我们很高兴与P3M Works合作，将这些解决方案带给英国政府机构及其供应链合作伙伴。”Akamai战略联盟副总裁Martin Hellmer表示。“网络威胁的愈加复杂以及供应链攻击向量的扩展要求我们采取零信任方法，不断验证每个访问请求，无论源或目标。”

Akamai的解决方案使政策引擎能够基于身份、设备健康和行为分析强制执行动态的、上下文感知的访问决策。这种方法通过持续验证所有访问请求，即使在断开、拒绝、干扰、间歇和有限带宽（DDIL）环境中，也最小化了供应链漏洞和内部威胁的风险。

联合提案的关键特性包括：

- 联合身份管理以实现安全的第三方协作
- 零信任政策执行，具有动态的基于属性的访问控制
- 在用户会话期间进行持续的身份验证和授权
- 支持包括硬件令牌和移动身份验证在内的各种身份验证工具
- 对第三方活动进行全面的审计和监控

Akamai和P3M Works将共同应对在复杂和分布式的英国国防环境中对韧性、上下文意识的访问管理的迫切需求。这项合作将在2025年第三季度开始面向英国政府机构及其供应链合作伙伴提供。

Akamai是CYBERUK的主要赞助商，这是英国政府的旗舰网络安全活动，由国家网络安全中心主办。此次活动于5月6日至8日在曼彻斯特中央举行，Akamai将在C10展位展出。

