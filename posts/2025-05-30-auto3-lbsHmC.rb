新实时调试工具专为开发人员和企业设计，以确保生产环境安全，优化故障排除并加快问题解决速度
Dynatrace（纽约证券交易所代码：DT），领先的AI驱动可观测性平台，今天宣布Dynatrace实时调试工具正式发布后受到了用户的积极采用。![图片](https://ai-techpark.com/wp-content/uploads/Dynatrace-U.jpg){ width=60% }

---
首次在2025年1月推出的实时调试工具是首个也是唯一一个在生产环境中无缝调试数千个服务的能力，而不会中断正在运行的代码，并通过其直观的自助工具加速开发人员的采用。

随着企业云和AI应用环境变得日益动态和复杂，传统的应用调试方法已无法满足这些需求。Dynatrace实时调试工具正面面对这一挑战，提供实时、非侵入式的洞察，快速加速问题解决并在大规模上简化性能监控。

早期采用者，包括TELUS，报告称调试时间减少了多达95%，使其成为企业在现代创新要求下不可或缺的工具。实时调试工具支持数千个始终在线的实例和开发者自助，满足最复杂的现代企业在亚马逊网络服务（AWS）、微软Azure、谷歌云平台（GCP）、RedHat OpenShift和其他企业云环境中采用云和AI本地技术的要求。

无论是调试云原生部署还是AI工作负载，实时调试工具都提供了一种可扩展的智能解决方案，可以减少时间、成本和复杂性，使企业能够在不妥协的情况下高速运作。
实时调试工具的主要特点包括：

即时数据访问：按需检索关键代码级数据，而无需添加新代码或重新部署。这消除了可能将调试周期延长数周甚至数月的延迟。实时调试工具通过Dynatrace平台访问生产系统，提供企业级隐私和安全控制用于调试。
非中断式断点：检查应用程序的完整状态，包括变量、堆栈跟踪等，而不会中断或中断其执行。实时调试避免了妨碍性能的传统调试技术。
企业级规模：实时跨数千个工作负载实例同时调试，快速识别未知的未知，加快创新的进程。该技术在后台运行，最小化性能影响，允许高度灵活和全面的覆盖。

引用
根据IDC分析师亚当·雷斯尼克（Adam Resnick）的说法，“开发人员希望拥有现代工具，以最小化他们在调试上花费的时间。处理生产问题时，使用传统调试方法是艰巨的。Dynatrace实时调试工具简化了这个过程，使得调试和解决关键应用问题变得更容易。”
“在使用Dynatrace实时调试工具之前，获取任何关于错误的信息都需要添加日志行、进行部署，并等待发布，”TELUS的首席可靠性工程师达娜·哈里森（Dana Harrison）解释道。“使用实时调试工具，以前需要45分钟的工作现在只需2分钟——调试时间减少了95%。这个工具帮助我们重新定义了如何处理和解决复杂的应用程序错误。”
“Dynatrace实时调试工具为管理日益复杂的云和AI工作负载的企业提供了变革性生产力和洞察，”首席产品官史蒂夫·塔克（Steve Tack）表示。“通过实现非中断式断点的实时调试，我们为开发人员提供所需的数据，同时保护应用程序性能。我们正在解决巨大的市场机遇，赋予开发人员在未来复杂代理AI环境中能够成功的经验。”

Dynatrace实时调试工具最近作为Dynatrace开发者可观测性的一部分提供，这是一整套解决方案，旨在为开发社区提供运行时洞察和故障排除能力。

