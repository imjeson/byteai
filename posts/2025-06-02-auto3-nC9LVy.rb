---

Comport Consulting Corp.，作为医疗行业创新技术解决方案的可信合作伙伴，欣然宣布任命 Michael Volpi 先生为医疗科技副总裁。![图片](https://ai-techpark.com/wp-content/uploads/Comport.jpg){ width=60% }

---
在其新角色中，Volpi 先生为医疗客户提供战略 IT 指导，帮助他们计划和执行复杂的技术举措，以改善病人护理、运营效率和系统韧性。

Michael Volpi 拥有29年医疗 IT 领域的丰富经验。他的背景包括在 Shared Medical Systems（被西门子医疗服务和 Cerner Corp. 收购）中负责 EMR 的基础设施和业务方面；为一个单一实体社区医院提供支持医院运营的 IT；以及担任新泽西州最大的多医院健康系统的技术服务副总裁。在那里，他管理着一个大型IT专业团队，每年监督25到50个重大的系统范围技术现代化项目，包括：

- 并购整合并大幅扩展门诊地点
- 新建8000平方英尺的数据中心和支持研究人员与临床医生的新癌症中心
- Epic EMR 的迁移和实施以及虚拟桌面 TDR
- 有韧性的 WAN 和无线基础设施升级
- 全组织范围内部署 Microsoft Office 365
- 应对停电和危机的准备工作

在 Comport，Michael 致力于利用他的经验帮助其他医疗专业人士保障、简化和优化他们的 IT 运营。他擅长将技术转化为改进患者护理、缩短收入周期、减少错误并延展 IT 预算。他将帮助制定 IT 战略、进行董事会沟通以及管理桌面和网络基础设施、数据中心运营、电信、存储和计算解决方案、韧性以及服务协作与管理。

“我们非常高兴地欢迎 Michael 加入我们的团队，” Comport 总裁 Mike Vencel 说。“Michael 深厚的知识和变革性领导力是我们医疗客户面对复杂挑战时的巨大资产。Michael 将进一步拓展 Comport 多年来在医院领域的经验，深化我们提供的影响。”

Volpi 先生是希望提升其技术战略和表现的医疗组织的免费资源。如需安排会议，请联系 Michael Volpi，网址为 www.comport.com/contact。

