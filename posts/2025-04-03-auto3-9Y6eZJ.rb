Opsera，这一受到全球百强企业信任的AI驱动的DevOps平台，今天宣布已完成2000万美元的B轮融资，由Prosperity7主导，日立风险投资参与。这轮融资标志着Opsera在过去一年内的一系列产品发布后，已成为市场上最灵活、最智能的AI驱动DevOps平台之一，适用于所有应用和团队。主要成就包括与GitHub、微软、AWS和Databricks签订关键行业合作伙伴关系、A+轮融资以来收入增长200%，以及新增多个百强客户。

通过Agentic AI和Unified Insights赋能开发者
DevOps行业正经历由AI驱动的自动化、安全集成以及快速、可靠的软件交付所推动的巨大变革。组织现在优先考虑Agentic AI方法——即自主AI代理系统优化工作流程、预测风险并自我修复基础设施——以保持竞争力。Opsera正在开创Agentic AI，以重塑DevOps工作流程，使软件工程团队能够超越静态自动化，迈向自适应、智能编排。传统DevOps自动化侧重于预定义工作流程，而Opsera的AI驱动方法引入了自主AI代理，这些代理实时动态分析、适应和优化软件交付。

这些嵌入在Opsera的AI驱动DevOps平台内的AI代理，充当智能副驾驶，持续监控和增强软件交付流程和管道。通过学习开发者的互动和活动、管道执行、发布流程和操作瓶颈，这些代理主动推荐优化、自动化复杂决策并自我纠正软件交付生命周期内的低效操作。

Opsera首席执行官Kumar Chivukula表示：“在Opsera，我们正在重新定义DevOps的可能性，凭借我们的AI驱动DevOps平台，赋能各团队，所取得的势头正是我们为客户提供价值的直接反映。我们很感激Prosperity7、日立风险投资以及现有投资者（Clear Ventures、Felicis Ventures、Taiwania Capital、Alumni Ventures）支持我们的愿景，助力组织实现前所未有的敏捷性、可靠性和自动化安全修复。通过Opsera Unified Insights，客户可以获得‘从代码到云’的深度和直观的指标——包括Copilot、DevEx、DORA、安全性、质量、GitHub高级安全等，显著提升软件开发周期和开发者生产力及体验。”

Qualys首席信息官Brad Bell表示：“Opsera的AI驱动DevOps平台彻底重新定义了我们对DevOps的处理方式。Opsera消除了支持我们复杂SaaS平台所需的自定义构建的摩擦。过去需要几周的手动工作，现在只需几小时，加速软件交付80%，解锁新的生产率和灵活性水平。简单来说，Opsera使我们能够更快地创新，更智能地运营。”

Prosperity7的董事总经理Abhishek Shukla表示：“Opsera的AI驱动DevOps平台为前瞻性软件开发组织提供了变革性的开发者生产力和跨整个软件生命周期的全球洞察力。他们与GitHub、Databricks和AWS的合作正在打开新的用例，将继续推动增长，这为我们提供了信心，选择领导这轮B轮融资。”

Clear Ventures董事总经理Rajeev Madhavan表示：“在过去四年半中，Opsera的发展势头不断增强，自我们最初的种子投资以来。他们证明了DevOps类别亟待现代化和整合的原始论点。Opsera继续创新，为其AI驱动的DevOps平台增添令人兴奋的新功能——包括Agentic能力、为Generative AI工具提供全球洞察，如GitHub Copilot、Amazon Q和Cursor，以及所有应用和团队的CI/CD编排。”

Databricks的市场推广集成副总裁Heather Akuiyibo表示：“随着对数据智能需求的增长，Opsera与Databricks的合作正在为客户在AI和LLM部署的旅程中带来简化和加速的部署周期。搭载在Databricks上的合作关系将数据操作和编排现代化。”

IDC研究发现，开发者估计使用AI编码助手的生产力平均提高35%。然而，组织在整个组织中难以跟踪采用情况并衡量这些收益。Opsera的Unified Insights中提供的指标应能帮助组织展示GitHub Copilot采用的投资回报，从而增强其跟踪和量化生产力改善的能力。

最近的关键里程碑包括：

Opsera AI代理：Opsera的Hummingbird AI将分散的DevOps过程转化为统一的AI驱动引擎。通过连接来自GitHub Copilot、Jira、VS代码、云部署和安全工具的洞察，Opsera的AI驱动DevOps平台提供主动的风险缓解（例如，阻止不安全的代码）、端到端可见性（例如，脆弱性到Jira的映射）和自我修复工作流（例如，自动修补操作系统映像）。
新的Unified Insights功能：Opsera的AI代码助手洞察提供有关开发者生产力、采用趋势和AI驱动代码建议有效性的可操作情报。企业可以主动衡量和优化AI代码助手投资的投资回报，同时改善软件交付工具、团队和环境中的开发者体验和生产力。
领导仪表板：作为Opsera Unified Insights的一部分，领导仪表板提供了对内圈（软件开发）和外圈（软件交付）活动、AI采用和AI驱动工具（如GitHub Copilot）影响的详细实时可见性。此外，它提供了开发者体验（DevEx）洞察、DORA指标和应用安全态势洞察——使领导者能够在规模上提升效率、创新、速度、安全和质量。借助这些洞察，组织可以优化DevOps和DevSecOps流程，最大化AI驱动开发效率，并在软件开发生命周期中推动持续改进。
扩展战略合作伙伴关系：

GitHub：Opsera加深了与GitHub的关系，作为ISV合作伙伴提供源码管理迁移服务，并为GitHub Copilot部署提供最先进的洞察和指标。
Databricks：Opsera加入了Databricks的Built on Partner Program和Technology Partner Program。Opsera和Databricks协作实现AI/ML模型部署，并具备安全和合规的保护，以及回滚能力的架构部署。
AWS：Opsera还被纳入Amazon Web Services（AWS）独立软件供应商（ISV）加速计划。通过将Opsera的编排能力与Amazon Q的强大结合，用户可以在AWS部署中解锁新的效率和创新水平。

