根据埃森哲的最新报告，人工智能（AI）正在深刻改变商业格局，然而大多数组织（90%）并未充分准备好应对其未来的AI驱动安全需求。![图片](https://ai-techpark.com/wp-content/uploads/Only-1-in.jpg){ width=60% }

---
埃森哲的“2025年网络安全韧性状态”报告显示，全球近三分之二（63%）的公司处于“暴露区”，这表明他们缺乏统一的网络安全战略和必要的技术能力。

该报告基于对2286名来自全球大型组织的网络安全和技术高管的调查，揭示了快速的AI采纳 dramatically加快了网络威胁的速度、范围和复杂性，远远超过了当前的企业网络防御。例如，超过四分之三（77%）的组织缺乏保护关键商业模型、数据管道和云基础设施所需的基本数据和AI安全实践。

“日益加剧的地缘政治紧张局势、经济波动和愈加复杂的运营环境，以及AI增强的攻击，使组织对网络风险的脆弱性加剧。该报告发出了一个警惕信号，网络安全不再能是事后处理的问题，它必须在每一个AI驱动的倡议中设计和嵌入，”埃森哲安全全球负责人保罗·达尔·辛（Paolo Dal Cin）表示。“采取这种主动的方法将有助于确保竞争优势、增强客户忠诚度，并将网络安全转变为商业推动力。”

尽管AI在企业中的快速普及，只有22%的组织实施了明确的政策和培训用于生成式AI。此外，极少数组织维持全面的AI系统清单，这对于管理供应链风险至关重要。此外，数据保护仍然不足，只有25%的组织充分利用加密方法和访问控制来保护敏感信息。

“生成式AI的快速发展代表着网络安全中一个深刻的范式转变，带来独特的挑战和机遇。通过在设计AI系统时，将安全性放在核心位置并不断监测和更新，组织可以领先于最关键的威胁，”埃森哲全球数据和AI安全负责人丹尼尔·肯齐尔（Daniel Kendzior）说道。“商业韧性需要能够迅速应对破坏性力量的准备，以及对组织有效采取行动的信心。”

研究还显示，在各个地区普遍存在网络安全的未成熟现象，突显出雄心与准备之间的关键差距。只有14%的北美和11%的欧洲组织具有成熟的网络安全态势。在拉丁美洲，77%没有基本的战略和能力，而在亚太地区，71%的组织仍处于“暴露区”，面临着严重的操作和财务风险。

研究根据组织的网络安全战略和技术能力，将其分为三个不同的安全成熟度区域。埃森哲称，顶层的“重塑准备区”仅包含10%的组织，他们具备适应性强的安全态势，这种安全态势会不断演变以应对新兴威胁。中间的27%在“进展区”表现出一定的优势，但在定义战略方向或实施防御上存在困难。面临最大风险的群体，即“暴露区”，占63%的组织，其网络准备有限，且对威胁的态度是被动的。这些条件在今天复杂的AI环境和全球风险因素的影响下愈加严重。

然而，处于重塑准备区的公司面临高级攻击的可能性减少69%，并且在阻止攻击方面的有效性提高了1.5倍。他们在IT和OT环境中的可见性也提高了1.3倍，技术负担减少了8%，客户信任度提升了15%，这表明更强的网络安全实践有助于提升韧性和商业价值。

为了达到“重塑准备区”，需要采取四项关键行动：

1. 开发和部署适合的安全治理框架和运行模式，以符合AI改变世界的现实，确立明确的责任并使AI安全与监管和业务目标保持一致。
2. 从一开始就设计一个安全的生成式AI数字核心，通过在AI开发、部署和操作过程中嵌入安全性来实现。
3. 维护具有安全基础的弹性AI系统，积极应对新兴威胁，增强检测能力，支持AI模型测试，改善响应机制。
4. 通过利用生成式AI自动化安全流程，增强网络防御能力，及早发现威胁，重新塑造网络安全。

