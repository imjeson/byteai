title: '在AI版权辩论中优先考虑艺术家而非科技，国会议员表示'
date: 2025-02-27
author: ByteAILab

---

两组跨党派的国会议员敦促政府优先确保创作者因其创作工作获得公平报酬，而不是简单方便地训练人工智能模型。

---
国会议员们认为，需要对用来训练生成性AI模型的大量数据有更多的透明度，并呼吁政府不要推进要求创作者选择退出其作品被使用的计划。政府对AI与版权法之间紧张关系的首选解决方案是允许AI公司在受版权保护的作品上进行训练，通过给予它们“文本和数据挖掘”的例外，同时让创造者有机会通过“权利保留”系统选择退出。

文化、媒体和体育委员会的主席卡罗琳·迪南奇表示，针对这些提案，创意产业普遍表达了“日益增长的担忧”，这“突显了艺术家面临的威胁——人工智能在未经许可的情况下窃取他们辛勤成果”。她补充道，任由创作作品“成为合法目标，除非创作者明确表示不允许”，就像“窃贼被允许进入你的房子，除非你门口有一块大牌子明确告诉他们盗窃是不允许的”。

“除了对版权的任何更改之外，还需要对用于训练AI模型的数据透明度进行更严格的要求，以便创作者可以清楚地知道他们的作品使用情况，从而获得相应的报酬。”

文化、媒体和体育委员会与科学、创新和技术委员会在本月早些时候与AI初创公司和创意产业代表举行了联合证据会议，随后对政府关于AI和版权的咨询做出了回应。国会议员们给部长们的信中呼吁政府改善训练数据的透明度，使创作者能够识别其作品的使用情况，确保任何选择退出的版权持有者不会因可见度降低而受到惩罚，并使消费者能够对使用哪种AI模型做出明智的选择。

信中警告称，如果没有这些措施，“最深远的影响将会落在已经在财务压力下运营的创作者和记者身上”。在过去几个月中，政府对AI提案的反对声在名人和创意产业中逐渐增长。周二，包括凯特·布什、达蒙·阿尔巴恩和安妮·莱诺克斯在内的1000多名音乐家发起了一张无声专辑以示抗议。

他们的信中还指出，尽管AI开发者建议训练数据构成“商业秘密”，但欧盟和加利福尼亚州已经引入了透明度要求，包括关于训练数据的详细技术记录保存。信中补充道，政府应考虑鼓励那些开发按使用收入分享模式的公司，称这“可以使[生成性]AI走出其‘Napster时代’，就像Spotify在音乐流媒体出现后经历了二十年的点对点数字盗版一样”。

国会议员们还要求政府为咨询中提出的每个选项发布完整的影响评估，并建立强有力的合规性、执法和补救机制，以应对版权问题。信中表示，其他地区，如美国和欧盟，“尚未解决这一问题”，尽管政府担心AI开发者可能会转移到拥有“更清晰或更宽松规则”的国家。

