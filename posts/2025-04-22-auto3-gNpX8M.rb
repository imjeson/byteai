Justin Borgman是大数据与分析领域的主题专家。在创办Starburst之前，他曾任Teradata（NYSE：TDC）的副总裁和总经理，负责公司的Hadoop产品组合。Justin于2014年通过收购他的公司Hadapt加入Teradata，Hadapt创建了“在Hadoop上使用SQL”，将Hadoop从文件系统转变为可由任何BI工具访问的分析数据库。他于2017年创办Starburst，旨在赋予分析师自由，分析无论数据集的地理位置如何，都能高效地使用数据，而不会妥协性能。

