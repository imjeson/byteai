您的行为健康（YBH）欣然宣布任命马修·祖比勒（Matthew Zubiller）为其新的首席执行官（CEO），即时生效。![图片](https://ai-techpark.com/wp-content/uploads/Your-Behavioral.jpg){ width=60% }

---
祖比勒接替了迈克尔·“迈克”·乔利（Michael “Mike” Joly），后者在YBH近30年的任期内建立并领导了组织旗下的三个知名行为健康品牌——神经健康水疗（Neuro Wellness Spa，NWS）、清晰行为健康（Clear Behavioral Health，CBH）和新生命之家（New Life House，NLH）。

祖比勒在医疗保健领导方面拥有超过25年的经验，专注于战略增长和运营卓越。他在扩大医疗保健、提供服务、技术和收入周期解决方案方面是一位经过验证的领导者。祖比勒最近担任了e4health的首席执行官和董事会成员，这是一家领先的国家医疗数据管理服务和收入周期解决方案提供商。他此前在其他顶尖医疗服务提供商中担任过高管职位，包括麦克森（McKesson）/变化健康（Change Healthcare，现在为Optum）、AMN Healthcare和微笑品牌（Smile Brands）。

“回顾过去的30年，我充满感激——为我们克服的挑战、为我们触动的生命，以及为让这一切成为可能的非凡人物，”乔利说道。“将接力棒传递给马特是我引以为豪的一刻。他将心与战略的完美结合，带领YBH的使命‘创造改变’走向未来。”

在乔利的领导下，YBH成为了一个行业领军者，因其对卓越患者护理和行为健康领域创新的奉献而广受认可。乔利将YBH塑造为今天这个受人尊敬的组织，并为其未来的成功打下了基础。他的亲力亲为和对改善生活使命的坚定承诺在公司及其服务的社区中留下了不可磨灭的印记。

“能够接替迈克的职位是一种荣幸，他的领导是基础性和变革性的，”祖比勒表示。“我期待继续他的优质护理传统，并致力于实现新的行为健康进展，以满足我们社区的需求。我们‘创造改变’的使命将始终是我们一切工作的核心。”

在祖比勒的监督下，YBH将继续致力于提供一流的行为健康服务，提供综合的心理健康和药物成瘾治疗项目，以改善加利福尼亚州个体的福祉。YBH的品牌家族包括神经健康水疗（NWS）、清晰行为健康（CBH）和新生命之家（NLH）。

“迈克是一位非凡的远见卓识的领导者，塑造了YBH的核心和灵魂，”您的行为健康首席医疗官玛莎·库（Dr. Martha Koo）表示。“我非常感谢迈克的经验、专业知识和才能，YBH因而获得临床卓越与改善众多生命的能力。我期待着迈克作为可信的董事会成员和顾问继续为YBH贡献力量，以及马特无疑将为YBH带来的新视角。我们将继续提供卓越的护理，为我们所服务的人创造有意义的改变。”

“我们非常高兴欢迎马特加入YBH，”YBH董事会成员及Comvest私募股权公司的合伙人安德鲁·希尔（Andrew Shear）表示。“马特丰富的领导经验、对行为健康未来的愿景以及协作的领导风格，使他成为引导YBH持续发展的理想领导者。”

祖比勒还是一位企业家、作家和顾问，为医疗保健行业贡献了宝贵的见解。他在美国癌症学会南加州分会及首席执行官委员会任职，并曾担任美国心脏协会董事会的领导发展主席。祖比勒拥有伦敦商学院、宾夕法尼亚大学沃顿商学院和加州大学伯克利分校的学位。


