title: '微软宣布将关闭Skype，历时超过二十年'
date: 2025-03-01
author: ByteAILab

---

Skype将于5月5日鸣响最后一次铃声，这家拥有二十年历史的互联网电话服务将在微软的退休计划中画上句号，微软表示将集中精力发展其自主研发的Teams服务。

---


成立于2003年的Skype迅速颠覆了2000年代初的固定电话行业，凭借音频和视频电话使公司成为家喻户晓的名字，鼎盛时期拥有数亿用户。但近年来，由于Skype的基础技术不再适应智能手机时代，平台在使用便捷性和可靠性方面与Zoom和Salesforce的Slack等竞争对手相比逐渐失去了竞争力。

随着疫情和居家办公使在线商务通话的需求激增，微软通过将Teams与其他Office应用程序紧密集成以吸引企业用户，从而对抗Teams的竞争，这曾是Skype的主要用户群体。

在线视频通信曾经几乎是Skype的独占领域，直到FaceTime、Zoom和Google Hangouts等工具相继崛起。Skype曾是技术产品的早期典范，其普遍性使得“Skype”一词成为动词，用户会用“Skype”联系某人，就像他们会说“Google”某事一样。

2011年，微软以85亿美元的价格收购Skype，战胜了谷歌和脸书，成为当时微软最大的一笔交易；当时Skype月活跃用户约为1.5亿。但到2020年，这一数字已降至约2300万，尽管在大流行期间有过短暂复苏。

多年来，微软在将Skype融入其工具套件方面遇到了挑战，无法应对竞争对手的崛起，包括苹果的FaceTime和谷歌的各类通信应用。自2017年推出团队协作产品Teams以来，Teams迅速成为优先发展产品。

“我们很荣幸能够成为这一旅程的一部分，”微软在周五表示。“Skype在塑造现代通信方面发挥了重要作用。”为了方便用户过渡，Skype用户可以使用现有凭证在任何支持的设备上登录Teams，聊天记录和联系人将自动迁移。

对于一些人来说，Skype仍在运营的事实可能令人惊讶，因为公司近年来对该平台一直处于低优先级状态。推出成功的通信工具一直是许多大型科技公司面临的挑战，包括谷歌。现在，Skype将与Duo和AOL即时通讯服务一样，被安放在通信工具的墓地中。

微软拒绝透露Skype的最新用户数据，并表示此举不会导致裁员。还补充说，Teams的月活跃用户约为3.2亿。

