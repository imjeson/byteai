Arria NLG，一家全球领先的自然语言生成AI和代理AI公司，自豪地宣布与Resolute达成战略合作，Resolute是一家专注于双用途技术、防御创新和商业战略的知名咨询公司。![图片](https://ai-techpark.com/wp-content/uploads/Arria.jpg){ width=60% }

---


十多年来，Arria为众多财富500强公司和政府组织（包括美国国防部）架构、开发并部署了先进的AI系统。Arria在其知识产权组合中拥有45项专利，提供独特且强大的AI技术。

Resolute和Arria共同识别了一个差距，Arria可以解决该差距，这主要应用于对准确性、控制、安全性和合规性具有最高标准的用例。一些例子包括希望在遵守AI治理标准的同时提高自动化效率的组织。这一合作关系应对了政府和商业部门日益增长的市场需求。

“与Resolute的合作加速了我们将智能、领域感知分析引入关键领域的使命，”Arria NLG首席执行官Sharon Daniels表示。“他们在国家安全和商业市场方面的领导力，加上Arria经过验证的AI编排和叙事智能，使我们能够在精度、规模和可解释性至关重要的领域推动创新。”

作为此次合作的一部分，经验丰富的国防、商业创新和资本战略专家Mark “Turk” Powell将担任Arria的战略执行顾问，提供战略监督和深厚的领域专业知识，覆盖联邦和企业项目。Powell在双用途创新方面拥有丰富的领导经验，并为众多大规模商业增长计划提供建议。

Resolute的领导团队包括前海军副部长Dionel Aviles和国防创新及投资资深人士Pete Peterson。他们的综合洞察和国家安全经验将支持Agentic AI在任务关键用例中的应用。

“Arria正在通过提供安全、有效且具有成本效益的技术来树立标准，”Powell表示。

Arria和Resolute共同塑造下一代企业和政府成果，在实时、高风险决策中要求速度、清晰和信任。这一合作关系强调了将AI操作化，以实现安全、可扩展和任务导向的成果的共同承诺——通过智能自动化将数据转化为战略优势。

