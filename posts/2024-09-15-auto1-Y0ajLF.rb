对于大科技公司的运营，这将涵盖从销售的硬件（如iPhone或Kindle）的制造过程到员工通勤过程中汽车排放的所有内容。

就数据中心而言，范围3排放包括公司内部数据中心建设过程中的碳排放，以及这些内部数据中心内部使用的设备制造过程中的碳排放。此外，可能还包括与合作的第三方数据中心的电力相关排放。然而，这些排放是否完全包含在报告中几乎不可能证明。“范围3排放存在巨大的不确定性，”戴特里奇说。“就会计而言，这个领域非常混乱。”

据戴特里奇介绍，一些第三方数据中心运营商将其与能源相关的排放放入其自己的范围2报告中，这样租用者就可以将这些排放放入其范围3报告中。其他第三方数据中心运营商将能源相关排放放入其范围3排放中，指望租户在其自己的范围2报告中报告这些排放。

此外，所有公司对这些范围3数字都使用市场为基础的指标，这意味着第三方数据中心的排放在官方数字中也被低估。在那些在脚注中报告其基于位置的范围3排放的公司中，只有苹果在2022年开始存在其官方范围3数字和基于位置的范围3数字之间的较大差距。

这种差距在很大程度上可以归因于数据中心排放的计算。苹果在2022年唯一对范围3方法论进行的更改是包括“远程办公、第三方云服务、电力传输和分配损失，以及范围1燃料的上游影响”。由于该公司积极列出第三方云服务在其官方范围3报告中不存在排放，这意味着所有与这些第三方服务相关的排放将只在2022年以后的基于位置的范围3排放中显示。2025年及以后尽管大科技公司隐藏这些排放，但它们有望继续增长。由于人工智能带来的额外负荷，数据中心的电力需求预计到2030年将翻一番，根据电力研究所的数据显示。谷歌和微软都将人工智能归咎于它们最近官方基础排放增加。

“根据我在2022年离职时所了解的情况，人工智能计算在谷歌的数据中心中的相对贡献是相当有限的，”目前担任实用性存储公司Gridstor首席执行官、前谷歌数据中心能源战略部门主管的克里斯·泰勒说。“两年前，[人工智能]还不是我们担忧的主要事项，至少在能源团队中是这样。”泰勒解释说，他在谷歌期间看到的数据中心增长大多归因于谷歌云的增长，因为大多数企业正在将其IT任务转移到该公司的云服务器。

