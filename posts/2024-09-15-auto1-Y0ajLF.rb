在这一类别中，从2020年到2022年，基于位置的排放总和至少高出275%（或3.75倍）于官方数字总和。亚马逊没有向《卫报》提供2020年和2021年基于位置的Scope 2数字，因此用于计算这些年份的是其官方（而很可能更低）数据。

第三方数据中心

大科技公司还从第三方数据中心运营商（或“共同托管”数据中心）那里租用了大部分数据中心容量。据Synergy Research Group称，大型科技公司（或“超大规模运营商”）在2022年占据全球数据中心容量的37%，其中有一半的容量是通过第三方合同获得的。尽管这组别包括谷歌、亚马逊、Meta、微软和苹果之外的公司，但它给出了这些公司与第三方数据中心活动规模的一个概念。

这些排放理论上应该归入范围3，即公司承担的所有排放，不能归因于其消耗的燃料或电力。

