Meta已将三位新成员任命为其董事会成员，其中包括终极格斗冠军赛（UFC）总裁兼首席执行官达纳·怀特，他是一位与即将上任的总统唐纳德·特朗普关系密切的人物。

---
这家社交媒体公司拥有Facebook、Instagram和WhatsApp，另两位新成员是汽车巨头约翰·埃尔坎和科技投资者查理·宋赫斯特，Meta首席执行官马克·扎克伯格在周一晚间的一条Facebook帖子中进行了宣布。将怀特纳入董事会是扎克伯格改善与特朗普关系的最新举措，特朗普曾一度被禁止使用Facebook。在特朗普于11月赢得连任后，扎克伯格在佛罗里达州的特朗普马拉拉戈俱乐部与他共进晚餐，并赠送了他一副Meta Ray-Ban智能眼镜，Meta还向特朗普的就职基金捐赠了100万美元，与其他大型科技公司如亚马逊的捐赠额相同。Meta还将其最著名的保守派人物乔尔·卡普兰提拔至公司最高政策职位，这也是为了加强与保守派的联系。Meta还计划取消事实检查员并推荐更多政治内容。

总统当选人是UFC的长期粉丝，经常参加重大比赛。他与怀特的关系可以追溯到2001年，当时怀特在特朗普的前赌场酒店——新泽西州的大西洋城的特朗普塔吉马哈举办了一场UFC赛事。多年来，特朗普在UFC比赛中与怀特同台出现，尤其是在他的2024年竞选中，借此努力吸引年轻男性选民。怀特亦曾在2016年、2020年和2024年共和党大会上发表讲话，并在特朗普的选举胜利派对上与特朗普同台发言，向观众简短致辞。选举后，两人曾在麦迪逊广场花园观看UFC付费直播赛事，组织者放映特朗普重回白宫的路程视频时，观众们报以热烈的掌声。

“怀特将UFC建设成为全球最有价值、增长最快和最受欢迎的体育企业之一，”扎克伯格表示。“我钦佩他作为企业家的能力以及打造如此受欢迎品牌的才华。”扎克伯格在混合武术方面也很活跃。他与另一位亿万富翁埃隆·马斯克之间似乎曾同意在2023年举行一场“笼斗”，但最终未能成行。

埃尔坎是另一位加入Meta董事会的人，是荷兰投资公司Exor的首席执行官，并担任其两个汽车公司的董事长——斯太尔南蒂斯和法拉利。扎克伯格表示，这位高管在管理大型全球企业方面拥有“丰富的经验，并为我们的董事会带来了国际视野”。宋赫斯特此前在微软工作，去年开始担任Meta在人工智能方面的顾问。

