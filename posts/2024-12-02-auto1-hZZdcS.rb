周六上午10点前，应急服务部门接到位于霍姆布什的DFO购物中心的警报，报告说涉及一辆特斯拉轿车的事件。来自九号频道的广播画面显示，这辆特斯拉Model S先是冲撞了两辆车，然后以高速继续朝另一辆车驶去，车身擦碰到那辆车。随即，这辆汽车驶出了停车场的边缘，坠落到下面的装卸区。

59岁的男性驾驶员和68岁的女性乘客在现场接受了治疗后被送往医院。目前，没有其他人因此事件受伤。从画面中无法确定事故发生时驾驶员是否控制了车辆。早些时候，美国当局调查特斯拉的自动驾驶功能时发现，该功能涉及13起致命车祸。澳大利亚《卫报》已联系特斯拉寻求评论。新南威尔士州警方在声明中表示，奥本警察指挥部的官员“到达现场后发现一辆轿车与三辆车辆发生碰撞，然后驶出一个边缘，坠落在一个装卸区。” 

“59岁的男性驾驶员和68岁的女性乘客在现场接受新南威尔士州救护车的急救治疗，因胸痛被送往皇家公园医院进行评估，”警方表示。“调查事件的具体情况仍在进行中，”警方声明中提到。

