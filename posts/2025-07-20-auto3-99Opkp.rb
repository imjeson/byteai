2025年ISG提供商透视®私有/混合云-数据中心服务报告评估了69家提供商在五个领域的能力：大型客户管理服务、中型市场管理服务、托管服务、共置服务和面向AI的基础设施咨询。

报告将富士通和Kyndryl评为三大领域的领导者。报告还将埃克森哲、凯捷、Claranet、DXC技术、Ensono、Hexaware、Infosys、Pulsant、Rackspace技术、T-Systems和TCS评为两个领域的领导者。阿托斯、Computacenter、德勤、Digital Realty、Equinix、Global Switch、HCLTech、IBM、LTIMindtree、NTT DATA、Redcentric、Telefonica Tech、Telehouse、Unisys和Wipro在一个领域中被评为领导者。

此外，LTIMindtree、Mphasis和NTT DATA在一个领域中被评为上升之星——ISG定义的“前景良好的组合”和“未来潜力高”的公司。

在客户体验领域，Persistent Systems被评为2025年全球ISG CX明星表现者，在私有/混合云和数据中心服务提供商中获得ISG客户声音调查的最高满意度评分，这是技术和商业服务行业的最高质量认可项目ISG卓越明星™计划的一部分。

