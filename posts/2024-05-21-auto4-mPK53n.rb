在本周二举行的谷歌I/O大会上，谷歌推出了一款全新的人工智能产品——Gemini Live。人们原本以为安卓系统的Gemini助手会带来某些新东西，但这就是谷歌，一切皆有可能。

---
值得一提的是，Gemini Live是在OpenAI首次消费者产品活动的第二天发布的，很难不让人猜想该产品是否是为了对抗ChatGPT Voice而发布的。 而且两者都采用了原生多模态人工智能模型，并拥有出彩的语音和视频功能。 目前，在全球人工智能竞赛中，领跑者无疑是OpenAI和谷歌，OpenAI似乎与苹果和iPhone建立了紧密的联系，而谷歌则掌控着安卓系统。 并且，相对于Rabbit r1或Humane Pin这样的人工智能设备而言，智能手机才是短期的赢家。ChatGPT Voice和Gemini Live都被集成到了现有的智能手机产品中，而且这两款产品目前都还未上市。至于下一代助手是否能够与之相比，这是后话了。...

01.Gemini Live与GPT-4o谁更胜一筹？

下图是谷歌发布Gemini Live时的发言： 
“今年夏天，我们将扩展Gemini的多模态功能，包括使用语音进行深入的双向对话。这种新体验被称为Live。”
![image](http://www.jesonc.com/FtRbN2_YW51yu2FBnFbvvAJB6ewy)
在实时视频分析和语音功能方面，谷歌是有所欠缺的。去年谷歌发布Gemini Ultra时，它发布了一段响应“实时视频”的视频，但生成的结果并不理想。不过，这一次谷歌把这项技术放在了I/O上试用，包括语音和视频对话。OpenAI和谷歌都为视频及语音对话提供了一个界面，都可以通过手机摄像头进行实时的视频分析。并且语音和视频的响应都很快，能够进行真正自然的对话，甚至可以中途打断人工智能。不过，两者也有一些明显的不同之处。OpenAI的ChatGPT Voice听起来更自然，能检测并响应情绪和声调，甚至能根据你的要求实时调整说话方式。在Gemini Live上并没有看到这种功能。二者另一个最大的不同是多模态。Gemini仍然依赖其他模型进行输出，包括使用Imagen 3输出图像和Veo输出视频。但GPT-4o是双向的原生多模态，“o”代表了全方位，可以创建自己的图像和声音。

02.AI的下一步发展：全方位语音助手

总的来看，目前人工智能的发展趋势似乎正在从文字输入转向语音输入。当人们看到OpenAI的公告时，可能就会意识到，这是人机界面的一次范式转变，其意义不亚于鼠标或触摸屏的推出。谷歌也将继续推出原生、自然的语音界面，Meta公司在其VR头显和Ray-Ban智能眼镜中配备了语音机器人MetaAI。虽然智能手机可能是目前的赢家，但这些语音AI模型真正的适配设备显然是智能眼镜。因为眼镜上的摄像头与眼睛同高，眼镜臂也可以将声波送入用户的耳朵，我们可以称之为完美的人工智能设备。目前的问题在于，OpenAI是否会进军硬件领域，推出自己的智能眼镜，或者这会成为新的Siri，并为未来的苹果眼镜产品提供动力。此外，谷歌是否真的有勇气复活谷歌眼镜，也是一个问题。

