title: 'atNorth庆祝三项重大奖项'
date: 2025-05-18
author: ByteAILab

---

这家公司获得了全球技术与人工智能奖、丹麦数据中心行业奖和数据中心评论奖的认可。![图片](https://ai-techpark.com/wp-content/uploads/atNorth-Celebrates.jpg){ width=60% }

---

atNorth，北欧领先的联合托管、高性能计算和人工智能服务提供商，宣布其在行业内获得广泛赞誉，系列重大奖项表彰其卓越的数据中心设计、可持续运营和营销优秀。
该公司在数据中心评论奖中被评为“年度数据中心设计项目”获奖者，并在丹麦数据中心行业奖中获得“年度可持续数据中心项目”奖项。atNorth还获得了全球技术与人工智能奖的“品牌运动年度奖”，并在“年度项目”和“可持续技术”类别中获得了“高度认可”。
这些奖项突显了atNorth创新的“未来数据中心蓝图”，该蓝图促进环境可持续性、能源效率和社区融入。通过展示其在丹麦的DEN01站点和冰岛ICE03校园的扩展，该公司展示了其创新的热能重复利用合作伙伴关系、对国家基础设施的支持以及对其运营所在地方社区的投资。
atNorth还因其快速品牌演变的成功与高影响力的沟通策略而受到表彰，重点聚焦于其位置的环境效益和基础设施的创新设计。
“我们很高兴能获得行业同仁如此优秀的认可。随着数据中心行业的快速扩展，我们将继续致力于‘为更好的世界提供更多计算’的愿景，推动创新、可持续性和负责任的运营，为客户提供最佳解决方案。” atNorth首席战略与市场传播官Fredrik Jansson说道。
这一消息紧随atNorth在本年度获得的其他行业荣誉，包括首席战略与市场传播官Fredrik Jansson入围Tech Capital Awards的EMEA数字基础设施领导者奖，首席发展官Anna Kristín Pálsdóttir被纳入《数据中心杂志》“2025年数据中心女性百强”，入选TechRound的Sustainability60，获得“最佳品牌发展奖”以及2025年冰岛计算机协会（Ský）的信息技术奖。

