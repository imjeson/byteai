title: '研究人员称，AI工具可能很快会操纵人们的在线决策'
date: 2024-12-31
author: ByteAILab

---

人工智能（AI）工具可能被用来操纵在线受众做出决策——从购买什么到投票给谁——根据剑桥大学的研究人员提出的论文。

---
这项研究强调了一种新兴市场，称为“意图经济”，在这个市场中，AI助手理解、预测和操纵人类意图，并将该信息出售给可以从中获利的公司。

研究人员在剑桥的利威赫姆智能未来中心（LCFI）表示，意图经济是取代注意力经济的下一步，后者令社交网络吸引用户在其平台上停留并向他们投放广告。

意图经济涉及精通AI的科技公司出售他们所知道的用户动机，从预订酒店的计划到对政治候选人的看法，卖给出价最高者。“几十年来，注意力一直是互联网的货币，”LCFI的技术历史学家乔尼·彭（Dr Jonnie Penn）表示。“与社交媒体平台如Facebook和Instagram分享你的注意力推动了在线经济。”

他补充道：“除非进行监管，否则意图经济将把你的动机视为新的货币。这将成为一个金矿，目标、引导和出售人类意图的人将获利。”

研究表明，支撑如ChatGPT聊天机器人等AI工具的大型语言模型（LLM）将被用来“预测和引导”用户，基于“意图、行为和心理数据”。

作者表示，注意力经济允许广告商通过实时竞价广告交易来购买访问用户的注意力，或通过获得一个月的广告空间来在未来购买它。LLM也将能够实时获取注意力，例如，询问用户是否想过观看一部特定的电影——“你有没有想过今晚去看《蜘蛛侠》？”——以及提供未来意图的相关建议，例如询问：“你提到过感觉工作过于繁重，我是否应该帮你预订我们之前谈论过的电影票？”

研究提出，未来的场景中，这些例子是“动态生成”的，以匹配用户的“个人行为轨迹”和“心理特征”。

“在意图经济中，一个LLM可以以低成本利用用户的节奏、政治立场、词汇、年龄、性别、对迎合性的偏好等，与经纪竞标结合，最大化达成特定目标的可能性（例如，卖出电影票），”该研究指出。在这样的世界中，AI模型将引导对话，为广告商、企业和其他第三方服务。

报告称，广告商将能够使用生成性AI工具创建定制的在线广告。它还提到马克·扎克伯格的Meta公司创建的一个AI模型Cicero，该模型获得了“人类级别”的能力来玩战略棋盘游戏《外交》（Diplomacy）——作者称这是一种依赖于推断和预测对手意图的游戏。

研究还指出，AI模型将能够根据“用户生成数据流”调整它们的输出，并引用研究显示模型可以通过日常交流推断个人信息，甚至“引导”对话以获得更多个人信息。

研究进一步提出一个未来的场景，即Meta将向广告商拍卖用户的意图，例如预订餐厅、航班或酒店。尽管已经有一个行业致力于预测和竞标人类行为，但报告表示，AI模型将把这些做法浓缩为“高度量化、动态和个性化的格式”。

研究引用了Cicero背后的研究团队的警告，即“[AI]代理可能会学会推动其对话伙伴，以实现特定目标”。研究还提到科技高管讨论AI模型如何能够预测用户的意图和行为，引用了全球最大AI芯片制造商的首席执行官詹森·黄（Jensen Huang）的话，他去年表示，模型将“弄清楚你的意图是什么、你的愿望是什么、你在给定情境下想要做什么，并以最佳方式向你呈现信息”。

