今天，先进诊断、长寿和预防健康公司Fountain Life宣布推出了Fountain Life CORE，这是其新的会员套餐，为数百万美国人提供了一个进入长寿和积极健康优化的起点。

Fountain Life致力于通过在其变成成为严重健康问题之前识别潜在问题，增强并保存每个会员的整个生活质量。该公司正在创建一种新型的预测性、预防性、个性化和数据驱动的医疗保健。通过利用先进的人工智能技术和高级诊断测试来在最早期阶段识别癌症、心脏病、代谢病和神经退行性疾病等情况，Fountain Life赋予个人过更长寿、更健康和更优化的生活。

对于每年$2995的价格，Fountain Life CORE会员资格提供了一条新成员优先考虑健康并开始迈出朝着优化健康和长寿的积极之旅的道路。CORE包括季度血液测试，分析数十个生物标志物，深入了解会员的心血管健康、代谢健康、激素平衡、炎症反应和营养水平。每次测试后，会员都会与Fountain Life长寿医生进行远程会诊审查，医生根据会员的生物标志物结果、生活方式、环境和遗传因素制定定制的优化计划。在医生访问之间，CORE会员将与Fountain Life认证健康教练一起实现定制健康计划中制定的目标。Fountain Life的方法根植于功能医学的原则，既要治疗症状，又要发现健康问题的潜在根本原因。

“Fountain Life从根本上改变了健康和长寿的方式。我们意识到传统的初级保健医生在全面评估健康诊断以优化幸福和促进长寿方面往往表现不佳，并且通常不采用根本原因、功能医学的方法。通过我们的新的CORE会员资格，我们为每个人开启了朝着长寿的积极健康之门。这一举措体现了我们致力于为每个人提供促进积极健康管理和长寿的资源的承诺，其中包括可访问一些目前最先进的诊断工具。” Fountain Life首席执行官兼联合创始人William Kapp医学博士。

Fountain Life CORE会员资格可以通过专门的Fountain Life移动应用体验轻松方便地管理。会员可以安排与Fountain Life医生和健康教练的抽血和预约，随着时间的推移监测他们的测试结果和进展，并访问全面的与长寿和健康相关的内容库。此外，会员资格还赋予个人访问额外的按需诊断测试，以检测无症状的心脏病、癌症、神经退行性疾病和代谢功能障碍。Fountain Life中心现在提供一些当今最先进的健康诊断，包括AI增强的全身和脑MRI以及冠状动脉CT血管造影。

要了解更多有关Fountain Life新CORE会员资格的信息，请访问fountainlife.com/core-membership。

