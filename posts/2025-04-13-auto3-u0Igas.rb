地位增强了其在AI基础设施解决方案领域的领导地位
ePlus公司（纳斯达克NGS代码：PLUS - 新闻）今天宣布，它已获得NVIDIA DGX SuperPOD专业合作伙伴状态。![图片](https://ai-techpark.com/wp-content/uploads/ePlus.jpg){ width=60% }

---
将这一资质加入到ePlus的AI专业知识组合中，验证了其提供全面的NVIDIA DGX SuperPOD服务的能力，确保为企业AI工作负载设计、部署和优化AI基础设施。

作为NVIDIA合作伙伴网络（NPN）的精英合作伙伴及DGX-Ready托管服务合作伙伴，ePlus展现了其通过广泛的资源团队成功提供专业知识的能力，为客户提供NVIDIA DGX SuperPOD解决方案，包括：

- 设计和架构 - 为AI训练、推理和研究设计和部署NVIDIA DGX SuperPOD配置
- 部署和网络 - 集成NVIDIA DGX系统、NVIDIA Quantum InfiniBand网络和高速存储
- AI软件优化 - 实施NVIDIA AI Enterprise、NVIDIA NeMo和NVIDIA Base Command，确保性能、可扩展性和运营效率达到最佳
- 高级支持服务 - 提供AI工作负载的监控、运营支持和持续优化

“AI在各大企业的快速崛起让许多组织急于采用这项技术，但缺乏必要的基础设施、资源或可扩展性来维持这一点，” ePlus首席运营官Darren Raiguel表示。“在ePlus努力帮助我们的客户为成功做好准备的同时，我们的专业知识也不断提升，通过获得NVIDIA DGX SuperPOD专业合作伙伴状态，证明了我们在这一领域的与众不同。我们为指导客户成功采用AI而感到自豪，并通过提供可扩展、优化和可靠的解决方案，推动实际成果。”

“AI正在改变各个行业，企业需要可信赖的提供商来帮助他们构建可扩展的、高性能的基础设施，以应对这些复杂的工作负载，” NVIDIA美洲合作伙伴组织副总裁Craig Weinstein表示。“通过获得NVIDIA DGX SuperPOD专业合作伙伴状态，ePlus为组织提供了经过验证的专业知识和端到端的解决方案，以帮助他们加速AI驱动的创新。”

NVIDIA DGX SuperPOD是一个可扩展的高性能AI基础设施平台，旨在应对企业AI和高性能计算的挑战。集成NVIDIA DGX系统、NVIDIA Quantum InfiniBand网络解决方案和AI软件，有助于消除瓶颈、加速工作负载并简化部署。

通过其AI Ignite组合，ePlus提供了一整套以AI为重点的解决方案，包括工作坊和评估、旅程支持、优化基础设施和高级服务。客户还可以探索ePlus AI体验中心，或利用最近推出的ePlus Secure GenAI加速器，这是一个指导和托管的概念验证产品，可帮助组织探索和完善思路，通过指标发现关键见解，并自信地快速测试GenAI用例。

