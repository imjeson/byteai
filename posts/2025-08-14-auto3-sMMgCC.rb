然而，热成像的广泛应用长期以来受到高成本、图像模糊和集成复杂的限制。Raytron作为全球红外热成像的领导者，正在通过其下一代热成像核心改变这一格局，旨在提供全天候精确监测。

Raytron的红外探测器：更广的范围、更清晰的图像、更高的灵敏度
Raytron基于世界首个6μm和8μm不冷却红外探测器和内部晶圆级制造的突破，批量生产从256×192到1280×1024的热成像核心，像素间距从35μm到8μm。关键是，Raytron的AI驱动图像处理，典型的如其专有的Matrix IV超分辨率算法，可以将640×512传感器的输出提升至接近1920x1080的清晰度。

Raytron的热成像核心为哪些行业带来益处
Raytron的高灵敏度不冷却红外探测器和热成像核心在多元化行业中展现出强大的性能：

- 工业预测性维护：检测电力变电站中的过热，防止能源存储系统中的电池热失控，并及时检测隐形气体泄漏。
- 汽车夜视：通过与吉利、比亚迪和长城汽车等一级汽车制造商的合作，为ADAS系统提供可靠的热夜视和行人检测。
- 安全与周界监控：提供对关键基础设施的入侵检测，长距离热摄像机不受完全黑暗、眩光或烟雾的影响。
- 户外夜视：在黑暗中持续监测野生动物保护、户外冒险和防火的工作，保护生态系统。

Raytron的热成像相机模块为OEM提供的解决方案
Raytron的超晶圆级封装（SWLP）技术具有双层封装，提供强大的防尘保护并允许在标准环境中组装。这减少了OEM对昂贵洁净室的依赖，大幅降低了系统集成的总体成本。此外，其微型热模块系列结合了紧凑的尺寸和低功耗，非常适合无人机、手持设备和智能手机。此外，凭借多种硬件接口（DVP、VOSPI、MIPI、USB）和丰富的软件开发工具包（SDK），OEM可以加速二次开发和算法优化。

Raytron的承诺
凭借1,200项专利的组合和48%的研发员工在3,000名员工中的比例，Raytron始终致力于为全球客户提供先进的红外热成像核心、完整的解决方案和可靠的售后服务。


