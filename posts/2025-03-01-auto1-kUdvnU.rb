title: '极端网络暴力可能与“0到100”杀手的崛起有关，专家称'
date: 2025-03-02
author: ByteAILab

---

“0到100”杀手的崛起，指的是那些在卧室里观看折磨、肢解和斩首视频后立即实施谋杀的个体，这表明极端网络暴力与现实中的暴力行为之间可能存在联系，专家们表示。

---
刑事司法专家们呼吁采用一种新的方法，借鉴反恐措施，以辨识没有前科的新型杀人犯，特指一些案例，比如尼古拉斯·普罗斯珀，他杀害了自己的母亲和兄弟姐妹，并计划实施针对小学的屠杀。

政府的独立恐怖主义立法审查员乔纳森·霍尔表示，出现了一种“新威胁群体”，其中包括在线极端化的恐怖分子以及那些“迷失于黑暗世界”的个体。他说：“有很多相似之处：他们是孤独的边缘人，性别以男孩居多；而互联网显然是核心要素；相当大比例的人有神经发散特征。网上的‘恐怖拼盘’促使年轻人走向暴力，英国警察局长指出：“我们必须明确这一点——这种行为如果没有互联网是无法存在的，因为它是某些类型暴力解决方案的源头。”

霍尔正在为内政部撰写一份报告，该报告是在南港袭击后委托的，旨在研究是否将大规模伤亡攻击的策划视为恐怖主义。他不赞同扩大恐怖主义的定义，但补充道：“问题在于我们能从一个群体的管理中学到哪些教训，来应用于另一个群体。”伯明翰城市大学的犯罪学名誉教授大卫·威尔逊表示，关于在线暴力是否导致现实生活中暴力行为的研究是一个“新兴领域”。虽然早期研究质疑了上一代人对视频游戏和电影中暴力内容的道德恐慌，威尔逊认为社交媒体不同，因为它更具吸引力，孤单地消费，并且算法会引导人们内容不断“加剧极端”。

他表示，在过去五年中，他询问了300名大一犯罪学学生，是否有看过在线的斩首视频。“所有人都举手，”他说，并补充这种普遍性反映在这些犯罪数量的增加上。他观察到“0到100”杀手的崛起，这些人的行为与逐渐升级的犯罪模式相反，直接转向谋杀。这与“混合意识形态”动机的上升有关，其中包括“内塞尔”文化、“极右派”以及大规模杀戮宣言。这个问题因青少年俱乐部和心理健康服务的削减而加剧。

格雷格·斯图尔特，曾担任法律协会青少年司法领导的刑事律师表示，成功的青少年司法系统改革能够发现社区中表现不良的年轻人，但却忽视了“特殊儿童”。他说：“以前的‘电梯’方式，现在已被禁用，因此你更有可能从低起点直接达到高严重性，”并补充道，孤独症、强迫行为与在线极端化之间存在重叠。他建议采取类似于“预防”的响应，教师和讲师应识别“思维模式和观点”。例如，纳森·萨阿迪在刺伤一名女性后，因询问有关谋杀的问题而引起了一位讲师的警觉。

伦敦东大学的犯罪与网络犯罪教授朱莉亚·戴维森表示，关于年轻人暴露在暴力内容下的“巨大证据”，让她担心这已成为一种“公共卫生问题”，尽管“科学上很困难”以确定与离线暴力之间的联系。年轻人会感受到“观看暴力行为的压力，这成为了归属于某个团体的考验”，这与安德鲁·泰特等影响者所倡导的有毒男性气质相吻合。戴维森表示，2017年关于《在线安全法》的讨论，最初集中在防止儿童观看有害内容，如儿童性虐待、网络欺凌和色情内容，但警方对在线暴力的日益关注也逐渐浮出水面，例如在奥利·史蒂芬斯谋杀案中。

阿尔穆登娜·拉拉，Ofcom儿童安全政策主管，赞同儿童面临“完美风暴”，包括暴力内容、宣传虐待和仇恨的内容，以及常常极具厌女内容和色情内容，所有这些几乎使他们无法避免。Ofcom正在努力在言论自由与儿童安全之间保持平衡，要求平台不得主动向儿童推送暴力内容，尽管拉拉表示“门槛并不是完全消除，而是解决与真实生命暴力相关的‘累积影响’”。

法律顾问罗娜·伍兹教授表示，她担心社交媒体平台的自我监管“恐怕不够”来解决这一问题，尤其是依赖于内容删除时，而平台必须转向“安全设计”。

