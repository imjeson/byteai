资深技术领导者将推动以客户为中心的增长和云转型卓越
CompQsoft Digital，全球微软解决方案和服务提供商，今天宣布任命安德鲁·杰弗斯为首席客户官（CCO）。![图片](https://ai-techpark.com/wp-content/uploads/Andrew-Jeffers-Join.jpg){ width=60% }

---
安德鲁将在这一战略角色中领导客户成功倡议，监督企业级数字转型，并最大化公司云和人工智能转型产品的客户价值。
安德鲁拥有超过25年的微软企业解决方案交付经验，建立了驱动客户参与、加速业务成果并提供高投资回报率的良好声誉。他善于设计可扩展的、以结果为导向的战略，确保客户成功和建立长期合作关系。
在加入CompQsoft Digital之前，安德鲁曾在多家全球领先的技术公司担任高级领导职务，包括德勤、Avanade、IBM/Neudesic、TTEC和DXC Technology。他通过优化售后参与、增强客户健康以及推动以创新为导向的服务交付，持续取得优秀业绩。
“安德鲁带来了丰富的经验和培育增长、文化与创新的良好记录，”CompQsoft Digital的总裁及董事会成员格雷格·莫瑟表示。“他的领导正是我们强化以客户为中心的战略所需。安德鲁对变革性客户旅程的热情与我们的愿景高度一致，我相信他将提升我们在每一个接触点为客户、团队成员和合作伙伴提供的体验。”
“我很高兴能加入CompQsoft Digital，建立一个以客户成果为中心的世界级微软合作伙伴组织，”安德鲁·杰弗斯表示。“我们的使命很明确：确保客户健康、推动客户保留，并通过以创新和卓越为根基的文化提供卓越价值。”

