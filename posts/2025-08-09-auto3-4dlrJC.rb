Fractal (www.fractal.ai)，全球领先的人工智能解决方案提供商，今天宣布其产品Cogentiq已在亚马逊网络服务（AWS）市场的新AI代理和工具类别中上架。![图片](https://ai-techpark.com/wp-content/uploads/Cogentiq.jpg){ width=60% }

---
客户现在可以通过AWS市场轻松发现、购买和部署AI代理解决方案，包括Fractal的企业级代理智能AI平台，从而加速代理和代理工作流的开发。

Cogentiq帮助组织弥合数据与可操作决策之间的鸿沟，超越被动分析，赋能主动决策。它的智能代理能够动态整合跨系统的数据，进行上下文信息分析，并自主发起行动，彻底改变企业与客户的互动、管理运营及推动增长的方式。

“通过在AWS市场提供Cogentiq，我们为客户提供了一种简化的方式来访问我们的企业代理智能AI平台，帮助他们更快地购买和部署代理解决方案，” Fractal首席产品官Himanshu Nautiyal表示。“我们在医疗保健、保险和消费品领域的客户已经在使用这些功能来统一数据、自动化运营和加速决策，这展现了Cogentiq的实际应用场景。”

Cogentiq提供了基本的功能，包括低代码/零代码接口、全面的现成连接器库以及一套预构建的特定领域代理。这些功能使客户能够构建和部署智能解决方案，并将洞察转化为行动。

Cogentiq作为供应商托管的API和容器化微服务解决方案可用，支持模型上下文协议（“MCP”）以简化代理通信。这使客户能够与其他AWS服务连接并灵活部署到其AWS环境中。

