欧盟委员会已要求X公司提供有关其算法的内部文件，随着对埃隆·马斯克社交媒体平台是否违反欧盟内容管理规则的调查加剧，该委员会希望在2月15日前查看有关其“推荐系统”的内部文档，包括最近对该系统的任何更改。

---
自2023年12月以来，X公司一直受到欧盟内容法（即数字服务法，DSA）的调查，主要关注其如何处理非法内容和信息操纵的传播。该公司被指控操纵平台系统，给予极右翼帖子和政治人物更大的可见度，超越其他政治团体。

近年来，欧盟受到越发强烈的压力，要求对马斯克的多次政治干预采取行动。马斯克将加入唐纳德·特朗普即将组成的美国政府，因其对欧洲领导人的一系列攻击以及对德国极右翼政党“替代德国”（AfD）的支持而激怒了许多欧洲人士。

欧盟数字专员亨娜·维尔库宁在一份声明中表示：“今天我们采取进一步措施，以察明X的推荐系统是否符合DSA的规定。”委员会对此前的报道进行了淡化，称其对大型科技公司的调查正常进行，强调美国总统当选人唐纳德·特朗普重返白宫不会影响其执法承诺。维尔库宁暗示，如果X被发现违反DSA，欧盟将毫不犹豫地利用其权力对其进行禁令或罚款。她表示：“我们致力于确保每个在欧盟运营的平台遵守我们的法律，以使在线环境对所有欧洲公民公平、安全和民主。”

一些欧洲政客指责马斯克干预选举，例如他本月与AfD领导人爱丽丝·魏德尔的直播谈话。委员会表示，马斯克有权表达自己的观点并与政治人物进行直播，但正在评估算法是否会提升单一叙事，并“影子封禁”其他观点，从而对公平选举构成风险。马斯克则表示，批评此事是对民主和言论自由的冒犯。

