Molly Deuson 和 Catherine Jones在Onward Robotics因创新的自动化解决方案推动履行行业向前迈进而获得认可

Onward Robotics，先进自动化技术和移动机器人的领先提供商，今天宣布，公司的两位杰出领导人——Catherine Jones 和 Molly Deuson——荣获2024年备受尊敬的供应链领域女性奖。![图片](https://ai-techpark.com/wp-content/uploads/2024/10/Onward-960x540.jpg){ width=60% }

---
作为公司商业组织的高级领导者，Jones 和 Deuson 因部署Onward Robotics颠覆性的Meet Me™自动化平台而受到认可，该平台可为仓储、分销和电子商务业务提供高效准确的订单履行。

从350多名提名者中选出，Onward Robotics领导人获得了值得关注的荣誉：方案设计总监 Molly Deuson 被评为“新星”，她的成就、辛勤工作和远见塑造了供应链网络，而高级产品经理 Catherine “CJ” Jones 赢得了“先锋”奖，为未来女性在物流领域铺平道路。

“Molly 和 CJ 是Onward Robotics大胆推动履行行业向前发展的出色代表，”CEO Lance VandenBrook表示。“我们很幸运能有她们这样出色的领导和深厚的行业专业知识加入我们的团队，我们非常自豪看到她们跻身顶尖供应链专业人士之列。”

作为Onward Robotics的高级产品经理，Catherine Jones 利用她15年以上的供应链和仓库自动化专业经验来定义公司的产品战略和路线图。CJ热衷于部署解决真实市场需求的复杂技术，并强调易用性，她以协作和以人为本的领导风格而获得认可。

“新星”奖得主 Molly Deuson 领导一个方案设计师、数据分析师和仿真工程师团队，负责向客户推荐如何利用Onward Robotics的创新Lumabot™ 自动移动机器人和Pyxis™技术来改善其运营效率并减少浪费。Molly的团队、同事和客户都欣赏她富有同理心的应对方式和熟练的变革管理风格，这些经验来自多年为大型仓储、分销和第三方物流服务提供商设计运营活动。

Onward Robotics的创新自动化技术将在即将举行的两个行业活动中展示：A3的自主移动机器人和物流会议（展位＃229）于2024年10月8日至10日在田纳西州孟菲斯举行，以及IntraLogisteX美国（展位＃210）于2024年10月22日至23日在佛罗里达州迈阿密海滩举行。

