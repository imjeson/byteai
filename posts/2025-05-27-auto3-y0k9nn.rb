Gcore 欢迎人工智能研究和行业领军人物，徐飞宇教授加入董事会。![图片](https://ai-techpark.com/wp-content/uploads/Gcore-Appoints.jpg){ width=60% }

---
  
Gcore，这家全球边缘人工智能、云计算、网络及安全解决方案的提供商，今天宣布以人工智能专家徐飞宇教授的加入增强其董事会。徐教授是一位著名的人工智能研究者，曾在 SAP、联想和德国人工智能研究中心担任重要领导职务。她的任命巩固了 Gcore 在推动人工智能创新和行业领导力方面的承诺。  
徐教授将帮助塑造公司的人工智能愿景、增长战略和长期目标。自2014年成立以来，Gcore 不断扩展其产品线，最近推出的 Everywhere Inference 提供灵活的部署选项，包括云、内部部署和混合环境。通过在边缘接入点运行，Everywhere Inference 为最终用户和应用程序提供了更快的处理和响应时间。作为首位在人工智能研究和创新领域担任重要职务的 Gcore 董事会顾问，徐教授将引导 Gcore 在边缘人工智能领域的增长和创新。  
目前，Gcore 在全球六大洲提供基础设施，使企业能够在全球范围内以超低延迟训练和部署人工智能模型。凭借超过 180 个边缘位置，包括 50 多个云位置和 14,000 个对等合作伙伴，Gcore 使人工智能工作负载更靠近用户，从而实现更快、更具可扩展性和成本效益的性能。随着公司不断发展，徐教授的专业知识将巩固 Gcore 作为全球人工智能创新领导者的地位。  
徐教授表示：“我很高兴加入 Gcore 的董事会顾问团队，这是一家具有巨大潜力的独特科技公司。Gcore 不仅提供开发人工智能应用所需的基础设施，还积极推动欧洲人工智能生态系统的发展。我很高兴能成为这个团队的一部分，一同塑造未来的技术与创新。”  
Gcore 首席执行官安德烈·雷滕巴赫评论道：“欢迎徐教授作为董事会顾问，我们深感荣幸。徐教授的战略愿景和深厚的人工智能专业知识将使她成为董事会的杰出成员。作为一位著名的科学家和商业领袖，徐教授对人工智能的深刻理解将为推进 Gcore 的使命——让世界随时随地连接人工智能，发挥至关重要的作用。”  

