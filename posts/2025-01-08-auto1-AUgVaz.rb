Meta决定结束事实核查以优先考虑“言论自由”，引发了社交媒体专家的警惕，以及对使用其平台（如Instagram）的伦理问题的质疑。该公司的亿万富翁创始人马克·扎克伯格于周二宣布，将用类似X平台的“社区笔记”替代平台的事实核查程序，这一功能允许用户对帖子添加上下文。更多的政治内容将被推送到Meta的平台上——其中还包括Facebook和Threads——同时某些主题（如移民和性别）的限制也将被取消。但这是否是一个重新考虑是否继续留在这些平台上的理由？以下是你需要知道的内容。

Meta的公告会影响到你吗？这一决定目前仅影响美国，但可能会扩展到其他地区。澳大利亚国家通讯社的首席执行官莉萨·戴维斯表示，其事实核查机构AAP FactCheck继续为Meta在澳大利亚、新西兰和太平洋地区提供服务。昆士兰科技大学数字媒体中心的阿克塞尔·布伦斯教授表示，这一“问题决策”可能是为了“讨好”即将上任的特朗普政府。“这对所有使用Meta平台的人来说都是一个实质性问题，因为这确实为更多误解的传播打开了大门，”布伦斯说道。

我应该退出Instagram吗？专家们表示，这完全是个人选择。布伦斯指出，Meta的公告目前只涉及美国，而其他地区的社交媒体用户需要等待观察其在自己所在地区的网络表现。他表示，在Instagram上看到是否会有更多“明显的政治内容”出现在用户的动态中将是很有意思的。帕特森教授则认为，社会“早就应该在社交媒体上不再获取新闻内容了”。“在一个完美的世界里，”她说，不满Meta决策的人应该退出Instagram。“但在现实中，这要难得多，”她承认。“这里毕竟有不少独立创作者的社区……他们通过这些平台向更广泛的社区展示自己的产品。”

如果我想离开Instagram，替代选择有哪些？帕特森指出，讽刺的是，实际的替代选择非常少。她表示，Twitter的情况则有所不同——注意到许多人在埃隆·马斯克收购该平台后离开，并将其更名为X，然后变得“更加极端”。但对于Instagram来说，她说，“目前并没有一个容易的替代选择”——TikTok“也有自己的问题”，而其他类似的平台在覆盖范围上则完全无法达到。“[对于]生活在乡村或偏远地区的人、少数群体或小型企业，这确实是一个与他人沟通并接触的好办法，”她表示。“目前，建立一个替代平台是不太可能的。所以，直白地说，我们确实面临着一些很糟糕的情况。”

关于其他Meta平台——WhatsApp、Threads或Facebook呢？墨尔本的数字权利活动家萨曼莎·弗洛拉尼对Meta的整体平台表达了关注。她强调，除了结束其事实核查项目外，Meta还在改变其仇恨行为政策，这将拆除对LGBTQ+人群、移民及其他边缘群体的保护。“这一点，加上繁琐的定向广告和猖獗的人工智能垃圾内容，使这些平台不仅不安全，甚至变得难以忍受，”弗洛拉尼说。“无论何时退出Instagram、Facebook和其他Meta产品都是一个不错的决定，但许多人——包括我自己——可能会发现很难离开。”弗洛拉尼表示，Meta“在踩踏竞争”方面做得相当出色，这意味着很少有替代平台具有相同的“用户数量基础”。“在某种程度上，我们需要强有力的国内科技监管以及更多的平台多样性，”她说。“但是从更大的角度来看，我们真正需要的是将在线空间与一种无情的市场增长至上的理念相分离。”

我需要完全停止使用社交媒体吗？澳大利亚数字权利观察组织主席丽齐·奥谢表示，Meta的公告是人们反思社交媒体使用情况的一个机会，但这并不意味着他们非要放弃。“很多人出于合理的原因在使用它，”她说。“很多人在使用它时并不开心。”她还表示，Meta的决定应该促使澳大利亚政府实施强有力的隐私改革，以更好地保护社交媒体用户的个人信息。“虽然社交媒体公司将其商业模式建立在尽量让用户保持参与的基础上，以收集更多数据并用于定向广告，但像澳大利亚这样的地方可以通过限制（例如欧洲的那种限制）减缓这一数据提取过程，”她说。“这意味着公司会更少地关注参与这一点及其带来的所有负面后果，例如极端化和极端内容的传播。”

悉尼大学数字文化讲师乔安妮·格雷博士表示，社交媒体平台是“一个重要工具”，是有益的。“我不认为有人在提倡社交媒体被完全禁止，或者不允许每个人使用，”她说。“但这些平台造成了系统性的和严重的伤害。我们应该对自己的社交媒体饮食更加慎重，看看什么对我们是有益的。”

