对于独立电影制作人、数字内容创作者、市场营销人员和首次使用者来说，Autodesk首次提供专业级AI驱动的视觉特效（VFX）和动画工具的免费访问——消除了全球创作者的成本障碍。![图片](https://ai-techpark.com/wp-content/uploads/Autodesk-launches.jpg){ width=60% }

---
  
今天，在SIGGRAPH 2025会议上，Autodesk, Inc.（纳斯达克：ADSK）推出了Autodesk Flow Studio（前身为Wonder Studio）更经济实惠的定价方案，包括其首个免费层和Lite计划价格的50%下降——从20美元降至10美元——这一举措将使这套工具更为可及。通过消除前期成本，艺术家们可以使用高端的人工智能（AI）驱动的3D工具创造以前只属于好莱坞的动画和视觉特效，Autodesk正在发出信号，标志着一个新的媒体和娱乐未来的AI时代——一个将创作者置于前沿的时代。此发布标志着在Autodesk收购Wonder Dynamics、该技术背后的公司一年后的重大里程碑。

Autodesk Flow Studio利用AI自动化复杂的视觉特效任务，比如动作捕捉、摄像机跟踪和角色动画，这样创作者可以专注于讲故事，而不是技术设置。用户只需提供视频剪辑和几次点击，即可将CG角色插入到实景镜头中，并进行渲染，使3D动画和VFX对所有人都更加容易接触。用户还可以将AI生成的资产（例如干净的画面、3D场景、阿尔法遮罩、摄像机跟踪）导出到Maya、Blender或Unreal Engine等工具中进行编辑和调整。这使得创作者能够加速复杂的动画和VFX工作流程，释放出更多时间进行更具创意的故事讲述。通过将这些能力免费提供，Autodesk降低了高质量内容创作的入门门槛，使更多艺术家、市场营销人员和数字内容创作者能够在自己的条件下进行实验、学习并实现他们的想法。

“让Autodesk Flow Studio对每个人都更易获取，反映了我们对创作者优先未来的信念——让AI赋予更多的艺术表现，而不是取代它，”Autodesk媒体与娱乐执行副总裁Diana Colella表示。“通过降低成本门槛，更多的讲故事者现在能够探索新的AI驱动工具如何帮助他们实现想法，无论他们在创意旅程中处于何种阶段。”

此次扩展推出了一个分层模型，旨在随着创作者的发展而成长——从首次用户到专业工作室。新的免费层和标准层与现有的Lite、Pro和Enterprise产品一同推出，给予用户更多灵活性，以选择符合他们创意需求和抱负的计划。免费层包括AI动作捕捉和实景拍摄等核心工具，而高级功能——如Wonder Animation和Wonder Tools——仍然仅在付费层中提供。对于更大的团队和工作室，Enterprise层提供可扩展的AI驱动流程访问以及增强的支持和数据控制——帮助它们在保留定义专业制作的创作过程和角色的同时，采用新技术。

“以规模运行AI模型对于技术初创企业来说历来非常具挑战性，”Wonder Dynamics的联合创始人Nikola Todorovic说道。“我们最初的使命一直是让我们的技术尽可能免费且可负担，但作为一家初创企业，我们的资源有限。现在，作为Autodesk的一部分，我们能够把Flow Studio作为一个免费产品提供，得益于强大的基础设施和云合作伙伴——这样更多的艺术家可以讲述更多的故事，无需工作室场地或数百万美元的预算。”

免费层的推出和灵活的定价模式面向全球用户，也正式完成了Autodesk Flow Studio与Autodesk生态系统的整合。这确保与Autodesk的标准产品、政策和流程保持一致，为用户提供更统一、一致和可扩展的体验。此外，Autodesk已将Flow Studio的Pro层包含在其媒体与娱乐系列中，且无需额外费用——为现有客户提供了无风险的方式去探索AI驱动的工作流程。这一做法反映了Autodesk致力于引导客户在媒体和娱乐领域不断发展的AI生态中，提供可访问、可信赖且能随着其创意追求扩展的工具。


