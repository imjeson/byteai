date: 2025-07-14
author: ByteAILab

---

66degrees，一家领先的人工智能、数据和云咨询公司，今天宣布任命布莱恩·格雷戈里为首席技术官（CTO）。![图片](https://ai-techpark.com/wp-content/uploads/66degrees.jpg){ width=60% }

---
格雷戈里在加入66degrees之前，曾在谷歌云经历了令人瞩目的八年旅程，期间负责了跨Alphabet的全球战略合作伙伴关系——包括GEO、Android、X（创意工厂）等项目。在此角色中，他帮助转型了一些谷歌最重要的客户，并在此过程中在2017年将谷歌云从约40亿美元的业务规模提升至2025年约490亿美元的年收入。

作为66degrees的首席技术官，格雷戈里将负责推动公司的技术愿景和交付战略——推动创新，实现数百万美元的大型企业项目，并解锁新的客户价值和业务转型水平。

“我非常高兴地欢迎布莱恩·格雷戈里加入我们，担任首席技术官，”66degrees的首席执行官本·凯斯勒表示。“布莱恩带来了独特的深厚技术专长、远见卓识和建设者的心态。他是我们领导团队的完美补充，帮助66degrees进一步扩大我们在人工智能、数据和下一代云架构方面的能力。他在转型世界一些最重要客户方面的经验让他成为帮助我们加深影响、推动创新、开启新一阶段增长的重要人物。”

在谷歌负责战略合作关系时，格雷戈里帮助谷歌的最重要客户进行转型，助力他们颠覆行业、加速创新，并更快地将新产品推向市场。在加入谷歌之前，格雷戈里在Express Scripts、Peabody Energy和CenturyLink担任高级技术领导职务，负责在财富100强环境中进行大规模的现代化和创新计划。

“作为首席技术官加入66degrees，是建立一些非凡事物的机会，”布莱恩·格雷戈里表示。“这个团队独特地位于人工智能、数据和云的交汇处——这是最有意义的转型正在发生的地方。我在这里推动边界，以目标为导向，与客户合作，创造重新定义商业竞争、增长和服务客户的影响。我对在谷歌的旅程深表感激，并对未来的道路充满激情。”


