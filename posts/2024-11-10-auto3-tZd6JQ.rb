在2024年全球移动宽带论坛（MBBF 2024）上，华为副总裁、华为无线解决方案总裁曹明推出了5G-AA解决方案。![图片](https://ai-techpark.com/wp-content/uploads/2024/11/Huawei-960x540.jpg){ width=60% }

---
曹明表示：“移动人工智能正在改变我们的生活。新兴的连接和服务对网络提出了越来越高的要求。华为的5G-AA 10解决方案通过全系列高级射频构建多维度的终极网络能力，通过环境站实现全域站数字化，并通过基于代理的数字工程师团队实现L4网络自主性。凭借如此出色的性能，这些解决方案将帮助运营商满足移动人工智能时代对服务的日益多样化需求。”

华为的5G-AA解决方案将通过“网络用于AI”和“AI用于网络”深化5.5G和人工智能的融合。 “网络用于AI”将使网络具备足够的能力，以满足不同的服务需求，并提高网络性能和用户体验。“AI用于网络”将通过数字站点和RAN代理实现L4自主权。运营商可以在各种场景中使用这些解决方案，以保持他们的领先地位。

1. 低于1 GHz的大规模MIMO为移动人工智能构建广泛的基础层。 低于1 GHz的大规模MIMO提供了创新解决方案，以满足现场工程的通用部署要求，将大规模MIMO引入低频段。 在提供广泛覆盖的同时，低频大规模MIMO改善下行链路、上行链路和低延迟体验。 它支持所有RAT的全接入、全物联网连接和全实时服务保证。

2. 独特的亚-6 GHz全波段一体化一盒式简化了单天线大规模MIMO部署。 Blade AAU X是行业首款使用单天线支持所有亚-6 GHz频段大规模MIMO部署的产品。 与其他大规模MIMO模块不同，该产品可以部署在单个杆上，并且需要的站点空间减少了50％，OPEX降低了70％。 它支持GHz级带宽的单杆部署，最大化了频谱效率。

3. 行业首款独特的U6GHz AAU确保连续10 Gbps。 U6GHz AAU集成了1500多个天线元件，并采用领先的MU-MIMO算法。 在这样领先的硬件和软件协作下，它支持高达100 Gbps的容量和高达10 Gbps的速度。 它使覆盖范围与C波段对准，以确保室内不间断的Gbps体验。

4. LampSite首次引入被动物联网（P-IoT）以升级室内数字智能。 LampSite X支持室内10 Gbps和P-IoT接入。 与其他解决方案相比，它的部署时间可缩短一半，并允许每天而不是每月进行资产清点。

5. 行业最轻量的AAU产品简化了中频段部署并具有巨大带宽。 EasyAAU是行业最轻量的AAU产品，重量仅为8T RRU型号的一半。 这使其能够部署在城市区域的墙壁上和农村地区的电线杆上，快速提供5G覆盖。 交付时间缩短了40％，容量也可以翻倍。

6. 集成天线和微波（IAM）首次克服了天线安装挑战。 集成天线和微波（IAM）将天线和E波段微波单元置于一个盒中，可将单个盒中的重量减轻30％，安装所需空间减少50％。 该天线还支持全波束自动搜索，有助于降低维护成本。

7. 全双工微波首次升级了传输容量。 行业首款全双工微波MAGICSwave使用超隔离天线和干扰消除算法来提高频谱效率。 它的容量高达50 Gbps，使运营商能够在移动人工智能时代满足巨大的数据传输需求。

8. 首个一盒式农村网络解决方案帮助缩小农村地区的连接差距。 RuralCOW代表了RuralStar和RuralLink的升级。 通过将前传单元和无线单元集成到一个盒中，它可以在不需要返程、BBU和主要电源供应的情况下部署。 这在偏远地区和郊区实现了多RAT部署的站点成本显著降低。

9. 基于代理的数字工程师团队实现更高级别的网络自主性。 基于智能RAN代理，已孵化出了基于代理的数字工程师团队，其中包括四名数字专家，执行无人值守维护、实时网络优化、24/7节能和准确的服务提供评估。

10. 独特的全域数字站点实现可视化站点级感知和控制。 环境站将站点的主要设备与天线、电源和传输设备连接起来，帮助实现全站点数字化。 这使网络能够提高节能和用户体验。

“移动人工智能即将到来。我们将继续与所有行业伙伴合作，并不断创新，促进5.5G和人工智能的融合，”曹明总结道。

