从现代化过时技术到实施负责任的AI治理，报告考察了驱动企业AI在2025年及以后成功的关键因素
EPAM Systems, Inc.（NYSE: EPAM），一家领先的数字化转型及服务和产品工程公司，今天发布了其AI研究报告“从炒作到影响：企业如何释放AI的真实商业价值”。![图片](https://ai-techpark.com/wp-content/uploads/What-Is.jpg){ width=60% }

---
该报告基于对来自九个国家和八个行业的7300名参与者的调查，考察了AI采用的当前状态、面临的挑战以及企业如何从AI投资中获得实际商业价值的机会。

研究揭示，来自美国、加拿大、英国、德国、瑞士、法国、荷兰、新加坡和阿根廷的受访者在企业AI采用方面存在显著的认知差距。近一半（49%）的受访者将其公司评估为“高级”AI实施，5%认定为“颠覆者”，32%为“称职者”，14%为“初学者”。然而，仅有26%的自认高级公司和颠覆者成功将AI用例推向市场。

“在ChatGPT发布后，以及在2023年和2024年期间，我们目睹了各行业的公司开始尝试AI并开发概念验证，主要针对提升生产力和运营效率的短期收益，”EPAM首席市场和战略官Elaina Shekhter表示。“这项新研究清楚地表明，我们现在进入了一个新阶段，成功取决于识别高价值用例，并战略性地优先考虑它们，以实现广泛的组织影响。能够有效整合其人才、数据和技术以围绕这些优先用例行动的企业，将在2025年及以后成功部署AI，并从其AI投资中获取商业价值。”

报告的主要发现包括：

- AI投资加速：公司计划在2025年将AI支出同比增加14%，显示出对AI驱动增长的持续承诺。
- 扩展AI仍然是挑战：虽然30%的技术先进公司已经成功实施了大规模AI，但许多组织在实验与全企业部署之间的鸿沟中挣扎。
- AI对商业的直接影响：颠覆者将53%的预期2025年利润归因于AI投资，显示出市场领导者的实质性财务影响。
- 治理和安全滞后于AI增长：企业预计实施有效的AI治理模型至少需要18个月，突显出在快速发展的监管环境中使AI合规的复杂性。
- AI人才仍然是优先事项：调查中43%的公司计划在2025年招聘与AI相关的职位，其中机器学习工程师和AI研究员为最受欢迎的职位。

“提高生产力和运营效率是普遍目标，但真正的转型在于弥合技术团队与业务之间的差距，”EPAM工程副总裁Dmitry Tovpeko表示。“随着AI重塑企业，开发人员从任务导向的用户演变为战略专家，负责任地利用AI解决从头到尾的场景。成功不仅取决于技术堆栈或云基础设施，而在于使技术团队与业务目标对齐，共同解决实际客户问题。”

为实现AI的全部潜力，报告强调整合人员、数据和技术的重要性，以释放真正的商业价值，并确定成功采用AI的四个关键领域：

- 人员、流程和文化：有效的AI实施需要强有力的高管领导，清晰阐明优先事项和重点领域。事实上，数据显示65%的颠覆者理解AI采用所需的技能。
- 业务和技术现代化：虽然31%的高管将过时技术视为AI采用的障碍，但真正的挑战是业务和技术团队之间缺乏对齐。一旦明确的组织目标被建立并传达，工程团队才能制定现代化战略。
- 安全：在数据保护、数据质量和云安全方面，安全仍然是高管和工程团队的普遍优先事项，35%的企业表示，达成现代化的首要挑战是缺乏复杂的安全程序。
- 治理和负责任的AI：虽然75%的高级公司声称已建立清晰的AI战略，但只有4%的颠覆者表示他们已开发全面的治理框架，尽管理解到有效治理通常需18个月。

据Forrester在其《2025年人工智能预测》报告中表示，2025年将重新聚焦于战略，深化商业与IT之间的合作，回归预测性AI，以及企业共同治理数据和AI的新技术和架构。

“在2023年，追逐AI的竞赛见证了许多实验，但少数先锋脱颖而出，将大胆的想法转变为可扩展的现实，”EPAM NEORIS首席AI官Nir Kaldero表示。“这些领导者将挑战视为机遇，将法规视为不可妥协的条件，并将未开发的领域视为创新空间。AI的下一个阶段不仅仅是实验，而是大规模部署—专注于企业范围内的高影响力用例，同时继续共同努力，使人员与文化、数据与云以及新流程对齐，以释放真正的指数级商业价值。”

报告最后指出，组织现在必须将AI与其商业目标对齐，而不是适应业务目标以适应AI能力。在这一新阶段的成功需要公司超越单纯为提升生产力和运营效率而实施AI。为了获得竞争优势，前瞻性的企业必须在整个价值链中战略性地利用AI，以推动收入增长并提升客户体验。

完整报告包含了针对不同阶段企业AI旅程的详细见解和实用建议。

