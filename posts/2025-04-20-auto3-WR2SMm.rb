方法论
本报告中发布的数据基于对7300名员工进行的调查，这些企业的员工人数超过10000人，调查对象包括C-Suite和副总裁级别的员工，以及来自九个国家（美国、加拿大、英国、德国、瑞士、法国、荷兰、新加坡和阿根廷）和八个行业（金融服务、生命科学和医疗科技、教育和商业信息服务、能源、零售和消费品、电信、媒体和娱乐、保险、汽车和制造）的工程师和开发人员。调查在2024年10月24日至2024年12月3日之间进行，由Censuswide合作收集数据。
