Reaxys AI搜索避免了构建复杂关键字搜索的需求，使其在材料科学、化学工程和聚合物科学等跨学科研究中尤为珍贵。

作为全球科学和医疗保健领域先进信息和决策支持的领导者，爱思唯尔（Elsevier）推出了Reaxys AI搜索，这是Reaxys平台上的一项创新功能，通过利用人工智能驱动的自然语言处理技术，改变化学研究的方式。Reaxys是第一个引入自然语言文档搜索的化学数据库，旨在提高研究人员在广泛复杂的化学研究信息中发现相关文献的能力。

Reaxys AI搜索在跨学科的研发领域（如材料科学、化学工程和聚合物科学）特别有效，因为它消除了构建复杂关键字搜索的需求。相反，Reaxys AI搜索能够解释用户意图，处理拼写变体、缩写及同义词，并返回每个查询的最相关文档。这意味着化学家、化学和材料工程师、聚合物科学家、制药和生物技术专业人员、学术研究人员及其他用户可以花更少的时间来搜索相关见解，而更多地致力于发现和创新。

Reaxys AI搜索建立在Reaxys可靠且综合的数据库之上，利用超过1.21亿份文献，包括专利和经过同行评审的期刊文章，始终如一地提供高水平的精确性和召回率。Reaxys AI搜索经过数百位化学家的测试开发，是Reaxys向人工智能转型的重要一步，此前推出了屡获殊荣的Reaxys预测性逆合成。未来版本将进一步完善AI搜索选项，并带来高级摘要功能。

爱思唯尔生命科学部门董事总经理Mirit Eldor表示：“Reaxys AI搜索标志着使化学数据更易于访问和可操作的重要一步。通过允许自然语言查询，我们旨在降低跨学科研究者和各个经验水平的研究人员找到所需信息的门槛，并以更高的信心进行快速搜索。推出这个早期版本使我们能够为研究人员提供即时价值，同时收集反馈，持续改进这一解决方案，这体现了我们对帮助推动人类进步的创新承诺。”

Reaxys AI搜索的早期发布版现已对所有Reaxys用户开放。用户可以在现有平台中访问此功能，同时保留现有的结构和关键字搜索选项，以便获得即时的实际体验。爱思唯尔仍然致力于与科学共同体密切合作，以优化和扩大这一技术，确保其满足化学研究的不断发展的需求。

Reaxys AI搜索的开发遵循爱思唯尔的负责任AI原则与隐私原则，优先考虑数据隐私、安全性和透明度。所有用户互动都是私密的，数据不会用于训练外部模型，结果均基于可靠的、经过审查的内容生成。

