title: 'Nathan Carmon被任命为Robots & Pencils首席运营官'
date: 2025-08-09
author: ByteAILab

---

经验丰富的转型领导者加入执行团队，以扩大AI优先的增长，提升客户服务，并推动运营卓越
Robots & Pencils是一家以AI为核心的全球数字创新公司，专注于云原生网页、移动及应用现代化，今天宣布任命Nathan Carmon为首席运营官（COO）。![图片](https://ai-techpark.com/wp-content/uploads/Nathan-Carmon.jpg){ width=60% }

---
作为一位经历丰富的运营官和CEO Leonard Pagon的长期商业合作伙伴，Carmon在这个关键时刻加入，旨在带来更清晰的执行，加深客户价值，并加速公司的AI优先势头。
Carmon的任务不仅仅是快速增长——而是要实现正确的增长。这意味着要投资于人才，完善交付系统，并确保每次客户互动都标志着清晰、关怀和可衡量的影响。他的到来标志着Robots & Pencils从移动先锋发展为AI优先咨询公司的又一个重要里程碑。
在过去20多年中，Carmon和Pagon共同建立、扩展并售出了咨询业务——从将Brulant从一个25人的初创公司发展成500人的强大企业，到在并购后使Rosetta的规模翻倍。作为Next Sparc的运营合伙人，Carmon在过去十年中帮助多个高增长企业将战略转化为成果。现在，他将同样的转型专业知识带入Robots & Pencils。
“Nathan是那种能让复杂的事情变得简单，能将雄心壮志变为可实现目标的罕见运营者，”Robots & Pencils的CEO及Next Sparc的总裁Leonard Pagon说道。“在我们建立Brulant时，我可以指望Nathan将宏大的愿景转化为日常的动能。我们快速扩张，因为他知道如何协调团队、提供价值，并快速精准地行动。有Nathan作为COO，我们不仅为未来做好准备——我们是为此而打造的。”
Carmon在每家公司都面临压力要求采用AI、现代化传统系统并以数字速度竞争的时刻加入。Robots & Pencils以挑战者的心态迎接这一时刻，部署高影响力的海豹突击队式团队，结合精英工程与直观的用户体验设计。作为一家在北美、拉丁美洲和东欧提供全球交付的公司，并与AWS、Salesforce和Databricks建立了合作关系，该公司独特地定位于帮助客户在AI时代中引领潮流。
“我曾帮助咨询公司从零起步规模化，我知道卓越的标准是什么，”Carmon说道。“Robots & Pencils具备这一切。这里的人才优秀，领导力大胆，市场时机更是绝佳。这个团队拥有我们再次实现成功的一切条件——而且是更大、更快、更智能的。”
“有了Nathan领导我们的内部运营，我可以腾出更多时间去做我能创造最大价值的事情——直接与客户接触，探索战略机会，并帮助塑造Robots & Pencils的未来，以满足市场需求，”Pagon说道。“这就是拥有一个你可以信任的领导者的力量。”
Carmon拥有密歇根大学的计算机科学学士学位和MBA。在加速数字转型的同时，他喜欢滑水、骑自行车、模型火箭以及与女儿和四个孙子共度时光。

