随着组织为VMware Cloud Foundation(R)（VCF）制定安全计划，这些新技术和指导工具使时间实现得以改善，并帮助有效维护所有关键和非关键应用的安全操作。

“组织往往需要管理成千上万的应用，这种复杂性使维持所有应用的可视化和横向安全变得困难，”Broadcom应用网络与安全部门副总裁兼总经理Umesh Mahajan表示。“VMware vDefend通过简化组织实现零信任和私有云安全目标的方式，突破了复杂性，提供了一种全面的横向安全实施方案。最新的vDefend创新进一步提高了这一效率，通过提供实时安全评估、简化操作的下一代安全服务平台以及代码化的微分段，进一步改善安全操作。”

VMware vDefend现作为VMware Cloud Foundation的高级服务提供。

私有云安全规划和评估的稳健性与安全智能
安全团队必须快速检测并调查其环境中的潜在漏洞。这需要在所有应用工作负载中采取主动的安全规划和快速的实施时间。为此，VMware vDefend在其安全智能工具中引入了安全分段评估和报告，以提供横向安全可视性和威胁分析。它提供了对组织安全分段态势的实时评估，旨在实施零信任私有云计划。该评估分析应用流量，以提供有关应用交互的及时、基于数据的洞察，精确找出由于不安全的网络协议和不当的应用通信而产生的潜在安全缺口，衡量进展并提供安全分段分数，并给出可行且易于实施的政策建议。此评估与规则建议相结合，帮助组织迅速在所有应用上通过VMware vDefend分布式防火墙展开横向安全保护，并在潜在漏洞面前保持领先。安全分段评估报告今天即可获得。

简化的安全操作
为了建立一个复杂的安全计划，组织需要一个一致、可靠的平台，以及一个优化的微分段方法，使客户能够在应用程序部署过程中应用安全性。vDefend通过引入以下新更新来满足这些需求：

安全服务平台（SSP）的更新：SSP是一个自包含的可扩展平台，简化了安全智能部署以及网络检测和响应、恶意软件防护等高级威胁预防工具的部署。新的SSP架构大大简化了用户体验，具有简化的网络设计、优化的生命周期管理、安全管理员的定制用户资料，以及配置和部署的更简单工作流程。增强的可扩展能力确保可视性和威胁预防能够自动扩展到大规模的VCF部署。

代码化的微分段：vDefend分布式防火墙提供了一种优化和简化的微分段方法。它内置在虚拟机管理程序中，能够将安全应用于每个工作负载，并采用API驱动模型，连接到自动化框架。这允许客户在应用程序部署过程中应用横向安全，并在应用程序环境中无缝扩展微分段。它具有一种声明式的上下文模型，以单一简化的方式部署客户的vDefend安全策略的全部意图，并包括内置自动化，消除了对外部脚本的需求。这一丰富的策略模型适用于虚拟机和容器工作负载，以确保一致的横向安全保护。

针对隔离环境的网络检测和响应增强：VMware vDefend的网络检测和响应（NDR）功能现在支持机制，使组织能够在没有外部网络访问的情况下，安全地更新内部操作中的威胁情报。这确保所有检测、关联和响应活动在闭合网络中以更高的保真度执行，利用内部和外部源的威胁情报。NDR为敏感、高安全性或分类环境中的针对性攻击活动提供了额外一层保护，并支持严格监管合规行业。

为安全VCF设计的VMware验证解决方案：这一最佳实践设计指南1具有指令性的用例指导，使安全团队能够快速为VCF的管理组件和应用工作负载推出零信任横向安全。

这些功能今日已可用。

第三方验证
第三方研究报告概述了vDefend的影响和价值。vDefend最近在SE Labs的高级安全测试报告中获得了高级威胁防护的AAA评级。SE Labs的方法论测试完整的攻击链，包括复杂的多阶段ransomware威胁，并使用威胁行为者常用的各种工具和技术来分析vDefend高级威胁防护的性能。AAA评级是供应商可以获得的最高评级，表示使用了顶级的威胁检测算法。此外，Forrester Consulting代表Broadcom进行的一项委托总经济影响（TEI）研究显示，代表受访客户的复合组织使用VMware vDefend实现了：

减少40%的网络安全 breach 风险
降低25%的安全操作支出
避免对网络安全保险费用的12%上涨

该研究还显示，复合组织使用VMware vDefend实现了116%的投资回报率。

