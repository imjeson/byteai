新vDefend功能简化并增强了针对高级威胁和 ransomware 的私有云横向安全实施
Broadcom Inc.（纳斯达克：AVGO）今日推出了VMware vDefend的新更新，使组织能够提升安全规划和评估、简化生命周期管理和操作，并在应用程序环境中无缝扩展安全性。![图片](https://ai-techpark.com/wp-content/uploads/Broadcom-Enhances.jpg){ width=60% }

