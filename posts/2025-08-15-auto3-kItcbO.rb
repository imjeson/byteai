今天，在SIGGRAPH 2025上，Autodesk, Inc.（NASDAQ: ADSK）推出了Autodesk Flow Studio（前身为Wonder Studio）的新定价政策，包括首个免费套餐以及Lite计划的价格下调50%——从20美元降至10美元——这一重大举措旨在让工具更具可及性。通过消除前期成本，使艺术家能够使用高级人工智能（AI）驱动的3D工具来创造曾经只属于好莱坞的动画和视觉特效，Autodesk正在为媒体和娱乐的未来信号一个新的时代——一个将创作者置于前沿的新时代。此次发布标志着Autodesk收购Wonder Dynamics公司一年后的重大里程碑，该公司创建了这一技术。

Autodesk Flow Studio利用AI自动化复杂的视觉特效任务，如动作捕捉、相机跟踪和角色动画，使创作者能够专注于讲故事，而不是技术设置。用户只需一个视频剪辑和几个点击，就可以将CG角色插入实拍场景并进行渲染，使3D动画和视觉特效对所有人都可及。用户还可以将AI生成的资产（如干净底片、3D场景、Alpha遮罩、相机跟踪）导出到Maya、Blender或Unreal Engine等工具中进行编辑和调整。这使得创作者能够加快复杂的动画和视觉特效工作流程，释放更多时间用于更具创造性的讲述。通过免费提供这些功能，Autodesk正在降低高质量内容创作的进入门槛，使更多的艺术家、市场营销人员和数字内容创作者能够在自己的条件下进行实验、学习并实现他们的创意。

“让Autodesk Flow Studio更易于为每个人所用，反映了我们对以创作者为中心未来的信念——在这里，AI进一步推动更多艺术表达，而不是取代它，”Autodesk媒体与娱乐执行副总裁Diana Colella表示。“通过降低成本门槛，更多的讲故事者现在能够探索新的AI驱动工具如何帮助他们实现创意，无论他们的创作旅程处于什么阶段。”

这一扩展引入了一种分级模型，旨在与创作者共同成长——从首次用户到专业工作室。新的免费和标准层级与现有的Lite、Pro和Enterprise产品一起，给用户提供了更多灵活性，以选择符合其创作需求和雄心的方案。免费的基础层包括AI动作捕捉和实景工具，而Wonder Animation和Wonder Tools等高级功能仍然仅限于付费层级。对于较大的团队和工作室，Enterprise层提供对AI驱动工作流程的可扩展访问，具有增强的支持和数据控制，帮助他们在保持专业制作所需的创造性流程和角色的同时采用新技术。

“在规模上运行AI模型一直是技术初创公司的一个巨大挑战，”Wonder Dynamics的联合创始人Nikola Todorovic表示。“我们最初的使命始终是让我们的技术尽可能免费和负担得起，但作为初创公司我们的资源有限。现在，作为Autodesk的一部分，我们能够将Flow Studio作为一款免费的产品推出，得益于强大的基础设施和云合作伙伴——因此更多艺术家能够讲述更多故事，而无需工作室环境或数百万美元的预算。”

免费层的推出以及灵活的定价模型向全球观众正式完成了Autodesk Flow Studio与Autodesk生态系统的整合。这确保了与Autodesk的标准产品、政策和流程的对齐，为用户提供更统一、一致和可扩展的体验。此外，Autodesk还将Flow Studio的Pro层包含在其媒体与娱乐集合中，无需额外费用，使现有客户能够以低风险的方式探索AI驱动的工作流程。这种做法反映了Autodesk引导客户穿越媒体和娱乐领域中不断发展的AI环境的承诺，其工具既可达成、值得信赖，并且能够随着他们的创作雄心而扩展。

