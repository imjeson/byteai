Imubit推出Optimizing Brain™解决方案：工业领域首个闭环AI优化解决方案，由强化学习驱动

休斯敦，德克萨斯州 – [2024年9月18日] – Imubit是一家全球闭环人工智能（AI）优化领域的领导者，推出了其Optimizing Brain™解决方案和工业AI平台。![图片](https://ai-techpark.com/wp-content/uploads/2024/09/Imubit-Laun-960x540.jpg){ width=60% }

---
这是首个基于强化学习技术的AI过程优化技术，获得了ARC咨询集团这一领先行业研究机构的认可，在他们的新的闭环AI优化（AIO）类别中。

Optimizing Brain™解决方案的核心是其基础流程模型™，与强化学习相结合。这些功能以数据为先导，将复杂的工业挑战安全地转化为高效的流程。通过提高利润率、减少排放量，并促进AI熟练的工作人员的培养，Imubit能够让工厂在快速变化的工业景观中实现运营卓越。

Imubit是美国十大炼油商中的7家信任的合作伙伴，在全球一些最大的工业加工厂中部署了90多个闭环AI应用程序，Imubit的技术正在提供切实的成果 – 提高炼油厂每桶30美分至50美分的利润，并在回转窑操作中将天然气消耗降低多达30%。

Imubit的首席执行官和联合创始人吉尔·科亨（Gil Cohen）强调了他们技术的转型影响：“ChatGPT和其他基础模型正在用其广泛适用性重塑行业。在Imubit，我们已经利用这种方法为工艺制造业打造了Optimizing Brain解决方案。它让客户能够在运营、规划和工程领域使用数据驱动模型，打破了工厂的分立，使所有技术人员能够在人工智能驱动的未来中茁壮成长。”

ARC咨询集团的行业分析师彼得·雷诺兹（Peter Reynolds）表示：“我们认为Imubit在实时工艺优化中使用强化学习是独特的并成为市场上的强劲差异化因素。”“这种方法促使我们在ARC重新思考我们如何对工艺优化和控制中的AI进行分类。Imubit属于一个新的类别，不同于传统的APC和优化解决方案，我们称之为Closed Loop AI Optimization，即AIO。”

Optimizing Brain™解决方案是在休斯顿举办的Imubit的“超越AI峰会和培训”活动上推出的，该活动汇集了一些最具前瞻性的工艺行业领袖。像美国马拉松石油公司、HF辛克莱、Citgo和Oxbow Calcining等运营商分享了Imubit如何帮助他们加快数字化转型之旅。

德雷克美国（Delek US）的业务转型高级总监凯尔·库奇塔（Kyle Kuchta）表示：“一旦您的初步商业案例得到解决，当您开始看到房间里充满了围绕着可能性的激动情绪时，这时您才真正能够实现人工智能的民主化。那时，您的运营团队、规划和工程团队、工程团队、IT团队可以探索所有这些您甚至可能都没有考虑过的其他问题，他们将开始发现利用Imubit平台为公司最大化价值的新方法。”

科亨补充道：“工业部门正在迅速发展。如果您不利用AI优化您的工艺操作，您的竞争对手就在利用。”
