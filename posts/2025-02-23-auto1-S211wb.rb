title: '内政部承包商在检查移民财务时收集英国公民数据'
date: 2025-02-24
author: ByteAILab

---

内政部被指控在对移民进行财务审查时收集了“数十万名毫不知情的英国家庭”的数据。

---
根据一名政府官员错误地发送给一家慈善机构的私人承包商的报告，信息涉及260多人，包括他们的姓名、出生日期和选民登记数据。这些人和申请者的唯一关系似乎是他们曾经生活或工作在同一地址或邮政区划内，但部分名单中列出的人早在1986年就已离开。该文件由信用报告公司Equifax于2024年6月25日生成，并在同一天通过电子邮件发送给了艾塞克斯和伦敦的难民和移民论坛（Ramfel）的一名案例工作者。这是为免除移民费用申请而准备的，该申请要求进行财务审查，以验证人员是否无力支付正常的签证、移民或国籍申请费用。到9月份为止，这一年共提交了超过80,000份这样的申请。

Ramfel的活动负责人Nick Beales表示，报告中提到的人数表明，内政部可能在“收集数十万名毫不知情的英国公民的财务数据”。Equifax在报告中附上免责声明，内容为：“此服务的可用信息的量和性质使Equifax Ltd难以验证……此服务仅为您自己的私人或内部用途提供。”Beales表示，内政部没有对最初标记的数据泄露的电子邮件作出回应，慈善机构于11月向内政部常务秘书Matthew Rycroft写了信。信中表示：“这引发了有关透明度、隐私以及潜在非自愿数据收集的严重问题，因为我们无法想象这些人，绝大多数可能是毫无先前与内政部联系的英国公民，会自愿同意内政部接收和存储他们的数据。”Ramfel询问了在使用后是否销毁第三方数据以及为尽量减少不必要的数据收集和共享而采取了哪些措施，但在12月收到的回复并未回答这些问题。

内政部客户服务部门的总监Joanna Rowland在一封信中表示：“我无法对个别流程进行详细评论，但我注意到您的建议，并已要求相关部门的官员进行考虑。内政部努力确保完全遵守英国通用数据保护法规和数据保护立法。这意味着处理并安全存储执行我们职能所需的最少个人数据，并删除不必要的数据。”内政部告诉《观察者报》，正在调查是否发生了数据泄露。目前，他们已不再使用Equifax进行签证费用减免处理。

政府统计数据显示，自保守党政府在2024年2月将移民健康附加费从624英镑增加至1035英镑以来，费用减免申请数量急剧上升。声明无法承担费用的人数从2023年最后三个月的13,600人猛增至2024年第一季度的18,500人、第二季度的22,800人，以及七月至九月间的25,600人，积压情况不断加重。

Beales表示：“在已经使申请人几乎须支付4000英镑的情况下，对于低收入或领取残疾福利的人额外进行财务审查显然是没有必要的。取消这些审查将有助于工党政府简化签证处理过程，减少导致人们等待超过一年的签证发布的长期延误，并停止对非自愿第三方数据的广泛收集。”

Equifax为政府部门和公共机构提供服务，包括工作和养老金部、税务与海关总署、国防部、学生贷款公司、司法部和NHS商业服务局。2023年，该公司因数据泄露而被金融行为监管局罚款1100万英镑，黑客因数据保护失误访问了近1400万英国消费者的信息。Equifax英国发言人拒绝评论，但指出法律指导表示，信用参考机构不需要获取同意进行数据收集，而是依赖数据保护法下的“合法利益”。内政部发言人表示：“任何数据泄露都是一个严重的问题，我们确保全面调查。我们继续采取强有力的措施，通过不断监测培训和保护措施来保护个人数据。”

这篇文章的副标题在2025年2月23日进行了修订。早期版本错误地表示Equifax意外将报告发送给慈善机构；实际上是由一名政府官员发送的，如本文所述。

