突破性的新平台在仓储、运输和人员管理中协调数百万个动作，以推动生产力的新前沿
基于Google Cloud和Snowflake的领先AI技术构建

全球最大的纯合同物流提供商GXO物流有限公司（纽约证券交易所代码：GXO）今天宣布推出GXO IQ，这是为物流行业开发的首个AI驱动的智能平台，由物流专家打造。![图片](https://ai-techpark.com/wp-content/uploads/GXO-Launches.jpg){ width=60% }

---
GXO IQ将帮助企业在当今全球供应链的复杂性中导航，通过部署行业领先的AI能力来协调更加高效、更加动态的物流操作。

“今天标志着GXO的一个里程碑，并且是行业的一个重要时刻，因为我们推出了GXO IQ，这是专门为物流开发的首个AI驱动的云原生操作系统，”GXO首席技术官Nizar Trigui表示。“在现代仓库中，很少有环境比这更动态或复杂。每天在每个站点都会做出成千上万的微决策。随着吞吐量的增加，专家需要一个与他们共同思考的系统，以有效地预测、管理和智能协调运营。这正是GXO IQ的构建初衷——成为未来的物流操作系统。”

GXO IQ基于20年的运营数据，利用一套专有的AI算法，智能协调库存分配和移动、订单拣选与包装、运输和人员管理等数百万个复杂的多步骤动作。GXO IQ由物流专家设计，并利用包括Google Cloud的Vertex AI和Snowflake Cortex AI在内的最佳技术栈。该工具还利用Google Cloud的API管理产品Apigee，为客户提供安全无缝的关键仓库数据访问。

GXO IQ由四个层级组成，创建一个无缝的智能操作平台：

数据层：GXO IQ利用GXO在为客户部署供应链解决方案方面的20年运营经验，以及来自数千名前线操作员的输入和一个利用数十亿交易的数据库。每天，超过2亿个信号被流式传输并组织，从而形成平台的数据层，训练出强大的AI算法以应对物流运营的各个方面。

AI协调层：AI算法在后台持续运行，实时预测需求变化、预判库存风险、主动发起库存移动，并识别每个订单的理想拣选、包装和运输流程。

端到端执行层：执行层包括一整套物流管理能力，带有市场整合和增值微服务，包括订单管理、仓储、退货和运输。这些服务完全整合成一个连贯的端到端解决方案，可以根据每个客户不断变化的需求进行定制。

体验层：一个独特的基于角色的界面，配有一个名为“GIL”的互动AI代理，提供单一视图，让用户能够查看重要信息并管理整个流程，从实时订单履行状态到异常管理，再到库存风险警报。GIL提供互动见解，识别机会，回答问题并将复杂性转化为可执行建议。

“我们的Vertex AI平台帮助GXO IQ提供商业所需的预测见解，以预见干扰并构建更具韧性、效率的供应链，”Google Cloud全球供应链与物流总监Paula Natoli表示。“我们将AI视为现代供应链的中央神经系统，将其从线性操作序列转变为智能、互联的生态系统。GXO IQ是这一转型的有力例证。”

“下一代运营需要的不仅仅是效率的优先技术，而是能够智能地预测、管理和协调运营。自动化到协调的演变是下一次飞跃。而我们已经实现了这一点，”Trigui补充道。

GXO IQ目前正在为美国客户提供GXO Direct，并将在2025年下半年广泛商业化。

