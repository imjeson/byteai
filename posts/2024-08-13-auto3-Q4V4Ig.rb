        UiPath在执行能力和愿景完整度方面表现最佳
        领先企业自动化和人工智能软件公司UiPath（NYSE：PATH）今天宣布，根据Gartner公司的研究报告，UiPath在2024年Gartner® Robotic Process Automation魔力象限图中被定位为领军者。![图片](https://ai-techpark.com/wp-content/uploads/2024/08/UiPath-L-960x540.jpg){ width=60% }

---
UiPath连续六年被评为领导者，而在本报告中，UiPath在执行能力和愿景完整度方面表现最佳。

        UiPath首席产品官Graham Sheldon表示：“我们相信这一认可体现了我们致力于推动创新，并提供一流企业自动化和人工智能平台的承诺，帮助企业提高效率，使人们的工作更加充实和战略。”他还表示：“我们充分利用AI与自动化相结合提供的巨大机遇，实现更高级别的自动化，以应对最复杂的流程。我们对UiPath平台持续投资突显了我们帮助逾1万名全球客户提高生产力、实现卓越成果并增强客户体验的承诺。”

        “魔力象限评估了13家企业RPA供应商，帮助企业为其自动化需求做出最佳选择。”可在此处免费获取2024年Gartner® Robotic Process Automation魔力象限图研究报告的副本。

        报告指出：“机器人流程自动化继续是核心软件市场，可提高运营效率，实现战术自动化。”

        UiPath业务自动化平台包括生成式AI、自然语言处理、流程编排、API集成、流程和任务挖掘、智能文档处理、低代码应用开发和应用测试。它可以部署在本地、云端或混合环境中。最近，UiPath宣布对UiPath平台进行了增强，包括UiPath Autopilot的普遍可用性，供开发人员和测试人员通过GenAI提示构建自动化、UiPath集成服务以实现快速开发和分享API集成以及对UiPath文档理解的改进，以支持复杂的非结构化数据处理与原生LLM。

        根据Gartner的说法：“领导者在RPA市场具有洞察力，可靠的业绩记录，影响市场方向的能力以及吸引和保留客户的能力。在RPA市场上，领导者展示了对企业客户需求的理解，以及扩展功能并向其核心RPA产品添加新产品和服务的机会。简而言之，领导者必须拥有市场领先的愿景，并有能力实现这一愿景。”

