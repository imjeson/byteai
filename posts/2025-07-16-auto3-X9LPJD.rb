Quest Software是一家全球领先的IT基础设施安全、微软和数据库环境现代化以及为AI驱动数据准备的公司，今天宣布全球发布Security Guardian Intelligence，这是一项针对其Security Guardian身份威胁检测和响应（ITDR）平台的生成式AI增强功能。该更新专门为混合Active Directory和Microsoft Entra ID环境而构建，帮助组织减少调查时间，并更快地应对身份威胁——即使没有专业的AD知识。

“Security Guardian Intelligence不仅能检测身份威胁，还能以业务或董事会层级的上下文来解释这些威胁，”Quest的总裁兼首席产品官Heath Thompson表示。“它为团队提供了一种更快的方式来优先处理实际风险并采取行动，而无需手动解释每一个技术细节。”

为什么这很重要：身份威胁是最大的风险之一

安全和IT团队面临着压力。基于身份的攻击正在快速增长，Active Directory问题导致的停机时间每小时可能超过73万美元。然而，团队仍然被警报过载、工具不连贯和AD专家短缺所拖慢。

控制身份威胁的时间越长，影响越大。一次成功的勒索软件攻击平均可以使系统停机23天，严重影响运营并延长恢复时间。

Security Guardian Intelligence (SGI)弥补了这一差距，提供了三项高影响力的功能：

- 简明的威胁摘要：将原始AD发现转化为任何人都能理解的可读见解
- 映射攻击者行为：与MITRE ATT&CK战术和现实世界的攻击场景相关联
- 内置修复：提供逐步解决方案指导，无需脚本或升级

“我们支持来自各行业的客户，他们被身份警报淹没，但缺乏内部专业知识来应对这些警报，”ivision的安全总经理和首席信息安全官Eric Aslaksen表示。“Security Guardian已经提供了可见性——SGI则增加了他们所缺失的上下文和速度。通过帮助呈现重要信息并指导响应，它正在成为我们身份安全工具箱中一个重要的工具。”

传统平台无法跟上

与仍以本地架构为中心并如今为AI功能进行改造的传统平台不同，Quest从一开始就是为云而构建Security Guardian，随时准备应对现代身份环境并实时使用生成式AI。由于性能和基础设施限制，传统本地工具通常难以将大型语言模型（LLMs）进行操作化，而Quest的架构使得安全和实时应用那些LLMs成为可能，覆盖实时身份遥测，无需额外的解决办法或附加组件。其结果是更快速的见解、更好的上下文和更可扩展的威胁响应。

云原生设计。从一开始就是为了AI准备

SGI直接嵌入到Quest的云原生平台中，专为Active Directory和Entra ID环境而设计。它使用点击-上下文模型，让用户在一步之内从身份警报转到业务影响和建议修复。SGI不仅仅是总结日志数据，它还分析实时信号，将其映射到真实世界的攻击者行为上，并提供清晰的行动指导——即使团队中没有资深身份专家。

无需额外费用内置

Security Guardian Intelligence现已向所有现有的Security Guardian客户提供，无需额外费用。它还与Quest解决方案无缝集成，包括其网络安全与恢复投资组合中的企业备份和灾难恢复、终端保护和24/7事件响应，使在攻击生命周期的每个阶段保护最关键资产变得更加容易。

