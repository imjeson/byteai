date: 2025-05-26
author: ByteAILab

---

Kharam将引导Discern的全球扩展，并重新定义组织如何构建和扩展网络安全规划  
Discern Security, 一家在AI驱动的安全管理领域领先的公司，已任命Evgeniy Kharam为新的首席战略官（CSO），此举标志着公司加速增长和创新努力的战略转变。![图片](https://ai-techpark.com/wp-content/uploads/Discern-Security.jpg){ width=60% }

---
Evgeniy拥有超过二十年的经验，涵盖安全架构、执行战略和网络安全传播。作为CSO，他将领导公司的战略路线图，推动市场扩展，并提升Discern的思想领导力，以便在全球范围内扩展其平台。  
“Evgeniy带来了罕见的技术深度、现实世界的客户洞察和富有远见的领导力，”Discern Security首席执行官Sai Venkataraman表示。“他简化复杂挑战的能力，将安全与业务成果对齐，以及赢得行业信任的才能，正是我们在Discern定义网络安全优化新类别时所需要的。”  
除了在企业领导层的工作，Evgeniy还创立了EK Cyber and Media Consulting，为网络安全供应商和管理安全服务提供商（MSSP）提供市场战略、产品整合和平台定位的建议。他的见解支持了投资尽职调查和并购评估，塑造了新兴安全企业的增长。作为网络安全社区中的受人尊敬的声音，他共同主持着领先的行业播客，包括Security Architecture Podcast和Cyber Inspiration。他对辅导和传播的热情体现在他的书籍《成功的架构：技术销售中的软技能艺术》中。Kharam共同创办了滑雪与滑雪板网络安全会议，并在北美和欧洲的大型企业中，主持了多个研讨会和评估，帮助组织弥补控制缺口并巩固其安全态势。  
“在帮助MSSP和增值转销商（VAR）扩大服务，向供应商提供功能战略建议，并启动行业会议和播客的多年经验后，我选择加入Discern，因为这是我见过的首个真正提供实时、数据驱动的清晰度的团队，我已经为此倡导了十多年，”Evgeniy Kharam表示。“Discern将猜测转化为可操作的见解——照亮资产覆盖、控制有效性和配置健康。”  
随着Kharam的加入，Discern Security准备加速其战略愿景，扩展到新的垂直市场，并深化全球市场执行。他的领导将增强公司的MSSP和VAR生态系统，通过主题演讲和媒体参与提升其公共形象，并更加关注产品采用——特别是在AI驱动的见解、董事会级别的报告和合理化控制管理方面。

