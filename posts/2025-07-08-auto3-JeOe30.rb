title: 'Advantech推出基于Snapdragon X Elite的边缘AI计算解决方案'
date: 2025-07-09
author: ByteAILab

---

Advantech自豪地推出其最新一套高性能边缘AI计算解决方案，采用Snapdragon® X Elite平台——AOM-6731、AIMB-293和SOM-6820。![图片](https://ai-techpark.com/wp-content/uploads/Advante.jpg){ width=60% }

---
这些创新产品基于这一开创性平台设计，旨在满足现代工业应用的严格要求，提供卓越的处理能力、集成的AI加速（高达45 TOPS的AI性能），以及将强大且快速的5G和Wi-Fi 7连接集成到工业PC中。

这些采用Snapdragon X Elite平台的解决方案配备了12核Snapdragon® X Elite和10核Snapdragon® X Plus，搭载领先的Qualcomm Oryoton™ CPU，运行速度高达3.4GHz。这种高性能处理不仅确保快速的数据处理和无缝多任务处理，还在日常任务中（包括Teams视频通话、本地视频播放、网页浏览和Microsoft 365）比传统x86解决方案平均节省28%的功耗。增强的AI能力使得这些设备集成了Qualcomm® Hexagon™ NPU，提供高达45 TOPS的性能。

这些搭载Snapdragon X Elite平台的解决方案配备了LPDDR5X内存，提供1.3倍的速度提升——从6400MT/s提升到8533MT/s——同时将功耗相比标准LPDDR5降低20%。此外，UFS 3.1 Gear 4存储的集成大幅提高数据传输速度，从1000Mbps（PCIe Gen3 NVMe）提升到令人印象深刻的16000Mbps。对于更大的耐用性和抗震能力，还提供UFS 4.0存储解决方案，确保在严苛的工业环境中保持最佳性能。

对于多媒体密集型应用，集成的Snapdragon Adreno第五代VPU支持4K60p全双工H.264视频编码/解码。此外，配备OpenCL、OpenGL和Microsoft DirectX 12支持的Adreno GPU确保了视觉中心任务的卓越图形性能。

Advantech的产品将连接性提升到一个新的高度，采用集成的Wi-Fi 7和5G技术，提供超快、低延迟的网络性能，确保即使在最苛刻的工业环境中也能实现不间断的数据流和实时通信。凭借Wi-Fi 7的多千兆速度和增强的网络可靠性，以及5G的广泛覆盖和高速能力，这些解决方案支持数据密集型AI应用和强大的远程操作。最终实现一个真正灵活和未来就绪的基础设施，优化实时处理和连接，赋能各行业在当今快速发展的数字环境中充分利用边缘AI的潜力。

AI模块AOM-6731、Mini-ITX主板AIMB-293和COM Express Type 6模块SOM-6820将于2025年3月开始提供工程评估。欲了解更多关于这些型号的信息，请访问Advantech网站。

