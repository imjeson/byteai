date: 2025-08-18
author: ByteAILab

---

对于首次，Autodesk推出了专业级的AI驱动视觉特效（VFX）和动画工具的免费访问权限——消除了世界各地独立电影制片人、数字内容创作者、营销人员和初次用户的成本障碍。![图片](https://ai-techpark.com/wp-content/uploads/Autodesk-launches.jpg){ width=60% }

---
 
2025年SIGGRAPH大会上，Autodesk公司（NASDAQ：ADSK）推出了Autodesk Flow Studio（前称Wonder Studio）的新、更加实惠的定价方案，其中包括首个免费方案以及针对Lite计划的价格降低——从20美元降至10美元——这是将工具集变得更加可获取的重大举措。通过消除前期费用，使艺术家能够使用支持高级人工智能（AI）的3D工具，创造曾经只属于好莱坞的动画和视觉特效，Autodesk正在为媒体和娱乐的未来信号发出一个新纪元——一个把创作者置于最前沿的时代。这一发布标志着Autodesk收购Wonder Dynamics（创造了这一技术的公司）一周年的重大里程碑。
Autodesk Flow Studio利用AI自动化复杂的VFX任务，如运动捕捉、相机跟踪和角色动画，使创作者能够专注于讲故事，而不是技术设置。用户只需一段视频剪辑和几次点击，便可将计算机生成的角色插入到实景场景中，并进行渲染，使3D动画和VFX对所有人都是可获取的。
用户还可以将AI生成的资产（如清洁底片、3D场景、阿尔法蒙版、相机跟踪）导出到Maya、Blender或Unreal Engine等工具中进行编辑和微调。这使创作者能加快复杂的动画和VFX工作流程，为更多创意讲故事留出时间。通过免费提供这些功能，Autodesk降低了高质量内容创作的门槛，使更多的艺术家、营销人员和数字内容创作者能够尝试、学习，并在自己的条件下实现自己的创意。
Autodesk媒体与娱乐执行副总裁Diana Colella表示：“使Autodesk Flow Studio更易于获取，体现了我们对创作者第一的未来的信念——AI应当更多地赋予艺术表达，而不是取代它。”她补充道：“降低成本门槛后，更多的讲故事者现在能够探索新的AI驱动工具如何帮助他们实现想法，无论他们在创作之旅的哪个阶段。”
这一扩展引入了一个分层模型，旨在伴随创作者的发展——从首次用户到专业工作室。新的免费和标准层级加入了现有的Lite、Pro和Enterprise产品，使用户能够更灵活地选择符合其创作需求和雄心的方案。免费层包括核心工具，如AI运动捕捉和实景拍摄，而更高级的功能——如Wonder动画和Wonder工具——仍然仅限于付费层。对于大型团队和工作室，Enterprise层提供可扩展的AI驱动工作流程的访问权限，提供增强的支持和数据控制——帮助他们在保留定义专业制作的创造过程和角色的同时采用新技术。
Wonder Dynamics（Autodesk公司）的联合创始人Nikola Todorovic表示：“在大规模运行AI模型对于科技初创公司来说一直非常具有挑战性。”他补充道：“我们的初衷是让我们的技术尽可能免费和经济，但作为初创公司，我们的资源有限。现在，作为Autodesk的一部分，我们能够提供Flow Studio作为一种免费增值产品，这得益于强大的基础设施和云合作伙伴——使更多的艺术家能够讲述更多的故事，而不需要工作室或数百万美元的预算。”
免费层的推出以及向全球受众的灵活定价模型的推出，也正式完成了Autodesk Flow Studio与Autodesk生态系统的整合。这确保了与Autodesk的标准产品、一致性和流程的对齐，为用户提供了更统一、更一致和可扩展的使用体验。此外，Autodesk已将Flow Studio的Pro层纳入其媒体与娱乐集合中，无需额外费用——为现有客户提供了一种低风险探索AI驱动工作流程的途径。这种方法反映了Autodesk的承诺，旨在引导客户在媒体和娱乐领域不断发展的AI领域中，通过便于获取、值得信赖且适合其创意雄心的工具进行探索。

