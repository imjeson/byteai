Reality Defender，这一获得RSA创新奖的深度伪造检测平台，与在线体验AI安全和安全解决方案的领先提供者ActiveFence今天宣布建立战略合作关系，将深度伪造检测能力集成到ActiveFence的AI安全基础设施中。

这一合作使得ActiveFence的客户能够通过Reality Defender的API无缝检测和防止视频、音频、图像和文本格式的合成媒体威胁。通过将Reality Defender的最佳深度伪造检测技术与ActiveFence的实时防护、AI防火墙和威胁情报能力相结合，此次合作为快速演变的AI生成威胁提供了全面保护。

随着生成性AI工具使得威胁参与者能够大规模创建越来越复杂的合成内容，企业面临来自深度伪造冒充、AI生成的欺诈和合成误信息活动带来的日益增加的安全和声誉风险。这一合作应对了急需的多层次AI安全基础设施，以实时检测、审核和修复有害的合成内容。

“深度伪造不再是孤立的威胁，它是GenAI驱动的操作浪潮的一部分，这种情况要求多层次防御，”ActiveFence的首席执行官兼联合创始人Noam Schwartz表示。“通过将Reality Defender的深度伪造检测与ActiveFence的实时防护结合，我们为企业提供了全面保护用户、平台和声誉所需的强大能力，以防止合成媒体滥用。”

“我们希望让企业级的深度伪造检测对任何开发者都易于访问，而不仅限于政府机构或财富500强公司，”Reality Defender的首席执行官兼联合创始人Ben Colman表示。“这一雄心驱动着我们与ActiveFence的合作——ActiveFence也受到财富500强公司和大量开发者的信任——使我们能够将这一影响延伸到现实保护中。公司和监管者意识到，对于GenAI而言，安全不能是被动的，而必须通过设计内置并通过集成的方式结合检测、审核和情报。这就是我们共同构建的内容。”

集成将在不久后可供ActiveFence客户使用，使他们能够在现有的安全和保障措施中利用Reality Defender经过验证的深度伪造检测模型。这一合作标志着在为企业提供所需的嵌入式、全面的AI安全基础设施以保护全方位的合成媒体威胁方面迈出了重要一步。

