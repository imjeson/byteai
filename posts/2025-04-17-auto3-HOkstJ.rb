Push Security 是浏览器级身份威胁检测与响应（ITDR）的先锋，已任命 Chris Tilton 为首席营销官 (CMO)，以增强其市场存在感和品牌领导力。Tilton 拥有超过二十年的网络安全市场营销经验，曾在 Bugcrowd、Cobalt.io 和 Probely 等公司担任领导职务。  
“Push Security 正在解决网络安全领域最关键的挑战之一：在浏览器级别保护身份，”Tilton 说。“团队的创新方法与现代企业的需求相一致。我很高兴能带领市场营销工作，扩大 Push 在行业中的影响力。”  
在 Bugcrowd，Tilton 担任市场营销副总裁，在提升品牌认知度和推动显著收入增长方面发挥了重要作用。他在确立 Bugcrowd 作为众包网络安全领域领导者的过程中扮演了关键角色。  
在 Bugcrowd 任职后，Tilton 加入 Cobalt.io 担任市场营销副总裁，领导增进公司扩张的计划，并巩固了其在渗透测试即服务（PtaaS）市场中的地位。最近，在 Probely 担任 CMO时，Tilton 负责市场营销运营，专注于自动化网络应用程序和 API 漏洞扫描解决方案，并在公司成功被 Snyk 收购的过程中发挥了重要作用。  
“在这个对 Push 至关重要的时刻，我们非常高兴欢迎 Chris 加入团队，”Push Security 首席执行官 Adam Bateman 说。“他丰富的网络安全市场营销经验和对这个领域的深入理解，将在我们继续成长和创新的过程中发挥不可或缺的作用。”  
随着网络安全环境的演变，基于身份的攻击日益增加，对浏览器级威胁的可见性和控制变得比以往任何时候都更加重要。Push Security 的平台赋能安全团队检测和缓解源自浏览器的身份威胁，提供对账户接管、会话劫持和未经授权访问的实时保护。  
Tilton 的任命恰逢 Push Security 重大发展之际，此前公司进行了产品增强，并因其在 ITDR 领域的进展获得行业认可。他的领导将对推动公司下一阶段的增长和客户参与至关重要。  
