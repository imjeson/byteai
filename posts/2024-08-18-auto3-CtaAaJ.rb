 Caresyntax 是快速增长的数据辅助手术领域的领导者。此次投资将加速 Caresyntax 的 AI 助力外科软件的采用和开发，并用于战略性并购活动。
Caresyntax 的数据辅助外科平台解决了全球3万亿美元外科市场中妨碍精密手术进展的关键数据缺失和碎片化挑战。通过使用人工智能收集、整合和分析全面数据，Caresyntax 提供实时和长期的临床决策支持，以改善患者的过程、提高医院运营效率，并实现更好的结果。
Caresyntax 的平台在外科手术前、中、后收集、整合和分析数据，充当外科医生的副驾驶员。通过在统一的厂商中立平台上链接来自不同来源的数据，Caresyntax 支持外科医生进行最佳准备，帮助患者理解和准备其手术，协助外科团队管理其工作流程以降低程序变异的风险。该平台的全面数据收集和分析功能还使后期外科风险评估和定制患者护理成为可能，从而实现更快的康复时间、降低成本和改善整体医疗交付。
目前，Caresyntax 的软件在全球 3,500 多个手术室中被超过 30,000 名外科专业人员使用，每年使超过 300 万次外科手术更加安全和智能。这笔新资金将使 Caresyntax 能够加快商业规模和其精密手术平台的采用，增强其人工智能和边缘至云应用，并支持战略性收购，以扩大在外科自动化领域的影响力和技术供应。

surgical.ai 与几家主要战略和金融投资者，包括 Symbiotic Capital、MTIP AG、Pictet Alternative Advisors、Aescuvest、PFM Health Sciences、BlackRock、Optum Ventures、Relyens 等一起，以八位数字的资本共同参与了本轮融资。

