surgical.ai，一家专注于外科自动化、机器人技术和软件的永久资本车辆，今天宣布积极参与了Caresyntax一轮1.8亿美元C轮融资。![图片](https://ai-techpark.com/wp-content/uploads/2024/08/surgical-960x540.jpg){ width=60% }

