随着人工智能重塑企业软件，Campfire的快速增长预示着财务与会计团队的新纪元。![图片](https://ai-techpark.com/wp-content/uploads/Campfire.jpg){ width=60% }

---

Campfire，这家迅速崛起的AI原生企业资源规划（ERP）软件领导者，今天宣布已完成3500万美元的A轮融资，由Accel领投，参与者包括Foundation Capital、Y Combinator、Capital 49及多位天使投资者，包括Vercel首席财务官Marten Abrahamsen、Mercury首席财务官Dan Kang、前Atlassian首席财务官Alex Estevez、前MongoDB首席财务官Michael Gordon、前OpenAI财务主管Sowmya Ranganathan，以及AirWallex联合创始人兼首席执行官Jack Zhang。
此轮融资将加速公司的使命，以重新定义人工智能时代的财务——推动更深入的产品开发、大规模的GenAI投资以及扩大覆盖范围，以服务更多雄心勃勃的全球公司。
ERP是企业软件中最大、最成熟的市场之一，市场总值超过1万亿美元，涵盖了Oracle、SAP和Intuit等传统巨头。这些平台自数十年前建立以来，已难以跟上今天快速变化的现代商业的需求。Campfire正在抓住这一大规模变革的时机，构建一个完全现代化、AI优先的ERP平台，已经成功吸引了NetSuite和SAP等成熟竞争对手的客户。
“我创立Campfire是因为，作为客户，我经历过传统ERP带来的痛苦，它曾经阻碍了我们的发展。我们的团队从头开始重建了ERP，利用最新的人工智能和现代架构，使财务团队能够加快月关闭、解锁更深层次的财务洞察，并做出更聪明的决策，”Campfire首席执行官兼创始人John Glasgow说道。“Accel曾是我在Invoice2go时的优秀合作伙伴和投资者，我在那里担任高管，并主导了公司6.25亿美元的出售给Bill.com。我很高兴再次与Accel团队合作。他们的信念和支持为下一阶段的增长带来了可信和信心。”
公司的GenAI对话界面Ember AI，基于Anthropic的Claude模型，使财务团队能够使用自然语言与数据进行交互，运行高级报告并自动化手动任务——从而带来真正的生产力提升和财务洞察。
“在Accel，我们长期以来一直支持变革性软件公司——包括重新定义网络安全的CrowdStrike、革命化工作场所生产力的Slack以及为AI基础设施设定标准的Scale AI。我们相信Campfire有潜力类似地重塑ERP市场，”Accel合伙人及Campfire董事会董事John Locke表示。“我们之前与John Glasgow合作，相信他在财务方面的深厚经验使他对首席财务官面临的挑战以及如何让人工智能提升财务团队有独特洞察。Campfire的快速产品执行、客户吸引力和对智能未来的愿景使其在领导下一波创新方面具备独特优势。”
Campfire的平台已经在多家企业中取代了传统产品，其客户如Replit、Trust & Will、Coder等都称赞其易用性、自动化功能以及支持复杂全球组织的能力。
“我在公司内部实施过Campfire，不止一次，而是两次。每次，我们在签约三周后便完成了账目闭合，”Flex的财务主管Brian Ehrlich表示。“在Flex，Campfire为我们节省了超过30万美元的实施成本，避免了雇佣1-2名全职员工，并提供了一个可扩展的系统，支持我们在员工超过1000人的增长中。借助Campfire，我们从不完整的对账转变为全面的GAAP应计会计，闭合速度明显加快。这是一个现代化的、AI驱动的平台，能够伴随我们走向未来——自动化过去需要团队数人完成的工作，无需顾问支持。”
“作为一个团队精简、需求复杂且业务量大的公司，NetSuite比起推动我们的发展，创造了更多摩擦，”Advisor360的会计经理Zach Doyle说道。“Campfire是能够与我们步调同步的现代ERP。Campfire帮助我们优化了财务流程，提高了速度，建立了随业务增长而扩大规模的基础，同时帮助我们在增长中保持精简。”
“作为一家快速发展的AI软件开发公司，我们的会计团队管理复杂的客户合同，”Coder的财务主管Daniel deCoen表示。“Campfire是能够跟上的AI原生ERP，自动化复杂的收入逻辑，并在我们扩展过程中提供了透明度。”
“在Trust & Will，快速增长带来了复杂的财务挑战，这需要现代、灵活的基础设施。作为一款AI原生ERP，Campfire独特匹配了我们业务的速度、复杂性和雄心。通过自动化手动流程并提供更深层次的财务洞察，Campfire为我们的团队提供了清晰度和时间，让我们专注于战略性工作，与我们的规模扩展并肩前行。”Trust & Will的首席财务官Ron Wangerin表示。
ERP市场正处于一个转折点，随着人工智能和云原生架构重塑格局，正出现“伟大的解包”机会。Campfire位于旧金山的团队快速开发出了一款定义类别的AI原生ERP，旨在支持从初创公司到IPO的各类公司。这轮融资将进一步加速公司的使命。
