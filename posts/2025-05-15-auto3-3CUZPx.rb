新版本为组织提供了建立数据信任的工具，通过对AI模型进行认证、提高发现能力并减少数据治理的手动工作。

该发布恰逢组织面临日益增长的压力，确保AI驱动的决策透明、可解释且基于可靠数据。

为什么这很重要
没有强有力的治理和可信数据，AI项目往往会失败。Gartner预测到2025年底，30%的生成AI项目将在概念验证后被放弃。
计划与现实之间的差距往往归结为基础性问题，如数据准备。erwin数据智能15通过自动化AI模型认证、个性化的数据发现体验和新的GenAI工具，加速政府治理任务，解决了这些问题。

“随着公司努力将AI投入生产，他们面临的一个关键问题是：我们的数据是否足够好以信任结果？” Quest Software副总裁Bharath Vasudevan说。“这一发布帮助公司自信地回答‘是’。我们提供工具来认证AI模型，快速识别最有价值的数据，并减少管理所需的时间。”

Quest对AI模型认证的自动化方法已经引起行业的关注，因为它能够显著改善AI的准备度和信任度。
“erwin数据智能15中的自动化AI模型认证为组织提供了对AI模型及其支持数据的准备性和可靠性的大视野，” IDC数据智能和集成软件副总裁Stewart Bond表示。“这种集中式的AI治理方法可以帮助组织推进其AI数据准备工作，支持更加成功的AI结果，同时应对监管和操作风险。”

erwin数据智能15的新功能

AI模型认证

使用Quest的结构化七步框架认证AI模型——内置于erwin数据智能15中——作为唯一通过数据智能自动化AI模型认证的解决方案。它不仅限于基本跟踪或合规性，而是指导团队通过一致的流程将可信模型投入生产。
自动跟踪模型成熟度和数据质量，使用可配置的认证框架。
将AI模型分类到可定制的成熟度层级中，状态对治理和数据科学团队可见，以便进行集中监督。

数据评估和信任评分

使用多达九个加权标准对数据进行评分——包括质量、相关性、使用情况和治理完整性。根据业务目标自定义每个标准的权重，并自动生成一个价值评分，以突出可信的高价值资产。
分配金、银或铜层级，使数据价值评分一目了然。
通过使高价值数据更易于发现和使用，支持数据货币化。

基于角色的市场见解访问

在erwin数据市场中提供基于角色的登录页面，以加速相关资产的发现和治理。
从现成可用的可视化库中选择，快速获取见解，并按资产类型过滤结果——如数据产品、AI模型或业务政策。
获取有关最近策划、高评分、推荐或需要关注的资产的可操作见解，以提高治理的速度和效率。

现有Microsoft客户的奖励

erwin数据智能15包括新的erwin标准数据连接器，可将元数据从Microsoft Dataverse导入组织，从而在应用erwin的所有能力时获得对它们的Microsoft数据环境的可视性，包括AI模型认证、自动治理、信任评分和数据市场集成。

erwinAI——减轻数据管理员负担的GenAI
与erwin数据智能15发布同步推出，erwinAI是一个基于GenAI的智能聊天机器人，减少手动治理，提供新的数据管理加速器。它减少了手动工作，帮助更快地弥补治理差距。初始功能包括：

加速表和列的分类以及商业术语定义的tlp生成。
管理员可以快速发现缺失的分类和不完整的定义，然后通过智能聊天机器人审查、批准并应用建议的更新，支持完整的审计跟踪。

更多erwinAI的功能将在2025年陆续推出，带来新的管理和发现加速器，旨在使数据治理更智能、更加直观。

