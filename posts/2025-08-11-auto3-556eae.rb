---

Arria NLG，全球领先的自然语言生成AI和代理AI的领军企业，自豪地宣布与Resolute达成战略合作关系，Resolute是一家专注于双重用途技术、国防创新和商业战略的知名咨询公司。![图片](https://ai-techpark.com/wp-content/uploads/Arria.jpg){ width=60% }

---

十多年来，Arria已经为许多财富500强公司和政府组织（包括美国国防部）架构、开发和部署了先进的AI系统。Arria拥有45项专利，提供独特而强大的AI技术。
Resolute与Arria共同发现了一个Arria可以解决的AI技术融合中的差距。这主要用于那些对准确性、控制、安全性和合规性标准要求极高的应用场景。例如，寻求在遵循AI治理标准的同时提高自动化效率的组织。此合作关系满足了政府和商业部门日益增长的市场需求。
“与Resolute的合作加速了我们将智能、域感分析引入关键领域的使命，” Arria NLG首席执行官Sharon Daniels表示。“他们在国家安全和商业市场的领导地位，加上Arria经过验证的AI编排和叙事智能，使我们能够在精准、规模和可解释性至关重要的地方推动创新。”
作为此次合作的一部分，Mark “Turk” Powell——一位在国防、商业创新和资本战略方面拥有丰富经验的专家——将担任Arria的战略执行顾问，提供战略监督和深厚的领域专业知识，涵盖联邦和企业项目。Powell在双重用途创新方面的领导经验丰富，并在多项大规模商业增长倡议中担任顾问。
Resolute的领导团队包括前海军副部长Dionel Aviles和国防创新及投资领域的老兵Pete Peterson。他们的洞察力和国家安全经验将支持任务关键型应用的代理AI的采用。
“Arria正在通过提供既高效又具有成本效益的安全技术，设定行业标准，”Powell说。
Arria和Resolute共同塑造下一代企业和政府成果，在实时、高风险决策中需要速度、清晰度和信任。这项合作关系强调了将AI运营化以实现安全、可扩展和任务对齐的成果的共同承诺——通过智能自动化将数据转变为战略优势。

