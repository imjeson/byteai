IFS，企业云和工业人工智能软件的领先提供商，今天宣布推出IFS Nexus Black™，这是一个战略创新计划，旨在加速工业组织高影响力人工智能的采用。![图片](https://ai-techpark.com/wp-content/uploads/IFS-Lau.jpg){ width=60% }

