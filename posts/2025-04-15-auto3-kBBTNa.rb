OpenText™（NASDAQ: OTEX），（TSX: OTEX）信息管理的领导者，帮助组织解决复杂的全球问题和影响深远的转型，提供全面的商业云、商业AI和商业技术（包括安全性）解决方案。通过创新的信息管理解决方案，包括其最新旗舰产品OpenText Cloud Editions 25.2的可用性，OpenText帮助组织创建新的数字劳动力，提高人类潜力和生产力。  
“商业正在被流程自动化、数据和新的数字知识工作者的力量重新想象，” OpenText首席执行官兼首席技术官Mark J. Barrenechea表示。“信息提升了所有组织，新的数字劳动力更快、更准确地完成工作，并释放人类才能以更大的速度和规模推动增长、创新和效率。我们很高兴与BMO和Catalent合作，推动他们通过信息管理进行转型。”  
OpenText的客户正在利用其全面的软件和AI套件来应对关键挑战，自动化流程，提高网络安全性，并在各行业推动快速创新。  

客户亮点：  
BMO：为运营卓越扩展内容管理  
BMO作为OpenText的长期合作伙伴，通过利用OpenText™内容管理（Extended ECM），将其信息管理能力扩展到20个业务领域、13,000名用户以及超过60个工作流程，一个智能的企业内容管理解决方案，在安全地管理信息生命周期方面取得了成功。BMO与OpenText的18年合作关系帮助提升了银行各部门的协作与合规组件，实现了更智能的工作流程和无缝的客户体验。  
“在BMO，我们认识到信息是提供卓越客户体验和推动运营卓越的基础性要素。与OpenText的合作对我们扩大内容管理能力、增强各业务领域之间的协作至关重要，并进一步拥抱AI和云计算等创新，” BMO公司和商业银行首席信息官Alok Daga表示。“在OpenText安全、智能的平台的帮助下，我们不仅超越了客户日益变化的期望，还解决了关键的监管和网络安全挑战。”  

Catalent制药解决方案：无纸化制造愿景  
Catalent正在领导数字化转型，作为其开发、制造和供应帮助人们更健康、更美好生活的产品的使命的一部分。这包括在全球制造现场推进数字化工作流程和数据集中化，减少浪费，提高效率。OpenText的云解决方案使Catalent能够满足严格的监管要求，并在私有云中安全运行，同时支持其向无纸化环境的过渡。  
“我们自豪地与OpenText合作，作为我们数字化转型旅程的一部分，成为领先的合同开发制造组织（CDMO），为制药和生物技术客户提供创新产品，这些市场需要严格的数据管理、安全和合规性，” Catalent企业应用副总裁Lindsay Fernandez表示。“通过与OpenText实现制造流程的端到端数字集成，我们正在实现Catalent的无纸化未来愿景，并构建一个未来准备好的数字生态系统，作为我们推动客户和他们所服务患者卓越承诺的一部分。”  

展望未来  
Barrenechea总结道：“OpenText在过去30年中一直在进行自动化，我们将继续在一个新的时代中进行自动化，同时利用AI的力量。信息正在被重新想象，以更智能的方式服务客户。OpenText世界2024展会正是证明，今年的大会不仅是创新的展示，它还证明了信息作为每个组织心跳的力量。随着AI和信息管理的前沿，未来对那些准备抓住机遇的人来说将无穷无尽。”  
OpenText世界2025将于2025年11月17日至20日在纳什维尔举行，注册现已开放。  

