---

Dot Inc. 受邀参加微软年度开发者大会“@Build 2025”，并于5月20日在西雅图会议中心发布了其无障碍解决方案 Dot Vista，该方案结合了微软的 Copilot+ PC 和 Windows AI Foundry 技术。![图片](https://ai-techpark.com/wp-content/uploads/Dot-Inc.jpg){ width=60% }

---
Dot Vista 是由 Dot Inc. 的人工智能团队与汉阳大学的 Yuongjae Yoo 教授的 AI 团队合作开发的。它是全球首款使用 Windows AI API 和 Dot Pad 的触觉显示屏将 PowerPoint 幻灯片文本和视觉内容（如图表和图形）转换为语音和浮雕图形的 Windows 无障碍应用，帮助视觉障碍用户更好地跟随演示。

这次展示是微软 Windows AI API 团队主办的一场会议的一部分，Dot 在一台集成了三大 Windows AI API 的 Windows Copilot+ PC 上进行现场演示：

- Phi Silica API: 快速总结文本密集型 PowerPoint 幻灯片。
- 图像描述 API: 提供幻灯片图像的上下文描述。
- 文本 OCR API: 提取和解释图表和图形中的数据。

Dot 的首席执行官 Eric Ju-Yoon Kim 表示：“我们选择 Windows AI API 是因为它们轻量级的架构，可以快速总结和提取关键信息，而无需依赖复杂的 LLM 服务器。” 

Dot 的旗舰产品 Dot Pad 打破了传统单行盲文显示器的局限，可以同时显示多达七行信息，使用户能够快速浏览大量信息。它还直观地呈现复杂内容，如数学公式、扩展代码片段和表格，提高了学习效率和工作生产力。

RNIB 无障碍专家 Dave Williams 强调了 Dot Pad 在促进包容性方面的变革潜力：“Dot Pad 提供了对地图到音乐的触觉访问，并使盲人用户与有视力用户之间的合作成为可能。作为一名盲人父亲，我终于可以通过触感体验我儿子的画作。随着 AI 的发展，这仅仅是个开始。”

牛津大学的领先触觉学习研究者 Maziar Zarrehparvar 表示：“我对 Dot 团队如何利用 AI 使我们的内容更加互动深感印象。我们相信，Dot 的先进机器学习模型将加速视觉障碍学生个性化学习的未来，牛津大学完全支持 Dot 基于 AI 的个性化教育方法。”

Dot Inc. 已与牛津大学和波士顿大学等领先机构合作，共同开发“触觉课程”。它还扩大了与图书馆及英国皇家国立盲人协会 (RNIB) 等组织的合作，设定了无障碍教育和数字公共服务的新标准。

这些全球努力使 Dot 成为韩国公共和教育领域以及北美、中央美洲和南美的重要无障碍合作伙伴。Dot 的开创性创新和社会影响在国际上获得了广泛认可，包括 CES 创新奖和 SXSW 创新奖。

