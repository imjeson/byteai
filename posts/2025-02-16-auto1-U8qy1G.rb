全球首富埃隆·马斯克本周誓言将监督政府机构的激进削减，声称某些机构应该“彻底删除”，并通过削减公共项目和裁员来实现这一目标。

---
尽管这些大规模裁员被框架为消除浪费的手段，但这也可能成为私人公司——包括马斯克自己的企业——的福音，政府越来越依赖这些公司来推动其许多关键倡议。马斯克和他在“政府效率部”（Doge）中的盟友，这个非正式委员会作为他削减开支努力的运作机构，已针对多个主要政府部门。他们已着手关闭美国国际开发署，削减教育部的预算，并接管负责联邦信息技术结构的总务管理局。Doge的工作人员还获得了财政部的接触权限，并将目光投向国防部、能源部、环保局以及至少十多个其他部门。

随着Doge在政府内开始进行深层次的削减，马斯克及其代表呼吁在联邦机构内实施新的人工智能系统，并彻底改革美国的武器计划。人道救援组织因马斯克的削减而感到不安，而科技和国防公司则看到在新的特朗普政府议程中更深入整合自己的机会。

马斯克的计划已经引起了硅谷主要企业如Palantir的兴奋，该公司的高管在上周的一次财报电话会议上称赞Doge，并谈及亿万富翁的突击队带来的干扰对公司的利好影响。Palantir近年来已赢得了数亿美元的美国军事合同，用于人工智能相关项目。

“我认为Doge将为政府带来绩效和透明度，这正是我们商业业务所追求的，”Palantir的首席技术官Shyam Sankar在电话会议上表示。Sankar称，削减开支将瞄准那些被称为“深层国家的圣牛”的落后软件项目。

“这是一场革命，有些人将会被裁掉，”Palantir的首席执行官亚历克斯·卡普补充道。“我们预计会看到意想不到的事情，并获胜。”

其他首席执行官和科技高管同样对马斯克表示赞赏，并告诉投资者Doge的计划代表了他们公司的机会。这个数十亿美元的加密货币交易所Coinbase的首席执行官布莱恩·阿姆斯特朗上周称赞Doge取得的“伟大进展”，并建议将政府支出放在区块链技术上。国防承包商如洛克希德·马丁和诺斯罗普·格鲁曼在最近的财报电话会议上对这项努力表示赞同，而生产捕食者无人机的通用原子能航空系统公司则在上月直接写信给马斯克，请求他加快五角大楼处理国防合同的速度。

随着各公司寻求从Doge的政府重塑中受益，马斯克通过自己的公司诸如SpaceX的合同价值数十亿美元，潜在地将在新政府下扩展。马斯克对政府机构的削减已引发与他在2022年收购推特后的疯狂裁员行为的比较，但Doge的长期影响可能更类似于SpaceX与NASA的关系，以至于现在它负责美国大多数的航天发射。如今，像马斯克的卫星通信提供商Starlink这样的产品已经变得如此关键，以至于美国现在依赖它们从事一切事务，从在外国冲突中的使用到国内灾难救助。

SpaceX在2006年赢得了其第一个NASA合同，获得了278万美元用于向国际空间站的货运飞行。该公司的火箭技术创新降低了航天飞行的成本和增加了发射次数，降低了较早的航空航天竞争对手的价格。随着NASA在2000年代末逐渐将其需求外包给私营公司，SpaceX继续与该机构建立了约150亿美元的合同。五角大楼向该公司授予了数亿美元的卫星合同，扩大了其影响力，并在马斯克拒绝让乌克兰使用Starlink对俄罗斯发起攻击时创造了一场危机。

如今，SpaceX控制了全球超过60%的活跃卫星，而就在上个月，NASA选择该公司救助两名被困宇航员。从外包补给飞行开始，SpaceX已变成美国一些标志性机构的依赖者，甚至在马斯克成为特朗普政府的一个固定成员之前就已经存在了。

剥除对马斯克利益冲突的监督

唐纳德·特朗普已赋予马斯克巨大的权力来整顿政府，其中周二发布的行政命令要求各机构与Doge在人员决策和削减方面进行协调。马斯克在白宫的影响力也使他的公司面临各种指控的联邦调查，这些指控涉及违反联邦劳动和证券法。特朗普已解散了一个调查特斯拉的监管机构。政府问责机构警告称，马斯克的众多潜在伦理冲突及其在政府中的行动缺乏透明度，可能会导致他利用自己的权力进行政治腐败。

“你并不需要成为任何类型的伦理专家就能理解，一个亿万富翁如果帮助资助总统的竞选活动，并拥有自己的政府合同，而被赋予了权力去彻底检视影响政府承包商支付的机构系统，这将存在多大的问题，”华盛顿责任与伦理公民组织（Crew）的执行主任唐纳德·谢尔曼表示。Crew是几个提起诉讼的倡导组织之一，指控Doge违反联邦透明度法。工会也已提起诉讼，但法官允许马斯克和特朗普的收购继续进行。

马斯克与Doge的联邦机构掌控与特朗普解雇了许多可以作为伦理冲突和不当行为检查的政府官员的举动同时发生。特朗普上任不久后便解雇了18位各部门的监察长，后者负责对政府官员的滥用行为进行监督。

“特朗普解除了可以调查马斯克及其随从在这些机构中所作所为的独立监察员，然后让他在这个机构内肆意妄为，”谢尔曼表示。马斯克担任“特别政府雇员”的角色，这使他能够绕过通常的财务披露要求，而周二白宫官员表示，虽然马斯克计划进行这样的报告，但这将是保密的，并不会向公众披露。

特朗普政府一直对马斯克在政府中的角色带来的伦理问题表示不屑一顾。白宫新闻秘书卡罗琳·莱维特上周与记者交谈时表示：“如果埃隆·马斯克遇到与Doge所监管的合同和资助相关的利益冲突，那么埃隆会退出这些合同。”

采购法律禁止政府官员对自己的业务进行自我交易，以及授予带有伦理冲突的合同。但这些法律是细致入微的，可能仅适用于与谁获得这些政府合同有直接决策权的人，这使得马斯克广泛影响的灰色地带开放了，使得政府官员可能因马斯克的影响而批准对他有利的合同，或者决定否决那些对他的竞争对手有利的合同。

“这部分法律并不是为了应对特朗普总统允许马斯克达到的政府功能的捕获程度而设计的，”谢尔曼说道。

