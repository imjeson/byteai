PsychNow今天宣布推出Chapter，这是首个以人工智能驱动的临床咨询平台，旨在改变患者与行为健康提供者之间的首次接触。
Chapter远远超越了传统的接收、书写或聊天机器人。它引入了一种以叙述为中心的评估方式，在这种方式中，患者自己的故事变成了一种新的生物标志物。通过捕捉不仅是症状，还有一个人的思考、感受和关系，Chapter揭示了以往在行为健康技术中看不见的更深层次的人类模式。

从接收低效到主动的临床伙伴关系
传统上，第一次会话往往用于收集历史信息，通常通过不连贯的表单和紧迫的对话进行。这第一次会话是在经过10-20分钟未报销的临床时间之后进行的，且可能需要经过几轮“电话联系”。临床医生在短时间内很难拼凑出患者的完整图景，延误了治疗介入，助长了职业倦怠，侵蚀了临床结果，并导致行政瓶颈。
Chapter完全翻转了这一模式：

患者以自然的方式、用自己的话和时间分享他们的故事。
增强的人工智能评估模拟真实世界的临床访谈，而不是核对表。
提供者收到叙述故事和细致入微的见解，揭示症状和诊断下的模式。

“在行为健康领域，最具信息性信号是患者的故事，不仅是发生了什么，还有他们如何理解这一切，”PsychNow的联合创始人兼首席执行官阿伦·瑞克林博士说道。“Chapter不仅捕捉症状，还捕捉一个人与世界的关系，使临床医生在真正的理解上获得了强有力的起步。”

Chapter：超越接收表，超越聊天机器人
与自我报告筛查、接收表单或人工智能书写不同，Chapter提供了一种完整的人本咨询体验，超越了定时会话，最终以集中的现场接触为高潮。它不仅仅是文书工作的自动化，而是第一次访问的数字起点。患者满意是因为他们得到了真正的理解。Chapter将接收时间减少了50%以上，患者完成率超过99%。几乎所有早期的临床采用者都报告了该产品的一致质量，并感到实践得到了重新点燃。提供者报告了更强的治疗联盟、增强的临床理解以及更快启动有意义的护理。

应对行为健康的时刻
随着行为健康和初级护理面临激增的需求、资源缩减和创纪录的职业倦怠，Chapter提供了一种在不牺牲人际连接的情况下扩展高质量护理的方法。
“这就是我们开始扩展有意义护理的方式——不仅更快，而且更好，”瑞克林博士补充道。“Chapter是我们更大愿景的第一步：以低于每周共付额的方式提供高质量的、人与人之间的心理健康护理。”

