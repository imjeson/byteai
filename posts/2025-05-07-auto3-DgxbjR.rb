新的功能简化了操作并降低风险，加快资本部署
Kapwork，这一AI平台专注于保理，今天宣布对其旗舰产品Kapwork Verify进行重大更新，以提升保理过程的速度和效率。![图片](https://ai-techpark.com/wp-content/uploads/Kapwork.jpg){ width=60% }

---
关键的新功能包括电子邮件验证代理和一个集中用户界面，可以实时查看所有发票的状态，而无需人工对账，从而使保理商能够跟踪其投资组合，并自信地大规模部署资金。
保理历来依赖人工流程来管理其发票操作，这使其面临着高昂的成本、低效与工作流中的脆弱性。从欺诈和信贷风险（如错误支付和虚假发票）到在对账支付时的资金损失，传统的保理行业每年损失数十亿美元，令许多企业濒临破产。Kapwork直接降低了这些风险，为保理 operations 带来了透明度、速度和安心。

消除摩擦，通过AI加速工作流程
在推出时，Kapwork通过直接连接到供应商管理系统（VMS）自动化了手动发票验证过程。如今，Kapwork将这种自动化扩展到电子邮件收件箱，这一直是保理工作流中的最大瓶颈。
在所有发票处理时间中，高达90%用于手动筛选数千条电子邮件线程以处理相关发票。Kapwork的新电子邮件验证代理会自动提取并汇总电子邮件中的交易状态，形成一个集中且全面的视图。通常需要1到2天的手动输入，现在只需几秒钟即可完成。新的、待处理的和已结算的交易实时显示，帮助保理商了解他们的库存。

Kapwork还推出了一个集中的用户界面，便于快速访问所有保理商的发票库存。新的界面跨多个来源（电子邮件、供应商门户、内部系统）对数据进行对账。Kapwork还可以提醒可供购买的新发票，甚至检测异常，从而降低欺诈风险。没有Kapwork，保理商需负责下载供应商数据并比较各个字段以识别变化，这导致成本高昂的人为错误和错失机会。

“保理需要速度、精确和信任，但手动流程使这一切变得更加困难。”Kapwork的联合创始人兼首席执行官 Mauricio Vergara 表示，“一个虚假的发票可能造成数十万美元的损失，而多年的小效率问题则会随着时间的推移侵蚀利润。我们构建Kapwork是为了帮助保理商最大化利润并更快为中小企业融资。”

自愈AI以提高可靠性
传统的自动化工具如机器人流程自动化（RPA）在系统或网站变更时往往会失效，迫使个人进行手动干预。Kapwork的平台由自脚本AI代理支撑，这显著提高了发票验证的速度和可靠性。Kapwork的代理在完成任务时自动编写和保存可执行脚本。如果代理遇到错误或网站变更，AI代理会自愈，以诊断和解决问题。这个过程显著减少了停机时间和维护开销，即使在复杂性和交易量上升的情况下，也能提供准确的实时数据。

实时自动化的保理未来
对于Kapwork的客户而言，这些好处是直接而可测量的。使用Kapwork的团队可以在不增加人手的情况下扩大资金部署，将花费在手动劳动上的时间再投资于增长，并降低在临时系统与内部解决方案之间操作的复杂性。
以总部位于纽约的精品保理公司TowerCap为例，该公司已处理超过15亿美元的发票。TowerCap在工作量方面显著减少，将发票验证的频率从每周两次提升到每日进行，加快了他们的资金部署能力，并积极影响整体盈利能力。高管们现在花更少的时间参与日常验证，转而专注于战略增长计划。

“保理已经达到了一个实时自动化、AI和云工作流程不再是可选项的地步。早期的采用者已经在数小时内为客户提供资金，减少了后台成本，并加强了欺诈控制——而不增加人手。”国际保理协会加拿大分会会长及Oscar Rombola’ & Associates顾问Oscar Rombola表示，“延迟这一转变的公司不会立即消失，但每一季度将会发现竞争在价格、速度和风险管理上的压力越来越大。”

