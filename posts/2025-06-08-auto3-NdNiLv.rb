---

Scope Technologies Corp. (CSE: SCPE) (OTCQB: SCPCF) (FSE: VN8) (“Scope Technologies”或“公司”) 今天宣布任命 Ted Carefoot 为首席执行官，接替 James Young，后者将继续以顾问身份支持公司。![图片](https://ai-techpark.com/wp-content/uploads/Scope-Tec.jpg){ width=60% }

---
  
Carefoot 是一位资深的技术领导者，在软件开发、网络安全、人工智能和风险管理领域拥有超过两年的经验，他最近担任 Scope 的产品副总裁。Carefoot 的职业生涯包括在电子艺术公司和迪士尼在线工作室加拿大的领导角色，具备开发大规模安全解决方案和合规框架的深厚专业知识。Carefoot 拥有治理、风险与合规 (GRC) 和综合数据隐私的认证，并因其在风险管理框架 (RMF) 实施方面的专业知识而受到认可——这在 Scope 扩展其后量子安全产品时是一项至关重要的能力。  
“随着 Scope Technologies 进入下一个增长阶段，我们致力于在量子安全创新和合规方面保持领先，” James Young 说。“Ted 的领导能力、行业经验以及在风险管理和法规标准方面的深厚专业知识，使他成为引导 Scope 走向未来的理想人选。我对他能够扩展公司并实现我们的使命充满信心。”  
在 Carefoot 的领导下，Scope Technologies 将继续专注于为企业和政府客户提供量子弹性加密和安全解决方案，帮助组织为日益增长的后量子密码学（PQC）标准的监管要求做好准备。  
“在这样一个关键时刻，我很荣幸能担任这一职位，” Ted Carefoot 说。“随着量子计算威胁成为现实，企业和政府必须立即采取行动以保护他们的数据。我期待带领 Scope 的优秀团队，帮助组织为这些新兴威胁的安全基础设施做好准备。”  
Scope Technologies 还宣布 Sean Prescott 辞去了公司的董事职位。公司感谢他担任董事期间的服务。Prescott 先生将继续担任 Scope Technologies 的首席技术官。  

限制性股票单位和股票期权的授予  
Scope Technologies 还授予总计 950,000 股的股票期权，行使价格为每股 $0.385，期限为自授予日起五年。股票期权须遵循董事会设定的归属要求。此外，Scope Technologies 向 Carefoot 先生授予了 1,500,000 个限制性股票单位（“RSUs”）。RSUs 的归属规则如下：在授予日期后的一年内归属 250,000 RSUs，二年内归属 500,000 RSUs，三年内归属 750,000 RSUs。  

