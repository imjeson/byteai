title: '尼日利亚起诉加密巨头币安，索赔815亿美元经济损失和欠税'
date: 2025-02-20
author: ByteAILab

---

尼日利亚已提起诉讼，要求币安支付795亿美元的经济损失，政府称该加密货币交易所的运营造成了这些损失，并要求支付20亿美元的欠税，法庭文件显示。

---
有关当局将币安——全球最大的加密货币交易所——归咎于尼日利亚的货币问题，并在2024年拘留了其两名高管，因为加密网站成为了交易当地奈拉货币的首选平台。币安尚未对请求置评，之前曾表示正在与尼日利亚联邦税务局（FIRS）合作，以解决潜在的历史税务义务。税务局声称，币安在尼日利亚拥有“显著的经济存在”，因此应当承担公司所得税。它正在寻求法庭宣布币安支付2022年和2023年的所得税，以及未支付金额的10%年处罚。此外，FIRS还要求对未缴税款按尼日利亚中央银行的贷款利率征收26.75%的利息。由于去年政府对该行业的打击，币安已面临四项逃税指控。这些指控包括未支付增值税、公司所得税、未提交税务申报以及通过其平台帮助用户逃税。币安正在对这些指控提出异议，并在去年3月宣布停止所有奈拉的交易和买卖。该公司还面临来自反腐败机构的洗钱指控，币安对此表示否认。

