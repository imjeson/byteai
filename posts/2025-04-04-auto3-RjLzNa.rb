新报告显示，67%的公司预计将维持或增加AI支出，而只有21%报告了任何证明的结果  
尽管结果令人失望，商业和技术领导者仍在持续增加对AI的投资，根据从领先的Salesforce和AI咨询公司Coastal发布的一份新研究报告。![图片](https://ai-techpark.com/wp-content/uploads/Organizations.jpg){ width=60% }

---


该报告调查了120多位来自使用Salesforce的组织的高级技术和业务领导者，揭示了公司如今在AI、数据准备和治理方面的策略。  
其中，有几个亮点：

- 67%的组织预计将维持或增加AI支出，但仅有21%报告了任何证明的结果  
- 43%的受访者预计在未来两年内，治理、伦理和风险将是他们在AI方面面临的最大挑战  
- 64%的公司认为在AI实施和采用方面缺乏明确的路线图和可测量的目标  

数据表明，尽管公司不想在AI革命中落后，但他们可能尚未采取所有必要步骤，充分利用可用的工具和解决方案。  
除了调查结果外，该报告《AI未能交付——该如何应对》还概述了组织在实现ROAI（AI回报）方面常犯的错误，以及公司应采取哪些结构性系统，才能从人工智能在商业中解锁真正的影响。

“Salesforce正在以创纪录的速度进行创新，其AI产品如Agentforce极具变革性。但是，从我们在这个AI时代的前排座位观察，我们知道企业必须专注于打好基础，才能实现真正的AI影响。这意味着现代基础设施能够将数据输入系统，重新设计流程，并确保每个举措与可测量的结果之间有明确的联系，”Coastal的首席执行官Eric Berridge表示。

随着数字化转型时代的到来，新“数据与AI现代化时代”逐渐形成，Coastal的报告为组织提供了扩展AI和取胜的指导，包括以下几点：

- 现代化核心基础设施，以利用AI能力  
- 开发统一平台，能够在云中无缝移动、建模和激活数据  
- 从第一天起便构建、测量并将AI举措与业务价值挂钩  
- 重新思考工作方式，以解锁自主自动化  

“拥有明确数据和AI路线图的公司实现正向投资回报的可能性是没有路线图公司的2.7倍——但令人震惊的是，64%的公司并没有路线图，”Berridge说。“这就是我们每天帮助客户解决的工作。”  
