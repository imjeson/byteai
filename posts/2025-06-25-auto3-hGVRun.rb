Actian的“数据合同优先”原则使企业能够对分布式数据资产实现全面的可视化和控制，从创建到使用。![图片](https://ai-techpark.com/wp-content/uploads/Actian-Embeds.jpg){ width=60% }

---
 
Actian，HCLSoftware的数据部门，今天宣布增强了Actian数据智能平台，帮助企业在使用前将分布式数据转变为可信和可靠的基础，为AI实施奠定了坚实基础。新功能自动化数据产品，并集成数据合同，以促进对数据的更高信任，从而确保准确性和可靠性，加速AI和数据驱动的举措。 
Actian的“数据合同优先”方法利用早期嵌入设计（shift-left principles）在数据生命周期中嵌入治理，借助数据合同和自动化流程，确保由各个域创建的数据产品从一开始就得到良好文档记录并易于使用，提供质量保证。 
随着企业逐步采纳分布式数据架构，数据产品方法的必要性变得尤为关键。ISG软件研究预测，“到2027年，超过60%的企业将采用技术以促进数据作为产品的交付。”¹ 
“随着企业接受数据产品以启用自助分析，它们需要强大的数据质量和治理。”ISG软件研究的分析与数据主任Matt Aslett表示。“通过将这些控制直接集成到数据管道中，Actian数据智能平台旨在帮助企业安全地实现数据民主化。” 
为了满足这一需求，Actian数据智能平台将数据合同直接嵌入开发工作流中，以定义明确的期望，自动执行合规，并在问题流入下游之前就捕获质量问题。最终结果是更可靠的AI成果、更快的洞察时间、更低的合规风险以及更低的运营成本。 
为了促进数据发现，各个领域将其定制的数据产品发布到Actian数据智能平台的企业数据市场中。专用API会自动在现有的持续集成和持续交付（CI/CD）管道中注册和更新数据产品，减少了手动工作并确保数据目录保持最新。 
“去中心化环境的挑战不仅在于跨域管理数据——更在于确保大规模的质量和治理。”Actian的CTO Emma McGrattan表示。“通过Actian数据智能平台，使高质量、良好记录的数据产品易于访问，促进数据民主化并支持数据共享文化，而我们以数据合同为导向的方法确保了从源到消费的可靠数据。” 
该平台支持去中心化的数据所有权，打破孤岛，以改善跨业务和IT用户的协作和数据民主化。借助简化的数据发现和使用，业务用户可以快速找到、理解和使用他们所需的确切数据。该平台的联邦知识图谱驱动的搜索引擎提供上下文感知的结果，同时保持企业范围内的合规性。访问请求工作流提供集中、透明的审批流程，并带有明确的审计轨迹。 
为了进一步增强数据的可访问性，Ask AI是Actian新推出的AI驱动自然语言查询功能，用户可以使用日常语言询问问题，结果根据组织的词汇进行排序。该功能消除了技术障碍，加速了组织内的自助分析。 
新功能跨越联邦元模型，提供完整的数据血缘可视化。当与Actian数据可观察性结合时，组织可以确保数据产品符合既定的数据合同。通过为定义良好的数据资产丰富上下文、质量指标、血缘和使用洞察，组织能够更好地利用数据进行AI和其他用例。 
新的数据合同和数据产品功能已在Actian数据智能平台上立即推出。Ask AI能力目前处于私密预览阶段。欲获取关于新功能和其他可用功能的更多信息，请查看“数据合同、AI搜索及更多：Actian的2025春季产品发布”博客。 

