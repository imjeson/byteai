title: 'AuditBoard被评为IDC GRC软件市场领导者'
date: 2025-07-16
author: ByteAILab

---

AuditBoard, 领先的人工智能驱动的全球风险连接平台，正在转变审计、风险和合规业务，今天宣布它在IDC市场调查中的“2025年全球治理、风险和合规软件供应商评估”中被评为领导者（文档编号：US53615325，2025年6月）。![图片](https://ai-techpark.com/wp-content/uploads/AuditBoard-2.jpg){ width=60% }

---
我们相信这一认可突显了AuditBoard客户通过利用公司AI驱动的连接风险平台来管理组织风险所实现的切实商业价值。

“我们很荣幸被评为2025年IDC MarketScape GRC软件领域的领导者，”AuditBoard首席技术官Happy Wang表示。“我们不断寻求客户反馈，以指导我们的产品路线图，更好地赋能客户进行风险决策，增强韧性，并促进组织的收入增长。”

根据报告，“具体的优势包括在整个风险生命周期中简化和自动化的风险管理程序，利用装备有AI的工作流程进行有效的风险管理，以及自动化和灵活的例外管理工作流程。”

“AuditBoard一直在持续满足市场需求，将其平台扩展到传统审计管理能力之外，”IDC治理、风险和合规服务研究总监Philip D. Harris表示。“公司的员工专业知识直接来源于审计和网络安全专业人士及主题专家，使其在竞争中独树一帜。此外，AuditBoard平台使企业GRC管理变得简单，使客户在平台部署后即可开始获得立即的价值。”

AuditBoard因其行业领先的平台和快速增长而一直受到认可，最近将国际业务扩展至德国。该公司现在服务于超过50%的财富500强企业，并连续第六年被评选为德勤快速增长500强企业，同时在2024年中被伦敦的Hg以超过30亿美元收购，这也是2024年最大的一笔风险投资支持的私募股权交易之一。

除了在“IDC MarketScape：2025年全球可持续管理平台供应商评估”(文档编号：US52995125，2025年5月)和“IDC MarketScape：2025年全球ESG报告和合规管理应用程序评估”(文档编号：US52995025，2025年5月)中被认可为主要参与者外，AuditBoard的连接风险平台连续两年被评选为G2最佳软件产品，获得2024年网络安全突破奖“最佳风险管理解决方案”，并在2024年Stratus Awards中被评为最佳平台即服务。

