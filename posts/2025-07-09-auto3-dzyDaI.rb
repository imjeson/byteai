里程碑推进了 Elastic 支持美国联邦政府最敏感和任务关键工作负载的使命  
Elastic（NYSE: ESTC），搜索人工智能公司，宣布 Elastic Cloud Hosted 在 AWS GovCloud (US) 上获得联邦风险和授权管理计划（FedRAMP）High “在处理”状态。![图片](https://ai-techpark.com/wp-content/uploads/Elastic-Cloud.jpg){ width=60% }

---
该里程碑强调了 Elastic 致力于提供安全和合规解决方案，以支持美国联邦政府保护受控未分类信息（CUI）及其他敏感的高影响数据，涵盖各种任务关键用例。  

FedRAMP High 是该计划中最严格的安全基准，要求超过 400 项严格的安全控制，以保护管理敏感未分类数据的云环境，包括与国家安全、关键基础设施和财务风险相关的信息。Elastic Cloud Hosted 已在 AWS GovCloud (US) 上获得 FedRAMP Moderate 认证，支持广泛的公共部门机构。  

“获得 FedRAMP High ‘在处理’状态是我们扩大对高安全工作负载的联邦机构支持的重要一步，”Elastic 公共部门全球副总裁 Chris Townsend 说道。“无论是推进零信任策略还是构建生成性 AI 应用程序，Elastic 始终致力于提供安全、透明、合规和具有成本效益的解决方案，帮助政府机构实现其使命。”  

拥有“在处理”认证，Elastic 正在与其赞助机构和第三方评估师密切合作，完成 FedRAMP High 授权过程的最后阶段。在获得最终授权后，Elastic Cloud Hosted 将被批准支持政府最安全的工作负载，以遵守 FedRAMP High 的要求。  

