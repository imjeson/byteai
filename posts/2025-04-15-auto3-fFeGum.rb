---

投资将加速SmarterDx临床AI平台的扩展，为医院和医疗系统提供额外的收入完整性解决方案和价值
SmarterDx, Inc.（“SmarterDx”或“公司”），作为收入完整性和护理质量领域的临床AI领导者，今天宣布来自New Mountain Capital, LLC（“New Mountain Capital”）的战略投资。![图片](https://ai-techpark.com/wp-content/uploads/SmarterDx-A.jpg){ width=60% }

---
New Mountain Capital是一家领先的以增长为导向的投资公司，管理资产超过550亿美元。此次投资将支持SmarterDx下一个增长和产品创新阶段，专注于为医疗系统创造价值，使其能够为患者提供最佳护理。

SmarterDx成立于2020年，由Michael Gao博士和Joshua Geleris博士创办，是一个专有的临床AI平台，帮助医院分析100%出院病历，确保准确捕捉每位患者的护理，防止收入完整性方面的任何漏洞。该公司的SmarterPrebill™解决方案使临床文档完整性（CDI）和编码团队能够验证每个病人的护理细节，帮助医院恢复数百万的已赚取收入，同时提升护理质量评分，而SmarterDenials™则为其提供了一种更快速、更有效的方式来推翻拒付并恢复丢失的收入。

通过New Mountain Capital的增长投资，SmarterDx将继续增强其技术和AI平台，同时加速现有产品路线图，并开发互补解决方案。SmarterDx在美国一些最大的医院和医疗系统中实现了首日5:1的投资回报率。公司已迅速确立其作为领先的医疗收入循环解决方案提供者的地位，超过40个领先的医疗系统，涵盖180家医院已加入其临床AI平台。SmarterDx还被Modern Healthcare评选为2024年“最佳商业”新兴企业中的佼佼者。

“这一战略投资标志着我们在扩展AI平台和开发新产品方面的关键时刻，这些产品将进一步帮助医院捕获其差异化价值——无论是在已赚取收入、护理质量还是运营效率方面，”SmarterDx联合创始人兼首席执行官Michael Gao博士表示。“我们认为，最准确和完整的患者故事不仅是良好的文档实践，也是推动医疗保健各个方面的重要数据层。我们期待着与New Mountain Capital合作，他们共享我们的信念，能够进一步加强我们的能力以支持医疗系统及其患者。”

“我们的北极星是帮助医院，这将加速我们构建可以推动实际结果的AI解决方案的能力。未来还有更多，我们对这一新篇章感到无比兴奋，我们能够提供更大的创新，”SmarterDx联合创始人兼产品与数据科学主管Joshua Geleris博士说道。

“我们对SmarterDx平台及其团队深感印象，尤其是Gao博士和Geleris博士所建立的以医生为主导的团队。他们对医院和医疗系统的承诺令人瞩目，我们很高兴能支持我们认为是医疗科技领域最优秀团队的企业，”New Mountain Capital私募股权部董事总经理兼总裁Matt Holt表示。“New Mountain对SmarterDx的投资是建立在我们多年关注临床与行政工作流程融合的基础上，我们很高兴支持SmarterDx作为一家专注于为医疗服务提供者提供高战略价值的主要平台公司。”

该交易需符合惯例监管审批，预计将在今年第二季度完成。Cooley LLP担任SmarterDx的法律顾问，J.P. Morgan Securities LLC担任财务顾问。Ropes & Gray LLP担任New Mountain Capital的法律顾问。

