 Plakar是一款开源备份和恢复平台，今天宣布其首个稳定版本的正式发布，同时完成了由Seedcamp主导的300万美元前种子融资，该轮融资还得到了HelloWorld、IrregularExpression、Galion.exe、Kima Ventures等领先风险投资公司的参与，以及包括Olivier Pomel（Datadog）、Solomon Hykes（Docker）和Alexandre Yazdi（Voodoo）在内的知名天使投资者的支持。

Plakar的现代架构使得用户能够以快速、可靠和资源高效的方式保护和管理各种环境中的数据。Plakar不仅仅是存储文件，它还保留完整的应用程序和人工智能管道上下文。它可以对数据进行内联去重和压缩，提供端到端的加密，并将数据组织成结构化、可查询的存储库。

与传统备份解决方案不同，Plakar将备份存储从沉睡的成本转变为活跃的、有价值的资源。其架构使得受保护的数据可以无缝地被用于生产工作流，例如人工智能模型训练、数据分析、合规审计，或将密集任务从生产系统卸载，从而释放出新的操作效率，加速创新。

“通过Plakar，我们提供了一种简化备份和恢复过程的开源解决方案，同时使企业级数据弹性对所有规模的企业可及，”Plakar的联合创始人兼首席执行官Julien Mangeard表示。“此次产品发布和融资公告是一个重要的里程碑，我们很高兴能够在如此知名的合作伙伴和投资者的支持下开始这段旅程。”

“在一个人工智能系统生成和处理前所未有的数据量的时代，确保这些数据的完整性和安全性至关重要，”Seedcamp合伙人Sia Houchangnia表示。“Plakar将开源灵活性与企业级特性相结合的做法，与我们赋能开发人员和企业使用可访问、可靠和高效工具的信念相一致。”

Plakar的引擎Kloset将每个备份转化为一个紧凑的、自包含的、不可变的数据单元，该单元带有自己的结构、元数据和加密。像数据容器一样，它消除了外部协调或依赖的需要，使得能够在任何环境中快速、安全和可移植地进行备份，从本地文件到分布式云系统。

Plakar的端到端工作流确保每个备份都保持轻量、安全且可即时恢复，其主要特点包括：

- 数据收集：不可变的内容寻址快照捕捉数据集和模型检查点的增量版本，以确保实验完全可复现，同时减少冗余存储量高达90%。
- 数据可视化：快照可浏览和验证，无需恢复，并且恢复过程精确快速。
- 数据安全：Plakar使用相同的端到端加密，确保符合严格的数据保护法规。
- 资源效率：通过结合先进的去重和压缩技术，Plakar最大限度地减少存储需求，允许更多的恢复点占用更少的空间。
- 互操作性：Plakar与各种存储环境无缝集成，包括基于云的存储、兼容S3的解决方案、本地服务器、NAS、SAN、磁带驱动器和Kubernetes卷。

通过此次融资，Plakar计划扩大其工程团队，加速功能开发并增强平台的可扩展性。此外，该公司还计划通过针对寻求高效、安全和可复现的数据管理解决方案的人工智能初创企业和大型企业来扩大客户基础。

