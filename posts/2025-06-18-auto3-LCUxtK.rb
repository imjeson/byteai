新的会议将于2025年9月29日至10月1日在德克萨斯州圣安东尼奥举行，解决数据中心供电的挑战与机遇
为了应对全球对数字基础设施日益增长的需求和对能源来源的迫切关注，行业内最大、最受尊敬的数据中心活动团队宣布推出数据中心世界POWER——一个专注于数据中心与电力交叉领域的新会议。![图片](https://ai-techpark.com/wp-content/uploads/Data-Center.jpg){ width=60% }

---
此次活动将在2025年9月29日至10月1日于德克萨斯州圣安东尼奥的海厄特公园乡村度假酒店举行。现在可以在www.dcwpower.com注册。

此次会议将汇聚数据中心决策者和能源创新者，强调行业中最紧迫的挑战和机遇：电力资源配置。此次活动得到了钻石赞助商VoltaGrid和铂金赞助商Industrial Info Resources的支持，数据中心世界POWER为正在应对容量限制、可持续性要求和高强度AI工作负载的领导者量身定制。会议日程将包括深具洞察力的主题演讲、解决方案驱动的讨论面板及精英网络交流机会。

“Informa Connect数据中心世界组合的高级总监Tara Gibb表示：“我们旗舰活动数据中心世界在2025年增长超过70%——这得益于响应云计算、人工智能、5G、远程工作、电子商务和数据密集型应用的数字基础设施扩展需求。通过数据中心世界POWER，我们提供了一个亲密且聚焦的平台，使当今顶尖决策者能够就如何高效、可持续并大规模地为下一时代的数据增长供电达成一致。”

选择德克萨斯州作为首发地点，因其在全球能源中心和快速增长的数据中心市场中的独特地位。该地区为能源与数字基础设施领导者的合作提供了自然的交汇点。根据在20多场会议中安排的发言人，能源提供者、基础设施领导者、可持续性专家和政策影响者都将探讨将大规模数字增长与韧性、效率和可持续能源基础设施相协调所需的策略和解决方案。

会议节目亮点：

- 电力 sourcing 和容量规划
- 能源效率与可持续运营
- 电网约束与基础设施投资
- 供应链韧性
- 电力系统中的技术创新

预计数据中心世界POWER将吸引来自全球的运营商、电力和基础设施供应商、技术和平台提供商以及承诺应对现代数据中心不断演变的能源需求的投资者。要了解更多信息并注册该活动，请访问 https://dcwpower.com/。

