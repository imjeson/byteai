title: 'GFI软件任命ADN为DACH地区独家分销商'
date: 2025-04-15
author: ByteAILab

---

战略合作伙伴关系的扩展增强了GFI在德国、奥地利和瑞士的AI驱动解决方案的交付及渠道效率
GFI软件，一家全球领先的小型和中型企业（SMB）AI驱动安全和通信解决方案提供商，今天宣布任命ADN Distribution GmbH为其在DACH地区（德国、奥地利和瑞士）的独家分销商，涵盖所有产品线，包括GFI屡获殊荣的Kerio产品系列。![图片](https://ai-techpark.com/wp-content/uploads/GFI.jpg){ width=60% }

---
这一战略扩展了GFI与总部位于德国博洪的ADN之间长达十年的现有合作关系，标志着GFI软件在客户服务和支持领域的雄心勃勃的渠道转型战略的一个重要里程碑。

经过短暂的过渡期，ADN将全面接管之前由ICOS Deutschland GmbH和ALSO Deutschland GmbH管理的所有订阅、合作伙伴和最终客户，从而创建一个流畅的高效分销渠道，改善支持并为合作伙伴和客户提供最大价值。

GFI软件首席执行官Eric Vaughan表示：“与ADN在DACH地区的分销整合为我们提供了更强的基础，以更好地为合作伙伴提供培训、资源和获取我们的AI驱动解决方案的机会，以及他们应得的个人支持。这一举措反映了我们不懈努力打造一个高效高冲击生态系统的决心，旨在服务整个DACH地区的企业——尤其是我们的Kerio客户，优先考虑他们的需求。随着GFI AppManager、RADAR AI、MailEssentials AI和Eloquens AI等创新产品的推出，与有能力和基础设施能够与我们共同扩展的合作伙伴签约至关重要，确保在每一个环节都提供卓越体验。”

ADN Distribution GmbH首席执行官Hermann Ramacher表示：“与GFI软件的合作扩展直接支持ADN通过我们可信赖的合作伙伴网络提供先进IT解决方案的使命。自1994年以来，我们在德国、奥地利和瑞士建立了作为增值分销商的强势地位，深谙云服务、安全和AI领域。这使我们能够独特地推动GFI的创新议程，并支持DACH地区的合作伙伴和客户。我们很自豪能够继续与GFI KerioControl和GFI KerioConnect等成熟解决方案合作，并欢迎新的经销商合作伙伴及其客户加入ADN生态系统。随着GFI AppManager AI和新Eloquens AI平台等即将到来的技术，我们期待着帮助将下一代IT解决方案推向市场。”

该过渡经过精心策划，以确保GFI的合作伙伴网络和终端用户的持续性。ADN的良好业绩记录、广泛的区域存在和在云服务、安全解决方案和AI技术方面的深厚专长，使其成为推动GFI在DACH市场下一个增长阶段的理想合作伙伴。

GFI软件在将AI驱动功能整合到其产品组合中处于前沿，正在变革企业管理和保护数字运营的方式。通过与ADN的合作，GFI将以下AI驱动产品带入DACH地区：

- GFI AppManager AI：一个统一的基于云的IT管理平台，配备先进的监控、实时警报
- GFI RADAR AI（风险和异常检测、警报和响应）：GFI AppManager中的AI/ML功能，旨在提升网络安全和性能
- GFI Archiver AI：通过智能搜索、自动电子邮件标记和合规存储（用于电子邮件、日历及文件历史）彻底改变数据归档
- GFI MailEssentials AI：一种智能电子邮件安全解决方案，利用AI生成和优化规则，阻止威胁并确保遵循不断变化的法规
- Exinda AI：基于AI驱动流量洞察的网络性能优化
- Eloquens AI：一个AI驱动的电子邮件自动化和会话管理平台，支持多语言并具有人工干预监督，完全与GFI KerioControl集成

有关DACH过渡的更多信息，GFI已设置专用电子邮件地址，联系方式为dach@gfi.com，ADN GFI团队可联系gfi@adn.de。
