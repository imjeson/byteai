CODIT推出基于AI的政策仪表板，面向21个APEC经济体的政府和行业领导者
计划在8月12日的APEC对话会上展示数字化转型和AI合作战略

CODIT是一家基于人工智能的法律和政策智能平台，参加了于8月5日在仁川松岛举行的2025年APEC数字与AI论坛的企业展览。![图片](https://ai-techpark.com/wp-content/uploads/CODIT.jpg){ width=60% }

---
在论坛上，该公司展示了其实时政策监控平台，旨在改善对于及时和准确的监管情报的获取。

该论坛作为APEC数字和AI部长会议的官方旁听活动，汇集了来自21个APEC成员经济体的政府和行业领导者。在展位上，CODIT介绍了其动态仪表板，该仪表板策划和总结了与人工智能和数字转型等核心论坛主题相关的政策动态。

该平台吸引了来自该地区的政策制定者和信息与通信技术专家的高度关注。韩国科学与信息通信部部长裴卿昊亲自访问了展位，以探索该平台。与会者表示，在像韩国这样快速发展的监管环境中，能够以英语获取简明的政策更新极大增强了全球利益相关者的可及性和实用性。AI驱动的仪表板能够自动分类和总结立法、新闻稿和基于用户定义主题的新闻文章的能力尤其受到好评。

CODIT目前提供基于韩国最大政策数据覆盖和专有AI总结引擎的政策响应支持服务。在今年下半年，公司计划推出Chat CODIT，这是一款互动AI代理，能够实时回答关于主要管辖区（包括韩国和美国）的政策信息的问询。

“能从政策制定者那里直接获得反馈，认为CODIT是应对现实政策挑战的实用解决方案，意义非凡。”CODIT首席执行官郑智恩表示。“随着数字环境以前所未有的速度进化，快速而准确地提供跨境政策洞察的能力显得尤为重要。CODIT将继续成长为一个全球平台，为更好的、更快速的政策决策赋能。”

CODIT还将在即将于8月12日举行的APEC公共私营对话会上发言，分享对数字转型与AI合作的战略看法。随着APEC推进其在AI合作方面的区域议程，会议将突出AI驱动的政策平台如何帮助构建一个更具包容性和可持续的数字生态系统。

