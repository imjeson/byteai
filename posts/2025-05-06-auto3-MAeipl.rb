收购实现领先的AI云平台——旨在更快迭代AI
CoreWeave, Inc. (Nasdaq: CRWV) 今日宣布已完成对Weights & Biases的收购。![图片](https://ai-techpark.com/wp-content/uploads/CoreWeave-3.jpg){ width=60% }

---
这一战略性组合增强了CoreWeave推动AI创新的能力，成为全球领先的AI云平台，帮助释放全球顶尖AI实验室和企业的全部潜力。
“今天的公告加速了我们以规模、性能和专业知识来推动AI创新的能力，满足加速计算的需求。这也是在我们最近的IPO基础上，创造新的增长途径和扩大公司能力，”CoreWeave的联合创始人兼首席执行官Michael Intrator说。“我对Weights & Biases团队在AI开发平台上取得的成就感到非常钦佩。他们的创新精神、客户至上和工程卓越是CoreWeave始终优先考虑的特质，未来我们将继续这样做，以支持AI开发者在推理时代的工作。现在与Weights & Biases携手，我毫不怀疑我们能够在AI领域为客户推动可能性的边界。”

CoreWeave与Weights & Biases计划共同提供领先的AI云平台——旨在更快地开发、部署和迭代AI。CoreWeave继续致力于Weights & Biases平台的互操作性，以使客户能够选择任何部署选项、任何基础设施提供商以及任何基础模型、框架或协议。

“我们预计这一合并将使我们更好地服务于开发者，加快对Weights & Biases平台的投资，保持对互操作性的承诺，并通过结合新AI技术堆栈的硬件和软件层 unlocking新的有价值功能，”Weights & Biases的联合创始人Lukas Biewald表示。“与CoreWeave一起，我们相信能够赋予客户实现AI潜力的能力。我期待着将我们的人才团队和互补的优势结合在一起，以及我们在联手后将取得的成就。”

Evercore和摩根士丹利担任CoreWeave的财务顾问，Davis Polk & Wardwell LLP担任CoreWeave的法律顾问。Qatalyst Partners担任Weights & Biases的财务顾问，Orrick, Herrington & Sutcliffe LLP担任Weights & Biases的法律顾问。

