IT Medical的创始人兼董事总经理亚历山大·波德戈尔尼已被接受加入福布斯科技委员会。
福布斯科技委员会是一个专为首席信息官、首席技术官和技术高管创建的专属社区，成员是根据其经验由评审委员会精选的。这包括推动商业增长的成功记录以及显著的个人和职业成就。
波德戈尔尼在产品工程和医疗保健IT领域拥有超过十年的经验，他和IT Medical团队结合临床知识与先进技术，创建安全、可扩展且用户友好的平台。
在波德戈尔尼的领导下，IT Medical已成长为全球医院、诊所和医疗创业公司的值得信赖的合作伙伴，帮助它们优化运营、改善病患护理，并为医疗服务带来真正的创新。
作为委员会的一员，波德戈尔尼将有机会接触独特的机遇，与其他行业领导者建立联系，并通过原创文章分享他的见解。他还将为发布在Forbes.com上的专家小组作贡献。
在这里，他将专注于如何在临床环境中应用新技术。波德戈尔尼将结合自己在医疗和软件领域的经验，分享人工智能、机器学习和分析等工具如何改善护理，支持医疗提供者和患者。
波德戈尔尼表示，他为能够加入福布斯科技委员会并被认可为一群创新科技领袖之一而感到自豪：
“这个机会使我能够分享我们在IT Medical所学到的经验教训，并参与关于创新如何真正服务于医疗保健的重要对话。”
