title: '5C集团成立，Hypertec Cloud收购5C数据中心'
date: 2025-04-14
author: ByteAILab

---

在一项标志性举措中，领先的AI云平台Hypertec Cloud收购了5C数据中心，这是一家在美国拥有超过2吉瓦（GW）计划容量的高级协同和数据中心提供商。![图片](https://ai-techpark.com/wp-content/uploads/5C-Group.jpg){ width=60% }

---
这一战略收购标志着Hypertec Cloud从其母公司Hypertec Group的分拆，以及5C集团的成立——一个专注于构建下一代AI基础设施的独立实体。

乔纳森·阿赫杜特被任命为新成立的5C集团的首席执行官，该集团结合了Hypertec Cloud的高性能云计算能力和5C数据中心在美国的广泛足迹。两者共同形成了北美最大的、最灵活的AI数字基础设施提供商之一，旨在构建将驱动行业未来的下一代AI工厂。

此次收购由三个关键战略目标驱动：
1. 提供全面的AI数字基础设施解决方案：通过结合Hypertec Cloud和5C数据中心，5C集团将提供集成的数据中心和AI基础设施，满足AI前沿实验室、AI本地企业和大型企业客户的需求。
2. 在受限市场中加快部署速度：随着对AI部署需求的增长，而大规模数据中心容量仍然有限，整合平台将使超大规模AI集群的上市时间更快。集成计算和数据中心设计将显著缩短部署时间，并实现同步基础设施的推出。
3. 推动性能和成本优化：借助Hypertec在硬件和基础设施设计、白手套支持和可持续工程方面的数十年经验，新实体将引入先进的冷却技术——如直接到芯片的液冷和浸没冷却——以支持更高的机架密度和能源节约。

“当前AI基础设施面临的最大挑战是对齐——即AI计算所需的资源与物理托管环境所能提供的之间的对接。我们通过将两者整合在一起解决了这个问题，”5C集团首席执行官乔纳森·阿赫杜特表示。“通过这项收购，我们将性能优化的AI计算基础设施以及大型、世界级数据中心园区的设计和运营的深厚专业知识结合在一起。这种组合使我们能够加快部署，推动密度向前发展，并为最大和最苛刻的AI用户提供无与伦比的效率，持续数年。”

“Hypertec Cloud的分拆标志着Hypertec集团的一个关键时刻，”Hypertec集团首席执行官西蒙·阿赫杜特补充道。通过收购5C数据中心，新成立的5C集团有望提供为明日工业的规模、速度和复杂性而设计的AI数字基础设施。我们将继续作为合作伙伴推进计算、存储和数据中心技术——提供真正的价值，推动AI创新的未来。”

5C集团在美国的AI优化数据中心园区组合包括600兆瓦（MW）以上的可用容量——在其总计2吉瓦（GW）中，预计在未来6到18个月内用于新的和现有的AI数据中心协同和计算客户。该平台专为满足AI工作负载的极端密度、功率和冷却需求而构建，为未来的AI创新奠定了基础。

