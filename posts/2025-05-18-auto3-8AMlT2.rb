随着全球AI数据中心的能源需求即将激增，领先的人工智能（AI）专用集成电路（ASIC）提供商Neuchips在2025年COMPUTEX上着手应对这一挑战，专注于能效AI解决方案。该公司以其安全性和隐私而闻名，将展示其AI解决方案如何在运行大型AI模型时节省电力。

请于2025年5月20日至23日在台北南港展览中心展位I0601a与Neuchips一同见面。

该公司的努力是在国际能源署（IEA）于4月发布《能源与AI》报告之后。这份报告的发现预计到2030年，全球数据中心的电力需求将增加到约945太瓦时（TWh），这仅稍高于今天日本的电力消费。特别是在利用AI的数据中心，电力需求预计将在2030年前增加超过四倍。

“在当今世界的能源挑战与AI的潜力之间，节能的AI解决方案已不再是可选项——而是标准。” Neuchips首席执行官Ken Lau表示，“在Neuchips，我们不断追求更高的能效，目前我们的Viper系列AI加速卡能够在仅45W的功耗下运行一个完整的140亿参数模型， comparable to a standard light bulb. 创新现在必须集中于性能和效率。”

协作实现能源高效创新
Neuchips因其高效能AI硬件解决方案而领先，这些解决方案通过离线运行LLMs提供最大的安全性和隐私，使各行各业的企业能够利用AI，同时保护敏感数据。该公司的产品包括N3000芯片系列和Viper PCIe加速卡，均完全支持Intel®和AMD CPU，并兼容Ubuntu和Windows。它们还支持多种LLM，包括Mistral Small 3（24B）、Llama 3.3（70B）、DeepSeek精简模型、Gemma 3（4B）等。

在2025年COMPUTEX上，Neuchips将展示多个合作项目的现场演示：

与国家高性能计算中心（NCHC）的新合作：该公司与台湾的国家高性能计算中心（NCHC）Under the National Institutes of Applied Research (NIAR)进行了合作，为云原生客户提供高效的解决方案。通过将Neuchips的AI加速器与NCHC的基础设施相结合，这一合作将减少云服务成本，同时确保数据安全和隐私。

与枫叶信息技术（MAPLE LEAF INFORMATION AND TECHNOLOGY）和Vecow的合作：这一合作创建了可以运行复杂生成AI应用的紧凑系统，而无需额外的电力配置。

与GSH的ShareGuru SQLPilot系统的联合解决方案：该演示将展示GSH的多代理技术与NL2SQL智能AI和高精度的本地AI会议分析系统及智能客户服务解决方案相结合。通过将LLM查询简化为复杂数据集，解决方案提供更高的准确性、用户体验、效率和洞察力。

与myLLM myPDA平台的联合：该平台也将与Neuchips的硬件结合，myPDA平台是一个为自主管理的LLM设计的云边缘集成GenAI应用平台，兼容代理AI。

