推出针对21个亚太经济合作组织成员经济体的人工智能驱动政策仪表板
计划在8月12日的亚太经济合作组织对话会上展示数字化转型和人工智能合作策略
CODIT，一家人工智能驱动的法律和政策智能平台，参加了于8月5日在仁川松岛举行的亚太经济合作组织2025数字与人工智能论坛的企业展览。![图片](https://ai-techpark.com/wp-content/uploads/CODIT.jpg){ width=60% }

---
在论坛上，该公司展示了其实时政策监测平台，旨在提高对及时和准确的监管信息的获取能力。

此次论坛作为亚太经济合作组织数字与人工智能部长会议的官方旁边活动，汇聚了来自21个亚太经济合作组织成员经济体的政府和行业领导者。在其展位上，CODIT介绍了一个动态仪表板，该仪表板策划并总结了与核心论坛主题相关的政策发展，包括人工智能和数字化转型。

该平台吸引了来自该地区的政策制定者和信息通信技术专家的显著关注。韩国科学与信息通信技术部长裴京勋亲自访问了该展位，以亲身体验该平台。与会者指出，在如韩国一样快速发展的监管环境中，以英语提供简明的政策更新显著增强了全球利益相关者的可及性和实用性。AI驱动的仪表板自动根据用户定义的主题分类和总结立法、新闻发布和新闻文章的能力受到了特别欢迎。

CODIT 目前提供基于韩国最大政策数据覆盖和专有AI摘要引擎的政策响应支持服务。在今年下半年，该公司计划推出 Chat CODIT，一种互动AI助手，支持对跨主要司法管辖区（包括韩国和美国）的政策信息进行实时问答。

“得到政策制定者的直接反馈真的很有意义，他们认为 CODIT 是解决现实政策响应问题的实用方案。” CODIT 首席执行官郑智恩表示。“随着数字环境以前所未有的速度发展，提供跨境政策见解的能力的速度和准确性变得比以往任何时候都更为重要。CODIT 将继续成长为一个全球平台，使更好、更快的政策决策成为可能。”

CODIT 还定于在即将举行的亚太经济合作组织公私对话会上于8月12日发言，分享有关数字化转型和人工智能合作的战略观点。随着亚太经济合作组织推进其区域人工智能合作议程，本次会议将强调人工智能驱动的政策平台如何帮助建立一个更加包容和可持续的数字生态系统。

