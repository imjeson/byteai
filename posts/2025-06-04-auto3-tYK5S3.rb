Snorkel Evaluate和Snorkel Expert Data-as-a-Service赋能企业在规模上评估和调优专业AI
今天，Snorkel AI宣布推出Snorkel AI数据开发平台上的两个新产品：Snorkel Evaluate和Snorkel Expert Data-as-a-Service。![图片](https://ai-techpark.com/wp-content/uploads/Snorkel-AI.jpg){ width=60% }

---
这些发布进一步推动了其使命，即将知识转化为专业AI——帮助团队利用Snorkel AI的程序化数据开发技术，从原型走向大规模生产。此外，Snorkel AI还宣布其在D轮融资中筹集了1亿美元的资金，估值达13亿美元，此轮融资由Addition领导。这笔新融资将推动在评估和调优专业AI系统方面的研究和创新。

尽管大型语言模型（LLM）提供了巨大的潜力，但企业不能自信地在专业业务案例中“开箱即用”部署它们。根据Gartner的预测，到2026年，未能建立可扩展AI数据实践的组织将导致超过60%的AI项目被放弃。实现生产就绪的AI需要领域特定的数据，以进行细致的评估和模型调优方法。

“我们正在看到围绕代理AI的势头，但大多数环境中的专业企业代理尚未准备好投入生产，” Snorkel AI的联合创始人兼首席执行官Alex Ratner表示。“企业需要领域特定的数据和专业知识来使这一现实成为可能。我们很高兴能满足这一需求，并帮助AI创新者开发专家数据，将他们的LLM和代理系统投入生产，借助我们的新产品，这些产品不仅完善了Snorkel的统一AI数据开发堆栈。”

Snorkel Evaluate
Snorkel AI正在扩展其AI数据开发平台，正式发布Snorkel Evaluate，允许用户建立专业的、细致的模型和代理评估。该新产品借助Snorkel AI独特的程序化方法来策划AI准备数据，使企业能够扩展其评估工作流程，从而自信地将AI系统部署到生产中。

Snorkel Evaluate包括程序化工具，用于创建基准数据集、开发专业评估者和错误模式修正。这些工具帮助用户超越通用数据集和现成的“LLM作为法官”的方法，高效地构建可操作的领域特定评估。

“为了释放Claude的全部潜力，我们需要具备领域专业知识和人类反馈的新评估方法，”Anthropic的收入负责人Kate Jensen表示。“Anthropic致力于与像Snorkel AI这样的创新者合作，确保AI系统经过优化、可靠，并与企业需求保持一致。”

Snorkel Expert Data-as-a-Service
Snorkel Expert Data-as-a-Service是一项精细化服务，旨在为企业提供前沿AI系统评估和调优所需的专家数据集。领先的LLM开发者已经与Snorkel AI合作，以创建适用于高级推理、代理工具使用、多轮用户交互和领域特定知识的数据集。

该服务结合了Snorkel广泛训练的主题专家网络与其独特的程序化技术平台，提供数据标注和质量控制，从而高效交付专业数据集。Snorkel Expert Data-as-a-Service使企业能够将内部专业知识和数据与使用外部专业知识开发的专有数据集结合起来。

Snorkel AI的D轮融资和市场势头
这些新产品的推出凸显了Snorkel AI的商业势头。今天，该公司还宣布在D轮融资中筹集到了1亿美元的资金，估值达13亿美元，此轮融资由Addition领导，参加的还有Prosperity 7 Ventures、现有投资者Greylock和Lightspeed，以及BNY和QBE Ventures等现有战略投资者。这一轮融资将Snorkel AI自2019年成立以来的总融资额提高到2.37亿美元。这笔新资本将支持公司扩展其统一AI数据开发平台的工程、研究和市场推广工作。

“通过Snorkel Expert Data-as-a-Service和Snorkel Evaluate等创新，Snorkel AI使组织能够更高效地构建AI模型，并确保它们在专业的现实应用中表现最佳，”Addition的合伙人Todd Arfman表示。“这种强大的数据驱动方法正在加速可靠AI的大规模部署——我们很高兴能与Snorkel AI合作，帮助他们重新定义企业AI的可能性。”

这一最新融资轮在Snorkel AI在财富500强公司和AI初创公司之间强劲增长的背景下进行，并且该公司在AI行业的广泛认可，包括入选《Fast Company》最具创新公司名单和《Forbes》的AI 50。

