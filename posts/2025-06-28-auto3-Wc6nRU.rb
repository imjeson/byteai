---

symplr®, 一家领先的企业医疗运营软件提供商，宣布任命Theresa Meadows, MS, RN, CHCIO, FHIMSS, FCHIME为驻外首席信息官。![图片](https://ai-techpark.com/wp-content/uploads/symplr-Ap.jpg){ width=60% }

---
Meadows是一位全国公认的医疗IT和网络安全领导者，将与首席执行官BJ Schaknowski及执行团队合作，为企业信息管理、创新和增长提供战略指导。她还将在Align symplr战略与医疗CIO的需求方面发挥关键作用，帮助塑造symplr运营平台的演变，以提供整合的工作流程和基于价值的解决方案。

最近，Meadows在德克萨斯州的Cook儿童健康护理系统担任首席数字和信息官，负责该健康系统的临床系统开发、基础设施、分析和网络安全。她之前曾在Ascension Healthcare和医疗咨询领域担任重要领导职务，推动临床和运营系统的优化。

“Theresa是一位具有远见的医疗IT领导者，能够将临床洞察与技术战略相结合，”Schaknowski说。“她对CIO优先事项和企业运营的深刻理解将帮助确保symplr运营平台继续解决真实客户问题并产生可衡量的影响。”

Meadows曾被医疗信息管理执行委员会（CHIME）评选为2022年年度CIO，并共同主持美国卫生与人类服务部的医疗网络安全工作组。她还是HIMSS和CHIME的研究员。

“symplr在支持安全、高效的医疗服务交付方面发挥着重要作用，我非常高兴在这样一个激动人心的时刻加入团队，”Meadows表示。“symplr运营平台是一个强大的解决方案，可以整合分散的系统，标准化操作并启用创新。我期待着通过CIO的视角来帮助塑造symplr的战略，确保我们提供医疗系统所需的成果和价值。”

