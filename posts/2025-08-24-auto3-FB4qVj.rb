title: 'FieldAI融资4.05亿美元以扩大具身AI机器人技术'
date: 2025-08-25
author: ByteAILab

---

随着Bezos Expeditions、Canaan Partners、Khosla Ventures、Prysm、淡马锡等投资者的支持，加速机器人领域首个风险意识基础模型的商业扩张
FieldAI，一家物理AI和机器人自主领域的领军者，今天宣布已在连续两轮融资中筹集了4.05亿美元。![图片](https://ai-techpark.com/wp-content/uploads/FieldAI-Rais.jpg){ width=60% }

---
投资者包括Bezos Expeditions、BHP Ventures、Canaan Partners、Emerson Collective、英特尔资本、Khosla Ventures、NVentures（NVIDIA的风险投资部门）、Prysm、淡马锡等。之前的投资者包括Gates Frontier和三星。得益于快速的客户采用和多个扩展合约，最新一轮融资超额认购，用于FieldAI的通用机器人智能，该平台在数百个复杂的现实工业环境中进行了成功的测试和部署。

FieldAI处于通用机器人革命的最前沿，开发了一种单一软件大脑，可以为许多不同环境中的各种机器人提供动力。FieldAI的机器人在全球多个客户现场的日常运营中得到部署。这些部署覆盖了从日本到欧洲再到美国的高复杂度环境，包含一些世界上最大的建筑、能源、制造、城市配送和检查等行业的公司。

FieldAI的系统在边缘实时自主操作，模型直接做出决策，无缝集成进真实的客户工作流程。它们记录了前所未有的真实世界数据和操作时间，展现了模型进化的变革性速度，并以规模化的方式提供了价值和成本效益的自主能力。随着各行业转向自动化以应对劳动短缺、安全风险和效率目标，对FieldAI平台的需求继续加速。

资本以加速全球扩展
新筹集的资金将加速FieldAI的全球增长，支持在运动和操作方面的持续产品开发，并使得战略招聘成为可能，计划到年底将员工人数翻倍。
“在规模上实现自主解决方案是一个极其困难的问题，但FieldAI团队的深厚专业知识和他们对具身智能的独特方法体现了一条务实的前进道路，”Khosla Ventures创始人Vinod Khosla说道。“FieldAI处于通用机器人革命的最前沿，其迅速部署的能力将释放长期的经济和社会价值。”

机器人智能的新纪元
FieldAI平台的核心是Field Foundation Models (FFMs)：一种专门针对具身智能构建的“物理优先”基础模型。这与传统的视觉或语言模型不同，FFMs从根本上设计以应对不确定性、风险，以及现实世界的物理约束。这使得机器人在管理它们没有接触过的场景时能安全可靠地表现出来，能够在没有事先地图、GPS或预定义路径的情况下，动态导航于非结构化环境中。

“我们的团队在现场花费了多年时间，推动了复杂环境下‘现场机器人’和安全关键机器人AI的重大突破，”FieldAI的创始人兼首席执行官Ali Agha表示。“通过对在复杂现实条件下部署机器人AI所需的韧性和稳健性的深刻理解，我们采取了根本不同的方法。我们从零开始设计了固有风险意识架构，而不是试图将大型语言和视觉模型强行转入机器人领域。通过Field Foundation Models，我们能够实现机器人操作在各种不同风险配置环境中的无缝扩展，超越传统解决方案的限制。”

FieldAI的架构在机器人领域标志着一次突破。FFMs的稳健性使得模型能安全、动态地适应新的和意外的条件而不需要重新编程，使得机器人在非结构化环境中可靠执行复杂任务成为可能。FFMs已在广泛的具身形式中得到了验证，包括四足机器人、人形机器人、轮式机器人和乘客级车辆。它们的硬件无关设计使得不同形状和操纵器能够使用相同的核心智能运行，从而加速部署和规模化。

“我们对与FieldAI团队在其旅程的下一个阶段建立合作感到兴奋和荣幸。他们的新一类基础模型提供了所需的可靠性和适应能力，以便在众多领域进行大规模的自主机器人部署，”Prysm Capital的共同创始人兼管理合伙人Jay Park说道。“FieldAI的革命性模型不仅大幅扩展了潜在的应用场景，还实现了风险意识的部署，这一点对于人工智能的扩展至关重要，并有可能重塑机器人与物理世界的互动方式。”

FieldAI团队由DeepMind、Google Brain、Tesla Autopilot、NASA JPL、SpaceX、Zoox、Cruise、亚马逊、DARPA、Tri等领域世界知名的机器人AI专家组成，结合了深厚的研究专业知识和无与伦比的现实部署经验。从火星探测器到地球的矿井和工厂，FieldAI团队推动了现场机器人的标志性突破——赢得了DARPA挑战赛，扩展了自主车队的基础模型，并成功实现了规模化的自主性。这种为实际应用而解决问题的精神正是公司名称的由来。
