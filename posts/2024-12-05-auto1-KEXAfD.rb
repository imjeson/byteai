title: '比特币价格首次突破10万美元，特朗普胜利引发加密热潮'
date: 2024-12-06
author: ByteAILab

---

比特币首次突破10万美元，创下新高，在一场由于唐纳德·特朗普当选胜利引发的狂欢中上涨。

---
这种全球最大、最有价值的加密货币在过去几周因希望总统当选者的重返白宫将带来新一轮宽松监管和支持政策而受到提振。周三，比特币触及历史新高103,619美元，自选举日以来，已上涨约45%。其他加密货币也享受了类似的涨幅。

“我们正目睹一个范式转变。在经历了四年的政治磨难后，比特币和整个数字资产生态系统正处于进入金融主流的边缘，”美国加密公司Galaxy Digital的创始人兼首席执行官迈克·诺沃格拉茨（Mike Novogratz）表示。

特朗普提名了加密货币游说者保罗·阿特金斯（Paul Atkins）担任证券交易委员会（SEC）主席，这表明他的政府将采取更友好的态度来面对近年来价值暴涨的数字资产，尽管对其金融风险仍存担忧。

“恭喜比特币爱好者！！！10万美元！！！你们的功劳！！！我们将一起让美国再次伟大！”特朗普在他的社交媒体网络Truth Social上写道。

突破六位数价格是比特币的重要里程碑。这种数字货币于2008年由一位使用化名中本聪（Satoshi Nakamoto）的软件开发者创建，形式数据化，没有实物形态。

支持者称赞比特币以及更广泛的加密空间为金融的未来。然而，其价值的攀升过程异常曲折，市场关于其成为商品和服务主流支付替代品的预测迄今未能实现。

“比特币突破10万美元不仅是一个里程碑；这是金融、技术和地缘政治潮流变化的明证。”香港独立加密分析师贾斯廷·德安瑟桑（Justin d’Anethan）表示，“这一数字不久前还被视为幻想，现在却成为现实。”

特朗普曾经将比特币称为“骗局”，但在他最近的总统竞选中改变了立场。他的竞选团队指责民主党在乔·拜登任内进行“反美国的加密打压”，承诺维护比特币挖矿和美国公民持有加密资产的能力，以及“免于政府监视和控制的交易”。

阿特金斯作为前SEC委员，在数字资产政策方面具有丰富经验，曾担任Token Alliance的共同主席，参与“制定数字资产发行和交易平台最佳实践”的工作，以及数字商务商会。

“阿特金斯将提供一个新视角，源于对数字资产生态系统的深刻理解，”区块链协会首席执行官克里斯丁·史密斯（Kristin Smith）表示。“我们期待与他合作……共同迎来美国加密创新的新一波浪潮。”

随着比特币价格的上涨，相关的加密股票也随之飙升。软件公司Microstrategy在不断筹集资金购买比特币后，截至12月初，已持有约402,100个比特币，今年股价上涨约540%。

特朗普本人于9月推出了一项新的加密业务——世界自由金融（World Liberty Financial），尽管细节尚不明确。亿万富翁埃隆·马斯克（Elon Musk），特朗普的主要支持者，也是加密货币的倡导者。

