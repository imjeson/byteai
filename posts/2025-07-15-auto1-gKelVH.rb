本周，埃隆·马斯克的X（前身为Twitter）旗下的人工智能聊天机器人Grok宣称自己是超级纳粹。然后公司的首席执行官辞职。在过去三年中，马斯克对社交网络的掌控似乎每周都在经历至少一次公共危机，更多的时候是多次。

马斯克的聊天机器人崩溃后获得军火合同
上周，马斯克的人工智能公司xAI的旗舰聊天机器人Grok自称为超级纳粹，称自己为“机械希特勒”。它发布了种族主义、性别歧视和反犹太的帖子，公司对此进行了删除。我的同事乔什·泰勒举了一个例子：Grok提到一个常见的犹太姓氏的人，称其为“在德克萨斯洪水中庆祝白人孩子悲惨死亡的未来法西斯”。

xAI对机器人的“可怕行为”表示歉意。就在此之前，马斯克自己下达了Grok要少“觉醒”的命令。

尽管出现了崩溃，xAI在周一宣布，已经获得了与美国国防部的一项最高达2亿美元的合同，以及其他主要人工智能开发者的合同。该合同旨在为该机构开发和实施人工智能工具。

这份合同可能是马斯克向政府展示新关系的最明显例证。尽管Grok的表现不佳且引发争议，但xAI依然被与那些展示出明显控制其人工智能产品的公司一同奖励。合同获胜者之中包括谷歌、OpenAI和Anthropic，这三家公司在公开承诺安全测试方面表现出色。相比之下，Grok多次因其有争议和攻击性的内容登上头条，像是在五月时曾发出关于“白人灭绝”的言论，回响着马斯克自己的话。

我的同事尼克·罗宾斯-厄利指出，xAI正在任何能找到的地方追求收入和投资：国防部的合同将为xAI带来收入的提升，因为它试图与OpenAI等更有实力的人工智能开发商竞争，OpenAI由马斯克的前同事、如今的竞争对手萨姆·阿尔特曼领导。马斯克一直在大力推动xAI，并试图利用他技术帝国的其他部分支持其未来，包括让SpaceX向这家初创公司投资20亿美元，允许其收购X（前Twitter），并宣布周日特斯拉股东将就其在xAI的投资进行投票。

混乱在马斯克的帝国中蔓延
世界首富似乎因其王国内的动荡变得更加绝望。他表示将组建一个独立的政党。xAI正在追求财务平衡。特斯拉的销售额正在下滑；其摇摇欲坠的机器人出租车正在接受调查。SpaceX的巨型火箭在发射后不断爆炸。

尼克·罗宾斯-厄利再次指出：近年来，马斯克在X之外的争议不断增多。他与唐纳德·特朗普的政治联盟始于2024年选举，这导致马斯克被任命为特定政府雇员以及成立了所谓的“政府效率部门”，但在公众面前于6月崩溃。这个科技大亨承诺要创建一个独立的政党。

与此同时，特斯拉作为马斯克财富的主要来源，其销售额因他的政治活动急剧下降，潜在买主和现有车主都在远离这一有争议的CEO。SpaceX，这家由马斯克创办的火箭公司，在其最新的火箭——庞大的星舰——频繁爆炸后面临困境。

琳达·雅卡里诺，X的尴尬代言人，从“玻璃悬崖”上跌落
周三，X的首席执行官琳达·雅卡里诺宣布将辞去社交网络的职务。就在Grok疯言疯语的前一天。

我的同事约哈娜·布胡扬和尼克·罗宾斯-厄利对雅卡里诺的任期进行了评估：在过去的两年中，雅卡里诺不得不应对马斯克的不确定性、平台上的内容审查和仇恨言论问题、与广告商关系紧张以及她的老板因在唐纳德·特朗普政府中发挥作用而受到的广泛反对。在某些情况下，她的反应是保持沉默；在其他情况下，她选择为公司辩护。然而，专家们表示，雅卡里诺担任的首席执行官只是名义上的。

X没有成为主流人才的目的地，也没有成为雅卡里诺所宣传的流媒体强国或“万事通”应用，反而成了马斯克发泄不满、支持后又与特朗普争吵以及推广其公司的一种喇叭。极右翼影响者、色情垃圾邮件机器人和表情包账户泛滥，而许多媒体机构则减少了对该平台的优先支持或完全离开。虚假信息和极端主义横行，有时甚至来自马斯克本人。

当雅卡里诺被任命时，《卫报》发表了一篇题为“琳达·雅卡里诺：推特的首席执行官是否拥有科技界最艰难的工作？”的文章，描述了她任期开始时所面临的障碍，最终却未能克服。两年后，我们可以肯定地说，她的确拥有科技界最不可能的工作：驾驭马斯克。

我的同事卡里·保罗在2023年报道：马斯克承诺会引入一位新首席执行官——他自己形容这一职位是“痛苦”的工作，任何人都“愚蠢”地接受。

当雅卡里诺成为公司首位女性首席执行官时，关于她站在“玻璃悬崖”上的讨论很热烈——这是一个通过研究提出的概念，认为女性在公司危机时更容易被提升至高职位，而失败的可能性也更大。分析师表示，她成功的关键在于马斯克愿意共享多少控制权。X的混乱公告对于一些人来说，已使雅卡里诺清理马斯克的烂摊子的希望灰飞烟灭。

自从马斯克接管以来，Twitter已经陷入下滑螺旋，面临着130亿美元的债务负担和广告商的大规模逃离——这历史上是公司的主要收入来源。Twitter正在寻找新的收入来源，而“万事通应用”可能是其出路。“如果她成功，她将载入史册；如果失败，则成为脚注，”市场研究公司Insider Intelligence的社交媒体分析师贾思敏·恩伯格表示。

