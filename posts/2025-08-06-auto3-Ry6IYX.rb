---

Kyndryl将其SAP服务与Nova Intelligence的AI代理进行对接，以加速客户向SAP Cloud ERP的迁移并实现成本节约。![图片](https://ai-techpark.com/wp-content/uploads/Kyndryl-and-Nova.jpg){ width=60% }

---
 Kyndryl（纽约证券交易所代码：KD），一家领先的关键企业技术服务提供商，今天宣布与SAP Clean Core现代化的多代理AI解决方案先锋Nova Intelligence达成战略合作，以加速为客户提供基于AI的SAP数字转型项目。 

在这一合作下，Kyndryl将其SAP现代化和数据转型服务与Nova Intelligence的代理AI解决方案相结合，支持企业客户进行SAP Cloud ERP（原SAP S/4HANA）转型，包括向SAP Cloud ERP迁移和现代化现有SAP Cloud ERP系统，以符合SAP Clean Core原则，进而更快速和高效地进行转型。

Kyndryl与Nova Intelligence的合作将在支持客户SAP Clean Core之旅方面进一步推动，通过将遗留的自定义代码转换为现代化和合规的应用程序，帮助客户实现转型。Kyndryl还将利用Nova Intelligence的代理AI能力来记录和分析自定义代码，并发现、构建和测试现代化的、符合Clean Core的端到端解决方案。

“Kyndryl与Nova Intelligence的合作深化和扩展了我们的SAP现代化服务，以持续满足客户在数字转型旅程中的各个阶段的需求，”Kyndryl全球应用、数据和AI实践领导者Michael Bradshaw表示。“通过利用Nova Intelligence的AI代理，Kyndryl正在扩展其快速且高效地指导客户向SAP Cloud ERP迁移的能力，同时为客户在支持其业务目标的过程中使用未来的SAP版本做好准备。”

“凭借Kyndryl全面的SAP专业知识、端到端服务和在自身SAP应用中应用Nova Intelligence的第一手经验，他们成为将我们技术带给客户的理想技术服务合作伙伴，”Nova Intelligence首席执行官Emma Qian表示。“通过我们的合作，我们将帮助更多客户以高效的方式加速其Clean Core之旅。”

Nova Intelligence的AI代理可以显著加快自定义代码的文档化、简化、生成和单元测试。结合Kyndryl的SAP现代化服务，该技术支持客户实现Clean Core合规，降低风险，并为当前和未来的SAP能力做好准备，同时持续维护业务连续性。

为了支持自己的数字转型，Kyndryl利用Nova Intelligence AI代理来现代化其两个关键的遗留SAP自定义应用程序，显著减少手动开发工作和转型成本。自2024年7月被宣布为RISE与SAP交付合作伙伴以来，Kyndryl已将其全面的SAP服务应用于为客户在其SAP Cloud ERP转型旅程中提供端到端支持。Kyndryl与Nova Intelligence的合作以及其数据转型套件使公司能够为客户提供更深入的指导和支持。

