特斯拉迄今为止唯一一次“电池日”举办在2020年9月，由于疫情原因，电池日比原定日期推迟了5个月，参会者的防护措施也很新颖——坐在停车场的电动车里观看活动。延期的五个月里，全球新能源车市场大爆发，全年销量首次突破300万大关。

---
同时，上海超级工厂开始爆产能，以月为单位刷新着产能记录，让这次电池日备受关注。

