AI构建AI是GFT最新的创新——使金融公司能够在几天内而不是几周内启动技术系统

全球数字转型公司GFT宣布，正在推动美国和加拿大金融机构的云和AI数字转型项目，推出其新款AI驱动的软件开发产品Wynxx。![图片](https://ai-techpark.com/wp-content/uploads/GFTs-Wynxx.jpg){ width=60% }

---
随着金融机构现代化其遗留系统，包括转向云平台以及在整个组织内部署新的AI用例，他们正在寻找在降低成本的同时保持质量的方法。GFT的Wynxx使这一目标成为可能，将云和AI能力的上线时间缩短了多达95%。这一专有的AI工具已经帮助一家全球顶级投资银行将云部署开发任务所需的时间从一个月以上减少到仅一天。

金融机构希望利用AI驱动的效率——但不到40%的金融机构甚至迈出了第一步：全面转向云环境。这在很大程度上是因为对于大多数金融机构而言，数字转型项目，如向云的迁移，都是一个复杂的工作，需要大量的开发者资源。因此，这些金融机构在寻找利用AI的力量时面临挑战——AI通常依赖于集中云生态系统——以通过降低运营成本，同时提升客户体验，带来竞争优势。

在这样的背景下，GFT将Wynxx引入美国和加拿大，使金融机构能够减少大规模数字转型项目所需的开发者时间和资源。该新产品使金融机构在关键的数字转型项目中大幅提升效率，包括：

从分散的遗留系统迁移到综合的云环境。GFT与领先的超大规模云服务商（包括AWS和Google）合作，为每个金融机构提供合适的云环境。由于云部署通常需要数月甚至数年才能完成，机构通常会选择缓慢且渐进地迁移到云。现在，借助Wynxx，GFT能够更快地交付其在过去20年中建立的领先云转型服务，使组织能够花更多时间创新利用云基础设施的新AI解决方案。

与最新的自动化进展保持同步。大多数金融机构在工作流程中已经利用某种形式的自动化技术——如欺诈预防、反洗钱等，但这些技术随着时间的推移不断改进。然而，更新旧的自动化工具仍然是一个挑战，尤其是当代码文档存在缺失时（即解释代码如何工作的文档）。借助Wynxx，GFT现在能够将文档所需时间减少超过90%，同时确保质量，以降低新更新的复杂性。

将AI注入手动且容易出错的任务，以扩展产品和增加收入。近年来，私人信贷等金融产品的需求迅速增加，但大多数公司无法以所需的速度扩大其手动信用风险分析，无法准确性，并因此影响信贷决策的收入。同时，构建一个具有公司内部风险控制和参数的专有AI信用分析系统通常被认为是不可能的，因为所需的开发者时间过长。现在，Wynxx使GFT能够构建与金融公司现有系统交互的定制软件解决方案，同时考虑到公司的内部风险偏好和控制，迅速完成。

“在过去的35年中，GFT在提供金融服务所需的有效自定义配置技术方面建立并维护了良好的声誉，以建设其业务，提供增强的客户体验并简化运营。” GFT北美、英国和亚太地区区域首席执行官Christopher Ortiz表示。“现在，借助Wynxx，我们能够在更短的时间内提供我们的客户一直期待的同样质量的服务。我们期待与客户共同解决他们最紧迫的业务挑战，现在的时间表是之前无法想象的，促进新的创新途径。”

Wynxx的推出是GFT实现到2025年成为一家完全以AI为中心公司的最新一步，届时AI将不仅成为公司内部运营的核心，也将成为其为客户提供服务的核心。
