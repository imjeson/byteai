title: 'Nathan Carmon被任命为Robots & Pencils首席运营官'
date: 2025-08-11
author: ByteAILab

---

经验丰富的转型领导者加入执行团队，以实现以AI为首的发展，提升客户交付，推动运营卓越
Robots & Pencils是一家全球数字创新公司，专注于云原生的网络、移动和应用现代化，今天宣布任命Nathan Carmon为首席运营官（COO）。![图片](https://ai-techpark.com/wp-content/uploads/Nathan-Carmon.jpg){ width=60% }

---
作为一名经验丰富的运营者和CEO Leonard Pagon的长期商业伙伴，Carmon在关键时刻加入，目的是提升执行力、深化客户价值，并加速公司的AI第一动能。

Carmon的任务不仅仅是快速增长——而是要以正确的方式增长。这意味着投资于人才，完善交付系统，确保每个客户的参与都有明确性、关怀和可衡量的影响。他的到来标志着Robots & Pencils从移动先驱向AI第一咨询公司的另一个重大里程碑。

在过去的20多年里，Carmon和Pagon一起建立、扩展和出售咨询业务——从将Brulant从一家25人的初创公司发展到拥有500人的强大企业，再到在收购后将Rosetta的规模翻倍。作为Next Sparc的运营合伙人，Carmon在过去十年中将战略转化为结果，涵盖了多个高增长投资项目。现在，他将这种转型专长带到Robots & Pencils。

“Nathan是一种稀有的运营者，他能将复杂的事情简化，将雄心勃勃的目标变为可实现的现实，”Robots & Pencils的CEO和Next Sparc的总裁Leonard Pagon说道。“在我们建设Brulant时，我可以依靠Nathan将大愿景转化为日常的动力。我们能够快速扩展，因为他知道如何协调团队、提供价值，并迅速而精确地行动。有Nathan作为COO，我们不仅为未来做好准备——我们为之而生。”

Carmon的加入恰逢每个企业在压力之下必须采纳AI、现代化传统系统，并以数字速度竞争的时期。Robots & Pencils正以挑战者的心态迎接这一时刻，部署高影响力的海豹特种部队式团队，将精英工程与直观的用户体验设计相结合。凭借其在北美、拉丁美洲和东欧的全球交付能力，以及与AWS、Salesforce和Databricks的合作伙伴关系，该公司独特地处于帮助客户在AI时代领先的有利位置。

“我帮助从零开始扩展咨询公司，我知道什么才是伟大的，”Carmon说道。“Robots & Pencils具备这种能力。这里的人才非常出色，领导力大胆，而市场时机再好不过。这支团队拥有我们再次实现目标所需的一切——只不过规模更大、速度更快、智能更强。”

“有Nathan领导内部运营，我就可以腾出更多时间去做我最有价值的事情——直接与客户互动，探索战略机会，并帮助塑造Robots & Pencils的未来，以满足市场需求，”Pagon说道。“这就是有一个值得信赖的领导者的力量。”

Carmon拥有密歇根大学的计算机科学学士学位和MBA学位。当他不在加速数字转型时，他喜欢冲浪、骑自行车、模型火箭，并与女儿和四个孙子共度时光。

