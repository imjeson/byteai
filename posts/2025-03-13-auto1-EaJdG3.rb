为什么加密货币享受政治盛宴

美国总统上周五在白宫接待了众多加密货币领袖，这似乎是对近几周加密行业接连获得胜利的一次精心策划的高潮。就在前一天，唐纳德·特朗普签署了一项行政命令，打造一个“战略储备”的加密货币项目，他称之为“数字黄金的数字福特诺克斯”。这只是近期多个声明中的一个，表明对这一行业的新态度。

在上周，美国监管机构宣布将不对大型加密公司进行处罚性调查——包括Coinbase、Gemini、OpenSea、Yuga Labs、Robinhood Crypto、Uniswap Labs、Consensys和Kraken。同时，还撤销了对一位购买7000万美元特朗普的迷因币$Trump的企业家的诈骗指控。两周前，美国主要金融监管机构的新领导宣称，“迷因币”等短命的加密货币将不再受到严格监管。

特朗普早在竞选活动中就已暗示了这一系列举措。他是第一位接受加密货币竞选捐款的主要政党候选人，并在竞选中承诺将美国打造成“加密货币之都”。周五，他重申了将国家打造为“比特币超级强国”的承诺。

作为一个行业，加密货币展现出与特朗普相似的受害者情结，后者总是以高位发声，仿佛他的母亲要他睡觉不得不忍受没有甜点的惩罚。特朗普任命的加密货币专员大卫·萨克斯表示，行业曾遭受“迫害”，“没有人能更好地理解这一点”。

他说的当然是特朗普。Gemini的联合创始人卡梅伦·温克莱沃斯在白宫加密峰会上表示：“这标志着对加密货币战争的另一里程碑。”尽管温克莱沃斯的不满似乎毫无理由，但实际上，乔·拜登的政府确实对加密货币采取了严格的监管态度，旨在将去中心化金融视为证券，适用与华尔街股票和债券相同的法规。这些调查的发起必然有其根源。

此次峰会是加密货币的胜利，也是其在政治上投资的巨大回报。根据《拦截者》的数据，与会者向特朗普的就职典礼捐款达1100万美元。在2024年竞选中，加密货币的支出接近2.5亿美元，远超任何其他行业，尽管该行业的主要政治行动委员会并没有对总统竞选提供资助。

加密货币虽然表现强劲，但若从比特币的价格来看，却无迹可寻，通常被视为该行业整体健康的象征。自年初以来，主角比特币下跌了约13%，这与整体美国股市的前景密切相关，投资者对此并不乐观，尤其是特朗普变幻莫测的关税政策让人感到困惑，导致美国股市在近几天内急剧下跌。

