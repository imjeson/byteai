左翼能否重拾技术乐观主义？

你对未来有信心吗？

对未来的期盼似乎在今天的政治气候中显得保守。技术乐观主义已演变为右派的专利。这是一个反直觉的发展：自由派政治可能旨在改善现状，但左翼因对硅谷亿万富翁的反对现在成为了技术悲观主义的一方。与此相对, 保守派政治的目的在于维护和回归过去，但美国共和党和世界各地的右翼高呼科技进步，在与其资助方的联盟中。

科技的快速进步似乎与自由主义理念相契合，以颠覆僵化的规范为目的人类利益服务。然而，如今以亿万富翁为代表的先进未来愿景大行其道，他们对不受限制的资本主义充满崇拜。我的同事阿曼达·方塔内拉-卡汉在一篇引入新系列《突破》的文章中探讨了左翼如何重拾对美好未来可能性的信仰：“希望稀缺，悲观情绪高涨，绝望弥漫。在我们时代的另一端，矗立着硅谷的亿万富翁，他们是地球上最乐观的人。当然，当你掌握足够的资金来影响国家政治时，保持乐观并非难事。然而，他们的乐观并不仅仅来自资金，还有对不加约束的科技进步深信不疑的信念。”

“但左翼是否能够并且应该提出自己的技术乐观主义？能够推出一个充满光明未来愿景的方案，与埃隆·马斯克所呈现的太空探索宏图相竞争吗？能够论证科学和技术应当被利用来实现突破、繁荣、可持续发展以及人类潜能的蓬勃发展吗？这种进步、由99%人群参与的技术乐观主义应当是什么样子，尤其是对历史上被排除乃至受害的有色人种社群，包括黑人？这些都是《突破》新系列探讨的问题。”

