---

title: '一个AI生成的乐队在Spotify上获得了100万次播放。

---
现在音乐业内人士表示，听众应该被警告'
date: 2025-07-15
author: ByteAILab

---

他们迅速走红，在几周内在Spotify上获得了超过100万次的播放，但后来曝光的热门新乐队“天鹅绒日落”（The Velvet Sundown）实际上是由AI生成的——包括他们的音乐、宣传图片和背景故事。这一事件引发了关于真实性的辩论，音乐行业内部人士表示，音乐流媒体平台应当在法律上有义务标记由AI生成的音乐，以便消费者能够做出明智的决策。

最初，这支被描述为“一个由人类创造性指导的合成音乐项目”的“乐队”否认他们是AI创作的，并于六月份发布了两张专辑《漂浮在回声之上》（Floating On Echoes）和《尘与静寂》（Dust And Silence），其风格类似于克罗斯比、斯蒂尔斯、纳什与扬（Crosby, Stills, Nash & Young）的乡村民谣。这一切变得更加复杂，当一名自称为“附属”成员的人向记者透露，天鹅绒日落在创作他们的歌曲时使用了生成性AI平台Suno，并且该项目是一个“艺术骗局”。

乐队的官方社交媒体渠道对此进行了否认，并表示该团体的身份正被“劫持”，随后发布了一份声明，确认该团体是由AI创造的，并表示他们“不是完全人类，也不是完全机器”，而是“生活在两者之间”。几位人士告诉《卫报》，当前的情况是，流媒体网站（包括Spotify）在法律上没有义务识别AI生成的音乐，这使得消费者无法了解他们所听音乐的来源。

伊沃斯学院（Ivors Academy）首席执行官罗伯托·内里（Roberto Neri）表示：“像天鹅绒日落这样的AI生成乐队能够在没有人类创作者参与的情况下吸引大规模受众，对透明度、著作权和同意权提出了严重的担忧。”内里补充说，如果“以道德方式”使用，AI有潜力增强 songwriting，但他的组织目前正关注他所称的使用AI音乐中的“深度问题”。

英国唱片产业协会（BPI）战略首席官索非·琼斯（Sophie Jones）支持对AI生成内容进行明确标记的呼声。她表示：“我们认为，AI应该用于服务人类创造力，而不是取代它。这就是为什么我们呼吁英国政府保护版权并引入新的透明度义务，使得音乐权利能够被许可和执行，同时呼吁对纯粹由AI生成的内容进行明确标记。”

《心情机器：Spotify的兴起与完美播放列表的代价》（Mood Machine: The Rise of Spotify and the Costs of the Perfect Playlist）的作者莉兹·佩利（Liz Pelly）表示，独立艺术家可能会受到那些以AI乐队为基础的人剥削，这些人可能会创建使用他们音乐进行训练的曲目。她提到了2023年的一起案件，一首歌曲在TikTok、Spotify和YouTube上上传，使用AI生成的声乐声称是周末（The Weeknd）和德雷克（Drake）的。环球音乐集团（Universal Music Group）表示，那首歌是“用生成性AI制作的侵权内容”，并很快被下架。

目前尚不清楚天鹅绒日落的专辑是基于什么音乐进行训练的，批评人士表示，这种缺乏透明度意味着独立艺术家可能会失去应得的报酬。佩利说：“我们需要确保，不仅是流行明星的利益得到关注，所有艺术家都应该有能力知道他们的作品是否被以这种方式利用。”

对于一些人来说，天鹅绒日落的出现是音乐与AI结合的逻辑下一步，而立法正在努力跟上这一快速变化的音乐生态系统。琼斯表示：“AI生成乐队和音乐进入市场的崛起表明，科技公司一直在未经授权或未付费的情况下训练AI模型，以直接与人类艺术创作进行竞争。”内里补充说，英国有机会在音乐领域引领道德AI的采用，但这需要建立稳健的法律框架，以“确保创作者的同意和公平报酬，并为听众提供明确的标签。”

音乐流媒体服务Deezer的首席创新官奥雷利安·埃罗（Aurélien Hérault）表示，该公司使用检测软件识别AI生成的曲目并进行标记。他说：“目前，我认为平台需要透明，并尽量通知用户。在我所称的‘AI自然化’的时期，我们需要在使用了AI时通知用户。”埃罗并没有排除在未来移除标记的可能性，如果AI生成的音乐越来越流行，音乐家开始将其用作“工具”。

Deezer最近告诉《卫报》，该平台上高达七成的AI生成音乐流媒体是欺诈性的。目前，Spotify并未对音乐进行AI生成标签，并因将一些播放列表填充以“幽灵艺术家”（创建库存音乐的假冒乐队）而之前受到批评。该公司的发言人表示，Spotify并不优先考虑AI生成的音乐。“Spotify上的所有音乐，包括AI生成的音乐，都是由获得许可的第三方创建、拥有和上传的。” 

