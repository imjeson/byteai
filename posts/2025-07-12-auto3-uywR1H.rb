title: '66degrees任命Brian Gregory为首席技术官'
date: 2025-07-13
author: ByteAILab

---

66degrees，一家领先的AI、数据及云咨询公司，今天宣布任命Brian Gregory为其首席技术官（CTO）。![图片](https://ai-techpark.com/wp-content/uploads/66degrees.jpg){ width=60% }

---
Gregory在加入66degrees之前，曾在谷歌云工作八年，负责领导跨Alphabet的全球战略合作伙伴关系——参与多个领域的项目，包括地理、Android、X（Moonshot Factory）等。在这一角色中，他帮助转型了谷歌的一些战略客户，从而在2017年将谷歌云的业务规模从约40亿美元提升至2025年的约490亿美元年跑率。

作为66degrees的首席技术官，Gregory将负责推进公司的技术愿景和交付战略——推动创新、使数百万美元的企业项目成为现实，并为客户创造新的价值与业务转型。

“我非常高兴地欢迎Brian Gregory担任我们的首席技术官，”66degrees的首席执行官Ben Kessler表示，“Brian带来了深厚的技术专长、富有远见的思维和建设者的心态。他是我们领导团队的完美补充，帮助66degrees进一步扩展我们在AI、数据和下一代云架构方面的能力。他在转型世界最重要客户方面的经验使他特别适合帮助我们深化影响力，推动创新，并开启我们下一个增长阶段——为我们的客户和团队。”

Gregory在谷歌领导战略合作伙伴关系的角色中，帮助转型谷歌的最重要客户，推动行业颠覆、加速创新，并快速推出新产品。在加入谷歌之前，Gregory曾在Express Scripts、Peabody Energy和CenturyLink担任高级技术领导角色，负责在财富100强环境中进行大规模的现代化和创新项目。

“作为首席技术官加入66degrees是一个建设非凡事物的机会，”Brian Gregory表示。“这个团队在AI、数据和云的交汇处，特别适合进行最有意义的转型。我在这里是为了突破界限、以目标为导向，引领客户共同创造重塑商业竞争、发展和服务客户方式的影响。我对在谷歌的历程感到深深感激，并对未来的道路感到振奋。”

