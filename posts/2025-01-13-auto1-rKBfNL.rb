title: '水资源短缺担忧，工党首个人工智能增长区位于新水库附近'
date: 2025-01-14
author: ByteAILab

---

工党的首个人工智能增长区将设立在距离英国30年来首个新水库的地方，这引发了人们对人工智能推进将加大该地区“严重压力”水资源供给的担忧。

---
基尔·斯塔默周一宣布，他将大幅增加人工智能能力，并减少希望建造数据中心的公司的规划限制，设立“增长区”，以减少制约。第一个增长区位于牛津郡的卡尔哈姆，距离由泰晤水公司规划的阿宾登水库仅七英里，该水库旨在为严重缺水的东南部地区提供水源。根据环境署的数据，该国的这一地区面临着最有可能缺水的风险。牛津郡面临着特别的水问题，在热浪期间部分地区依赖瓶装水。

人工智能数据中心使用大量水，因为它们的服务器会产生热量。为了防止计算机系统过热并关闭，数据中心使用冷却塔和外部空气系统，二者都需要干净的新鲜水。根据微软全球数据中心的信息，人工智能每消耗1千瓦时能源需用水1.8至12升，而一项研究估算到2027年，全球人工智能可能会消耗高达66亿立方米的水，这几乎占据了英格兰年消费的三分之二。即使不增加大量人工智能数据中心，到2050年，英国每天面临近50亿升的水供应短缺，预计需求超过可持续水资源供应，这相当于当前公共供水量的三分之一。东南部预计在同一时间段面临每日超过25亿升的潜在缺口。

人工智能可能会抹消企业在减少用水方面所取得的成果；政府正在寻求到2037-38年间相比2019-20年水平减少商业（非家庭）用水9%，而目前企业正在实现6.1%的减少。绿党共同领导人阿德里安·拉姆齐表示：“当社区面临热浪、干旱和未来十年的水短缺时，这一战略将我们锁定在向人工智能数据中心提供大量水之中。一项估算称，人工智能相关的基础设施可能很快消耗相当于600万人的丹麦六倍的水。这对牛津郡卡尔哈姆等水资源匮乏社区的居民意味着什么？”雷丁大学的汉娜·克洛克教授补充道：“英格兰南部已经对水资源施加了严重的压力，随着更多住宅的建设和高科技产业的扩展，压力变得更加严重，这些都需要更多的电力和水。我们知道，随着气候变化，英国降水量将更加不稳定，炎热干燥的夏季将加重对数据中心所需冷却系统的需求。”

人工智能数据中心还消耗大量能源；国际能源署预计，从2022年到2026年，数据中心的总电力消耗可能会翻倍，达到1000太瓦时（TWh），相当于日本的电力需求水平。根据研究公司SemiAnalysis的计算，预计到2030年，人工智能将占全球能源生产的4.5%。今年1月，全球最大的可再生能源企业采购商亚马逊宣布已购得苏格兰一家海上风电场超过一半的产能。

南安普顿大学的人工智能教授戈帕尔·拉姆楚恩表示：“人工智能的扩展一直是国家电网的担忧，但人工智能计算需求的增长速度让所有人意外，除非我们在适当政策下平衡上述权衡，否则我们所拥有的所有廉价且绿色的能源都将被大型科技公司所消耗，使本已面临能源贫困的家庭面临更大压力。”一些科学家表示，人工智能可能带来环境机会。剑桥大学的教学研究员肖恩·菲茨杰拉德博士指出：“人工智能的能耗不应孤立地考虑，而应与智能化控制能为我们能源系统带来的潜在节能一同考虑。智能化控制的集成可能为我们已经拥有的能源系统提供了巨大的机会，这些控制机制能够释放出潜能。”

科技、创新与技术部已被联系以发表评论。

