DataBahn.ai是一家为现代企业工作负载构建安全原生数据管道平台的先驱，今天宣布任命普雷斯顿·伍德为首席安全和战略官。作为一位拥有超过25年领导经验的杰出网络安全和技术高管，伍德将帮助推动DataBahn的使命，改变安全数据管理并实现全球企业的智能自动化。

伍德曾在包括Zions Bancorporation、Bank of the West和City National Bank在内的多家大型金融机构担任三次首席信息安全官（CISO）和两次首席技术官（CTO）。他以将安全和技术战略与商业目标对齐而闻名，曾领导开发强大的安全框架、实时威胁检测程序和先进的分析计划。

在DataBahn.ai，伍德将与工程和产品团队合作，扩展平台的战略愿景并深化客户关系。他将领导开发下一代解决方案，例如智能的、人工智能驱动的数据代理工作流，为企业级数据编排带来自动化、上下文和精确性。凭借其在CISO和CTO的双重经验，伍德将担任双重角色——向企业客户推广DataBahn的创新，同时将下一代客户需求反馈回产品路线图。伍德拥有犹他大学的工商管理硕士学位和韦伯州立大学的计算机科学学士学位。

“我一直认为，安全和IT团队需要在适当的时间获取正确的数据——并辅以上下文和实时分析，以做出更聪明、更快速的决策。”伍德表示。“DataBahn的平台独特地解决了数据发现、获取和实时可见性中存在的持续挑战。我很高兴加入一个如此专注于客户成功和创新的团队，并期待帮助推动DataBahn实现提供安全、智能和高度可扩展的数据解决方案的使命。”

销售领导团队扩大

除了伍德，DataBahn.ai还通过两项关键的销售领导任命加强了其市场能力：

佩曼·法德被任命为西部地区高级副总裁，拥有超过15年的企业网络安全销售和领导经验。他曾在Lumifi和Castra等领先的MDR和SIEM提供商担任高级职务。法德因建立高效能团队和规模化的合作伙伴优先市场策略而闻名。在DataBahn，法德将负责监督西部地区的战略账户，加速国家扩张，并领导一支专注于客户价值和收入增长的客户经理团队。

特雷沃·克朗普顿被任命为EMEA地区副总裁。在IT行业拥有超过30年的经验——近一半时间致力于在欧洲、中东和非洲地区建设和领导初创组织——克朗普顿在帮助安全专业人士获得复杂数据环境的可见性方面拥有深厚的专业知识。在DataBahn.ai，他将领导公司的EMEA扩张，帮助客户应对日益复杂的数据环境，同时降低存储成本并简化操作。

“随着普雷斯顿、佩曼和特雷沃的加入，我们正在组建一个强大的领导团队，结合了技术卓越、深入的客户共鸣和成熟的市场执行能力，”DataBahn.ai的联合创始人兼首席执行官南达·桑萨纳表示。“他们在安全、数据管理和全球GTM战略方面的集体经验使我们能够加速增长并为全球企业提供更大的价值。”

