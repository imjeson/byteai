朱尔斯·怀特（Jules White）曾认为，他的11岁儿子需要学习编程才能获得成功。

---
然而，现在这位范德比尔特大学计算机科学教授认为，让詹姆斯学习一个新的、更有用的技能——如何提示人工智能（AI）聊天机器人——更为关键。自从OpenAI在2022年发布ChatGPT以来，怀特便开始教他的儿子使用生成性AI。起初，他向詹姆斯演示ChatGPT如何利用他们家地板上玩具的照片来创建游戏。后来，怀特向他展示了AI的幻觉缺陷，鼓励儿子用《吉尼斯世界纪录》的验证信息来驳斥ChatGPT生成的世界纪录声明。经过两年多的实验，怀特的儿子现在已经学会将AI整合到日常活动中，从制作学习材料到确定没有价格标签的鞋子的价格。“我的目标不是让他成为生成AI的高手，”怀特说。“而是给他打下基础，让他用AI进行创造、建构，探索不同视角，丰富他的学习。”怀特是越来越多教孩子使用AI聊天机器人的父母之一，他们希望孩子在未来能够负责任地使用这些工具，作为学校、工作和日常生活的个人助手。

父母们对于生成性AI的普及如何影响孩子的个人成长感到好奇。根据2023年一项Ipsos民调，近三分之一的父母表示，应该禁止在学校使用ChatGPT等AI工具，以避免作弊，因为越来越多的年轻学生依赖聊天机器人来完成作业。尽管关于AI对孩子批判性思维能力影响的研究有限，联合国儿童基金会及其他儿童健康组织正质疑依赖聊天机器人作为课堂作业的做法可能对认知发展产生的影响。AI的快速演变也可能使技术不那么熟练的父母在干预孩子不当使用时变得更加困难。根据2023年国家家长联盟的调查，只有16%的有孩子在幼儿园至12年级的父母表示他们对AI的能力有坚定的认识。

虽然OpenAI和谷歌的服务条款规定，使用他们的AI模型的用户须年满13岁，但一些父母却在谨慎的情况下，违反规则向年幼的孩子介绍AI。这些父母希望帮助孩子们将AI视为一个不完美的工具，能够扩展而不是阻碍人类的能力。“这将占据一切，”怀特说。“我希望他能做好准备。”与孩子一起接触AI的父母相信，尽早让孩子接触AI可以增强创造力，磨练批判性思维和提高沟通能力。AI顾问奥拉·汉德福德（Ola Handford）在2023年初向九岁和12岁的孩子介绍了ChatGPT，通过每周五晚上的“乐趣之夜”。其中一些夜晚专注于自由探索，而另一些则有结构性，比如通过一只松鼠的视角重写泰勒·斯威夫特的《Shake It Off》。她的孩子们现在将AI用于研究、食谱、辩论和旅行时寻找活动。这些活动成为了有关深度伪造和AI伴侣等风险的讨论的跳板，这是与犯罪、情感依赖和自杀等相关的AI的阴暗面。汉德福德向她的孩子们展示AI工具，包括Character.ai，解释那些虚拟形象是计算机生成的，而不是现实中的人。她强调，如果孩子们将AI伴侣视为人类，可能会产生“严重后果”。

“深度伪造和AI伴侣是高优先级的关注点，因为它们可能会影响我的孩子们并造成严重伤害，”汉德福德表示。“玩弄这些工具能够提高意识，”她相信。怀特也在与儿子一起强调负责任的AI使用。这位教授的活动旨在向11岁的孩子展示，AI可以解锁创造可能性，例如构建有助于学习的游戏，而不仅仅是作为搜索引擎或逃避写作的方式。 没有指导，怀特表示，孩子们可能会滥用ChatGPT来作弊，导致“智力懒惰”。“如果你的目标是复制并粘贴ChatGPT的答案，你就不能真正为社会做出贡献，”怀特说。他补充道，他希望他的儿子将AI视为一种重新想象工作流程的工具，而对AI技能的需求日益增长。

除了在就业市场上获得竞争优势，一些父母还将AI视为帮助增强与家人之间的同理心和信任的工具。加州橙县教育部的AI管理员库纳尔·达拉尔（Kunal Dalal）每天都使用AI与他的四岁儿子建立联系。他们使用ChatGPT创作音乐，并使用AI艺术生成器如Dall-E生成个性化图像，包括达拉尔童年在印度孟买的插图。他表示，最引人注目的用例是促进与儿子进行艰难对话。

当达拉尔觉得自己对儿子过于严厉或意识到儿子情绪挣扎时，他们会围坐在手机上的ChatGPT旁边进行讨论。达拉尔称这种策略为“沟通三角”。例如，如果达拉尔注意到他的儿子心情不佳，他可能会打开ChatGPT的语音功能，向AI解释情况，然后询问聊天机器人作为父亲该如何安慰儿子。AI随即会验证儿子的情绪，建议积极聆听技巧，并提供减轻压力的实用方法。他表示，ChatGPT的回答能够帮助达拉尔理解在孩子感到沮丧时需要什么，导致一次增进亲密关系的深入交谈。“这无关ChatGPT所说的内容，重要的是它在我儿子和我之间架起了一座桥梁。”

然而，正如哈佛大学研究AI对儿童和家庭影响的助理教授徐颖（Ying Xu）所警告的，虽然一些父母以开放的态度接受这一技术，但AI也可能在技术被滥用的情况下造成父母与孩子之间的隔阂。徐指出，孩子们如果将AI的知识视为绝对，可能会在寻求指导时选择向AI而非父母请教。这一点尤其在研究中普遍观察到，即父母不知道孩子使用AI的频率，而宣传其AI伴侣对儿童友好的公司则进一步加剧了这一问题。她表示，过度依赖 AI可能模糊现实人际互动与AI生成回应之间的界限。

尽管如此，徐表示向孩子们介绍生成性AI仍有好处。接触这项技术可以扩展信息来源的范围，并使其使用变得越来越无缝。但向儿童暴露AI需要一个受控、审慎的环境。与《卫报》交谈的父母表示，他们会优先考虑监督。怀特和达拉尔只允许他们的孩子在获得许可的情况下使用ChatGPT，而汉德福德则表示，她的孩子在个人时间不会像社交媒体那样使用AI，后者她认为更危险，因为平台如TikTok可能使孩子接触到不良内容。达拉尔甚至限制儿子可以进行的ChatGPT查询次数，以减少环境成本。

展望未来，怀特和其他人表示，他们计划随着孩子们的成长增加AI活动的复杂性，正在出现的AI代理工具提供了新的学习机会。“我对拥有自主权的AI感到兴奋，”这位教授说。“我们还没有探索这个，但它是下一个目标。”

