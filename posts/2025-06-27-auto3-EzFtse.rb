        Threat Protection Pro™获得AV-Comparatives认证，连续第二年获此殊荣
        网络安全功能Threat Protection Pro™，包含在领先的VPN应用NordVPN中，再次被独立测试机构AV-Comparatives认定为检测钓鱼网站的顶级工具之一。![图片](https://ai-techpark.com/wp-content/uploads/NordVPN.jpg){ width=60% }

---

        “钓鱼网站是全球互联网用户面临的最大威胁之一。它们从毫无戒心的用户那里窃取个人数据、登录凭证和财务信息，然后要么重定向这些用户，要么完全消失。我们全力以赴保护我们的客户免受这些网站造成的后果，”Threat Protection的产品经理Domininkas Virbickas说。
        AV-Comparatives是一家独立测试实验室，于2025年5月15日至28日进行了一项广泛的测试，以评估各种网络安全产品在检测和阻止钓鱼网站方面的有效性。该公司对228个钓鱼网址和200个干净网址进行认证，以查看它们如何保护用户。NordVPN的Threat Protection Pro™检测到90%的钓鱼网站。
        去年，NordVPN成为第一家获得AV-Comparatives认证的反钓鱼保护的VPN服务提供商，今年我们再次接受测试，继续成为唯一拥有此认证的VPN提供商。
        要通过AV-Comparatives的反钓鱼保护认证，必须检测和阻止至少85%的使用的钓鱼网址，并且不对合法的在线银行及相关网站造成任何误报。
        
        如何识别钓鱼网站并保护自己
        虽然Threat Protection Pro™是一个强大的保护工具，但钓鱼攻击始终在不断演变。保持良好的网络卫生同样重要。
        以下是一些简单的在线安全提示：

        验证网址。始终检查浏览器地址栏中的网址。注意可能表明这是一个假网站的域名变体。是否将您重定向到某个子页面，而您应该在主页上？是否有可疑的前缀？
        仔细阅读文本。如果您有丝毫怀疑，请再次检查电子邮件或消息。它是未经请求的吗？是否催促您做某事，试图引发恐慌？有没有拼写错误或其他错误？
        启用双重身份验证（2FA）。2FA增加了一个额外的安全层，要求在您登录之前进行额外的身份验证步骤，比如发送到您手机的一次性代码。即使黑客获得了您的密码，他们也无法在没有这个第二因素的情况下访问您的账户。
        检查网站的协议。确保您访问的网站使用HTTPS协议。合法网站优先考虑安全性，在地址栏中会有一个锁形图标，而伪造网址的网站可能只使用HTTP。

        NordVPN的Threat Protection Pro™适用于每个订阅，帮助用户每天更安全地浏览。
