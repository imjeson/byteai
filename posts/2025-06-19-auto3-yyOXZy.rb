此次发布引入了一个统一的系统，以提高生产力、治理和交付——将分散的项目转变为一个可信的、可重复的人工智能工厂。它还宣布了Zero-to-AI，一项新服务，旨在促进企业内部已验证的人工智能文化变革。

人工智能交付是投资回报的瓶颈
人工智能实施正在快速扩展，但商业影响仍然滞后。根据Domino的2025年REVelate报告，虽然88%的组织表示他们在将模型投入生产方面做得更好，但近60%的组织预计投资回报率不足50%。
NavAlytix的联合创始人及前Fitch Group首席数据官Heidi Lanford表示：“将模型投入生产不再是困难的部分，实现大规模的商业价值才是。这需要一个将实验与治理、有序交付连接起来的系统。Domino提供了这一基础。”
这种从部署到规模化、受管控影响的关注转变，需要整个人工智能生命周期的新基础设施和纪律。
Domino Data Lab的联合创始人兼首席执行官Nick Elprin表示：“我们正在重新定义人工智能生命周期，使其更快、更安全、更高效。我们的2025年春季发布为企业提供了一个人工智能工厂，将实验转化为影响——内置治理和自动化，以大规模交付人工智能产品。”

面对这些挑战，Domino的2025年春季发布推出了新的能力，从实验到合规，再到企业级部署，重设每一个人工智能生命周期阶段。

从摩擦到流动：Domino如何重置人工智能生命周期
提高生产力而不增加IT负担：数据科学团队因使用限制性工具和基础设施负担而浪费时间。Domino的春季发布通过新的能力消除了这些障碍，加速开发、增加重用并交付商业价值——而无需增加IT负担。团队现在可以：
- 通过将本地工具连接到Domino工作区，加速开发——结合了熟悉环境的灵活性、可扩展计算、可重复性和企业级治理。
- 利用交互式应用在人工智能的最后一公里上瞬时交付，弥合技术工作与决策之间的差距，使业务团队能够更快地根据人工智能驱动的洞察采取行动，而不牺牲治理或控制。

内置治理，而不是附加官僚制度：典型的人工智能治理往往会减缓人工智能交付并增加人工手动负担。Domino的春季发布对此进行了变革——增强了嵌入日常工作流程中的治理，以加速治理生命周期、降低风险，并便于规模扩展。通过此发布，组织能够：
- 通过自动检查实时跟踪政策合规性，将审核准备时间缩短70%——使用正确的指标，在合适的工件上，无需手动操作。
- 通过结构化发现和条件批准，加速模型验证和审查，简化协作，标记风险，并确保在Domino内的完整可追溯性。
- 通过直观的可视化构建器接口，使风险管理团队能够创建和管理治理政策，桥接风险和数据科学利益相关者，以建立超越良好意图的治理。
- 通过新的有序部署功能自动执行政策，阻止不合适的模型操作。

在控制成本的同时扩展人工智能交付：春季发布增强了Domino对IT和基础设施团队的价值，帮助对齐人工智能工作负载与性能、数据局部性和成本目标。增强功能包括：
- 新的成本中心仪表板，可按用户、项目或工作负载跟踪计算成本。加上Domino的预算警报，这一新能力帮助团队防止浪费，正当化基础设施支出。
- 现在普遍可用的Domino Volumes for NetApp ONTAP，为数据科学家提供按需访问企业数据的能力，而IT则通过可靠的ONTAP治理和混合基础设施管理保持控制。
- Domino现在是NVIDIA Enterprise AI Factory验证设计的一部分，给予IT团队信心，以扩展Domino自己的人工智能工厂架构并结合经过NVIDIA优化的全栈基础设施。

几个春季发布功能——包括Domino Apps和增强的合规工作流程——在5月20日的Domino生命科学聚焦RevX活动上首次亮相。在一个生命科学用例中，团队可以利用这些最新的平台能力，通过从大型多源数据集中预测患者资格来加速临床试验招募。这些创新可以作为跨行业扩大人工智能驱动突破的蓝图。

从用例到影响的加速路径Zero to AI
Domino还宣布了Zero to AI，一项新服务，帮助企业实现人工智能工业化，通过快速推动高影响用例从试点到生产。它结合了Domino的架构专业知识、实施支持和培训，为客户提供可扩展的、审计合规的人工智能解决方案。通过Zero to AI，客户获得：
- 应用Domino经过验证的方法，以创纪录的时间提供生产就绪的人工智能——为未来项目创建可重用的基础。
- 受软件工程最佳实践启发的模板优先开发，具有模块化管道、可重用代码和加速交付的用户界面。
- 具有版本控制的、可测试的组件的审计就绪架构，确保每次部署都可扩展且合规。

