<h3><strong><em>01.</em></strong></h3>
<h3><strong>9个季度以来营收首次增长，</strong></h3>
<h3><strong>主要由个人系统业务拉动</strong></h3>
<p class="ne-empty-p"><br/></p>
<p>第三季度，惠普实现了9个季度以来的首次营收增长，总营收同比增长2%，环比增长6%，达135.19亿美元。这主要是由惠普的个人系统业务拉动的。</p>
<p class="ne-empty-p"><br/></p>
<div class="ne-atomic">
<div class="ne-img-wrapper" data-align="c">
<img alt="" src="http://www.jesonc.com/Fl2H4zGuy6n8oaAzn_mgcomJibrG" style="width:661px"/>
</div>
</div>
<p>▲惠普过去9季度营收变化情况（图源：惠普）</p>
<p class="ne-empty-p"><br/></p>
<p>惠普各项业务的第三季度业绩如下：</p>
<p><strong>个人系统业务</strong>：营收为93.69亿美元，同比增长5%，环比增长11%。增长主要来自于商用个人系统销售。</p>
<p><strong>印刷业务</strong>：营收为41.43亿美元，同比下降2.8%，环比下降6%。其复苏速度低于预期，惠普称这主要是由于需求疲软和日元走低给竞争对手带来的优势。</p>
<p><strong>投资</strong>：营收为700万美元。</p>
<p>个人系统业务中，商用产品贡献了<strong>71%的营收</strong>。</p>
<p class="ne-empty-p"><br/></p>
<div class="ne-atomic">
<div class="ne-img-wrapper" data-align="c">
<img alt="" src="http://www.jesonc.com/Fna6di1tDHVTUsvtuTcxSctd0zB5" style="width:661px"/>
</div>
</div>
<p>▲惠普个人系统业务营收构成（图源：惠普）</p>
<p class="ne-empty-p"><br/></p>
<p>全球范围内，惠普在<strong>PC市场的份额基本持平</strong>，但环比增长1.3%，这主要是由<strong>工作站</strong>和<strong>消费者溢价产品</strong>等高价值类别的增长推动的。</p>
<p>惠普在<strong>印刷领域的营收同比下降3%</strong>。他们称办公市场的复苏不及预期，在中国的办公业务受挫，但全球家用市场营收实现增长。</p>
<p>第三季度，<strong>惠普的毛利率为21.5%</strong>，同比略有上涨。惠普称这主要是由于他们节省成本的努力，抵消了商品成本上涨和产品转型的影响。</p>
<p>然而，由于对关键领域和员工的持续投资，GAAP运营支出同比增长了9.7%。但惠普首席财务官Karen Parkhill称他们将继续投资，这些投资最终会转化成企业未来的增长动力。</p>
<p>第三季度，惠普非GAAP摊薄每股净收益为0.83美元，GAAP摊薄每股净收益为0.65美元，<strong>同比下降0.03美元。</strong></p>
<p>惠普称，他们本季度通过股票回购和股息向股东返还了近8.7亿美元。若没有出现高投资回报率机会，他们将逐渐把100%的自由现金流返还给股东.<br/></p>
<h3><strong><em>02.</em></strong></h3>
<h3><strong>消费者市场增长预计同比下降，</strong></h3>
<h3><strong>下调第四季度与全年预期</strong><br/></h3>
<p>惠普预测，个人系统业务第四季度<strong>商用市场将持续强劲</strong>，但<strong>消费者市场会持续疲软</strong>，下季度增长可能会低于历史水平，保持在5%-7%区间的上半部分。</p>
<p>印刷市场虽然趋势有所改变，但复苏将会继续放缓，这主要是由于持续的竞争性定价压力。</p>
<p>基于上述原因，他们<strong>降低了对第四季度和全年的预期</strong>.</p>
<p>他们预计，第四季度非GAAP摊薄后每股净收益将在0.89美元至0.99美元之间，第四季度GAAP摊薄后每股净收益将在0.74美元至0.84美元之间.</p>
