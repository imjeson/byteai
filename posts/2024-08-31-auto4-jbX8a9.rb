<p class="ne-empty-p"><br/></p>
<p>对于全年，他们目前预计非GAAP摊薄后每股净收益将在3.35美元至3.45美元之间。2024财年按GAAP计算的摊薄每股净收益将在2.62美元至2.72美元之间.<br/></p>
<h3><strong><em>03.</em></strong></h3>
<h3><strong>结语：惠普业绩喜忧参半，</strong></h3>
<h3><strong>AI PC的未来尚未到来？</strong></h3>
<p class="ne-empty-p" rel="nofollow noopener"><br/></p>
<p>由于疫情期间的消费者市场需求被充分释放，今年消费者对PC的购买欲整体有所下降。在被问及AI PC在今年7月的销售中占比多少时，惠普首席执行官拒绝透露.</p>
<p>虽然AI PC被认为是未来PC市场的重要增长点，但其相对高昂的价格让不少企业对大规模采购AI PC持谨慎态度。或许，AI PC的增长，还需要再等等.</p>
