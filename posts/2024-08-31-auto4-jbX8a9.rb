<p>此外，惠普预计印刷市场和消费者PC市场会持续疲软，因此他们下调了今年第四季度和全年的预期。</p>
<p>截至收盘，惠普在财报公布当天的股价下跌2.36%，其股价在盘后交易中继续下跌，跌幅已经扩大至4.49%.</p>
