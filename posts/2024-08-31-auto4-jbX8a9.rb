<p>智东西8月29日报道，今天，惠普发布截至今年7月31日的2024财年第三季度财报。

---
惠普Q3实现了<strong>9个季度以来的首次营收增长</strong>，营收为<strong>135.19亿美元</strong>，同比增长2%，高于分析师预期；净利润为6亿美元，<strong>同比下降16%</strong>；每股收益为0.83美元，低于分析师预期。惠普称其<strong>在中国市场遇冷</strong>，PC和打印机等产品销售疲软。</p>
<p>在财报电话中，惠普总裁兼首席执行官Enrique Lores称他们将在AI PC上大举发力，他认为这对于解决方案提供商来说是“一个难以置信的增长机会”。</p>
<p>惠普在本季度发布了AI PC新品，其NPU最高具有55 TOPS算力。不过，Lores也承认，<strong>惠普的AI PC产品线“对我们的结果影响还非常非常小”。</strong></p>
<p>他继续说道：“在采用方面，<strong>短期内消费者对AI PC的采用会更快</strong>，<strong>因为商用总是受到许多客户必须经历的评估过程的限制</strong>，需要一些时间才能对我们的结果产生影响。”</p>
<p>Lores称，到2027年，也就是其AI PC产品推出3年后，<strong>AI PC将占其PC产品出货量的50%左右</strong>，推动平均售价上涨<strong>5%-10%</strong>。</p>
<p>另据科技分析机构Canalys今年8月公布的数据，2024年第二季度，全球AI PC出货量达<strong>880万台</strong>，其中<strong>macOS</strong>设备占比<strong>60%</strong>，<strong>Windows</strong>设备占比<strong>39%</strong>。Windows AI PC出货量环比增长<strong>127%</strong>，惠普在其中占比约为<strong>8%，</strong>联想占比为<strong>6%</strong>，戴尔占比略低于<strong>7%</strong>。Canalys预测，AI PC 2024年出货量将达到<strong>4400万台</strong>，2025年有望达到<strong>1.03亿台。</strong></p>
