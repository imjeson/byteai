惠普企业（NYSE：HPE）今天宣布扩展其以安全为先、AI驱动的网络产品组合，引入了基于行为分析的网络检测和响应（NDR）能力，由HPE Aruba Networking Central提供支持。
该公司还通过将其基于云的通用ZTNA方法扩展到基于校园的本地区域网络，增强了其覆盖范围。这种新的本地边缘功能将云中定义的相同访问控制策略直接带到校园和数据中心，提供卓越的用户体验，并实现一致的执行，无论用户的位置或连接方式如何。
新的NDR解决方案利用HPE Aruba Networking Central的数据湖中的遥测数据，训练和部署AI模型，监视和检测易受攻击的物联网设备中的异常活动，这些设备在支持业务流程的关键任务中起着越来越重要的作用。随着物联网为组织提供数据来训练和激活生成式AI模型的机会越来越大，监测网络流量模式、连接状态或动态设备属性的更改，这些是成功妥协的指标，也变得至关重要。
“企业越来越意识到网络中未经保护的物联网设备是其安全解决方案中的一个观察盲点。这些设备可以被利用来发起更大规模的网络攻击，因此也是攻击面不断增大的主要因素之一，”HPE Aruba Networking首席技术和安全官Jon Green表示。“此外，随着安全团队越来越依赖网络提供零信任安全解决方案，HPE Aruba Networking提供了一种能够利用单一访问控制策略来保护应用资源的能力，无论是本地还是外部，客户都可以采纳，以减少重叠和潜在混淆的控制。”
此外，为了加速威胁响应，HPE Aruba Networking Central将攻击检测与新的策略建议相结合，通过拦截潜在的攻击来保护免受威胁。为了确保建议不会干扰网络运营，团队还可以在执行和响应工作流程的一部分以实施之前预览对其安全策略的更改。
“公司需要AI驱动的行为网络检测和响应、通用安全策略以及边缘到云的执行，以规模化保护用户、设备和应用——这是企业中越来越多的AI资产成为攻击目标的一个关键考虑因素，”Lopez Research创始人兼行业分析师Maribel Lopez表示。
这些新工具进一步扩展了HPE Aruba Networking的安全产品组合，延续了今年5月RSA大会上发布的新解决方案，包括HPE Aruba Networking Central内的AI驱动安全可观测性和监控功能，以及公司的首个SSE防火墙即服务。此外，在会议上，HPE Aruba Networking被评为“RSA大会2024最酷的网络安全产品”之一。
HPE Aruba Networking Central和HPE Aruba Networking SSE将在2024年8月7日至8日举办的Black Hat USA 2024展会上展出，位于1160号展台。此外，HPE还将展示安全计算管理和与网络安全控制互补的Zerto快速空隙恢复解决方案，以全面保护免受勒索软件等网络威胁。

