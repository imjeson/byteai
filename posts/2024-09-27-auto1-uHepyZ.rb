老板希望“吸引员工的职业道德”，但此举激起了工厂工会的愤怒。

---
近几周来，德国特斯拉电动汽车工厂的一位主管派遣经理前往检查大约两打因病假而继续领薪九个月的员工的家中。工厂制造总监André Thierig表示，家访在该行业很常见，公司只是想“吸引员工的职业道德”。这一来自埃隆·马斯克美国总部的汽车制造商的举动引发了柏林-勃兰登堡吉加工厂12000名员工中所代表的工会IG Metall的愤怒。该工会一直反对所谓的苛刻工作条件，包括“不合理”的长工作时间和糟糕的健康安全记录。“几乎所有工厂区域的员工都报告工作负荷极高，”工会地区主任Dirk Schulze说。“在人手短缺时，生病的员工受到压力，而健康的人被迫背负额外工作。“如果工厂管理者真的想降低病假率，他们应该打破这种恶性循环。”工会表示，在工会所称的“恐惧文化”下，柏林郊外工厂的病假率通常高达15%或更高。然而，Thierig表示，一些员工正在利用德国的劳动保护法。“他说，在该工厂1500名临时工中，这些工人的工作条件类似全职员工，因病假缺席率仅为2%。 “在我们的出勤分析中，一些现象显而易见：在周五和晚班，相比其他工作日，大约有5%的员工请病假，” Thierig说。“这并不表明工作条件不佳，因为工作日和所有班次下的工作条件是一样的。这意味着德国社会体系在某种程度上被利用。”

