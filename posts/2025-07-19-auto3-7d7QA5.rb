2025 ISG Provider Lens® 私有/混合云 - 数据中心服务报告评估了69家提供商在五个象限中的能力：大型账户的托管服务、中型市场的托管服务、托管托管服务、共置服务和AI就绪基础设施咨询。报告将富士通（Fujitsu）和金德里尔（Kyndryl）评为三个象限中的领导者。将埃森哲（Accenture）、凯捷（Capgemini）、Claranet、DXC技术（DXC Technology）、恩索诺（Ensono）、Hexaware、印孚瑟斯（Infosys）、Pulsant、Rackspace技术、T-Systems和TCS评为两个象限中的领导者。阿托斯（Atos）、计算中心（Computacenter）、德勤（Deloitte）、数字房地产（Digital Realty）、Equinix、全球开关（Global Switch）、HCLTech、IBM、LTIMindtree、NTT DATA、Redcentric、电信技术（Telefonica Tech）、Telehouse、Unisys和Wipro被列为一个象限中的领导者。

此外，LTIMindtree、Mphasis和NTT DATA被ISG定义为在一个象限中具有“有前途的投资组合”和“高未来潜力”的“新兴之星”。在客户体验领域，Persistent Systems被评为2025年全球ISG CX明星表现者，成为私有/混合云和数据中心服务提供商中的佼佼者。Persistent Systems在ISG客户之声调查中获得了最高满意度评分，这是ISG卓越之星计划的组成部分，也是技术和商业服务行业的最高质量认可。

