方法论
本报告中发布的数据基于对7,300名来自10,000+企业的受访者的调查，受访者在C级和副总裁级别以及跨越九个国家（美国、加拿大、英国、德国、瑞士、法国、荷兰、新加坡和阿根廷）和八个行业（金融服务、生命科学和医疗技术、教育和商业信息服务、能源、零售和消费品、通信、媒体和娱乐、保险、汽车和制造）中均匀分布。调查于2024年10月24日至2024年12月3日进行。调查数据与Censuswide合作收集。

