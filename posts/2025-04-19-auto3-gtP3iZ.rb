EPAM系统公司（NYSE: EPAM），一家领先的数字化转型和产品工程公司，今天宣布发布其AI研究报告“从热潮到影响：企业如何利用AI解锁真正的商业价值”。这份报告基于对来自九个国家和八个行业的7,300名参与者的调查，考察了当前AI采用的状态、面临的挑战和希望为企业的AI投资创造切实商业价值的机遇。

研究揭示了企业AI采用中受访者在感知与现实之间存在显著的不一致。在来自美国、加拿大、英国、德国、瑞士、法国、荷兰、新加坡和阿根廷的受访者中，近一半（49%）的人将其公司评定为“先进”AI实施者，5%的人被认定为“颠覆者”，32%为“称职者”，14%为“初学者”。然而，只有26%的自认为是先进公司和颠覆者的受访者成功将AI用例推向市场。

“随着ChatGPT的发布，在2023年和2024年，我们目睹了各行业公司开始试验AI并开发概念验证，主要针对生产力和运营效率的即时改善，” EPAM的首席营销和战略官Elaina Shekhter表示。“这一最新研究清楚地表明，我们现在进入了一个新阶段，成功依赖于识别高价值用例并有策略地优先考虑这些用例，以实现广泛的组织影响。能够有效地将其人才、数据和技术围绕这些优先用例对齐的企业，将是实际将AI部署到规模并从其AI投资中捕获商业价值的那些公司，时间点在2025年及以后。”

报告的关键发现包括：

- AI投资加速：公司计划在2025年将AI支出年增长14%，表明对基于AI的增长的持续承诺。
- 扩大AI规模仍然是挑战：尽管30%的技术先进公司成功实施了AI，但许多组织仍然难以弥合实验与企业级部署之间的差距。
- AI对商业的直接影响：颠覆者预计将53%的2025年利润归因于AI投资，显示出市场领导者的切实财务影响。
- 治理和安全滞后于AI增长：企业预计需要至少18个月才能实施有效的AI治理模型，突显了与快速发展的监管环境之间的对齐复杂性。
- AI人才仍然是优先事项：43%的受访公司计划在2025年招聘与AI相关的角色，机器学习工程师和AI研究人员是最受欢迎的职位。

“提高生产力和运营效率是普遍目标，但真正的转型在于弥合技术团队与业务之间的差距。” EPAM的工程副总裁Dmitry Tovpeko说道。“随着AI重新塑造企业，开发人员正从任务导向的用户转变为战略专家，负责任地利用AI进行端到端的场景。成功的关键不在于技术栈或云基础设施，而在于将技术团队与业务目标对齐，以解决真实世界的客户问题。”

为了实现AI的全部潜力，报告强调了与人、数据和技术对齐的重要性，以解锁真正的商业价值，并确定了四个成功采用AI的关键领域：

- 人员、流程和文化：有效的AI实施需要强有力的高管领导，清晰地阐明优先事项和关注领域。事实上，数据显示65%的颠覆者理解AI采用所需的技能。
- 业务和技术现代化：虽然31%的高管认为过时技术是AI采用的障碍，但真正的挑战是业务和技术团队之间缺乏对齐。一旦建立并传达清晰的组织目的，工程团队就可以制定现代化战略。
- 安全：安全始终是高级管理人员和工程团队的优先事项，特别是在数据保护、数据质量和云安全方面，35%的企业表示其实现现代化的最大挑战是缺乏成熟的安全计划。
- 治理和负责任的AI：尽管75%的先进公司声称已经建立了清晰的AI战略，但只有4%的颠覆者表示他们已经制定了全面的治理框架，即使理解有效治理通常在18个月内到位。

根据Forrester在其《2025年预测：人工智能》报告中的说法，2025年将重新关注战略，加深商业和IT之间的合作关系，向预测性AI回归，以及新的技术和架构的出现，企业将共同治理数据和人工智能。

“2023年，利用AI的竞争使许多企业参与尝试，但一小部分先驱脱颖而出，将大胆的想法转化为可扩展的现实。” EPAM NEORIS的首席AI官Nir Kaldero表示。“这些领导者将挑战视为机遇，将法规视为不可谈判，将未知领域视为空间进行创新。AI的下一阶段不仅仅是实验，而是大规模部署——专注于企业范围内的高影响用例，同时继续努力将人员与文化、数据与云、新的流程对齐，以解锁真正的指数型商业价值。”

报告总结，组织必须将AI与其商业目标对齐，而不是调整商业目标以适应AI能力。在这一新阶段成功的关键在于，企业必须超越仅仅为了提高生产力和运营效率而实施AI。为了获得竞争优势，具有前瞻性的企业必须战略性地跨越其整个价值链利用AI，以推动收入增长和提升客户体验。

完整报告包括了针对不同阶段企业AI旅程的详细见解和实际建议。

