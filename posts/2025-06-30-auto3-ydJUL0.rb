FANUC宣布发布重新构建和设计的ROBOGUIDE版本10，这是其行业领先的离线机器人编程和仿真软件的最新版本。ROBOGUIDE旨在提高效率、可视化效果和用户体验，继续成为希望简化自动化设计和实施的企业的首选解决方案。
ROBOGUIDE允许用户在3D环境中创建、编程和模拟机器人工作单元，而无需物理原型，从而降低成本并提高自动化规划的准确性。随着这一下一代软件的推出，FANUC还引入了几个强大的升级，包括：

新虚拟现实功能 – 用户现在可以在完全沉浸的环境中体验工作单元，提高了前景视野和空间意识。这使得工程师和操作员能够以更直观和深入的方式与自动化设计进行互动，并通过虚拟现实播放和漫游进行体验。
高性能64位架构 – ROBOGUIDE现在是一个64位应用程序，提供更高的性能、增加的内容容量和更强的处理能力，用于复杂的自动化系统。
现代化用户界面 – 采用了带有功能区工具栏、浮动和停靠窗口以及改进的图形，ROBOGUIDE V10使得导航更加直观和高效。通过新的拖放工具，用户可以轻松快速地查找和定义机器人。
增强的本地CAD导入支持 – ROBOGUIDE现在支持导入更多本地CAD格式，简化了集成和设计过程，使得导入、调整和优化自动化布局变得前所未有的简单。

“通过发布我们的增强版ROBOGUIDE V10，我们提供了一款更智能、更强大的仿真工具，以满足自动化行业不断发展的需求。”FANUC美国机器人应用部门总经理Eric Potter表示。“在几乎所有行业中，这款沉浸式仿真软件使制造商能够在实际安装之前有效地可视化工作单元的有效性，而无需物理原型设置的需求或费用。”
