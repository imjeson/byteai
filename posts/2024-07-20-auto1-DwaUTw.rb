一个全球性的IT故障带来了周五的混乱，使航班停飞，并影响了从医院到政府机构的一切。

---
在所有混乱中，悬在头顶的问题是：微软Windows软件的一个有缺陷的更新是如何将社会的大部分部分带到了停滞状态的？

问题起源于位于奥斯汀的一家名为CrowdStrike的网络安全公司，它是全球科技行业的主要依赖对象，包括微软在内，主要依赖于其Falcon程序，该程序可以阻止恶意软件和网络攻击的执行。Falcon通过确保访问一系列内部系统并自动更新其防御来保护设备 - 这种集成程度意味着如果Falcon出现故障，计算机也会随之瘫痪。在CrowdStrike周四晚上更新了Falcon之后，微软系统和Windows个人电脑遭遇了“蓝屏死机”，被困在一个恢复引导循环中，无法使用。

微软是一个巨头，拥有显著的市场影响力，在欧洲和美国主导着云计算基础设施。因此受影响的不仅仅是个人电脑，还有服务器和一系列其他系统。来自用户、设备、服务和企业的压倒性请求引发了一系列与微软产品（包括Azure Cloud和Microsoft 365）相关的级联故障。困扰Azure的故障导致365服务遭受额外但是分开的干扰。一个巨大的混乱随之而来。

这就是为什么CrowdStrike的故障更新演变成了历史上最大的IT故障，但这并没有告诉我们全球计算基础设施似乎存在一个单一故障点的原因？至少一名CrowdStrike高管也产生了相同的疑问。

CrowdStrike的副总裁Drew Bagley说：“他们的IT堆栈可能只包括一个操作系统提供商、云提供商、生产力、电子邮件、聊天、协作、视频会议、浏览器、身份、生成AI和越来越多的安全性。”“这意味着建筑材料、供应链甚至建筑检查员都是一样的。”

这就是这两天发生的事情的核心所在。重要的不仅仅是如此多公司依赖于CrowdStrike，还有云基础设施依赖于像微软这样的强大公司，而这些公司又将企业置于独占性和反竞争性的实践之下，将服务和产品集中到一个日益狭窄的范围内。

2023年6月，美国联邦贸易委员会收到有关云计算业务实践的公众意见征求电话。微软和亚马逊，这两家主导这一领域的公司，回答称竞争“蓬勃发展”，“非常动态和竞争激烈”。相对于另外两家，作为不那么重要的参与者，谷歌则不那么谦虚，并提供了一份11页的文件，指控微软扼杀竞争。

“微软复杂的许可限制组织客户，尤其是其现有的本地企业客户，在转向云时无法选择任何其他云提供商，并最终将这些客户锁定在其Azure生态系统中，”谷歌在其投诉中说。

谷歌是正确的，尽管它自己也犯有同样的错误。全球云基础设施服务市场的三分之二都受到这三家公司的控制，它们几乎让供应商在转换之间设置了无法逾越的技术壁垒，阻碍供应商的转变 - 实质上是将其锁定。

当世界各地的机场都出现了蓝屏死机时，美国联邦贸易委员会主席Lina Khan发推文称：“如今这样的事情时有发生，一个小小的故障就导致系统范围的故障，影响到从医疗保健和航空公司到银行和汽车经销商的各个行业。数以百万计的人和企业付出了代价。这些事件揭示了集中如何创造脆弱的系统。”

为什么集中、整合和垄断会让我们处于风险之中？问题不仅仅在于我们使市场同质化，使每个人都暴露于应该是孤立的服务中断。集中给予了重新构建市场的能力。垄断者会将公司从市场中赶出去，并重新设计竞争者的参与条款，以确保他们不会对现有的巨头构成威胁。供应商生态系统对微软的依赖可能被视为削减成本，就像微软对像CrowdStrike这样的另一家公司的依赖会被视为削减成本。

真正的代价是外部化的：当这些服务关闭时，谁才是真正受害者？CrowdStrike首席执行官George Kurtz失去了数亿美元的财富，但他会恢复。微软和CrowdStrike失去了一些客户和一些业务，但在一两年内无疑会比之前获得更多。这不仅仅是在这次故障中，而是在任何故障中都是如此。

对于那些需要紧急服务、医院、机场或政府机构的人来说是否也如此？对于依赖数字调解和计算的整个社会来说是否也如此，因为这些过程是由专制的经济公司而不是民主政治参与者驱动的？

我们以前也发生过这样的故障，但没有任何变化，部分原因是科技行业一直擅长转嫁责任。如果继续这样下去，那么垄断者将为所欲为，而每个人都将忍受他们该遭受的代价。

