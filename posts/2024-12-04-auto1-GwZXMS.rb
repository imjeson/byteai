上周末，这个名字在互联网上引起了广泛关注——但在ChatGPT上却没有。

---
大卫·梅耶瞬间在社交媒体上走红，因为流行的聊天机器人似乎无意与他有所牵扯。大量的聊天机器人用户花了几天时间，尝试让ChatGPT写出“大卫·梅耶”这个名字，但聊天机器人却拒绝响应，其回复在“似乎出错了”与“我无法产生回应”之间交替，或仅仅停留在“大卫”这个名字上。这引发了一阵关于梅耶身份的网络猜测。也有人猜测这个名为大卫·梅耶的人要求将他的名字从ChatGPT的输出中删除。

ChatGPT的开发者OpenAI对此情况给予了一些澄清，称梅耶问题是由于系统故障造成的。“我们的一个工具错误地标记了这个名字，导致它在回应中无法出现，这本不应该发生。我们正在着手修复。”OpenAI的一位发言人说。

一些社交媒体上的猜测认为，梅耶可能是大卫·梅耶·德·罗斯柴尔德，但他向《卫报》表示与此无关，并提到了围绕其家族名字所产生的阴谋论。“不，我没有要求删除我的名字。我从未与ChatGPT有过任何联系。遗憾的是，这一切都由阴谋论驱动。”他告诉《卫报》。

据了解，该故障与已故学者大卫·梅耶无关，后者因其名字与车臣武装分子阿赫梅德·查塔耶夫的化名相匹配而被列入美国安全名单。然而，答案可能与英国和欧盟的GDPR隐私规则更为贴近。OpenAI的欧洲隐私政策明确指出，用户可以从其产品中删除个人数据，这一过程也被称为“被遗忘权”，即某人从互联网上移除个人信息。

OpenAI拒绝评论“梅耶”故障是否与“被遗忘权”程序相关。OpenAI已修复“David Mayer”问题，现在可以使用该姓名进行回应，尽管其他在周末社交媒体上出现的名字仍然会触发“似乎出错了”的回应。

法律事务所Addleshaw Goddard的合伙人及数据保护专家海伦娜·布朗表示，“被遗忘权”请求适用于从处理个人数据的任何实体或个人——从AI工具本身到使用该工具的任何组织。“在大卫·梅耶事件的背景下，整个人名被从整个AI工具中删除这一点很有趣。”她说。

然而，布朗补充道，完全删除所有能够识别特定个人的信息对于AI工具而言可能会更为复杂。“对于AI工具而言，处理涉及的庞大数据量和工具的复杂性，造成了隐私合规问题，”她说，并补充道，删除与单一人相关的所有信息并不如删除他们的名字那么简单。“在开发AI模型并生成输出的过程中，收集了大量的个人数据，包括来自公共来源（如互联网）的数据。这意味着追踪和删除所有能够识别单个个人的个人信息在实际操作中，可能是几乎不可能的。”

