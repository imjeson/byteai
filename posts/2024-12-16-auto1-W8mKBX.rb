最近在美国东海岸，新泽西州出现了大量夜间无人机目击事件，这引发了恐慌和阴谋论，范围从秘密政府计划到外国势力的阴险阴谋。

---
即将上任的总统唐纳德·特朗普在没有证据的情况下声称，卸任政府在隐瞒某些与这些目击事件有关的信息，而新泽西州的官员们则在匆忙寻找答案。以下是我们对无人机目击事件的了解和未知之处：

它们在哪里？
在11月和本月，有数十名目击者报告在新泽西州看到无人机，目击地点既包括居民区，还包括军事研究和制造设施、铁路站以及特朗普的一处高尔夫球场。自从关于这些目击事件的报告在网上蔓延以来，越来越多的未知飞行物（UFO）在全国各地被目击。甚至在德国一处美国军事基地也报告了无人机目击事件。

这些目击事件中有多少是真实的？
FBI的一名官员表示，作为专门处理无人机目击事件的热线，已收到5000条线索，其中不足100条被认为“值得进一步调查”。国土安全部的一名官员称，目前没有证据表明存在对公共安全的威胁。

这些飞行物可能是什么？
毋庸置疑，许多目击事件涉及的是无人机。美国联邦航空管理局（FAA）注册的无人机接近80万架，其中大约一半是“娱乐性使用”，即用于娱乐。较大的无人机通常用于商业用途，如农业或基础设施开发的土地勘测，或拍摄空中镜头。

其他UFO可能是误认为无人机的飞机或直升机。一些在网上发布的视频中包括喷气发动机的嗡嗡声以及客机特有的红绿灯光，而许多无人机并没有这些。繁忙的机场中，空中交通管制员常常将飞机逐一排队着陆，这会给人一种几架飞机漂浮在空中或缓慢移动配合的错觉。FBI官员表示，近期靠近主要机场的“无人机”目击事件与商业航班的降落模式相符。

这些不明无人机比一般的更大吗？
目击报告中提到“车大小”的无人机，这些无人机大大超过了业余爱好者使用的型号。然而，天空中物体的大小有时很难估计，明亮的飞机由于夜间的光学错觉可能显得更近。

为什么会引发这种狂热？
与所有UFO目击事件一样，驳斥某种秘密计划理论极为困难，尤其是在有成千上万的目击报告的情况下。此外，减轻公众焦虑是一项艰巨的任务，政府官员不想表现出忽视公众关注的样子，因为这可能会使真心关心的市民感到沮丧，或者导致关于掩盖的阴谋论。

一名不愿透露姓名的高级政府官员在周末的白宫简报会上试图平衡缺乏机密阴谋证据与保证公众他们不会忽视恐惧之间的关系。“我们显然非常认真对待这件事，”他说。“目前，我们尚未识别出任何理由相信……没有任何刑事活动涉及，也没有国家安全威胁，众人可见的公众安全威胁，或与这些无人机相关的恶意外国行为。”

无人机危险吗？
全球的航空监管机构一直在努力应对数百万无人机的快速激增，但往往未能成功。许多国家，如美国，限制无人机可以合法飞行的高度，制造商通常会对产品进行“地理围栏”，防止其进入军事敏感区域的空域。然而，这些限制可以被破解，无人机过去曾导致广泛的混乱，包括机场的关闭。暴增的无人机问题一直是航空界的重要关注，并偶尔演变成更广泛的公众舆论危机。

