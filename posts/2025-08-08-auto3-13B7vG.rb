Super X AI Technology Limited（NASDAQ：SUPX）（“公司”或“SuperX”）今天宣布正式推出其最新的全合一多模型服务器（“MMS”）。作为SuperX首个支持多模型动态协作的企业级AI基础设施，MMS旨在实现即开即用、多模型融合，并深入集成应用场景。它为企业提供安全高效的全栈AI解决方案，具有针对各种规模企业的定制规格，标志着大型模型应用朝向多模型智能代理协作的新纪元。这是SuperX向大型企业AI客户提供的综合解决方案，紧随SuperX于2025年7月30日发布的XN9160-B200 AI服务器之后，进一步扩展了SuperX的企业AI基础设施产品组合。

为了帮助企业客户在新一波生成式AI浪潮中获得决定性的竞争优势，这款全合一多模型服务器预配置了高性能的大语言模型（LLMs），包括OpenAI新发布的顶级开源模型GPT-OSS-120B和GPT-OSS-20B。基准测试结果表明，GPT-OSS-120B在某些关键测试中（如大规模多任务语言理解（MMLU）和美国邀请数学考试（AIME））甚至超过了多款领先的闭源模型，这在OpenAI于2025年8月5日的新闻稿中有提及。这意味着SuperX的客户将能够以优越的成本效率获得全球领先的AI推理和知识处理能力。

对于SuperX的客户而言，这不仅仅是技术领先——而是一场效率革命。客户无需花费数月时间进行复杂的模型集成、硬件适配和性能调优。SuperX提供了一种即开即用、安全可靠、完全优化的企业级生成式AI平台，让客户能够立即部署先进的生成式AI应用和代理，从而快速响应市场变化。这一“交钥匙”解决方案是SuperX的核心差异化优势，将这款设备从单纯的硬件设备转变为一个完整的企业级生成式AI解决方案，推动业务创新并驱动智能决策。现在访问SuperX网站，尝试最新的AI模型：https://cloud.superx.sg/chat

突破技术边界：多模型融合架构促进行业创新
这款全合一MMS支持预配置、调用、加速、管理和迭代多种模型，包括推理模型、通用模型、多模型、语音合成/识别模型、嵌入模型、重排序模型和文本转图像模型。它与最终应用场景实现深度融合，在四个关键方面实现功能突破：

认知跃升：多智能代理之间的协作，建立在不同模型之上，支持更复杂的业务应用场景。例如，它可以根据文本描述直接定位视频片段，准确识别人物、行为等信息。
知识赋能：内置的门户助手和知识库系统支持60多种预配置的场景特定代理（如正式文档起草、法律咨询、政策比较），创建成“你所想的就是你所得到的”闭环业务流程。
云协同模型缓存：通过本地和基于云的模型库的联接，用户可以立即体验到最新模型的强大，无需等待。
全合一集成：全合一MMS提供一个统一的从芯片到模型服务的整合平台，处理复杂技术架构的集成和优化，用户无需关注模型底层的技术栈。

企业就绪：解决企业AI部署挑战的交钥匙解决方案
全合一MMS针对现实企业需求设计，解决AI采用中的关键障碍——数据隐私、部署复杂性和操作可扩展性：

AI数据安全（针对AI服务器）：基于NVIDIA Confidential Computing技术和NVIDIA Blackwell平台，MMS具有受信执行环境（TEE）。除了保护AI知识产权（IP）以及支持安全、保密的AI训练、推理和联邦学习外，企业还可以保护其最大模型，同时确保性能。
成本有效的部署：通过全栈硬件-软件集成，此服务器可在几分钟内部署，而无需额外基础设施或IT资源。它经过优化，适用于中小企业，同时也可通过集群扩展到大型企业，为传统的MaaS API服务提供高性能替代方案。
工作流程效率：MMS配备预配置的模板和操作指南，允许业务用户通过无代码或低代码接口迅速构建智能代理，以适应广泛的企业应用场景。

