Augmentir，全球领先的连接前线工作的AI公司，今天宣布推出其工业AI代理工作室，作为其Augie™工业AI工具套件的强大补充。这一前沿版本使制造公司能够快速、轻松地构建针对其独特操作需求量身定制的安全、专用AI代理——推动新的自动化、效率和劳动力生产力水平。

Augmentir的AI代理工作室使制造商能够使用无代码工具快速构建、定制和部署自己的工业AI代理。它支持广泛的用例，包括劳动力培训和发展、技能管理、安全、质量、运营和维护等。

专门的AI聊天代理与操作- 为每个角色构建专业代理，配备经过策划的行为和领域专业知识，以在组织内提供更快、更准确的答案。为每个代理添加操作，以在人类监督和批准下执行广泛的任务。  
自主AI代理- 部署自主行为的代理，分析实时的连接工人数据，以做出明智的决策并触发适当的操作，例如发送电子邮件或短信、整合数据、生成报告等。  
与现有工具和系统的无缝集成- 将AI代理连接到企业系统（如ERP、CMMS、QMS和LMS），以及数字化工作流程和数据源，以实现前线任务的跨系统执行。

“我们工业AI代理工作室的发布对于希望自动化复杂工作流程和优化前线操作的制造商而言是一个游戏改变者，进一步巩固了我们的工业AI领导地位。”Augmentir的联合创始人兼首席执行官Russ Fadel表示。“凭借快速创建与连接工人数据和系统交互的专用AI代理的能力，制造商现在可以推动持续改进、减少错误，并加速他们的数字化转型旅程。”  
重新定义制造业的前沿操作  
AI代理工作室拓展了Augie™，Augmentir的工业AI套件的功能，使制造公司能够超越传统数字化。通过构建能够自主管理和执行任务的代理——从培训和技能管理到维护工作流程——制造商可以确保前线操作更加动态、以数据为驱动且高效。

关键用例包括：

数字精益教练：轻松构建充当精益教练的AI代理，帮助加速精益转型倡议。  
适应性培训和技能管理：AI代理可以根据前线工人的技能和培训数据行动，支持前线经理识别优势和劣势、技能缺口，并推荐培训路径。  
运营：支持更主动的关键绩效指标(KPI)跟踪、卓越的运营可视性、持续改进和全面生产维护(TPM)的运营代理。  
安全：AI代理自动分析安全数据和活动，提供早期预警通知。  
主动维护执行：代理监控设备健康状况，并与CMMS集成以触发工单、报告问题或在故障发生之前启动预防性维护任务。

AI驱动的前线卓越的新纪元  
作为Augmentir持续承诺于工业操作转型的一部分，AI代理工作室为制造商打开了一种新的可能性维度。公司现在可以利用专用AI代理来支持人类工作者、增强决策能力并增加前线智能——使前线团队能够专注于更具价值的工作。

