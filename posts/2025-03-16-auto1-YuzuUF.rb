title: '“深感不安”：英国Starlink用户因马斯克的政治操弄而选择关闭'
date: 2025-03-17
author: ByteAILab

---

特斯拉的销量猛跌，X平台用户流失严重，而现在，似乎在那些转向埃隆·马斯克的Starlink卫星系统以便在偏远地区保持连接的用户中也出现了裂缝。

---
尽管Starlink的用户数量在持续增长，但一些订阅者对马斯克的政治操弄表达了不满，表示将不再使用这一高速卫星互联网系统。苏格兰小提琴手巴里·尼斯贝特（Barry Nisbet）表示，马斯克在特朗普就职典礼上的争议性致敬是他离开Starlink的最后一根稻草，尽管这将令他处于不利的境地。尼斯贝特说：“我已经感到非常不安很长一段时间了，他在美国大选中的角色以及他拥有的垄断让我深感困扰。”

尽管Starlink主导了卫星互联网市场，但用户的不安迹象出现的同时，欧洲科技爱好者对本土替代品终于浮现出的前景愈发兴奋。自特朗普与乌克兰总统泽连斯基发生争执以来，法国Eutelsat（欧洲航天电信公司）股票上涨500%。Eutelsat首席执行官向彭博社表示，该公司可能在几个月内在乌克兰等地取代Starlink。同时，拥有英国网络Inmarsat的Viasat也在与欧洲各国政府就取代马斯克的Starlink展开谈判。

在英国，Starlink主要用于那些网络覆盖差的农村地区的人们和企业，尽管也有一小部分是“准备派”（preppers）和马斯克的追随者。位于英格兰南部的一名Starlink安装人员表示，目前在需要快速接入互联网的情况下，没有更好的替代选择。他说：“在行业内，我们现在处于一个进退两难的境地。一方面，这是一个存在于许多农村地区的必要工具和解决方案，特别是在基础设施投资不足的情况下。但另一方面，我们现在不得不面对马斯克——客气点说，就是个混蛋。”

生活在诺森伯兰半乡村地区的顾问理查德·奥皮（Richard Opie）表示，他在疫情期间获得了Starlink，因为BT网络容量不足，但现在正在考虑是否继续使用该公司。他说：“这在偏远地区是个救星，但事情自那时以来发生了政治上的变化。埃隆·马斯克是一个非常不同的人，特斯拉的展厅正在被烧毁。我对他与特朗普的关系感到不安，以及他所施加的压力。”

尼斯贝特在Facebook“Starlink UK用户组”上的评论——他无法再为Starlink辩护——引发了用户间的激烈讨论。另一位Starlink用户梅尔·塞耶（Mel Sayer）写道：“我拒绝去特朗普的酒店，因为我不愿意给特朗普一分钱。现在，我也不想资助‘总统马斯克’参与他的政治活动。”迈克·布朗（Mike Brown）回复道：“我赞同你的看法，但目前被Starlink束缚住了。亚马逊将于明年推出一个竞争服务，我会尽快跳槽。”而另一些人则选择坚守Starlink。西萨塞克斯的顾问保罗·菲金斯（Paul Feakins）说：“人们使用Starlink是因为他们无法通过其他方式获得互联网连接，但这些愚蠢的人认为通过取消订阅来表达他们的观点是有意义的，因为他们在某些方面与埃隆意见不合。”

Starlink已被要求发表评论。Starlink在英国的增长迅速，从三年前的13,000名用户增长到去年的87,000名。由于某些地区的高需求，该公司甚至开始对这些地区收取额外的“拥塞费”。该公司通过卫星星座提供超快的宽带速度。英国客户通常每月支付75英镑的费用，外加299英镑的硬件费用。对于非客户而言，Starlink可能因其在乌克兰的使用而更为人知，自2022年2月俄罗斯入侵以来，Starlink终端被急送至乌克兰，对基辅的战场通信至关重要。然而，近日乌克兰可能被剥夺Starlink的前景日益增加，路透社报道称，美国谈判人员已威胁乌克兰将失去该系统。马斯克在X平台上发布道：“如果我关闭这一系统，他们整个前线将会崩溃。”

