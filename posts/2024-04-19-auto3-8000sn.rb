Converge Technology Solutions Corp.（“Converge”或“公司”）（TSX：CTS）很高兴宣布已与Forum Systems达成战略合作伙伴关系，以利用QuantumSim™，这是一个安全的多LLM网关。![图片](https://ai-techpark.com/wp-content/uploads/2020/06/Buyer-Guide-500x281-1.jpg){ width=50% }

---


正如Arun Chandrasekaran最近在Gartner®研究中所述，“生成式AI（GenAI）有潜力改变各行各业的业务。大多数业务和技术领导人相信，尽管GenAI带来重大风险并可能在不久的将来出台潜在法规，但其好处远远超过风险。”

Converge和Forum的合作结合了尖端的GenAI功能以及IT专业知识，以增强Converge的咨询服务。这将使客户深入了解其组织效率，并使他们能够规模化部署负责任的GenAI。QuantumSim™的三个模块——QS SecureGPT、QS Contracts和QS Benefits将处理复杂的业务挑战。

QS Contracts允许法律和采购团队快速分析带有LLM的合同，为Converge的客户提供独一无二的洞察力，让他们能够深入挖掘企业中的数千个合同，通过识别重叠功能、低效和冗余来帮助理清IT支出。此外，该解决方案将使商业、法律和采购团队能够通过公共和私人LLM与其合同互动。QS ContractsGPT允许企业:
建立事实依据：从众多来源（包括MSA、EULA、SOW、发票、PO等）摄取合同信息建立企业记忆：建立合同谱系，标准化跨合同的条款和条件，并保留SME分析和风险模板降低风险和冗余成本：通过与供应商合同历史比较，评估入站合同风险，并降低重叠产品成本

通过QS Benefits，该合作将通过标准化福利语言和加速文件发现和分析积极影响Converge的客户在医疗生态系统中，最终实现流程的简化和提高会员满意度。

“Converge很高兴与Forum Systems合作，为我们的客户提供这一开创性的AI解决方案，”Converge首席执行官Greg Berard表示。 “人工智能处于如今技术景观的前沿，Converge将保持在为客户提供AI和分析最新创新的最前沿。我们很高兴将这一新产品推向市场，并看到它与我们珍贵的医疗客户实施。”

Forum Systems首席执行官Mamoon Yunus分享了Berard的热情，补充道，“合同管理和医疗福利是展示LLM力量的绝佳机会。将Converge的深厚领域知识与我们的LLM专业知识相结合对双方都将证明有益。”

