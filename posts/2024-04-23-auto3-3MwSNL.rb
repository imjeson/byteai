新功能包括深入的AI响应跟踪洞察，具有实时用户反馈和模型比较功能，有助于推动持续改进AI应用程序的性能、质量和成本，同时确保数据安全和隐私。通过60多个集成，New Relic AI监控是最全面的解决方案之一，帮助组织更快地找到AI应用程序问题的根本原因，进一步推动他们对AI的采用，并在AI之旅的每个阶段为他们提供支持。

“根据我与我们客户群中CIO、CTO和高管的交流，很明显，每家公司都在思考如何借助AI扩大业务规模，”New Relic首席客户官Arnie Lopez说。“采用AI可能会带来高昂的成本并在其技术堆栈中引入复杂性。IT和技术领导者转向New Relic，因为可观测性对于帮助他们自信地应对充满激动的AI未来、优化性能和质量、控制成本至关重要，最终提供卓越的客户体验。”

组织渴望采用AI为客户提供更好的数字体验。根据Gartner的数据，到2026年，超过80%的企业将使用生成AI API或部署生成AI启用的应用程序。虽然对AI存在强烈且不断增长的需求，但组织在将AI纳入其技术堆栈时遇到困难，因为它引入了复杂性。New Relic AI监控直接应对这一挑战，并通过为组织提供其整个AI生态系统的统一视图以及其性能数据的其余部分，使组织易于管理其AI堆栈的复杂性。

主要功能包括：
自动插装：New Relic代理提供了对Python、Node.js、Ruby、Go和.NET语言等流行框架（如OpenAI、AWS Bedrock和LangChain）的简便设置。
全面的AI堆栈可见性：横跨应用程序、基础设施和AI层的整体视图，包括AI指标，例如请求数量、响应时间和令牌使用量。
具有终端用户反馈的AI响应视图：快速识别AI响应中的趋势和离群值，分析情绪，并在单个整合视图中查看用户反馈。
每个响应的深度跟踪洞察：使用诸如LangChain之类的工具跟踪AI响应的生命周期，以修复性能和质量问题，如偏见、有毒性和幻觉。
增强的数据安全性：通过排除AI请求和响应中的敏感数据（PII）进行监控，保持组织的安全性和合规性政策。
模型比较：在单个视图中比较正在生产中运行的基础模型的性能和成本，选择最适合您需求的模型。
快速启动集成：具有60个关键组件的最全面的监控AI生态系统解决方案的集成，如NVIDIA GPU和Pinecone、Weaviate等矢量数据库。

New Relic AI监控作为其一体化可观测性平台的一部分，作为其基于使用量的定价模型的一部分，已普遍可用。请通过联系您的New Relic账户代表或注册免费账户来开始使用。

