New Relic AI监控的新功能包括扩展的应用程序语言支持、安全和隐私控制以及实时用户反馈集成，帮助组织有信心地使用AI
New Relic，作为每位工程师的全方位可观测性平台，宣布增强了New Relic AI监控，推出了一系列功能强大的新功能，以满足开发AI应用程序的组织日益增长的需求。![图片](https://ai-techpark.com/wp-content/uploads/2024/04/New-Relic-960x540.jpg){ width=60% }

