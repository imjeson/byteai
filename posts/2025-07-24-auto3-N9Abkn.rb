Cognizant（纳斯达克：CTSH）今天宣布，其人工智能实验室获得了两项新的美国专利，并因在西班牙马拉加举办的GECCO（遗传与进化计算会议）上提交的关于“通过人工智能实现人类专业知识”的研究论文而荣获金奖。  
“在2025年上半年获得两项新的美国专利——使我们的人工智能实验室的美国专利总数达到59项，还有23项专利待审，这突显了我们不断创新的步伐，”Cognizant的人工智能首席技术官Babak Hodjat表示。“再加上在GECCO上获得的金奖，这些里程碑反映了我们致力于开创变革性人工智能技术，并将前沿思想转化为实际影响的深刻承诺。”  
最新的两项专利突出显示了Cognizant人工智能研究中的关键创新：  

- 美国专利第12,282,845号（于2025年4月22日颁发）涵盖了一种多目标深度神经网络架构共进化的方法，旨在提高模型性能和资源效率。该方法的应用范围从医疗图像分类到自然语言处理。  

- 美国专利第12,292,944号（于2025年5月6日颁发）概述了一种通过泰勒级数展开优化损失函数的方法，旨在提高训练效率并增强模型的鲁棒性，尤其是在数据有限的情况下。

这些创新是由Cognizant的研究人员开发的，包括Dr. Jason Liang，Dr. Elliot Meyerson和Risto Miikkulainen教授，进一步巩固了Cognizant在推动人工智能和机器学习前沿的领导地位。  
为了使人工智能的承诺更具实用性并广泛可及，今年早些时候，Cognizant的人工智能实验室开源了其Neuro AI多智能体加速器。Neuro AI帮助企业加速其人工智能代理的开发和采用，转变其业务流程，以实现自适应操作、实时决策以及为客户定义目标和监督量身定制的个性化客户体验。  
Cognizant人工智能实验室于2024年3月在旧金山开设了其旗舰人工智能研究设施，还因其在GECCO 2025上对人类竞争性结果的突出贡献获得了金奖，涉及的工作是RHEA（通过人工智能实现人类专业知识）。RHEA由Dr. Elliott Meyerson和Risto Miikkulainen教授开发，采用进化人工智能技术，从人类专家团队生成的数百个模型中提取和重组，尤其是那些提交给XPRIZE疫情响应挑战的模型，形成精炼的决策策略。在严格评估中，RHEA超越了单个专家提交的表现，发掘了即便是在未完全开发的人类想法中潜藏的潜力，提出了创新的疫情政策建议。这个里程碑再次强调了Cognizant在利用全球人类专长推动下一代人工智能解决方案中的领导地位。  
Risto Miikkulainen，德克萨斯大学奥斯汀分校计算机科学系副教授表示：“RHEA是一个强有力的例子，展示了进化人工智能如何能够放大全球人类智慧——不仅通过匹配专家解决方案，还能超越它们发现新颖的、高影响力的策略。这一认可得到了Humies奖委员会的支持，增强了以种群为基础的人工智能作为解决世界最复杂挑战基础的前景。”

