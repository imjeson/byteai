一位来自昆士兰的职业YouTuber被判赔偿350,000美元加利息和费用，因为布里斯班地区法院裁定这位YouTuber以“鲁莽”的态度对美国玩家比利·米切尔进行了诽谤，误称其诉讼与另一位YouTuber自杀有关。

---
威廉“比利”米切尔是一位美国游戏玩家，自1982年以来在《唐老鸭》和《吃豆人》上保持着世界纪录，这些纪录获得了《吉尼斯世界纪录》和视频游戏数据库Twin Galaxies的认可。米切尔对卡尔·乔布斯提起诉讼，要求40万美元的普通损害赔偿和5万美元的严重损害赔偿。乔布斯制作关于“竞速”的视频（尽快完成游戏），以及游戏记录和作弊问题，在2021年的一部YouTube视频中对米切尔提出了多项指控。他指控米切尔作弊，并“追求毫无根据的诉讼”，法院判决中如此写道。

法院听取了米切尔在2017年被指控使用仿真软件而非原始街机硬件来作弊的事件，Twin Galaxies对此进行调查，并随后取消了米切尔的成绩，禁止他参加其比赛。在Twin Galaxies的决定之后，《吉尼斯世界纪录》也取消了米切尔在《唐老鸭》和《吃豆人》上的所有纪录。判决指出，乔布斯的2021年视频还将另一位YouTuber阿波罗·传奇在2020年12月的自杀与他与米切尔的“和解”所带来的“压力”联系在一起，并错误地声称阿波罗·传奇必须支付米切尔“一大笔钱”。该判决指出，视频的多个版本被发布后，相关指控被删除或处理，乔布斯于2021年7月发布了撤回视频，两个月后发布了原始视频。该视频的首次观看人数超过50万，其中20万人来自澳大利亚，法院听证时提到。

法官肯·巴洛发现，乔布斯对米切尔做出了五项诽谤性指控，包括指控米切尔要求阿波罗·传奇支付他一大笔钱，暗示他“本质上是将阿波罗·传奇逼入了绝境”。巴洛发现这一指控是“建立在谬论之上”的，并且乔布斯在发布视频前未进行进一步调查，“鲁莽地对其真伪漠不关心”。

乔布斯否认视频中有这些指控。他辩称米切尔因为曾被揭露为作弊者而有不良声誉。法官巴洛发现米切尔确实有主播作为作弊者和对指控他作弊的人的诉讼者的声誉，但他认为乔布斯严重损害了米切尔的声誉并造成了困扰。他形容乔布斯具有一种“不愿承认错误和不愿退让”的自我推崇和自我保护的倾向。巴洛将乔布斯的行为框架为一场对米切尔的“十字军东征”，称他试图“向他的观众展示自己是那位打败米切尔巨龙的骑士”。

判决摘要指出，法院并未被要求裁定，并且也未裁定米切尔是否在他的世界纪录中作弊。法院判处米切尔因非经济损失获得30万美元的赔偿，并因乔布斯发布该视频两次、嘲讽米切尔的投诉、未能道歉和撤回指控、对米切尔的“明显恶意”而获得5万美元的额外损害赔偿。乔布斯被判向米切尔支付超过4万美元的利息，从发布之日起计算，以及米切尔的费用。

米切尔此前曾在美国起诉Twin Galaxies，该案于2024年1月和解。和解的部分内容包括Twin Galaxies发布了一份声明，指出米切尔“提供了专家意见，表明他在录像中记录的游戏表现可能显示了原始未修改的唐老大陆街机硬件的操控，如果相关的硬件出现故障，这可能是由于组件的退化”。该公司表示将恢复米切尔在其网站的官方历史数据库中的所有先前记录，并删除关于米切尔记录的争议线程。米切尔的《吉尼斯世界纪录》在2020年6月也被恢复。

