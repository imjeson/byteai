埃隆·马斯克因呼吁释放汤米·罗宾逊，并指责基尔·斯塔默未能起诉大曼彻斯特奥尔德汉的儿童强奸犯而引发愤怒。

---
这位与唐纳德·特朗普关系密切的亿万富翁在他的X社交媒体账号上置顶了一条信息，称“释放汤米·罗宾逊！”，并标记了因蔑视法庭入狱的极右翼活动家。除了多条支持罗宾逊的消息外，马斯克还多次提到关于罗奇代尔和奥尔德汉的强奸团伙，批评首相和儿童保护部长杰斯·菲利普斯对丑闻的回应。在一条帖子中，马斯克表示：“在英国，强奸等严重犯罪需要检察院的批准才能对嫌疑人提出指控。当时任检察院首长的是谁？基尔·斯塔默，2008-2013。”他还表示，菲利普斯“应该被关进监狱”，因为她表示应由奥尔德汉市议会对当地的儿童剥削进行调查，而不是国家政府。此外，他还提到：“她拒绝调查强奸团伙的真正原因是这显然会导致基尔·斯塔默（当时的检察院主任）受到指责。”马斯克转发了多条呼吁释放罗宾逊（本名斯蒂芬·雅克斯利-伦农）的评论，他因重复对一名叙利亚难民的虚假指控而在去年十月入狱。马斯克在英国政治中的最新介入引发了工党议员们的愤怒，他们中的许多人一直在推动政府停止在X平台上的发言。 

这位特斯拉联合创始人及X社交媒体的拥有者曾多次抨击斯塔默对去年夏季英国骚乱的反应。有报道称，他正在考虑向英国改革党捐赠高达1亿美元（约合8000万英镑），并在两周前与该党的领导人奈杰尔·法拉奇及财务主管尼克·坎迪合影。一位工党议员表示：“这次马斯克真的推得太远了。推特（现在的X）迅速变成了一个污水坑，甚至对一般的非党派人士来说也是如此。”一些部长表示，他们认为这应当成为政府退出马斯克平台的一个触发点。然而，其他人警告称，退出可能会引发外交事件，考虑到马斯克在即将成立的特朗普政府中的角色。唐宁街拒绝发表评论。改革党的发言人未就亿万富翁对罗宾逊的支持是否会改变该党接受他捐赠的意愿作出回应。奥尔德汉市议会的一位发言人表示：“幸存者是我们结束儿童性剥削工作的核心。无论未来的调查如何，我们已经承诺将他们的意愿置于首位，我们不会违背这一承诺。我们都认识到，过去犯下了可怕的错误，儿童的声音被忽视或 dismissed。通过确保幸存者的声音被听到，我们可以帮助确保未来不再有儿童受到伤害。”

