今天凌晨，OpenAI、微软、谷歌、亚马逊、苹果、特斯拉等全球著名科技企业CEO祝贺特朗普赢得选举。

他们认为，特朗普对于美国AI、科技的发展、创新至关重要。

---
一个很关键的原因是，特朗普对AI、科技领域的监管会非常宽松，例如，特朗普曾推翻了奥巴马时代的网络中立性法案，这一政策为互联网服务巨头提供了更大的自由度和发展力。

