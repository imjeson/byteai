Audos旨在寻找并培养日常创业者，帮助他们建立百万美元的AI企业，今天宣布获得1150万美元的Pre-Seed和Seed融资，由True Ventures领投，Offline Ventures、Bungalow Capital以及包括Bjarke Ingels、Leila Zegna、Niklas Zennstrom和Mario Schlosser在内的知名人士参与。过去一年，Audos已经帮助创业者推出了数百个正在被客户使用的AI应用程序。这笔资金中的大部分将用于支持新创业者。

Audos成立于2024年，由Henrik Werdelin和Nicholas Thorne创办，他们是成功品牌BarkBox、Ro和Managed By Q背后的创业工作室Prehype的创始人。Audos帮助单个创业者迅速将其专长变为商业机会，通过独特的AI工具、人工支持和资金组合。

“在过去一年中，我们帮助创业者推出了数百个AI应用，比如米其林星级侍酒师的葡萄酒品鉴、餐食计划、约会、圣经研究和个性化产后健身，”Audos联合创始人Nicholas Thorne说道。“我们的系统简化了AI代理的创建，这些代理可以在几乎任何地方与客户互动。它们代表了未来的创业方式——全天候运行，快速扩展专长，并提供个性化体验而没有传统的开销。随着创业者的成长，我们的AI不断识别新机会并自动化扩展，实现系统性的增长。”

Audos的项目关注四个关键支柱：

**百万美元商业模型**：通过成功创业者的经验方法，识别有潜力的日常创业者，并将其独特专业与可行的AI商业机会进行匹配，使用一种专有框架使个人优势与商业机会相一致。结果集中在创始者市场契合、盈利能力、可持续性和从第一天起解决实际问题。

**通过实践快速市场验证**：帮助创业者立即提供真实服务，而不仅仅是测试概念，处理技术实施和客户获取，让创业者可以专注于建立关系。

**Audos项目**：一个将人类专业知识与AI工具相结合的全套系统。基于创始人从众多成功初创公司中总结出的成功模式，该项目帮助创业者发现商业概念，识别和定位客户，并创建全天候在多个渠道上运营的AI应用程序。该项目的代理功能通过自动客户服务、可行动见解、自动驾驶功能和基于增长机会与客户数据的战略迭代更新，释放企业潜力。

**对齐的资本与支持**：一种模型，Audos只有在创业者成功时才会成功，提供灵活的融资资本、支持以及在创始人社区内共享的收益。这种方法通过收入共享体系提供从第一天起的资源，无需个人风险，允许创业者以自己的节奏成长。

“我们正在见证技术如何重塑创业方式的显著变化，”Audos联合创始人Henrik Werdelin说道。“虽然60%的美国人想成为创业者，但传统障碍——时间、资金模型、专业知识和复杂技术——使大多数人被边缘化，甚至包括那些拥有宝贵技能和强烈工作热情的人。我们正在支持一个新的世代，使任何地方的创业者都能利用AI构建与他们的专业和兴趣相一致的盈利企业。”

“让Audos真正特别的是他们对当今创业者角色的大胆重新想象，”True Ventures的合伙人Tony Conrad说。“Henrik和Nicholas在小企业融资和传统风险投资之间释放了一个强大的机会——为拥有不同技能集的创造者、建设者和创始人打开了大门。通过利用AI，他们的平台使新一代能够建立高收入、可持续的企业，而无需牺牲独立性或愿景。我们对他们重塑创业和塑造未来的使命感到鼓舞，未来任何有抱负、创意、专注和激情的人都可以蓬勃发展。”

