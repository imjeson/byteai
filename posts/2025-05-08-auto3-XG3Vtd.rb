Persivia，这家医疗行业最先进的数字健康平台背后的公司，今天宣布了两项重大进展——来自Aldrich Capital Partners的1.07亿美元投资和其健康数据处理系统的美国专利第12,254,975 B2号的颁发。![图片](https://ai-techpark.com/wp-content/uploads/Persivia-Secures.jpg){ width=60% }

---
这一双重里程碑标志着医疗保健的新纪元，Persivia正准备扩大其经过验证的AI首驱平台，彻底改变护理的交付、测量和管理方式。

新授予的专利保护了CareSpace®的核心AI能力，这是Persivia的模块化数字健康平台。与典型平台仅仅呈现数据不同，CareSpace®使用Soliton®，这一现在已获得专利的AI引擎，能够实时摄取、分析、优先处理并提供可操作的洞见。这使得护理团队能够快速响应最紧迫的需求，管理复杂的患者群体，并简化工作流程——所有这些都在统一的界面中完成。

Aldrich的投资——在二月底完成——为迅速扩展和深入投资平台创新、客户成功及市场执行提供了资金支持。这也使Persivia处于少数几家公司可以声称的位置：资助充足、临床验证并且现在在法律上保护作为一家获得专利的AI驱动平台的创新者。

“这不仅是Persivia的胜利，这也是美国医疗保健整体的一天，”Persivia的首席执行官Mansoor Khan博士说。“凭借行业领先的平台、新资本以及新颁发的专利，我们准备兑现一个经过20年发展而成的愿景——用一个现在已经获得专利、经过验证和大规模应用的AI核心来解决医疗保健最棘手的挑战。”

这一转型的核心是CareSpace®，一个旨在帮助组织在从按服务付费向基于价值的护理转型时蓬勃发展的数字健康平台。它支持广泛的用例——从数据、护理、质量和人口健康管理到分析、虚拟护理、风险调整等等。CareSpace®基于模块化架构，核心是Soliton® AI引擎，使医疗机构能够灵活部署他们所需的正是所需的——而无需承受点解决方案的复杂性。

“如今大多数医疗保健AI解决方案是附加在现有平台上的点解决方案，”Persivia的首席医疗官Fauzia Khan博士表示。“CareSpace®平台围绕Soliton® AI引擎构建，该引擎是系统的核心。将AI置于所有数据流、分析和工作流程之心的愿景构成了我们的专利基础，该专利于2020年初申请，早于AI如今成为流行词的时期。”

随着医疗行业面临越来越大的压力要以更少的投入做更多的事情，Persivia提供了一个结合速度、智能和互操作性的平台——推动更好的结果、降低成本并减少疲惫感。凭借这一专利的颁发，公司传达了一个明确的信息：智能AI驱动护理的时代已经到来，而Persivia在引领这一潮流。

