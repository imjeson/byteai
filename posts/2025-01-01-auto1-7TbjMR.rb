阿曼妮·基拉维在每次失恋时都会制作一个分手播放列表，一共三次。

---
尽管这些播放列表中包含了像戈提耶的《曾经的我》和一共和的《道歉》这样的歌曲，它们本应成为浪漫分手的好背景音乐，但其实并非如此。每当基拉维被三家不同的银行和支付处理商告知他们将停止与她共同创办的穆斯林众筹平台LaunchGood的合作时，这些播放列表便成了她的心声。Stripe表示，在从事五年的众筹业务后，它正在限制在这一领域的工作。Stripe还告诉该公司，它不想再参与任何国际人道主义工作，而这正是一个面向穆斯林社区的众筹平台的前提条件。另一家银行则表示，穆斯林和阿拉伯名字太多，调查这些名字是否属于受制裁个人太难。“人们并不知道，穆罕默德是被制裁最多的名字，”曾是LaunchGood首席运营官的基拉维说。随着每一次被拒，她感觉LaunchGood都濒临崩溃。“如果你不能接受付款，作为一家公司就无法生存，”她表示。

在市场上几乎没有现成的选项可以提供基拉维和她的共同创办人所寻求的稳定性和可靠性。因此，他们做了越来越多的穆斯林创始人所做的事情：创造自己的解决方案。为减少如果另一家银行决定停止与LaunchGood合作可能对业务产生的影响，基拉维和她的共同创办人克里斯·布劳维特和奥马尔·哈米德决定建立一个银行和支付提供商的网络来合作。他们希望不再有一个单一的失败点能够吞噬他们的业务。当他们被一家银行抛弃时，这种情况在过去几年中不断发生，但他们有几条备份。

现在，距其成立已经超过10年，LaunchGood已成为一个家喻户晓的名字，帮助其大部分穆斯林用户筹集了近7亿美元。虽然该平台也托管个人筹款活动，但LaunchGood更以其强调慈善捐赠而闻名，并使其用户能够在神圣的斋月期间进行每日自动捐赠。对于许穆斯林来说，该网站成为了斋月期间捐款的主要途径，在此期间，许多人每天都会向慈善机构捐款。

该网站的崛起及其面临的挑战并非个例。据研究和咨询公司Dinar Standard的估计，哈拉消费市场（包括哈拉食品、金融服务以及其他针对穆斯林的商品和服务）已达到全球约2万亿美元。然而，2022年由社会政策与理解研究所（ISPU）进行的一项研究发现，穆斯林在美国的银行和其他金融机构中，他们的账户受到关闭、调查或其他挑战的几率高于其他信仰群体，这使得企业有效地剥夺了穆斯林客户的许可。

因此，基拉维在2024年决定辞去LaunchGood首席运营官的日常职责，创办了一家名为PayGood的支付处理公司。她希望为穆斯林企业和慈善机构提供LaunchGood团队曾努力拼凑而成的：一个可靠的、不歧视的支付系统。“有时候感觉这几乎是生存危机。如果你在这个世界上没有金融通道，你能不能生存？”基拉维说。

“当（我的共同创办人和我）在11年前开始时，我们只是想建立一个社区。”基拉维说。“我们从没想过会成为合规方面的专家。有些时候这感觉是如此的生死攸关。”尽管LaunchGood是较早进入基本上未开发的穆斯林友好技术领域的平台，但其姐妹公司也是越来越多的科技和软件公司中的一部分，这些公司旨在为美国、加拿大和英国的穆斯林提供服务。但自10月7日哈马斯袭击后，穆斯林友好企业的定义也开始发生变化。针对穆斯林消费者，某种程度上意味着要与穆斯林社区的伦理和价值观对齐——无论是提供哈拉友好的约会应用、无利息贷款还是端庄的服装。现在，这一新兴的穆斯林友好技术公司群体正在以隐性或显性的方式回应其预期客户日益增长的需求：让他们更容易停止资助以色列对巴勒斯坦人的压迫。

“今年加沙发生的事件触动了一个非常不同的神经，”基拉维说。“巴勒斯坦永远是穆斯林心目中的一个永久议题，但今年的破坏水平和规模是前所未有的。这加速了整体伊斯兰生态体系和经济的形成。人们开始有了新的意识：‘让我确保用我的美元投票。’”

即使尚处于测试阶段的PayGood也已开始作为穆斯林在考虑现有平台是否符合自己价值观时的替代方案，出现了作为主要支付处理商如Stripe和PayPal的替代选择。PayPal在2021年停止了对西岸和加沙的巴勒斯坦人提供服务。穆斯林和支持巴勒斯坦的创始人开始质疑Stripe的领导。在11月底，Stripe首席执行官帕特里克·科利森在特拉维夫发布了一张照片，这一举动被许多人视作对以色列政府及其行动的默许。基拉维说，此后几周，她接到了至少20个创始人的电话，他们正在寻找简单的支付解决方案和替代选择。“这是我看到穆斯林社区最坚决表态的时候。他们在说：‘我想把我的钱投向我希望支持的地方。’”基拉维说。“他们感到道德上很矛盾。人们只想转向一种与他们的价值观相符的产品。”

正式推动停止国际支持以色列占领西岸和对加沙的封锁的运动，称为抵制、撤资、制裁（BDS），已近二十年。然而，在过去一年中，越来越多的穆斯林参与到了按照这一运动花钱的事业中。越来越多的公司开始回应社区对“BDS安全”替代方案的呼声，抵制官方BDS名单上的主要品牌似乎也变得可持续。

为了满足经过深思熟虑的消费需求，阿迪尔·阿布塔拉创办了Boycat。在目前的形式下，该应用允许人们扫描或搜索品牌，以判断这些品牌是否与“支持以色列占领、种族隔离或侵犯巴勒斯坦人权利的活动”有关。阿布塔拉还启动了一个网站，当可能时建议这些品牌的替代品。最终，阿布塔拉希望该应用成为一个主要的“伦理替代品市场”，不仅涉及巴勒斯坦问题，还包括所有人权侵犯问题。“为了赋予某人做出正确决定并用他们的钱包投票的能力，我们需要创建一个类似BDS的工具，不仅告诉你应该避免什么，还告诉你应该花钱去哪里。”阿布塔拉表示。“我们希望使伦理购物变得轻松。”

没有替代方案，摆脱那些从以色列占领获益的公司的困境是困难的。然而，当努林·阿布巴克和雅拉·欧尔法利成立一个名为Makani Homes的房屋互换平台时，她们不到一年前并没有打算打造一个抵制以色列的Airbnb替代品。她们希望给穆斯林提供更多符合社区共享偏好的选项，比如无酒精或无猪肉的住房或配备坐便器的房子。

但当她们在此期间推出该平台时，许多穆斯林希望更多地依赖于Airbnb，此前该公司逆转了对在被占领的西岸地区租赁房屋的禁止。根据国际特赦组织的说法，Airbnb在2020年在西岸有200个房源，该组织呼吁该公司“停止从建立在被占领巴勒斯坦土地上的非法定居点获利”。随着Makani扩展其服务，创始人们变得更加自信，能够将该应用定位为在减少对在西岸运营的公司的资金支持方面的一个途径。该网站迄今为止仅提供了房屋互换的功能，现在刚刚推出了类似于Airbnb的房屋租赁服务。该公司还组织穆斯林友好的体验。

“让人们知道，仅仅因为公司存在且庞大，并不意味着你必须将钱全部交给他们，这就是我们的初衷。”阿布巴克表示。

早在2013年，基拉维和其共同创办的团队在筹集启动Funding时碰到了困难。她表示，当时几乎没人愿意对穆斯林社区下注，更不要说是一个专注于穆斯林的众筹平台。此后几年，投资者越来越认识到投资穆斯林聚焦企业的商业机会。2017年，一个哈拉约会应用Muzz成为首个获得Y Combinator投融资的穆斯林核心应用。哈拉投资平台Wahed已筹集了近9500万美元，目前估值为3亿美元。

这些公司在很多方面为Boycat和Makani等初创企业铺平了道路，使穆斯林运营公司的融资环境大大改善。在过去一年中，越来越多的科技人员开始围绕巴勒斯坦事业更加积极，支持亲巴勒斯坦公司的基础设施开始逐渐形成。保罗·比格尔在其创办的公司董事会被踢出后写下了一篇支持巴勒斯坦的博客，之后创办了一个名为“为巴勒斯坦科技”的孵化器，旨在为以某种方式倡导巴勒斯坦自由的公司提供支持。到11月，该孵化器正在支持包括Boycat在内的36个项目。尽管“为巴勒斯坦科技”在某些方面不同于传统孵化器——例如目前并未提供资金，并且并非所有支持的公司都是初创企业，但其运行团队提供类似初创企业的建议，专注于如何推动支持巴勒斯坦运动的目标，比格尔表示。“我们正在努力解决科技行业的共谋问题，以不同的方式进行。”

公开支持巴勒斯坦仍然带来了挑战。阿布塔拉表示，绝大多数风险投资公司不会考虑投资Boycat，尽管该应用在不到一年内就获得了相当的动量，用户超过了一百万并与官方BDS运动建立了合作关系。

尽管穆斯林运营和亲巴勒斯坦公司面临着持续的挑战，基拉维对面向穆斯林社区的企业依然充满信心。“现在就是大好时机，”她说。“今天开始比我们第一次开始时容易得多。只需小步开始，值得对这个社区进行下注。”

