## 亮点

GenAI工具包使目的明确的首个现成AI代理得以实现  
该工具包使国防团队能够支持国防部（DoD）关键任务决策，具备可解释性、安全性和人机协作架构  
为量身定制的代理AI奠定基础，适用于维持、物流和规划  
为前线的非技术用户提供快速、可解释的洞察

维尔图利克斯（Virtualitics），使命AI公司，今天推出了其为Virtualitics AI平台设计的生成AI工具包，这是一个基础平台，专门用于部署使命特定的AI代理，旨在加速国防维持、物流和准备工作流程中的决策制定。![图片](https://ai-techpark.com/wp-content/uploads/Virtualitic.jpg){ width=60% }

---
这一发布标志着向能够在复杂的国防部（DoD）环境中支持更快、更明智决策的AI代理的重要一步。

“我们不仅仅是在国防环境中投放通用的大型语言模型（LLM），”维尔图利克斯首席营收官Rob Bocek表示。“我们从前线向后工程解决方案，满足操作人员的需求，帮助他们更快、更自信地行动。”

GenAI工具包是Virtualitics AI平台的基础性补充，提供安全、灵活和容器化的大型语言模型（LLM），以及定制功能，包括检索增强生成（RAG）、为开发者准备的接口和Readiness Sigma（RSIG），这是一种全新的领域特定摘要能力。这些工具旨在简化AI部署以满足操作需求，加快洞察生成，同时保持透明性和信任。

## 推出GenAI工具包：构建使命准备的AI代理

与期望用户从头开始构建自己代理的通用AI平台不同，维尔图利克斯以使命背景为起点，围绕特定的准备挑战构建代理，例如零件短缺、维持规划和资源分配。每个准备AI代理旨在减少决策负担，从数据孤岛中提取关键信息，并提供与人类工作流程对齐的可解释输出。

在Virtualitics GenAI工具包中，包含了一些旨在帮助国防开发人员安全、快速和精确的部署智能代理和GenAI能力的工具，包括：

- **检索增强生成（RAG）**：将安全、可信的数据源连接到生成AI模型，以提供有依据的响应。
- **容器化LLMs**：可部署在任何批准的系统上，保护敏感数据并使模型特定于任务。
- **开发者接口**：简化GenAI能力集成到在Virtualitics AI平台上运行的AI应用程序中。
- **Readiness Sigma（RSIG）**：快速汇总大量领域特定报告，以提取关键信息。

“在维尔图利克斯，我们采取了一种根本不同的方法来处理生成AI，从使命出发，而不仅仅是模型，”维尔图利克斯首席科学家Dr. Sagar Indurkhya表示。“我们的生成AI工具包是专门构建的，以确保我们开发的AI代理在操作上是扎实的、安全的和可解释的。我们的目标是赋能开发人员和运营人员，尤其是非技术决策者，拥有清晰、上下文和控制。”

此次发布建立在维尔图利克斯在国防创新中的发展势头上。该公司是去年获得四个Phase III SBIR生产合同的不到1%的公司，通过与美国空军、太空部队及其他政府机构的实际部署验证了其AI平台。像集成准备优化（IRO）这样的应用程序已经支持关键任务的维持和物流。GenAI工具包进一步推进了这一进程，为未来能够通过自然语言与用户互动、理解上下文并精准回应的准备代理奠定了基础。

首批由GenAI工具包驱动的准备AI代理将在今年夏天准备就绪，首个使用案例涉及预测性维护和物资预测。这些代理将在现有IRO应用程序中工作，以简化复杂决策，使AI能够在行动现场发挥作用，而不去消除人类在其中的作用。

维尔图利克斯对代理AI的理念反映了其核心承诺：提供安全、可解释、专为任务设计的操作级AI。

