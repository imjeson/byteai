GPU Droplets支持推理和复杂的AI工作负载  
数字海洋控股公司（NYSE: DOCN），为数字原生企业提供最简单的可扩展云服务，今天宣布NVIDIA RTX 4000 Ada系列、NVIDIA RTX 6000 Ada系列和NVIDIA L40S GPU现在可供客户使用，作为DigitalOcean GPU Droplets。![图片](https://ai-techpark.com/wp-content/uploads/DigitalOceans.jpg){ width=60% }

---
  
这些新推出的GPU Droplets将NVIDIA强大的加速计算平台与最佳的图形和媒体加速带入DigitalOcean的平台，并补充现有的NVIDIA H100 GPU Droplets和H200裸金属GPU。通过在多个价格点上扩大对NVIDIA GPU的访问，DigitalOcean GPU Droplets有助于降低AI采用的门槛，并为下一代AI驱动的应用提供支撑——从生成式AI、大型语言模型（LLM）推理、小型模型训练和微调，到3D图形、渲染和视频应用。  
“凭借更广泛的由NVIDIA加速的GPU Droplets产品组合，DigitalOcean继续简化数字原生企业的云计算和AI，”DigitalOcean首席产品和技术官Bratin Saha表示。“这些新的GPU Droplets为客户提供了更广泛的可负担得起的GPU，以满足各种AI工作负载的需求。”  
访问DigitalOcean由NVIDIA加速的GPU Droplets带来几个关键优势，包括具有竞争力的定价、简单的设置过程以及企业级服务水平协议（SLA）。与其他云提供商需要多步骤和技术知识才能配置安全、存储和网络要求不同，DigitalOcean的GPU Droplets可以在一个页面上通过几次点击轻松设置。  
此外，使用GPU Droplets的客户可以访问整个DigitalOcean产品组合，包括GenAI平台和Kubernetes服务。  
这些新推出的机器的主要特点包括：  

- NVIDIA RTX 4000 Ada系列GPU是一款强大的单槽GPU。关键用例包括内容创作、3D建模、渲染、视频及推理工作流，具有卓越的性能和效率。  
- NVIDIA RTX 6000 Ada系列GPU基于NVIDIA Ada Lovelace GPU架构。RTX 6000 Ada系列结合了第三代光追核心、第四代张量核心以及Ada世代CUDA核心，并配备48GB显存。可用案例包括渲染、虚拟工作站、AI、图形和计算性能。  
- NVIDIA L40S GPU配备多达八个L40S张量核心GPU，每个GPU有48GB的显存，具备第四代NVIDIA张量核心、第三代NVIDIA光追核心及DLSS 3.0技术。可用案例包括图形、渲染和视频流。  

“NVIDIA最新的GPU技术正在帮助企业加速他们的AI项目，从试点到生产，”NVIDIA加速计算产品总监Dave Salvator表示。“DigitalOcean的简单而可扩展的云平台使得在NVIDIA技术上部署高级AI工作负载变得更加容易，因此组织能够更快速、更便捷地构建、扩展和部署AI解决方案。”  
这些新的GPU Droplets目前在DigitalOcean的多伦多数据中心提供。  
