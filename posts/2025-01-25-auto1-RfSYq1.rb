title: '保罗·麦卡特尼表示，关于人工智能的法律变更可能会“剥削”艺术家'
date: 2025-01-26
author: ByteAILab

---

保罗·麦卡特尼爵士警告称，如果拟议的版权法改革继续推进，人工智能可能会“剥削”艺术家。

---
他在接受BBC采访时表示，这些提案可能会削弱作家和艺术家的动力，从而导致“创造力的损失”。最近，英国零售业正在拥抱自动化，以降低人员成本。

政府最近发起了一项关于使用受版权保护的材料来训练人工智能模型的咨询。作为披头士乐队剩余的两位成员之一，麦卡特尼说道：“有很多年轻人，他们写出了美妙的歌曲，但他们并不拥有这些歌曲，也与这些歌曲没有任何关系。任何人都可以轻易地剽窃它。”

“事实是，钱是流向某个地方……有人在获得报酬，那么为什么不应该是那个坐下来创作《昨日》的人呢？”

关于是否正确和公平地使用版权材料来训练最新的AI工具模型的争论正在全球范围内展开，创意产业的公司和个人就他们所认为的未授权使用其材料发起了法律诉讼。与此相反，一些出版机构和媒体公司则与人工智能公司签署了许可协议，以允许他们的材料用于训练这些模型。

这并不是麦卡特尼第一次表达对人工智能可能对艺术构成威胁的担忧。在2023年12月，他曾警告称，人工智能可能“只会接管”，并与朱莉安·摩尔、斯蒂芬·弗莱和休·博内维尔等演员签署了一份请愿书，声称“未经授权使用创意作品来训练生成性AI对这些作品背后的人的生计构成了重大且不公正的威胁，绝不应被允许”。

小说家凯特·莫斯也支持一项并行活动，旨在修订数据法案，以便能够执行英国现有的版权法，这意味着创作者可以在授权使用其材料时进行公平的报酬谈判。

政府表示，将利用这项持续至2月25日的咨询来探讨辩论的关键点，包括如何提高创意和人工智能领域之间的信任，以及创作者如何授权并获得使用其材料的报酬。

麦卡特尼呼吁政府重新考虑这些计划。“我们是人民，你们是政府。你们应该保护我们。这是你们的责任，”他说。

“所以，如果你要通过一个法案，请确保保护有创造力的思想者和艺术家，否则你将无法拥有他们。”

在2023年11月，麦卡特尼和林戈·斯塔尔利用人工智能技术创建了《现在和然后》这首歌，成功将约翰·列侬1977年录制的家庭演示中的人声单独提取出来。

文化部长丽莎·南迪在去年表示：“本政府坚信我们的音乐家、作家、艺术家和其他创意工作者应该能够了解和控制他们的内容如何被人工智能公司使用，并能够寻求许可协议和公平的报酬。”

确保这一点并实现法律确定性，将有助于我们的创意和人工智能部门共同发展与创新。

