首个原生集成将数据+AI可观测性引入Salesforce CRM和数据云，使客户能够构建信任的AI代理，通过Agentforce
Monte Carlo作为数据+AI可观测性领域的领导者，今天推出了其新的Salesforce原生集成功能，帮助团队确保关键业务工作流程和AI应用程序所需数据的可信度。![图片](https://ai-techpark.com/wp-content/uploads/Monte-Carlo.jpg){ width=60% }

---

随着这一发布，Monte Carlo成为首个提供Salesforce CRM和Salesforce数据云的端到端监控的数据+AI可观测性平台，这两个系统是企业中最关键和数据丰富的系统。 Monte Carlo与Salesforce数据云的集成为Agentforce（Salesforce的AI代理平台）提供AI就绪数据，使用户能够构建、定制和部署自主AI代理，以支持员工和客户。
早期访问用户已经识别出这一新集成的一些业务关键用例：
“Monte Carlo的Salesforce集成将为我们CRM数据带来全新的信心。我们很高兴能够在问题出现之前（例如缺失字段或同步中断）捕捉到它们，以避免它们影响Snowflake或报告。此外，在我们逐步淘汰公式字段的过程中，我们将使用这一功能来确保所有数据管道的可靠性。我们能够在坏数据影响我们的市场推广运营之前阻止它，提高高管对我们洞察的信任。最重要的是，它将节省我们的运营团队数小时，并促进数据质量作为每个人的责任。” — Pedro Sá Martins，Outsystems工程主管
继续阅读，了解Monte Carlo的新Salesforce原生集成能做些什么，以及如何利用它来从Salesforce数据中释放更多价值。
为Salesforce数据带来信任
Salesforce数据驱动着市场推广团队的业务关键决策。然而，数据质量问题，如重复、空值和无效格式，通常在为时已晚之前不会被检测到——这会导致收入损失、预算浪费和客户信任下降。
通过Monte Carlo的新Salesforce集成，团队现在可以直接在Salesforce环境中监控CRM和数据云数据的可靠性，使数据团队和业务用户都能轻松发现、分类和解决数据问题，而无需等待数据进入数据仓库。
该集成为现代企业提供了强大的好处：

数据、销售和收入运营团队可以快速协作，跟踪并解决问题，确保不影响下游系统
市场推广团队可以验证其360度客户概况，减少在错误活动上浪费的支出
AI团队可以创建强大的应用程序，确保所用的底层数据可靠
业务用户可以利用Agentforce使用同样值得信赖的数据来自动化日常工作流程

“Salesforce是企业中最关键的系统之一，但考虑到它在组织中涉及的个体数量，就不会惊讶于它受到坏数据的影响，”Monte Carlo联合创始人兼CTO Lior Gavish表示。“通过这一集成，我们正在帮助客户确保这一关键信息源的准确性，因为潜在问题往往是最具破坏性和最难以检测的。”
原生、可扩展且易于使用
