SEO的逻辑终点：谷歌搜索结果中驱逐的幻影
有关大规模移民逮捕的消息近期在美国蔓延，但仔细观察这些移民局的报告却呈现出不同的故事。插图：Angelica Alzona/Guardian Design；照片来源：Kevin Mohatt via Reuters

有多种方式可以操控谷歌搜索，以提升网站在结果页面中的排名，互联网上最有价值的地块。事实上，这个领域为此而建立，称为搜索引擎优化（SEO）。谷歌的算法会根据网页上的不同因素来判断其相关性和权威性。政府网站域名凭借其权威性已经获得了额外的加分。

SEO策略已经被美国每个中等水平的食谱博主掌握，为什么政府不该了解呢？美国移民和海关执法局（ICE）在1月24日更新了数千条新闻稿，使旧新闻稿在谷歌搜索中似乎焕然一新，令一名长期从事移民执法的律师对执法行动的搜索困惑不已。

我的同事达拉·凯尔报道：
有关大规模移民逮捕的消息在美国蔓延，报道显示ICE的特工在社区内展开了行动，四处抓捕。快速的谷歌搜索ICE的行动、突袭和逮捕返回了大量政府新闻稿。标题包括《ICE在为期四天的科罗拉多行动中逮捕85人》、《新奥尔良针对123名犯罪非公民开展定向行动》和在威斯康星州逮捕83名犯罪外籍人士。

但仔细看这些ICE报告却讲述了不同的故事。
科罗拉多的那场为期四天的行动？发生在2010年11月。新奥尔良针对的123人？是去年的2月。威斯康星州呢？是2018年9月。全美各州有成千上万的例子——ICE新闻稿飞速升至谷歌搜索结果的第一页面，似乎最近才发生的执法行动，实际上却是几个月或几年前的事。其中一些，比如“在内布拉斯加州逮捕的44名逃犯”，早可以追溯到2008年。

所有那些飞速升至谷歌搜索结果顶部的ICE新闻稿都标记了相同的时间戳，并写着：“更新：01/24/2025”。

了解Spotify在 AI 功能方面的壮大以及人性化推荐的做法
全屏查看照片：Joan Cros/NurPhoto/Rex/Shutterstock
比约克最近称Spotify“可能是对音乐人造成的最糟糕的事情。流媒体文化改变了整个社会和一整代艺术家。”

如何应对如此不利的宣传，作为实验音乐的领军人物抨击你的公司？然而，Spotify早已做好准备。公司在发布上周首个全年盈利的阳光明媚的财报之前，已启动了攻势。首席执行官丹尼尔·埃克表示，他看到了AI发展带来的“音乐发现方式进行变革的潜力”。

在1月中旬，《华尔街日报》发表了对Spotify独特编辑部负责人的个人专访，标题为《决定新艺术家命运的播放列表权力经纪人：Spotify以算法推荐著称，但苏莉娜·翁为寻找新热门歌曲带来人性化的触感》。翁告诉该报，她每天花三个小时躺在地板上听新歌。多么异想天开，多么人性化！她将自己塑造成机械巨兽中的反安卓灵魂：“AI机器学习在解析大数据集方面非常出色，但当没有数据时，例如对于新发布的歌曲、艺人，它该如何处理呢？”

我怀疑翁能注入多少人性。Spotify的播放列表确实是巨大的 tastemakers，但服务6.4亿用户显然不可能依靠手心的热忱来精心策划，加上应用程序逐渐减少对人性化的重视。它根据你的音乐偏好和你搜索的活动（例如“跑步混音”、“每日行驶”或“愉快清扫混音”）生成播放列表，且不再需要人类的参与。该流媒体平台在2023年12月裁员约1700名员工，其中包括许多人类播放列表制作人员。四个月后，它推出了AI播放列表生成器，用户可以根据文本提示填充播放列表，这一功能可能是对这些策展人的工作成果进行训练的。

Spotify在《华尔街日报》中将翁塑造得像国际象棋棋盘上的主教。任何一位当前科技公司员工的采访均是为特定目标而进行的。像翁这样高级的执行人员不会在没有十几位公关专业人士和Spotify高管的批准下接受记者采访。在这些坐下来之前，她并不负责拍照，尽管或许她能在拍照时倾听一小时的新歌。

那么Spotify唱的是什么曲调呢？该公司强调其推荐服务的人性化，然而其财务与产品决策则在高喊AI的口号。公众对艺术的AI干预并不买账；数以百计的音乐和其他领域艺术家反对生成式AI，控诉其开发者窃取他们的作品。作为一家科技公司，Spotify希望AI能够在数亿用户的规模上优化其推荐服务。这才是其资金去向；这也是工程师们的工作目标。作为一家音乐公司，它则需要人性化的假象。苏莉娜·翁，于是顺理成章地成为了公司的人性化象征。

我预计更多公司将遵循相同的公关操作手册，在大力投资AI的同时，强调人类所进行的小调整。即使是比约克本人也没有免疫力。在一场主要关于在Apple TV+上推广她的新演唱会电影的采访中，她对Spotify进行了抨击。

