冲击整个局面：特朗普有行政命令；马斯克有Doge
唐纳德·特朗普自他担任总统以来已发布创纪录的行政命令：结束出生公民权、禁止19岁以下的人进行性别转变、为1月6日袭击事件的暴徒赦免等。世界首富马斯克掌管着所谓的“政府效率部”，对一系列联邦机构进行了冲击，声称其目的是“削减浪费、欺诈和滥用”。涉及的机构包括消费者金融保护局、教育部、劳动部，以及更为严厉的美国国际开发署（USAid）。特朗普和马斯克正竭尽所能地“冲击整个局面”——这是一种前特朗普政府策略师史蒂夫·班农所倡导的战术，旨在故意压倒对手和媒体。班农说得没错，想要跟上节奏确实很困难。

特朗普和马斯克都擅长边缘政策。他们通过将对手逼到互相毁灭的边缘，强迫他们接受所要求的条件，然后再后退。特朗普曾宣布将通过行政命令对所有自墨西哥和加拿大进口的商品征收25%的关税，这对这两个最大贸易伙伴的经济构成巨大威胁，可能会将这三国的经济推入衰退之中。然而，在加拿大和墨西哥各自同意向美国边境增派10,000名军队后，特朗普推迟了关税一个月。

马斯克宣称要解散USAid。他可能会——该机构的名字已经从门上被移除——但更可能的结果是该机构的缩减版将被并入国务院。国务卿马尔科·鲁比奥已自封为该机构的负责人，工作人员被锁在门外。马斯克的足迹在一封发给200万名美国政府员工的“分叉邮件”中可见一斑。他们会全体辞职，导致政府瘫痪吗？这几乎不可能。部分收购员工队伍是否达到裁员的同样效果？是的。

马斯克曾多次使用这一玩法，尤其是在X（前Twitter）身上。他快速撤回办公室租约，导致公司未能支付仍欠的租金。他解雇了约四分之三的工作人员，并将遣散费的问题留给法院处理，在一些前员工起诉后。马斯克在谈判赔偿时似乎注定能支付更少。然而，这种激进的手段并不总是奏效。关于X，马斯克常常发推文指责社交网络，让自己获得关注，威胁要收购，签订了440亿美元的收购合同，后来却尽力想退出协议。但他最终被迫完成收购，并在最近的采访中承认X的估值过高。当一位法官撤销他在特斯拉的560亿美元薪酬时，他曾表示会考虑无条件离开公司，而法官对此毫不在意，第二次裁定他的薪酬过高。马斯克既没有离开特斯拉，也没有现行的薪酬计划。

硅谷软件公司喜欢说自己快速行动并打破常规，在创新的名义下迭代产品。我认为马斯克的做法是快速推进到可能破坏一切的地步。这似乎是特朗普最喜欢的事之一。总统本人并没有太大不同。还记得美国差点与伊朗开战吗？他在飞机起飞时叫停了空袭。

马斯克的下一个目标似乎是医疗保险和医疗补助。那会破坏些什么，又会伤害谁？

