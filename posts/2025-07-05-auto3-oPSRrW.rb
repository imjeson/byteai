犹他商业杂志表彰Willis在人工智能和设计创新方面的变革性领导力
今天，Domo（纳斯达克：DOMO）宣布，Domo的首席设计官Chris Willis获得犹他商业杂志颁发的卓越执行奖。![图片](https://ai-techpark.com/wp-content/uploads/Domos.jpg){ width=60% }

---
该年度奖项旨在表彰犹他州商业和科技生态系统中的杰出领导者。
“在Domo近十五年的工作中，我有幸参与构建第一款云原生、移动优先的数据平台，”Willis表示。“每天驱动我的动机是我们的使命，即为每个人——从普通从业者到首席执行官——实现数据和人工智能的民主化。通过关注情感设计原则，我们不仅仅是在使数据可访问；我们是在为解决真正的人类问题提供真正有用的工具。”
Willis在推动Domo的“情感设计”方法方面发挥了重要作用，该方法专注于全面理解个别客户的需求，而不是创建一刀切的解决方案。在他的领导下，Domo的设计团队通过开发AI驱动的工具，改变了组织使用数据的方式，显著加速了客户分析和应用数据的能力。
“Chris是Domo不可或缺的资产，”Domo的首席技术官兼产品执行副总裁Daren Thayne说道。“设计是商业中不可分割的一部分——它改变了用户与复杂技术的互动方式。Chris能够将复杂的技术理念转化为易于理解的内容，并通过强有力的领导、指导和协作，对我们在Domo的演变和增长产生了深远影响。”
Willis对可访问性的承诺尤为重要，最近推出的Domo AI Chat功能，使用户能够用自然语言与数据库进行交流，使该平台无论技术背景或工作环境如何，都能轻松访问。
Chris的影响力远不仅限于产品设计。他的团队最近开发了一个AI框架，允许客户使用几乎任何大型语言模型与Domo平台结合，为他们构建和扩展AI工具提供了前所未有的灵活性。
