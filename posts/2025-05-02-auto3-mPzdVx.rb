date: 2025-05-03
author: ByteAILab

---

新功能支持agentic AI、对话查询和实时RAG；BYOK使安全可扩展分析适用于成本和合规敏感的环境
StarTree，云端实时分析公司，今天宣布其企业工作负载的实时数据平台新增两个强大的AI原生创新：模型上下文协议（MCP）支持和向量嵌入模型托管。![图片](https://ai-techpark.com/wp-content/uploads/StarTree.jpg){ width=60% }

---
这些功能使StarTree能够为以代理为中心的应用程序、实时检索增强生成（RAG）和对话查询提供满足企业AI系统所需的速度、时效性和规模的支持。

“下一波AI创新将由实时上下文驱动——理解当前发生的事情，”StarTree的联合创始人兼首席执行官Kishore Gopalakrishna说。“StarTree作为实时分析基础的传统与AI的发展完美互补，通过提供规模化的新鲜洞察推动AI的未来。正在发生的变化是应用程序消费者的转变为自主代理。”

AI的强大程度取决于其背后的信息架构。就像云计算迫使企业数据系统进行了根本的重新设计一样，AI也正在引发同样深刻的变革。随着代理系统的出现，传统的数据架构——为接受慢查询和陈旧数据的内部用户设计——已经无法跟上。代理AI需要亚秒级的查询速度、实时的上下文意识以及支持群体自主代理并行工作的能力。这标志着数据平台角色的根本转变——从静态存储转向动态引擎，以帮助代理完成任务。

StarTree一直在这方面做好承诺，在最新的数据上提供每秒数百万次低延迟查询。但为了扩展这一基础并充分释放下一代AI原生应用的潜力，需推出新功能。即将推出的新功能包括：

- 模型上下文协议（MCP）支持：MCP是一个标准化的方式，使AI应用能够连接和与外部数据源及工具交互。它允许大型语言模型（LLM）访问StarTree中的实时洞察，以便采取超出其内置知识的行动。可用性：2025年6月
- 向量自动嵌入：简化并加速实时RAG用例中向量嵌入的生成和摄取，基于Amazon Bedrock。可用性：2025年秋季

StarTree平台现在支持：

- 以代理为中心的应用程序——通过支持新兴的模型上下文协议（MCP），StarTree允许AI代理动态分析实时结构化企业数据。借助StarTree的高并发架构，企业可以支持数百万个自主代理实时做出微观决策——无论是优化配送路线、调整定价还是防止服务中断。
- 对话查询——MCP简化和标准化了LLM与数据库之间的集成，使自然语言到SQL（NL2SQL）部署变得容易且更稳定。企业现在可以让用户通过语音或文本提出问题并即时获得答案——例如，一个打车司机问，“我今天赚了多少钱？”接着是“这个月呢？”以及“我在哪些时间和地点赚得最多？”每个问题都建立在前一个问题的基础上。这种无缝的对话流需要的不仅是语言理解，还有一个能够实时提供上下文响应的数据平台。
- 实时RAG——StarTree的新向量自动嵌入使可插拔的向量嵌入模型能够简化源到嵌入创建到摄取的数据流。这简化了检索增强生成管道的部署，使构建和扩展AI驱动的用例（如金融市场监测和系统可观察性）变得更加容易，而无需复杂的拼接工作流程。

StarTree通过自带Kubernetes（BYOK）扩展部署灵活性
StarTree还宣布了自带Kubernetes（BYOK）的全面可用性，这是一种新的部署选项，使组织能够在自己的Kubernetes环境中全面控制StarTree的高性能分析基础设施，无论在云端、本地还是混合架构中。
通过BYOK，企业可以保持对其基础设施的全面治理和控制，同时仍然利用StarTree的实时性能和易用性。该模型非常适合金融服务和医疗保健等受监管行业，因为严格的数据驻留、合规性和安全政策通常禁止使用传统SaaS模型。此外，它还为工作负载稳定、可预测的组织提供了一种成本高效的解决方案，能够节省计算和出口费用。

“实时洞察不再是可选的，但企业往往受限于基础设施的约束，”Gopalakrishna说。“通过BYOK，我们消除了这些障碍。公司现在可以在任何需要的地方部署StarTree，而不影响性能、安全性或成本控制。”
BYOK加入了StarTree现有的部署选项，包括完全托管的SaaS和自带云（BYOC），为客户提供根据其操作和监管要求选择最佳模型的灵活性。可用性：目前正在私有预览中

实时分析峰会2025：即将于5月14日举办
StarTree将在2025年5月14日举行的实时分析峰会上展示许多新创新，这是一个虚拟活动。活动将邀请Uber、Netflix、AWS等的演讲者，探索AI驱动的分析、数据基础设施和各行业新兴用例的未来。与会者将获得有关实时分析如何推动各行业数字化转型的重要见解，涵盖金融、电子商务、游戏、网络安全等多个领域。

