title: 'Solidus AI Tech与Secret Network合作部署机密AI工具'
date: 2025-07-04
author: ByteAILab

---

Solidus AI Tech与Secret Network已开始技术和战略合作，将机密计算能力引入Solidus AI Tech的去中心化AI平台。![图片](https://ai-techpark.com/wp-content/uploads/Solidus-AI.jpg){ width=60% }

---
此次合作的重点是使开发人员能够利用Secret Network的安全基础设施构建和部署以隐私为保护的AI模型和工具。

作为第一步，AI Tech已将Secret Network的隐私保护大语言模型SecretAI - DeepSeek R1集成到Solidus AI Tech的基础AI模型库中。该模型旨在支持机密推理，确保用户输入对基础设施提供商和模型托管方不可访问。Solidus AI Tech已同意与其开发团队评估技术集成。

两队目前正在探索将SecretVM集成到Solidus AI Tech平台作为专用AI应用程序。这将使开发人员能够在机密和可验证的环境中部署代理，利用受信执行环境（TEEs）在AI应用程序的整个生命周期中保护敏感数据，并利用加密可验证性来代码任何代理运行的代码。

“此次合作反映了我们对构建安全和去中心化AI基础设施的共同承诺，”Solidus AI Tech的John Mendez表示。“机密计算是开发人员和用户都重要的一层，我们很高兴与Secret Network合作，为人们提供所需的工具。”

Secret Network Foundation的执行董事Lisa Loud补充道：“我们视此次合作为推进AI中隐私和信任的重要一步。通过与Solidus AI Tech合作，我们正在使机密计算更易于开发人员访问，从而构建下一代智能系统。”

进一步讨论正在进行中，探讨在Solidus AI Tech的代理Forge界面中探索机密部署选项。此功能将允许用户在代理创建时选择SecretVM作为其执行环境。两个团队也一致同意创建一个基于知识的代理，训练于Secret Network文档中，以帮助开发人员，并承诺在集成进展中共同进行市场营销和宣传。

此次合作基于对伦理AI部署、数据保护和透明基础设施的共同愿景。两队已同意继续进行开发规划、沙箱测试和后续会议的安排。

