在今年，家长对智能手机的担忧引起了巨大反响。那些没有智能手机的孩子是感觉被剥夺了吗？还是更有生活情趣呢？

无论如何，智能手机已经完全占据了我们自己和孩子的生活。但不可避免的反弹正全面展开。这不仅仅是家庭围绕屏幕时间限制发生争执，或者父母徒劳地试图减少对成人、极端化或消费主义内容的暴露。随着对手机具有成瘾性、干扰儿童学习、创造力和注意力的认识日益增强，以及超过97%的12岁孩子拥有智能手机，学校已经开始采取行动。今年2月，英国政府发布了关于智能手机的指导意见，一些学校自那以后已经禁止了智能手机的使用。同样在2月，两位担忧的家长创建了WhatsApp群组“无智能手机童年”。根据其联合创始人戴西·格林维尔(Daisy Greenwell)的说法，这个在线社区现在已经拥有超过12万名成员，“在英国每个郡都有一个当地群组，这些群组中还有成千上万的学校群”。

那么，对于这领先于潮流的没有智能手机的少数孩子来说，他们是如何看待这一切的呢？

安妮贝尔，13岁
我是我所知道的唯一一个没有智能手机的人。我知道它们相当容易让人上瘾。我有一些朋友在Snapchat上花费很多时间 - 其中一个朋友在她的手机上设定了为期三小时的屏幕时间限制，并且将整个时间都用于Snapchat上。我们一起时，一些其他朋友会在TikTok或YouTube上滚动，我感觉被排斥了。有时我的朋友与他们没有遇见过的来自另一所学校的人交流，所以我认为社交媒体是一个很好的找人的方法。但它也有负面影响，因为你可能会和你不想交谈的人接触，尽管我不知道有任何人在网上被欺凌过。如果有一个他们不认识的人，他们通常会把对方拉黑。

其中一个积极的地方是，我与真实世界中的朋友更加联系紧密，并且我可以在我妈妈的手机上加入一些WhatsApp群组，这有助于我与班级群聊连接。我觉得拥有智能手机会让我从其他事情分心。在社交媒体上，每个人都在说：“你需要买这款美颊腮红或那款保湿霜。”还有：“哦，我买了这个是因为TikTok上有人说它很好。”然后下周，每个人都在说：“哦，你买了这款保湿霜，那太老了。”如果看到你的朋友都知道所有新潮流，那表明他们对此相当上瘾。今年夏天，我花了很多时间学吉他和唱歌，如果我有智能手机，我想我是不会这么做的。

在学校，我们有Yondr计划，你必须将手机锁在一个袋子里。如果被抓到手机在外面，会被处以关押。有些人是如此上瘾，以至于他们无法将其锁在外面五个小时。但很多人不会让他们的手机随身，你可以真正和他们交谈。

亨利，11岁
我11岁时得到了一个智能手机，然后我妈妈拿走了，因为她说我看手机时的态度变得很糟糕，这导致了争吵。她让我停下来，但我不会，因为我想要完成游戏的某个关卡或和朋友聊天。

