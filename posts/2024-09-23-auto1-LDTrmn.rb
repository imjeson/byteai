我是我所有朋友中唯一没有智能手机的人。当我说我不想要一个时，人们总是感到相当困惑。一些成年人说：“那很好”，但不是我的朋友和同龄人。我很擅长避免被欺负，所以我没有因此而被人欺负过。

我读书很多。我可能是年级中阅读最为先进的人之一。我不知道这跟这有什么关系，但也许我在晚上读书时，其他人都在看手机。

迈克，11岁
我没有智能手机，因为我父母认为存在太多危险。我不知道有什么危险；他们不告诉我。我认为他们至少应该告诉我危险是什么。

没有智能手机，我将错过发送朋友生活中改变性体验的机会，以及能够通过视频通话看到某人真实的生活。我的朋友现在仍然给我打电话，但他们现在更多地在WhatsApp上与其他人交谈。

我不认为我的大多数朋友对他们的手机上瘾。我认为他们只是恰好到了应该拥有手机的年龄。这就是我感到相当恼火的原因。我毫不怀疑，在六年级结束和七年级开始之间，正是拥有手机的好时机。我认为让一名孩子在六年级之后拥有手机并不危险。我们很成熟 - 你在八年级开始挑选自己的GCSE课程。

一开始我接受了没有手机的事实，但然后许多我的朋友都有了智能手机。我感到非常被排斥。害怕错过 - 这是我对此感到烦恼的主要原因。

