手机上的游戏很有趣，但主要是在假期无法与朋友联系。我完全不能和朋友说话。我妈妈说她会给我一部诺基亚手机，但我们还没有得到。

在我不拥有手机时更友好我部分同意我妈妈的看法。没有它，你就根本不会想到它。我有两个更小的兄弟，经常有一个会悄悄拿走我的手机搜索视频和玩我的游戏。这会在我们之间引起争吵。

我不想要一个非常简单的手机的唯一原因是因为我经常使用WhatsApp，而我的所有朋友都在用。我被邀请加入了几个大的WhatsApp群，但因为我不能经常使用手机，我总是最终删除它们因为我有太多消息。

如果我妈妈给我一部诺基亚，我想我会使用它，因为经常当我从学校走回家时，我到家时我妈妈和爸爸不在，如果我无法与他们联系，那真的很麻烦。我唯一想对他们说的是：如果你想限制我，让我难以绕过它。我通过要求他们输入密码然后偷偷看他们的方式得到了密码。

杰克，13岁
我不想要一个智能手机。从我看到的我的父母，它似乎就是很烦人；不断发出响声。我手机的唯一用途是联系父母和朋友，所以我有一部旧诺基亚手机。

有时我确实感觉缺少WhatsApp和Snapchat而被排斥，但看到实际上发生的事情，仅仅是感到烦人，什么有趣的事也没有发生。他们只是相互之间发送一些随机的东西。

我有一种与其他人不同的幽默感；在社交媒体上的一些东西，让我完全惊讶于它是如何滑稽的。我认为一些人真的会觉得很有趣，但我肯定可以挑出那些只是随波逐流的人。

