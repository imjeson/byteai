惠普企业公司（NYSE: HPE）今天宣布，行业最先进的私有云产品组合实现了一次代际飞跃，通过近十年的领导地位和HPE Morpheus软件重新定义私有云给客户带来的价值。HPE正在改变企业现代化混合IT的方式，以提高成本效率、性能和简便性，并宣布以下内容：

HPE Private Cloud Business Edition 现已提供HPE Morpheus VM Essentials，能够减少多达90%的虚拟机许可证费用，并支持多种超管理程序和自助云消费。这些新节省使得该产品组合的总拥有成本（TCO）预计将降低2.5倍，基于行业领先的分离式超融合基础设施（dHCI）。

HPE Morpheus软件系列现已普遍可用，包括支持虚拟化工作负载的HPE Morpheus VM Essentials和用于统一云管理的HPE Morpheus Enterprise Software。两个软件产品都包括HPE的HVM超管理程序，并按插槽许可以降低TCO。VM Essentials客户将能够升级到完整的HPE Morpheus Enterprise。

新服务简化了各类客户的虚拟化现代化。

“企业在IT现代化的关键时刻，必须应对不断增长的管理复杂性和不断上升的虚拟化成本，从而为核心增长领域释放投资。我们是分离式基础设施的领导者，而我们的私有云将这一领导地位与新软件结合，提供统一的虚拟化和云管理。HPE正在为客户提供选择、简便性和成本效率，使其超越竞争对手并重新投资于创新，”HPE混合云执行副总裁兼总经理兼首席技术官Fidelma Russo表示。

HPE Private Cloud Business Edition通过HPE Morpheus VM Essentials提供了根本性的节省和简便性  
虽然许多IT基础设施提供商现在认识到分离式基础设施的成本优势，HPE通过HPE Private Cloud Business Edition结合HPE Morpheus VM Essentials重新定义了全栈私有云。Business Edition可以作为dHCI或HCI提供，使组织能够从边缘到数据中心选择优化的虚拟机架构。VM Essentials管理HPE虚拟机和传统虚拟机，允许客户根据需要自定义其虚拟机。

经过验证的企业级虚拟化解决方案：VM Essentials旨在支持全球部署，并配备了从基础设施到工作负载的全面企业服务支持。  
AI驱动的经验从设置到运营：HPE自动化了Business Edition的基础设施设置和生命周期管理，AIOps预测并预防高达86%的问题，释放了IT资源。

“Aeropuertos Argentina的企业技术和运营IT经理Roberto Valenta表示，‘HPE Private Cloud Business Edition已通过我们在遗留支持合同、服务即消费、数据中心空间和能源效率上的节省来偿还投资。其性能、可扩展性和可靠性是我们关键任务工作负载的基础，从航班跟踪到机场到达和出发。Business Edition驱动我们提供更快、更顺畅的服务愿景，减少乘客的等待、登记和安检时间。’”  

Commvault，网络弹性和数据保护解决方案的领导者，将成为第一个支持图像基础虚拟机备份和恢复的VM Essentials生态合作伙伴，预计在5月发布更新。

HPE Morpheus软件统一云管理并降低成本  
新的HPE Morpheus Enterprise Software统一云管理大企业和服务提供商。从一个简单的界面，组织可以管理和治理虚拟化、基于容器和第三方运行时的裸金属、内部和公共云基础设施。HPE Morpheus Enterprise自动化应用程序提供，能够快速将应用程序部署到几乎任何超管理程序或云，并通过分析驱动的右尺寸功能降低高达30%的云成本。

Morpheus Enterprise和VM Essentials是硬件无关的独立软件，并已新近验证可在Dell PowerEdge服务器和NetApp AFF阵列上运行，除了HPE ProLiant Compute Gen11和Gen12服务器之外。新的HPE ProLiant Gen12服务器使客户可以在虚拟化软件上节省多达27%，每年的电力消耗降低65%，以及7:1的减少数据中心占地面积。

VM Essentials与HPE Aruba Networking CX 10000的结合使得TCO降低多达48%，性能提高多达10倍，并提供微分段、DPU（数据处理单元）加速和增强安全性。VM Essentials还为HPE Alletra Storage MP B10000提供简单、细粒度的存储管理。

HPE通过企业级服务简化虚拟化转型和现代化  
新的HPE云平台服务 – 虚拟化现代化简化了虚拟化现代化。HPE服务提供专门的评估、端到端的强大着陆区流程，以及用于重新平台化工作负载、现代化基础设施、数据迁移、团队成员培训和基础设施管理的服务，以降低开销。HPE最近被评选为IDC市场格局：2025年全球混合IT基础设施咨询和集成服务领导者。

可用性

HPE Private Cloud Business Edition与HPE Morpheus VM Essentials现已可用。新Business Edition系统与HPE SimpliVity将在2025年第三季度推出。  
HPE Morpheus软件集成HPE Alletra Storage MP B10000现已可用，预计将在6月推出HPE Aruba Networking CX 10000。  
HPE Morpheus Enterprise Software现已作为独立软件提供。

