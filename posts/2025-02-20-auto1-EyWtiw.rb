title: '马克·扎克伯格的慈善机构在向员工保证将继续开展后减弱了多样性、公平和包容性工作'
date: 2025-02-21
author: ByteAILab

---

创始人马克·扎克伯格和妻子普莉希拉·陈的盈利性慈善组织在企业多样性方面做出了重大回撤。

---
根据《卫报》获悉的内部电子邮件和其他通信，陈扎克伯格倡议（CZI）在周二晚间通知员工，该组织将实际上结束对内部和外部多样性、公平与包容性（DEI）的努力。1月10日，CZI的领导层曾向员工保证，其对DEI的长期支持没有变化。扎克伯格的公司Meta当天早些时候宣布将终止其DEI项目，这一消息正值唐纳德·特朗普第二次就职前的几天。

CZI的首席运营官马克·马兰德罗在给所有员工的电子邮件中写道，他一直在审查该组织的项目，以确保其与我们的科学慈善事业的重点以及当前的法律和政策环境保持一致。他写道：“鉴于变化的监管和法律环境，我们将不再在CZI设立多样性、公平、包容性和无障碍团队。”

该组织还撤回了其多样化候选人计划，该计划旨在确保所有空缺职位都能面试来自多样化背景的合格候选人。Meta上个月也取消了类似的规定。CZI的数十亿美元资金来自扎克伯格和陈夫妇的巨额个人财富，这部分财富来自扎克伯格作为Facebook创始人20多年来创办的社交媒体公司Meta，Meta旗下拥有Facebook、Instagram和WhatsApp。

当Meta在今年早些时候决定结束自身的DEI努力时，CZI员工对该组织可能会跟随其步伐感到担忧，因为该公司与慈善机构往往会实施类似的企业政策。作为Meta的首席执行官，扎克伯格最近几个月明确表示，他希望与特朗普的新政府保持良好关系，而特朗普政府已结束联邦招聘中的多样性倡议，并向企业施压以同样行事。

尽管CZI的首席人力资源执行官上个月告诉员工：“Meta对DEI努力的变化不会影响我们”，员工们依然对组织是否将坚守其承诺保持警惕。CZI历史上曾多次随着Meta的领导进行运营变更，从裁员到最近的返岗命令不一而足。CZI的一位发言人拒绝对此评论。

此外，马兰德罗写道，CZI已经结束了所有“社会倡导资金”，包括关注移民改革和种族公平的工作及资助。“我们将仍然履行少数几项已承诺的多年度资助，但这些资助不会支持政治活动，”马兰德罗写道。为此，CZI在其社区团队进行了裁员，社区团队曾致力于关注负担得起的住房和“经济包容性”等工作，并将团队的其他成员调往组织的其他地方。根据另一封《卫报》查看的电子邮件，社区团队负责人将在下个月离开CZI。CZI的社区工作主页上已删除任何关于包容性或经济公平工作的提法。

CZI的网站在最近几天发生了更为剧烈的变化，删除了许多其多年公然支持企业多样性及支持在科学研究中代表性不足群体的过去陈述。变化之一是，CZI以前专注于自身员工多样化背景的DEI工作页面不复存在。另一个此前表示基金会通过“多样性、公平和包容性视角”开展所有工作的页面已移除该短语。陈在CZI主页上的一句名言：“运气不是国家战略。我们需要建立不让运气成为每个人的选项的策略。”已被删除。多年来，CZI向众多项目和组织提供了数千笔资助，许多资金聚焦于多样性工作，以及国特朗普政府曾批评的机构，比如国家卫生院。

CZI还在周二通知那些已经申请其科学多样性领导奖的人员，该奖项将向所选研究人员发放115万美元的资助，通知他们该项目“决定不再继续”。尽管这一项目的选择过程已经在进行中，但组织并未在通知中说明停止该项目的原因。申请该奖项的一位人士原本期待本月收到消息，却收到了简短的电子邮件，告知该奖项将被停用。该人士告诉《卫报》，这一消息“极为令人失望”，并指出“这是CZI面临的一个更大问题”。

