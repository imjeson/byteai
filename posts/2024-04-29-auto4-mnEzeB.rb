AI 可能暂时救不了 OPPO 的急。
这几天，手机圈最津津乐道的就是三大市场研究机构给出的今年第一季度国内出货量报告。

---
尽管最终的市占率和排名存在很大争议，但三份报告都指出了苹果和 OPPO 的同比下滑，其中 OPPO 的降幅为 14%或 15.5%。
但 OPPO 显然认为 AI 才是更值得聚焦和押注的领域。
4 月 26 日，36Kr 报道指出 OPPO 的 XR 探索已经停摆了数月，并援引多位消息人士称，XR 相关人员将转向 AI 方面，探索 AI Device 或机器人等方向，同时也保留了少数人员，继续 AR 方向的预研。
**坦白讲，看到 OPPO 在 XR 领域的大撤退，小雷的第一反应是：情理之中，意料之外。**
情理之中在于，OPPO 今年对于 AI 手机的重视相信已经路人皆知了。与此相反，很多人根本不知道 OPPO 过去几年在 XR 方面的动作，就算是造出过几代 AR 眼镜和一款 MR 头显，也从来没有面向消费市场。
...
<p>且不同于其他形态的产品，眼镜或者头显都离人的眼睛、嘴巴和耳朵更近，可以将数字内容以更有表现力的方式叠加在人眼视觉上，可以更容易排除环境音的干扰与 AI 进行交互，也不用每一次交互都需要抬起手机甚至打开摄像头。</p>
<p>在个人电脑尚处于早期阶段的 1980 年，乔布斯曾将个人电脑比作「大脑的自行车」，借由个人电脑，大脑（人类智能）可以实现更高效的计算和处理。但从这个角度出发，AI+XR 或许才是更好放大人类智能的技术组合，是大脑真正的「自行车」。</p>
