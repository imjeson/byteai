宁波东方理工大学甬江论坛2024即将于**8月15日**截止申请，当前已收到来自海内外数百名优秀学者报名，欢迎感兴趣的海内外专家学者尽早申请！

The application deadline of **August 15, 2024** for the Yongriver Forum 2024 @ Eastern Institute of Technology, Ningbo is approaching and we have received applications from hundreds outstanding scholars worldwide. We warmly welcome scholars interested in this forum to submit your application by August 15, 2024!

![Image 1](https://image.jiqizhixin.com/uploads/editor/f9c4c2f9-7085-4499-bb05-4876a300794e/%E5%9B%BE%E7%89%871.png)

**01** 论坛背景

Background

宁波东方理工大学（暂名）是一所由社会力量举办、国家重点支持、省市共同建设的小而精、高起点、高水平、国际化新型研究型大学，以服务国家发展、推动社会进步为使命，注重基础研究、突出前沿交叉、强化工程技术，坚持志存高远、求真务实的培养理念，培养具有宽厚基础、创新能力、家国情怀、国际视野的拔尖创新人才，以卓越的教育、科研和创新推动社会发展。

东方理工将于**2024年8月31日-9月1日**以线上线下相结合形式，组织召开东方理工“甬江论坛2024”。

---
我们诚邀有意加盟东方理工的海内外优秀学者报名参加东方理工“甬江论坛2024”，分享各自领域的最新研究成果，促进多学科学术交流，期待与您携手在“书藏古今、港通天下”的历史文化名城宁波，共创一个世界级的教育机构与科研平台。

Eastern Institute of Technology, Ningbo (EIT) cordially invites outstanding and enthusiastic scholars who are committed to excellence in research and teaching to participate in the 2024 Yongriver Forum. This forum aims to discuss pressing scientific and technological topics and facilitate the exchange of the latest research findings in a variety of scientific fields. Selected scholars will have the opportunity for onsite or online interviews for tenure-track or tenured faculty positions at EIT.

![Image 2](https://image.jiqizhixin.com/uploads/editor/43a5a3cd-0dcd-4604-9833-a4881e266907/1723195032338.png)

**02** 论坛学科领域

Scope of the Forum

宁波东方理工大学（暂名）组建理学、工学、信息、商科四大学科群，重点发展数学、物理、化学、生物、计算机等基础学科，以及集成电路、人工智能、智能制造、新能源、新材料、新医药等国家急需的前沿交叉学科，形成以理、工、信、商为主体，兼备特色人文社科的学科体系。

东方理工实行国际学术机构通用的准聘-长聘（Tenure-track）制度，面向全球招聘助理教授、副教授、教授和讲席教授。当前招聘领域涵盖理学、工学、信息等学科群，包括但不限于下列方向：

- **理学**：理论与应用数学、统计学与数据科学、物理学、化学、生命科学
- **工学**：机械、力学、航空航天、机器人、智能制造、材料、能源、生物医学工程、土木、环境、海洋
- **信息**：电子、微电子、集成电路、计算机、人工智能、软件工程、自动化控制、信息工程、通信工程

The forum focuses on but not limited to the following scientific areas:

- **Science**:
  Basic and Applied Mathematics, Statistics and Data Science, Physics, Chemistry, and Life Science.
- **Engineering**:
  Mechanics and Mechanical Engineering, Robotics and Advanced Manufacturing, New Materials, Chemical and Molecular Engineering, Biomedical Engineering, Civil and Environmental Engineering, Ocean Engineering, Clean Energy and Sustainable Technologies.
- **Information Science and Technology**:
  Electronics, Microelectronics, Integrated Circuits, Computing, Artificial Intelligence, Software Engineering, Automation Control, Information and Communication Engineering.

