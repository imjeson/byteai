---

资深转型领导者加入执行团队，以推动以 AI 为首的增长，提高客户交付，并推动运营卓越
Robots & Pencils 是一家以 AI 为首的全球数字创新公司，专注于云原生网页、移动和应用现代化，今天宣布任命 Nathan Carmon 为首席运营官 (COO)。![图片](https://ai-techpark.com/wp-content/uploads/Nathan-Carmon.jpg){ width=60% }

---
作为一位经验丰富的运营者和首席执行官 Leonard Pagon 的长期商业伙伴，Carmon 在一个关键时刻加入，以带来更为精确的执行、深化客户价值和加速公司的 AI 首先的增长。

Carmon 的任务不仅仅是快速增长——而是要正确增长。这意味着要投资于人才，优化交付系统，确保每次客户合作都能带来清晰、关怀和可衡量的影响。他的到来标志着 Robots & Pencils 从移动先驱向 AI 首选咨询公司的又一个重要里程碑。

在过去的 20 年里，Carmon 和 Pagon 一起构建、扩展并出售咨询业务——从将 Brulant 从一家 25 人的初创公司发展为一家 500 人的强大企业，到在收购后将 Rosetta 的规模翻倍。作为 Next Sparc 的运营合伙人，Carmon 在过去十年中一直致力于将战略转化为高增长企业投资组合的实际成果。现在，他将同样的转型专长带入 Robots & Pencils。

“纳桑是那种稀有的运营者，可以将复杂的事情变得简单，并使雄心勃勃的目标成为可能，”Robots & Pencils 首席执行官 Leonard Pagon 说。 “当我们在建立 Brulant 时，我可以依靠纳桑将宏伟愿景转化为日常的动力。我们快速扩展，因为他知道如何将团队统一、交付价值，并快速而精准地行动。有纳桑担任首席运营官，我们不仅准备好迎接未来——我们为之做好了准备。”

Carmon 在每个企业都面临压力以采用 AI、现代化遗留系统以及以数字速度竞争的关键时刻加入 Robots & Pencils。该公司正在以挑战者的心态应对这一时刻，部署高影响力的“海豹突击队”风格团队，融合了精英工程和直观的用户体验设计。凭借北美、拉丁美洲和东欧的全球交付能力，以及与 AWS、Salesforce 和 Databricks 的合作关系，该公司处于帮助客户在 AI 时代领先的独特位置。

“我帮助咨询公司从零开始扩展，我知道出色的表现是什么样子，”Carmon 说。“Robots & Pencils 具备这一切。这里的人才卓越，领导力果敢，市场时机恰到好处。这个团队拥有让我们再次实现成功所需的一切——只不过是更大、更快、更聪明。”

“在纳桑领导我们的内部运营时，我可以有更多的时间专注于我最能创造价值的地方——与客户直接互动，探索战略机会，并帮助塑造 Robots & Pencils 的未来，以满足市场需求，”Pagon 说。“这就是拥有一个值得信赖的领导者的力量。”

Carmon 拥有密歇根大学的计算机科学学士学位和 MBA。当他不在加速数字转型的时候，他喜欢滑水、骑车、模型火箭，以及与女儿和四个孙子孙女共度时光。

