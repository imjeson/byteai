Permiso Security，领先的身份安全公司，保护人类、非人类和人工智能身份，横跨混合和多云环境，今天宣布了几项战略性的人事任命和高管晋升，因为Permiso正在响应其行业领先的身份安全平台的不断增长的需求。![图片](https://ai-techpark.com/wp-content/uploads/Permiso-Builds.jpg){ width=60% }

---


为了帮助更多客户采用Permiso独特的身份解决方案，Brian Crockford已加入公司担任首席收入官。Brian在网络安全销售领导方面拥有超过20年的经验，将负责市场推广功能。最近，他曾担任Phosphorus Security的销售副总裁。

Permiso最近还宣布了两项重要的内部晋升：

Ian Ahl，领导Permiso著名的威胁研究团队P0 Labs，已晋升为首席技术官。在新角色中，Ian将负责Permiso平台的所有产品创新和技术战略。
Sanjeev Williams，自2023年成为Permiso团队的基础成员，已晋升为产品高级副总裁。Sanjeev将领导产品管理和设计团队，继续交付帮助安全团队清点和保护其整个身份结构的突破性解决方案。

“我们在Permiso构建的团队反映了我们正在解决的挑战——复杂、快速变化，并且对现代企业安全至关重要，”Permiso的联合首席执行官兼联合创始人Paul Nguyen表示。“吸纳像Brian这样的世界级人才，同时提升像Ian和Sanjeev这样的成熟领导者，为我们下一个增长篇章奠定了基础。”

Permiso解决方案的需求由不断增加的身份网络安全攻击浪潮所强调，包括来自Scattered Spider的攻击，该组织继续针对领先的零售商、航空公司和保险公司，造成数百万美元的中断损失。威胁行为者通过盗窃身份或泄露凭证获取对系统和记录的访问，进而破坏系统或勒索。随着人工智能使得身份冒充变得更加难以检测，Permiso的解决方案将变得更加相关。

这些领导层变动随后还有其他几位关键成员的新增：

Austin Hall作为现场首席技术官加入Permiso，担任客户和潜在客户在应对复杂身份安全挑战时的技术顾问。
Garrett Stephens加入担任产品市场高级经理，专注于向各行业的安全领导者传达Permiso平台的价值。
Evan Davis作为高级需求生成经理加入，采用数据驱动的方法来推动品牌认知和管道增长。

该公司的增长势头进一步体现在两个重大产品里程碑上：

在4月，Permiso推出了Permiso平台，一个统一的身份清单、姿态管理和身份威胁检测解决方案，覆盖云和SaaS环境。
在6月，Permiso宣布即将推出Permiso Discover，一个免费的身份清单和可见性工具，适用于人类和非人类身份。

Permiso也继续获得行业认可。就在上周，该公司被选为KPMG私营企业技术创新者大赛的美国决赛选手，4月时Permiso被评选为SC奖项中最具前景的早期阶段初创公司。

