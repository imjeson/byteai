Reka的多模态和多语言语言模型将在Oracle云市场上提供
企业客户现在可以部署从文本、图像和视频中融合输入的生产就绪AI模型
AI研究和产品公司Reka已选择Oracle云基础设施(OCI)来支持其推动科学进步并为人类和企业建立生成式AI模型的使命。![图片](https://ai-techpark.com/wp-content/uploads/2020/06/Buyer-Guide-500x281-1.jpg){ width=50% }

---
与Oracle合作，Reka还将通过在由NVIDIA GPU驱动的OCI AI基础设施上进行训练的支持超过20种语言的多模态模型，提供给Oracle云市场。
Reka首席执行官兼联合创始人DaniYogatama表示：“我们正在与Oracle合作，将生成式AI的力量带给全球更多组织。”“OCI的高性能AI基础设施能力、专门的工程支持和全球覆盖范围将继续使我们能够快速高效地创新并发布新模型。”
Reka由来自DeepMind、Google Brain和FAIR的高度经验丰富的科学家和工程师团队创立，已经开发出领先的多模态AI模型，使组织能够使用多语言文本、代码、图像和视频的输入部署生成式AI应用。这些应用程序可以为创意任务生成概念，回答各种问题，并从大型数据集中提取见解。此外，Reka的模型使开发能够通过分析和直接行动改进以及启用各种新的业务用例的AI代理。通过与Oracle合作，Reka计划继续扩大其全球影响力，并推进其对企业级AI产品和解决方案的重点。
Oracle云市场提供Reka Core和Reka Flash模型，这是Oracle和Oracle合作伙伴提供的企业应用程序的集中存储库，通过私人提供。
模型构建器利用OCI的高性能能力，实现更快的AI部署
与许多其他AI初创企业和企业一样，Reka利用OCI的专为AI定制的能力更快、更可靠地构建和训练模型。对于训练大型语言模型(LLMs)，OCI Supercluster可以扩展到4,096个OCI计算裸金属实例，配备32,768个NVIDIA H100张量核心GPU，这些 GPU 通过超低延迟RDMA集群网络连接，支持HPC存储选择。OCI计算虚拟机和OCI的裸金属NVIDIA GPU实例可为生成式AI、计算机视觉、自然语言处理、推荐系统等应用程序提供动力。Oracle的专门工程支持团队将与客户合作，从规划到启动，确保成功。
