2025年ISG Provider Lens®私有/混合云——数据中心服务报告评估了69家提供商在五个象限中的能力：大客户管理服务、中型市场管理服务、托管服务、共置服务和AI就绪基础设施咨询。

该报告将富士通（Fujitsu）和Kyndryl评为三个象限的领导者。它还将埃森哲（Accenture）、凯捷（Capgemini）、Claranet、DXC科技、Ensono、Hexaware、印孚瑟斯（Infosys）、Pulsant、Rackspace科技、T-Systems和TCS在两个象限中评为领导者。在一个象限中，阿托斯（Atos）、计算中心（Computacenter）、德勤（Deloitte）、Digital Realty、Equinix、Global Switch、HCLTech、IBM、LTIMindtree、NTT DATA、Redcentric、Telefonica Tech、Telehouse、Unisys和Wipro被评为领导者。

此外，LTIMindtree、Mphasis和NTT DATA在一个象限中被评为新兴明星——ISG定义的具有“有前景的产品组合”和“高未来潜力”的公司。

在客户体验方面，Persistent Systems被评为2025年全球私有/混合云和数据中心服务提供商中的ISG CX明星表现者。Persistent Systems在ISG的客户声音调查中获得了最高的客户满意度评分，这是ISG卓越之星™项目的一部分，作为科技和商业服务行业的优质认可。

