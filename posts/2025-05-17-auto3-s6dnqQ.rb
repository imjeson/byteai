随着全球AI数据中心的能源需求预计将大幅上升，作为领先的人工智能（AI）应用专用集成电路（ASIC）提供商，Neuchips正在努力应对这一挑战，在2025年台北国际电脑展(COMPUTEX 2025)上展示其以能效为中心的AI解决方案。该公司因其安全和隐私而闻名，将展示其AI解决方案如何在运行大型AI模型时节省电力。

请于2025年5月20日至23日，一同前往台北南港展览中心1馆的I0601a展位访问Neuchips。

该公司的努力恰逢国际能源署（IEA）在4月发布的《能源与AI》报告。据预测，到2030年，全球数据中心的电力需求将翻倍，达到约945太瓦时（TWh）——这仅略高于日本目前的电力消费。对于利用AI的数据中心，电力需求预计将到2030年增加四倍。

Neuchips首席执行官Ken Lau表示：“在当今世界的能源挑战和AI潜力之间，节能的AI解决方案不再是可选项——它们已成为标准。在Neuchips，我们不断追求更高的能源效率，目前我们的Viper系列AI加速卡可以在仅45W的功耗下运行完整的140亿参数模型，这与标准的灯泡相当。创新现在必须集中在性能和效率上。”

### 合作推动能效创新

Neuchips以其高能效的AI硬件解决方案而领先，能够最大限度地保障安全和隐私，支持离线运行大型语言模型（LLMs），使各行业的企业能够在保障敏感数据的同时利用AI。该公司的产品包括N3000芯片系列和Viper PCIe加速卡，均全面支持Intel®和AMD CPU，并与Ubuntu和Windows兼容。它们还支持多种LLM，包括Mistral Small 3（24B）、Llama 3.3（70B）、DeepSeek蒸馏模型、Gemma 3（4B）等。

在2025年台北国际电脑展上，Neuchips将通过现场演示展示几项合作：

- 与台湾国家高性能计算中心（NCHC）的云端AI计算进军：该公司已与隶属于应用研究国家机构（NIAR）的NCHC合作，为云原生客户提供其高能效解决方案。通过将Neuchips的AI加速器与NCHC的基础设施相结合，该合作将降低云服务成本，同时确保数据安全和隐私。

- 与MAPLE LEAF INFORMATION AND TECHNOLOGY和Vecow的合作：此次合作创造了紧凑系统，能够在无需额外电源配置的情况下运行复杂的生成式AI应用。

- 与GSH的ShareGuru SQLPilot系统的联合解决方案：该演示将展示GSH的多代理技术与NL2SQL代理AI及表格增强生成（TAG）结合应用于高精度的本地AI会议分析系统和智能客户服务解决方案。通过简化LLM查询至复杂数据集，解决方案提供了更高的准确性、用户体验、效率和洞察力。

- 与myLLM myPDA平台的合作：同样也将与Neuchips的硬件结合，该myPDA平台是一款云边缘集成的GenAI应用平台，设计用于自主LLMs，并兼容代理AI。

