这次合作将使护士能够使用尖端解决方案。Abridge的新产品由Epic支持，将Abridge的生成式人工智能平台的优势与对现有Epic护理工作流的无缝集成结合在一起。

作为Epic Workshop计划下一阶段合作的一部分，该计划旨在为医疗保健行业带来新的创新技术，Abridge、Epic和Mayo Clinic将与Mayo的护士合作，优先考虑该工具将产生最大影响的工作流。 Mayo的护士将在设计和测试此解决方案中发挥重要作用，该解决方案与Abridge面向医生的文档产品一起使用。

“这次合作的核心是Mayo Clinic的护理人员，”梅奥诊所首席护理官Ryannon Frederick，M.S.，R.N.说。“我们直接让他们参与此技术的开发，以确保其使用满足护理和患者护理工作流程的独特需求以及环境解决方案的监管要求。我们很高兴能够借助我们护理人员的知识和专业技能来帮助塑造文档的未来，其中文档可以自动和有机地生成。”

护理工作流程通常独特地包括快节奏、持续和广泛范围的活动，可能涉及多名患者和数百项任务。日常工作可能包括患者和团队之间的沟通、临床记事和数据采集。Abridge、Epic和Mayo Clinic共同努力确定了应用此技术的最高影响领域。

“Abridge的人工智能技术、Epic的开发和Mayo Clinic的护理实践专业知识的结合努力和专业知识将帮助我们快速开发和迭代这项技术，以满足我们的护理人员和患者的需求，”梅奥诊所首席临床系统和信息官Edwina Bhaskaran，M.S.N.，R.N.说。“这项技术将通过每天为患者提供护理的护士的专业知识来塑造。我们的共同目标是在年底之前让工具到达我们护士手中。”

“Abridge平台的扩展，具有专门的内部Epic住院工作流中的护理产品，将有助于解决全国范围内护士不断增加的行政负担问题，”Abridge的首席执行官兼创始人Shiv Rao博士说。

“这项协作工作有可能重新思考护士如何在电子健康记录中记录对话，通过将患者和护士的声音直接录入记录，” Epic公司护理副总裁Emily Barey说。“我们知道每次对话对于获得最佳结果至关重要，我们对于作为值得信赖的助手的角色感到兴奋，可以将这些对话转化为行动，以便护士能够提供他们想要提供的护理。”

