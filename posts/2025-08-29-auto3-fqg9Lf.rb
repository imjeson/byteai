InvroMining, 一家成立于2016年的伦敦技术和数字资产平台，今日宣布扩展其基于人工智能的量化基础设施。![图片](https://ai-techpark.com/wp-content/uploads/InvroMining.jpg){ width=60% }

---
此次发布集成了系统化策略执行、实时监控及开发者访问开源SDK，使量化数字资产的参与对全球更多用户变得更为可及。

### 关键亮点

**AI量化集成**  
InvroMining的平台应用先进的机器学习技术来识别交易信号，执行交易位并重新平衡投资组合，使用户能够以更高的透明度和自动化参与数字资产市场。

**易于参与**  
该平台旨在吸引个人和机构，降低技术门槛，为那些希望通过系统化AI量化策略建立额外收入来源的人提供结构化的入门点。

**可持续运营**  
凭借全球135个由可再生能源供电的数据设施，InvroMining致力于将增长与碳中和原则相结合。

**跨平台访问**  
用户可以通过网页和移动应用程序监控投资组合活动和策略结果，确保实时了解账户表现。

### 领导者评论  
“InvroMining的目的是弥合复杂的量化系统与普通用户之间的差距，”InvroMining首席执行官Vladimir Jedla说道。“我们的重点是提供基础设施，不仅确保安全和可持续发展，还使参与者能够受益于结构化、基于规则的收入机会。”

### 路线图
- AI量化套件对衍生品和期货的更广泛支持。
- 在亚太地区和拉丁美洲扩展并提供本地化的入职支持。
- 社区输入新策略模块的治理特性。

