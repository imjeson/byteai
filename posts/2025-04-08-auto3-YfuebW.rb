利用一位麻省理工学院数学家的未发表定理（因其早逝而被世人遗忘），宾夕法尼亚州的一位发明者证明了爱因斯坦之前被否定的一些理论实际上是正确的，因此能够在商业现成的计算机芯片上实时模拟数十亿个量子粒子。

Essam Abadir 正在推出物理计算的FRame And QuanTum Language虚拟机（FRAQTL VM）——世界上首个超计算机，超越了基于最佳理论叠加的量子计算机的能力。

当其他公司追求硬件量子霸权时，FRAQTL提供了一种激进的替代方案：通过使用现成的经典计算硬件超越量子硬件。换句话说，FRAQTL让超计算的未来已经到来，并向所有人开放。

这一开创性的技术现已在您的个人电脑上公开检测，作为EGPT（电子图纸理论）项目的一部分，可在www.DeSciX.net上找到。它以区块链基础的去中心化科学（DeSci）形式发布，开放源代码并支持Polygon区块链。研究人员、开发者、创新者和对存在感到好奇的人士均被邀请加入这一计算、物理学、人工智能和哲学的社区革命。

“基于硬件的量子计算曾被誉为未来，而FRAQTL超计算机在量子到来之前便已让我们超越。”发明者Essam Abadir这样解释道。超计算不仅解锁了所有量子计算所梦寐以求的能力，如计算冷聚变、对金融市场进行指数级增强建模或设计个性化癌症药物，还开启了全新的范式，如通用人工物理智能（GAPI）。像这样的技术和人工智能对我们的未来至关重要，社会有道德责任确保公共利益。它们不应被单一政府或公司拥有，而应该向所有人开放。”

在您的网络浏览器中计算一个“不可能的”量子问题
诺贝尔物理学家理查德·费曼激发了量子计算领域，并曾关于双缝实验（DSE）说道：“我们选择研究一个绝对不可能用任何经典方式解释的现象，其中蕴含着量子力学的核心。实际上，它包含了唯一的奥秘。”费曼所说的“不可能用经典方式”意味着没有任何强大的经典数字计算机能够复制它。

之前仅能模拟少量光子的难题，如今FRAQTL能够计算费曼认为“绝对不可能”的问题。所有人都可以在他们的网络浏览器中探索一个实时的DSE模拟，模拟成千上万的光子，以及驱动其工作的极少几行源代码。

赋予人工智能想象的能力
尽管世界集中关注由大语言模型驱动的AGI，越来越多的关注点也转向了AI“世界模型”。借助AI世界模型，FRAQTL的超计算解锁了一个潜在的更基础的概念：通用人工物理智能（GAPI），以及计算机想象未来状态的能力。物理学和科学本质上是一种想象或预测未来的追求，给出现在所了解的信息。GAPI是一个新范式，允许计算机像人类一样“真实地物理想象”未来，而无需首先经历它们。与只能基于过去数据进行预测的LLM不同，GAPI允许计算机在没有大量数据集的情况下进行想象。

想象一下：
- 机器人：机器人自主适应前所未见的复杂场景，例如进行复杂的手术。
- 虚拟现实/增强现实/计算机生成图像：创造出前所未有的沉浸式现实——例如在火星上行走，虚拟与物理世界无缝融合。
- 自主驾驶汽车：自主车辆无缝导航任何地形，并主动预测不可预见的事件。
- 安全系统：安全系统预测威胁并应对挑战，超越传统安全措施的限制。
- 产品设计：A.I.系统理解物理用户体验，创造独特的消费产品。

EGPT与FRAQTL背后的科学：熵作为自然的计算机代码，解决P=NP
EGPT通过假设爱因斯坦是正确的，认为量子力学是不完整的，且宇宙从根本上是数字化的而非连续的，挑战传统物理。它通过显示知名未解决的科学问题P=?NP和量子引力实际上是同一个问题，利用组合数学（“球到箱子”）和分形递归替代微积分，从而使FRAQTL得以解决。这种方法将量子“球”放入重力框架“箱子”中。

FRAQTL基于严格的数学证明（以前的ResearchGate预印本，现在在DeSciX.net上，且AI代理已受过相关内容的训练）：

- 量子计算与混沌不连续体中的分形压缩：介绍了用于引发混沌的重力相对论的N体分形计算语言FRAQTL。提出了爱因斯坦-牛顿法则框架，正式证明分形压缩是数字宇宙中物理学的语言，并为量子叠加和纠缠提供经典解释。
- 没有吸引力，你什么也得不到：将重力引入量子力学。为重力完成量子力学和爱因斯坦的EPR论点（量子力学不完整）提供正式证明。量子引力下普朗克定律的完整离散推导作为EGPT新兴物理的证明，提出“暗物质”或能量单位的分形离散可分性，并建议分形尺度不变性的后果可以促进基本力的统一、MOND和全息原理。
- P可能等于NP：将P=NP形式证明为随着系统规模的增长而快速概率收敛，因此提及“可能等于”。首次通过Rota的熵定理将物理熵映射到数字香农熵，其次将其映射到NP完全的SAT，运用香农的编码定理得到O(N log N)的多项式时间解决方案。发明者在1993年从他的麻省理工学院教授Gian-Carlo Rota那里了解到Rota的定理，Rota可惜于1999年去世，因此该定理未能发表。
- 熵游戏：通过映射熵与NP完全形式之间的关系，探讨P=NP和物理可计算性的问题。
- 专利申请和开放源代码FRAQTL VM：详细介绍了使用分形递归算法进行物理计算的背景和实施，这类似于图形引擎的实现。

