伴随至强品牌焕新，英特尔隆重发布了英特尔®️ 至强®️  6能效核处理器。数据中心与人工智能事业部副总裁兼至强E-Core产品线总经理Ryan Tabrah介绍道，英特尔®️ 至强®️  6能效核处理器具备更高的内核密度，现已推出的6700E系列处理器每个 CPU 拥有多达 144 个内核，能为数据中心中的高密度、横向扩展工作负载提供出色的性能和能效。支持将200个基于第二代至强®️ 处理器的机架整合为66个，整合率高达3:1。这种出色的机架密度，实现了可以在更少的空间和更低的功耗下处理相同的工作负载。

此外，英特尔®️ 至强®️  6能效核处理器还具有出色的能效表现，与第二代至强相比带来4.2倍的机架级性能提升和2.6倍的每瓦性能提升，是在电力、空间和散热受限的情况下的理想选择，并具备更大的内存带宽，出色的输入/输出能力，内置众多加速引擎以及具备多达 64 条 CXL 2.0 通道，能够为高并行和大吞吐量的云级工作负载带来更高性能和能效，并满足 SLA 要求。

此外，英特尔®️ 至强®️  6能效核处理器能够随服务器占用率的提高，提供可扩展的每瓦性能，使整个负载的功耗/性能呈近线性关系。这意味着对于性能要求严苛的工作负载，在高负载下可以高效利用电能，从而更快地完成任务。

