在过去十年，越来越多的企业将业务迁移到云端，云计算成为推动数据中心建设的主要驱动力。随着人工智能的不断发展，尤其是大语言模型等AI技术的爆发和广泛应用，市场对算力的需求也在不断增加，进一步推动了数据中心的持续变革。

---
当下，数据中心不仅要满足包括AI、Web和微服务、数据分析等多样化工作负载对算力的需求，还需要应对架构设计、供电、机架密度、制冷散热、降低TCO以及可持续发展等多种挑战。

为了满足现代数据中心不断增长的算力需求和多样化的工作负载，英特尔于今天在英特尔GTC科技体验中心举办发布会，发布了全新英特尔®️ 至强®️  6 处理器家族，其采用Intel 3制程工艺，包括性能核（P -core）和能效核（E-core）两个微架构版本，针对包括 AI 在内的各种工作负载进行了深度优化设计，两款处理器的组合可以将数据中心的性能和效率提升到新的高度。

英特尔市场营销集团副总裁兼中国区总经理王稚聪在开幕致辞中谈到，中国经济正进入以人工智能等为代表的新质生产力推动高质量发展的下半场。英特尔稳步推进“四年五个节点”战略，新推出的英特尔®️ 至强®️ 6处理器作为摩尔定律的又一力作，就是面向数据中心的高能效和高性能产品，将有助于激发和释放新质生产力。

英特尔公司全球副总裁兼首席市场营销官Brett Hannath发布了全新的品牌焕新战略。新品牌是应行业和产品技术变化需求，以更简洁的命名和更深刻的含义赋能品牌属性，提升至强品牌的高性能、高能效、高质量和高安全特性的市场认知。

