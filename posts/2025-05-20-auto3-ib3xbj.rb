title: '埃里克·布兰德里兹加入Symbotic董事会'
date: 2025-05-21
author: ByteAILab

---

经验丰富的首席财务官带来了在工业技术的高增长环境中以及在复杂全球制造组织中的金融和会计领导方面的深厚经验
Symbotic Inc.（纳斯达克代码：SYM），作为供应链中AI驱动的机器人技术的领导者，今天宣布埃里克·布兰德里兹于2025年5月14日当选为其董事会成员。![图片](https://ai-techpark.com/wp-content/uploads/Eric-Branderiz.jpg){ width=60% }

---

布兰德里兹先生在公共和私人公司财务与会计方面拥有近30年的职业生涯，曾在工业技术的高增长环境中工作。最近，他担任Enphase Energy的执行副总裁兼首席财务官。在Enphase Energy之前，布兰德里兹先生曾是特斯拉的副总裁、企业控制器和首席会计官。他还在SunPower公司、知识宇宙公司、Spansion和先进微设备公司等多个公司的高级财务与会计职位上工作，职业生涯伊始于安永会计师事务所。
“我代表董事会，对埃里克的加入深感兴奋，”Symbotic董事长兼首席执行官里克·科亨（Rick Cohen）表示。“埃里克带来了深厚的金融专业知识和成功的纪录，指导公司度过关键的增长阶段，并在帮助新上市公司实现显著更大规模方面发挥了关键作用。我期待与他合作，继续将我们尖端的机器人和AI驱动的自动化技术推广到全球各种客户和环境中。”
“我很荣幸在公司发展如此激动人心的时刻加入Symbotic的董事会，”布兰德里兹表示。“Symbotic在其领域内是一家领先者，拥有独一无二的自动化技术，我期待利用我在以增长为导向的技术公司中的经验来支持Symbotic的持续创新和快速发展。”
布兰德里兹先生目前还担任Cognizant Technology Solutions Corporation和Fortive Corporation的董事会成员。他是加利福尼亚州的注册会计师，并获得阿尔伯塔大学商业经济学学士学位，专业为会计。

