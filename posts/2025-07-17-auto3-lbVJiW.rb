Henson Group与myCloudDoor今天宣布他们的战略合并，旨在全球范围内扩展微软解决方案能力。两家公司将共同加强其在微软技术栈中的服务组合，同时在全球范围内加倍专业和托管服务能力。新的合并组织将被称为ALIANDO。

作为第一大Azure云解决方案提供商和全球前1%的微软合作伙伴，ALIANDO在推动行业创新方面具有良好的定位，成为行业的倍增器。

“这次合并代表了我们赋能每一位客户实现更多目标的重要一步。通过与myCloudDoor的结合，我们更好地满足客户全球需求，”Henson Group首席执行官David Fuess表示。“我们很高兴推出新名称ALIANDO，这反映了我们统一的愿景和对卓越的承诺。”

ALIANDO将在微软技术栈中提供差异化的解决方案——Azure、现代工作、数据与人工智能、商业应用程序和网络安全，专注于Azure上的SAP、Azure VMware解决方案（AVS）、Copilot和Azure AI服务（Azure OpenAI、Microsoft Fabric + AI）。

myCloudDoor首席执行官Marcos De Pedro补充道：“这次合并的战略利益巨大。结合我们在Azure、数据与AI和网络安全方面的专业知识，加上我们增强的能力，将使我们能够为客户提供无与伦比的价值。”

两个公司的整合将扩大客户在美洲、EMEA和亚太地区的服务覆盖范围，同时启用新的云卓越中心和各大主要地域的解决方案。Houlihan Lokey作为合并的独家财务顾问，在整个交易过程中提供了战略指导和行业专业知识。

