埃马纽埃尔·马克龙为欧洲对埃隆·马斯克日益增加的批评发声，指责这位世界首富直接干预大陆的民主进程，包括下个月德国的迅速联邦选举。

---
法国总统周一与挪威和英国首相以及德国政府发言人一道，回应了马斯克通过社交平台发表的一系列支持极右翼政党和攻击欧洲左派政治家的激烈帖子。这位社交媒体平台X的拥有者是唐纳德·特朗普的亲密盟友，在花费超过2.5亿美元帮助他连任后，已被新任美国总统请求减轻联邦预算，担任特别顾问。

“十年前，谁能想到世界上最大的社交网络的拥有者会支持一种新的国际反动运动，并直接干预选举，包括在德国的选举，”马克龙在对法国大使的讲话中这样说道。马克龙曾与马斯克培养了建设性的关系，最近还邀请他参加巴黎圣母院的重开仪式，但这次并没有提到这位亿万富翁的名字，挪威的中左派首相乔纳斯·加尔·斯特øre也是如此。然而，两个领导人所谈论的对象却显而易见。

“我发现，一个拥有巨大的社交媒体接触和庞大经济资源的人如此直接地介入其他国家内政令人担忧，”斯特øre在公共广播公司NRK中表示。对有关马斯克是否会直接介入挪威政治的问题，斯特øre补充道，他希望该国的政治家们——面临九月的总选举——“会对此发出警告，并与此类努力保持距离”。

有一名德国政府发言人提到了马斯克的名字，并坚称他对选民的影响是有限的。“普通人、理智的人、体面的人在这个国家中占据绝大多数，”发言人在柏林的一次例行新闻发布会上表示。“我们行事如同马斯克的推特声明能对8400万人口的国家产生影响，这只是错误的半真半假的观点。”

柏林上周指责马斯克试图通过一篇在《星期日世界报》上发表的争议性客座评论影响德国的关键23日联邦选举。马斯克还将与极右翼、反移民的德国选择党（AfD）的领袖阿丽斯·维德尔进行一次小时的直播对话，马斯克曾在上个月的一则帖子中表示“只有AfD才能拯救德国”。他声称，该党在民调中排名第二，是“德国最后的希望”。他还称德国社民党的总理奥拉夫·肖尔茨为“傻瓜”，称总统弗兰克-瓦尔特·施泰因迈尔为“反民主暴君”。

德国副总理罗伯特·哈贝克周一表示，马斯克对AfD的支持是亿万富翁为一个无法对社交媒体和其他科技公司施加严格监管的弱欧洲所做的“逻辑和系统性”策略。肖尔茨自己在上周末的一次采访中表示，他不会努力与马斯克建立关系。“我不相信取悦马斯克的价值。我乐意将这留给其他人，”他说。“原则是：不要给恶棍喂食。”

英国首相基尔·斯塔默周一在回应马斯克数天的恶意攻击时为自己的施政辩护，并表示传播谎言和虚假信息的人员并不关心受到影响的人，而只关心他们自己。马斯克的攻击集中在过去在斯塔默担任公诉局局长期间首次曝光的儿童性虐待丑闻，要求进行新的公众调查，并呼吁工党首相辞职。斯塔默对此回应说：“我不会将此单独与埃隆·马斯克相提并论”，但他补充道“某些批评已经越过了一条界限”。马斯克随后在新的X帖子中称这位首相为“极其可鄙”。

