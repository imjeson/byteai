Microsoft Azure 的客户现在可以访问 Lumifi 加强的威胁检测、调查和响应技术
网络安全公司 Lumifi Cyber, Inc.（Lumifi）宣布他们的托管检测与响应（MDR）服务现已在 Microsoft Azure Marketplace 上可用。![图片](https://ai-techpark.com/wp-content/uploads/2024/08/Lumifi-960x540.jpg){ width=60% }

---
Lumifi 客户现在可以利用备受信任和高效的 Azure 云平台，结合安全工具 Microsoft Azure Sentinel 和 Microsoft Defender for Endpoint。
Azure Marketplace 上的 Lumifi MDR 服务可以帮助组织显著增强其网络安全姿态。通过结合持续的自动化威胁搜索与编排和响应，Lumifi 提供了积极的托管网络安全服务，确保全国企业针对勒索软件和新兴安全威胁获得全面保护。
Lumifi 的 MDR 服务在 Azure Marketplace 上可赋予客户选择，在最适合其需求的云平台上增强其安全姿态的权利，”Lumifi 的现场首席信息安全官 Grant Leonard 表示。Leonard 进一步表示，“这巩固了我们为企业提供所需工具以在不断演变的网络威胁环境中保护其自身和客户的承诺。”
Microsoft 提供的是唯一一个具有 XDR 和 SIEM 之间完全集成式、本地级别支持的安全平台。Microsoft Sentinel 提供了可扩展的、云原生的 SIEM 性能，同时具备高级的安全编排、自动化和响应（SOAR）能力，而 Microsoft Defender for Endpoint 通过高度可定制的检测和响应工作流来实现自动化。
Lumifi 的首席执行官和创始人 Michael Malone 表示：“我们的 MDR 服务加入 Azure Marketplace 是 Lumifi 的重要里程碑。我们不仅可以扩大业务范围，还可以以一种更易获得、可扩展和简单的方式交付我们的尖端网络安全解决方案。”
要了解有关 Lumifi 在 Azure Marketplace 上与 Microsoft Defender for Endpoint 和 Microsoft Sentinel 及其功能集成的 MDR 服务的更多信息，请访问：https://www.lumificyber.com/partners/technology-partners/microsoft/。

