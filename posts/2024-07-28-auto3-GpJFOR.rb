随着攻击面的扩大，防御者需要灵活性和黑客思维来应对。这些服务通过允许防御者根据他们所面临的最紧迫威胁不断调整安全服务，从而增强网络安全姿态。

“我们创建了‘按需网络安全’产品组合，以帮助客户现代化其安全计划，减少不断扩大的攻击面风险，”Coalfire的网络安全服务执行副总裁查尔斯·亨德森说道。“通过一份协议，客户可以利用我们的顶尖安全从业者提供的服务，并采取面向威胁的方法来优先处理其环境中的漏洞、配置错误和威胁。这些服务体现了我们的人+技术方法，以建立强大的网络安全姿态。”

以下是“按需网络安全”产品组合中包含的服务：

- 程式化应用安全：威胁建模，SAST/DAST，合作伙伴集成，应用渗透测试
- 红队和专业化：生成式AI和LLM测试，威胁情报测试，敌对模拟
- 程式化曝光管理：攻击面管理，漏洞管理，运营技术，云和身份
- 传统OffSec：应用程序和网络渗透测试，社会工程，物理评估，硬件和移动测试，合规性测试
- 合规性：FedRAMP渗透测试，FedRAMP红队测试，PCI渗透测试
- 合并和收购：发现和情报收集，网络风险尽职调查，网络计划整合策略和评估，后并购计划启动
- 防御：及时威胁猎手，IR策略审查，桌面演练，高管培训
- 主动程序：暗网监控和下线；持续威胁猎手

