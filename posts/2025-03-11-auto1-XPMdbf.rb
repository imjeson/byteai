苹果最新的智能手机是 iPhone 16e，提供了基本的现代 iPhone 体验，包括最新的芯片和 AI 功能，但价格比其他型号略低。

---
iPhone 16e 的售价为 £599（€699/$599/A$999），是 iPhone SE 系列的精神继承者。与仍保持老式厚重设计和主屏按钮的 iPhone SE 不同，16e 拥有与 iPhone 14 相同的机身设计，以及来自售价 £799 的 iPhone 16 的芯片。这意味着 16e 拥有全屏设计和铝合金机身。对于 iPhone 14 来说，这一设计非常出色，今天仍然令人满意。手机轻便且相对紧凑。6.1 英寸 OLED 屏幕足够大，适合大多数使用需求，但亮度不及标准 iPhone 16。

16e 的顶屏有老式的 Face ID 槽，不像其他近期 iPhone 系列中使用的“动态岛”。该机型仅提供黑色和白色，并且虽然它有替代旧款 iPhone 静音开关的便利操作按钮，但缺少其他 iPhone 16 系列中的相机控制按钮。

整部 16e 都存在这些与标准 iPhone 16 体验的小妥协。它配备了更高效的苹果 5G 调制解调器——这是 iPhone 的首次应用，但仅支持 wifi 6，而不是最新的 wifi 7。16e 还缺少 Thread 和超宽带无线电，后者用于苹果精准查找遗失物品及其他系统。

16e 的 A18 芯片相比普通 iPhone 16 略有不足，缺少一些图形处理能力，整体性能略逊一筹。然而，尽管如此，这仍然是一款非常快速的芯片。手机支持无线充电，但缺少自 2020 年以来一直是 iPhone 特色的优秀 MagSafe 磁性附件技术。MagSafe 支持各种磁性配件，从钱包、手机夹到车载手机支架和充电器，而这些在 16e 上都无法使用，这是一个很奇怪的省略。

这些小缺点是否重要取决于你的使用情况，是否你的家里有使用 MagSafe 的配件，是否在家中的 wifi 7下使用，或是否拥有大量苹果的 AirTag。但至少，16e 的电池续航表现出色，在各种条件下的日常使用中可持久使用 52 小时，比普通 iPhone 16 多出四个小时。这意味着重度使用情况下每两天或每隔一天需要充电一次。

16e 拥有一系列苹果智能功能，包括新视觉智能系统，利用相机识别物体，但大多数功能的吸引力不是特别强。

