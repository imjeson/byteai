可持续性

iPhone 16e 使用 20W 或更高 USB-C 电源适配器（不包含）充电至 50% 只需 26 分钟，完全充满则需 96 分钟。苹果表示，电池的循环次数应超过 1,000 次，同时保持至少 80% 的原始容量，并且更换电池的费用为 £95。过保修期的屏幕维修费用为 £225。16e 提供维修指南，并获得 iFixit 专家的维修性评分为 7 分（满分 10 分）。其包含超过 30% 的再生材料，包括铝、钴、铜、玻璃、黄金、锂、塑料、稀土元素、钢、锡和钨。该公司在其报告中详细说明了手机的环境影响。苹果还提供以旧换新和免费回收计划，包括非苹果产品。

相机

尽管相机应用程序与其他 iPhone 相同，但缺少 0.5 倍变焦、微距摄影和一些更高级的功能。被削减的一个功能在相机方面影响更大：16e 仅配备一颗 48MP 后置相机，而其他 iPhone 则配备两颗或三颗。

唯一的主摄像头可以进行 2 倍的传感器变焦，实现某种形式的远摄拍摄，但没有超广角镜头。大多数时候，我并没有特别想念它，因为主摄像头在各种光照条件下拍摄出色。但是在某些情况下，当你不能后退时，就不能获得所需的拍摄效果。其人像模式稍逊，而缺少微距摄影模式和 iPhone 16 的新高级摄影风格也是一个遗憾。它与其他 iPhone 系列共享相同的 12 兆像素自拍相机。

总体而言，相机表现令人满意，但如果你对摄影有任何兴趣，16e 并不是你选择的机型。

价格

iPhone 16e 起售价为 £599（€699/$599/A$999），存储为 128GB。相比之下，iPhone 16 的售价为 £799，iPhone 16 Plus 为 £899，iPhone 16 Pro 为 £999，iPhone 16 Pro Max 为 £1,199，谷歌 Pixel 8a 为 £499，三星 Galaxy S24 FE 为 £549。

判决

iPhone 16e 是一款稳固的手机，提供了大部分核心 iPhone 体验，但削减了一些功能以降低成本。许多人可能认为，较慢的 wifi、稍逊的芯片、没有相机控制按钮以及较老式的屏幕设计不会太重要。但缺少 MagSafe 和只有一个后置相机对某些人可能是阻碍选择的因素。

16e 相比 iPhone 15 和从第三方零售商处仍可购买的较旧便宜型号，所具有的优势仍然是苹果智能功能，其中一些功能实用，但许多功能不尽人意，或在第三方应用上表现得更加出色。

16e 最大的问题是其价格。尽管比普通 iPhone 16 便宜约 25%，£599 仍然处于市场高端。这使得 16e 仅对希望获得最新 iPhone 的顾客来说是理智选择，若不接受其他选择，则不如从第三方零售商那里购买更好的新机型或翻新机型。

优点：快速，屏幕表现优秀，尺寸合适，操作按钮，USB-C，卓越的电池续航，长期软件支持，Face ID，苹果智能功能。

缺点：只有单一相机和没有微距模式，没有相机控制按钮，老式屏幕设计，没有 MagSafe，没有 wifi 7，没有 Thread 或 UWB，AI 功能优缺点参半，价格仍然昂贵。

