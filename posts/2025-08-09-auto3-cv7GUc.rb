title: 'Fullpower-AI与Tempur Sealy签署10年智能床合作协议'
date: 2025-08-10
author: ByteAILab

---

Fullpower-AI®，一家领先的人工智能健康和 wellness 技术公司，今天宣布与 Tempur Sealy International（“Tempur Sealy”）达成变革性合作伙伴关系，签署了一项为期 10 年的全球许可协议，授权 Tempur Sealy 使用 Fullpower-AI 的 KOA Sleeptracker-AI Large Action Model (LAM) 平台。![图片](https://ai-techpark.com/wp-content/uploads/Fullpower.jpg){ width=60% }

---
作为协议的一部分，Tempur Sealy 将对 Fullpower-AI 进行 2500 万美元的 C 轮投资，使公司的估值达到 1.6 亿美元。

这一长期协议确保了 Fullpower-AI 的尖端 Sleeptracker-AI 平台将持续在全球 Tempur-Pedic 智能床生态系统中部署。迄今为止，已有超过 30 万台 Tempur-Pedic 智能床配备了 Fullpower-AI 的专利人工智能技术，提供实时生物传感、个性化睡眠洞察和科学支持的健康指导。这一合作巩固了 Tempur Sealy 作为智能睡眠解决方案全球领导者的地位。

Fullpower-AI 创始人兼首席执行官 Philippe Kahn 表示：“与 Tempur Sealy 的联盟是 Fullpower-AI 使命的自然下一步。Tempur-Pedic 在高端床垫方面树立了金标准，我们正在通过人工智能、科学和全球规模重新定义睡眠。”

KOA Sleeptracker-AI LAM 平台将 Tempur 智能床转变为智能健康系统，持续学习和调整，以提供个性化的睡眠指导和优化。这一合作为全球睡眠科学和个性化健康监测的加速创新奠定了基础。

Tempur Sealy 首席执行官 Cliff Buster 评论道：“我们很高兴能加深与 Fullpower-AI 的合作关系，确保其行业领先的先进 Sleeptracker-AI 平台将成为 Tempur-Pedic 产品组合的核心部分。通过持续的创新来满足消费者的睡眠需求是我们工作的核心，而与 Fullpower 的合作确保我们将始终处于行业的前沿。”

