HealthBook+，一家领先的人工智能数字健康公司，今天宣布PaiGE™的正式推出，这是一款革命性的个人健康助手，可以向患者和临床医生提供实时、个性化的健康见解。
借助PaiGE作为其数字优先、人工智能驱动的健康促进平台的核心，HealthBook+正在解决医疗领域最持久的挑战之一：使患者能够在临床访问之间在健康上取得实质性进展。通过结合临床数据、生活方式输入和AI驱动的分析，PaiGE使个人能够掌控自己的健康，同时为临床医生提供可操作的情报，提升护理质量并节省时间。
“PaiGE正在真正改变我们与健康的互动方式，”HealthBook+首席执行官Christopher Turner表示。“作为我们一体化指导和参与平台的关键特性，PaiGE帮助人们理解健康信息，引导他们采取最佳的健康行动，并为临床医生提供更清晰的患者在诊室外的生活视角。”
对于消费者而言，PaiGE是一个智能健康伴侣，它简化了医疗体验并使他们能够保持参与。它提供个性化的生活方式建议、易于查看和分享的健康记录、基于情绪和生活方式追踪的见解，以及一个安全、有引导的环境来询问健康相关的问题。在临床医生的个性化指导与PaiGE的智能支持相结合下，患者还会被提示进行日志记录和跟踪他们在访问之间的进展，帮助他们在遵循护理计划方面保持问责和得到支持。他们在健康旅程中获得了AI和临床医生驱动指导的最佳结合。
PaiGE还作为临床医生的智能虚拟助手，能够无缝集成到他们的工作流程中，帮助减少管理工作，挖掘见解，并提供主动、精准的护理。它提供患者自报活动的简明摘要、针对健康相关查询的AI驱动回答，以及简化文档和护理协调的工具。临床医生还可以通过PaiGE直接传达个性化指示，从而提升患者参与度，并增强对就诊之间挑战或成功的可视化。
HealthBook+是一个下一代智能代理AI平台，能够将来自电子健康记录、可穿戴设备、文档、图像和患者日志的结构化和非结构化健康数据聚合为一个统一的纵向健康记录。该代理主动帮助用户在碎片化系统中查找、组织和整理医疗记录，揭示缺失数据，确保连续性。
作为一个可扩展的API优先生态系统，HealthBook+ AI代理与提供者、支付方和数字健康服务集成，帮助患者导航医疗系统——降低采取行动的门槛，实现高接触的护理交付，同时最小化临床医生的工作量，并自动化患者的日常行政和文档任务。
根据美国及国际生活方式医学委员会主席Dr. Amy Mechley的说法：“HealthBook+代表了主动、个性化医疗的未来。通过利用人工智能和生活方式医学的力量，我们正在弥合数据与可操作的临床洞察之间的差距——赋予医生和患者专注于真正重要的事情的能力：朝向最佳健康的下一步。这一平台提升了临床效率，简化了工作流程，并扩展了临床医生的智力覆盖范围。最重要的是，它通过精准指导的个性化护理促进了更深层次的患者参与，这种护理是主动的，而非反应性的。”
PaiGE集成了生活方式医学，以推动患者健康的真正转变，为临床医生提供有关生活方式医学支柱的洞察，这些支柱构成了健康的基础。这使得无论患者是在诊所还是在家，护理都能更加连接和全面。
“我们早就知道健康并不止于诊所门口，”Turner说。“HealthBook+将整体情况聚焦，而PaiGE使其变为可操作。”
PaiGE在HealthBook+平台上的推出标志着公司使命的新篇章，即使个性化、预防保健变得更易于获取和有影响力。该平台最新版本旨在为广泛使用和基于会员的初级保健（MBPC）社区提供更有效、更具同情心和更高效的医疗体验。
