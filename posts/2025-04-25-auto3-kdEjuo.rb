IT Medical的创始人兼董事总经理亚历山大·波德戈尔尼已被接受为福布斯技术委员会的成员。

福布斯技术委员会是一个独特的社区，面向首席信息官、首席技术官和技术高管，成员由审查委员会根据其经验进行遴选。这包括推动业务增长的良好业绩记录以及显著的个人和专业成就。

波德戈尔尼在产品工程和医疗信息技术领域拥有超过十年的经验，他和IT Medical的团队结合医疗知识与先进技术，创建出安全、可扩展且用户友好的平台。在波德戈尔尼的领导下，IT Medical已成长为全球医院、诊所和医疗初创企业的可信伙伴，帮助他们简化运营、改善患者护理，并为医疗服务的提供带来真正的创新。

作为委员会的一员，波德戈尔尼将获得独特的机会，与其他行业领袖建立联系，并通过原创文章分享他的见解。他还将对福布斯网站发布的专家小组做出贡献。届时，他将专注于如何在临床环境中应用新技术。利用他在医疗和软件方面的经验，波德戈尔尼将分享如何使用AI、机器学习和分析工具来改善护理，支持提供者和患者。

波德戈尔尼表示，他很自豪加入福布斯技术委员会，并被认可为一群创新技术领导者中的一员：“这个机会让我可以分享我们在IT Medical所学到的经验，并参与到关于创新如何真正服务于医疗保健的重要对话中。”

