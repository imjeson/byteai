然而，他并非孤例。马斯克是一群有着南非白人统治背景的自由主义亿万富翁组成的“PayPal黑帮”的一部分，这些人在美国科技产业和政治中影响深远。他们包括彼得·泰尔，这位出生于德国的亿万富翁风险投资家和PayPal联合创始人，1970年代住在一个崇拜希特勒的南部非洲城市。泰尔是特朗普竞选的主要捐赠者，并对福利项目和女性投票的权利表示批评，称其破坏了资本主义。关于泰尔的2021年传记《逆流而上》声称，他在斯坦福大学读书时曾辩护种族隔离政策，称其“经济上有利”。前PayPal首席运营官大卫·萨克斯，现为特朗普的主要募款人，他出生在开普敦，童年在南非侨民中长大，家族因早年移民美国。第4位成员罗尔夫·博塔，南非最后一任外交部长皮克·博塔的孙子，曾担任PayPal首席财务官，虽然保持较低的政治声势，但与马斯克的关系密切。

在他们之中，马斯克由于拥有社交平台X而显得格外突出。近来，X成了极右翼观点的温床，而马斯克则与特朗普紧密相伴。特朗普曾提名马斯克担任“政府效率部”的负责人，目的是在联邦机构中大刀阔斧地削减和改革。有人画出了一条直线，连接马斯克在种族等级制度上成长为一名白人男性的形成经历，以及他如今站在特朗普身边的真相。

在特朗普就职典礼前一周，特朗普的前顾问史蒂夫·班农描述南非的白人南非人是“世界上最种族主义的人”，并质疑他们在美国政治中的参与，同时称马斯克为一个恶劣影响，应该回到他出生的国家。然而，其他人对此持怀疑态度，认为马斯克日益激进的观点并不能简单地追溯到他在比勒陀利亚的成长经历。著名南非作家乔尼·施坦伯格最近指出，试图通过马斯克的种族隔离时期童年来解读他是一种“糟糕的想法”，会导致“肤浅”的结论。

