马斯克于1971年出生的南非，彼得·泰尔则在童年时从德国迁入南非，当时南非的总理约翰·福斯特曾是三十年前一个与希特勒结盟的法西斯军事组织的将领。奥西瓦布兰德瓦格（OB）在第二次世界大战前不久成立，反对南非作为英国的盟友参战，并与德国军事情报之间进行 plotting。福斯特毫不掩饰地表示愿意同希特勒的国家社会主义意识形态相结合，并称其与南非的基督教民族主义理念相似。

虽然欧洲的纳粹主义已经被打败，但基督教民族主义在福斯特的领导下依然生机勃勃，其推行的种族分类和分层被视为防止“黑色危险”的必要措施。在学校中，这种基督教民族主义教育试图围绕着一个国家历史的单一版本塑造南非身份。而马斯克和泰尔则被灌输认为，主要是荷兰殖民者的后代的阿非利坎人是南非冲突中的真正受害者。

在种族隔离时期，大多数学校按种族进行隔离，尽管在法律上白人学生普遍享有相同的基督教民族主义教育，但白人社会本身却是分裂的。在英语学校接触的历史叙述常常成为隐性拒绝种族隔离意识形态的基础。

马斯克曾就读于约翰内斯堡的高中，后来进入比勒陀利亚男校，这所学校的其他校友包括在白人统治崩溃后成为南非最高法院法官的埃德温·卡梅伦，以及移居英国后成为反对种族隔离的主要活动家的彼得·海因。昔日《卫报》南非负责编辑菲利普·范尼凯克的阿非利坎父母，虽然在英语学校就读，却对种族隔离政府产生了敌意。

泰尔则在南非和其事实上的殖民地南西非接受培训，这个地方在1990年独立成为纳米比亚。他的父亲在附近的铀矿工作，泰尔生活的城市斯瓦科普蒙德在当时以对纳粹主义的继续崇拜而闻名。1976年，《纽约时报》报道该城部分人依然以“海尔·希特勒”互致问候。

与此同时，马斯克的父亲埃罗尔也参与采矿业务。他曾声称他的赞比亚翡翠矿股权让他“赚到钱多得连保险箱都关不住”。马斯克的母亲梅耶称，家中曾拥有两处住宅、一架飞机、一艘游艇和几辆豪华汽车。埃罗尔曾表示他反对种族隔离，加入进步联邦党，但因其不满一人一票的主张而退党。

马斯克的外祖父乔舒亚·哈德曼在1950年从加拿大移居南非，因为他喜欢新当选的种族隔离政府。20世纪30年代，哈德曼在加拿大是一个起源于美国的边缘政治运动“技术主义俱乐部”的领导人，该运动主张废除民主，推崇精英技术官员的统治。二战期间，加拿大政府因其反战倾向而禁止该运动。战争结束后，哈德曼领导一个支持《犹太人长老的议定书》的政治党，但没取得任何成效，最终移居南非。

正如范尼凯克所言：“我们所有白人南非人，凭借着我们的特权和在种族等级制度中的地位，成长为相信我们是主人种族的存在，即使我们并没有主动思考过。”马斯克与泰尔在南非的成长经历，让他们在显著的特权中得到保证，而这种特权若不看作是体系性种族压迫的结果，而是考取个人能力的自然结果，便让他们自然而然形成了这种观点。

无论马斯克是否承认，他很明显地将自己南非的部分带到了其他地方。

