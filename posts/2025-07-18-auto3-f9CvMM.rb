---

Dualboot Partners，一家领先的人工智能驱动的数字现代化解决方案提供商，今天宣布其3PO已在AWS Marketplace的全新人工智能代理与工具类别中上线。![图片](https://ai-techpark.com/wp-content/uploads/Dualboot.jpg){ width=60% }

---
这使AWS客户能够轻松发现、购买和部署Dualboot的人工智能现代化解决方案，以便以传统方法的几分之一成本理解和迁移过时应用程序，加速AI代理及代理工作流的开发。

3PO帮助组织加速代码现代化，利用人工智能驱动的分析实现无缝迁移，并减少与手动方法相关的成本，使客户能够更快、更准确地现代化遗留代码。

“我们很高兴能将3PO带入AWS Marketplace的新人工智能代理与工具类别，”Dualboot Partners首席执行官Ben Gilman表示。“这使客户能够在他们的云工作流中更轻松地集成人工智能驱动的代码现代化，加速转型计划，避免了通常的复杂性和成本。”

3PO提供核心功能，包括人工智能驱动的分析和解释、无缝的迁移过程以及代码现代化的成本效益自动化。这些功能使客户能够分析遗留代码、有效迁移并保持功能，同时大幅缩短时间和成本。

随着AWS Marketplace中人工智能代理和工具的可用性，客户可以显著加快采购流程以推动人工智能创新，减少对供应商评估和复杂谈判所需的时间。通过使用AWS账户进行集中采购，客户对许可、付款和访问保持可见性和控制。

作为AWS Agentic AI专业服务解决方案，3PO支持模型上下文协议（MCP），以实现流畅的代理通信，并与Dualboot的AI驱动DB90方法论无缝集成，结合产品、设计和工程专长以交付业务成果。这使客户能够灵活地在其AWS环境中部署，同时使现代化努力与更广泛的数字转型战略保持一致。

要了解有关AWS Marketplace中3PO的更多信息，请访问Dualboot Partners在AWS Marketplace的页面。要了解有关AWS Marketplace中新推出的代理和工具类别的更多信息，请访问 https://aws.amazon.com/marketplace/solutions/ai-agents-and-tools。

