title: '微软超额完成营收预测，但云服务业绩不佳拖累股价'
date: 2024-08-01
author: ByteAILab


微软在最新的季度财报中超出了分析师的预测，周二披露其营收同比增长15%。

---
但备受关注的Azure云计算服务的增长未达预期，微软股价在盘后交易中下跌多达7%。市场预期微软在其第四季度财报中将报告稳定增长，主要依靠其云服务。这些服务的营收增长了29%，低于分析师预测的30%至31%，导致抛售加剧，加剧了大型科技公司近期市场的困境。

在微软的财报中，首席执行官萨蒂亚·纳德拉试图增强市场对公司业绩的信心。“我们在本财年取得的强劲表现既反映了我们的创新，也反映了客户持续对微软的信任。” 纳德拉在财报声明中表示。“作为一个平台公司，我们专注于满足今天我们在规模平台上的客户的使命关键需求，同时也确保我们引领AI时代。”

微软近年来已在人工智能领域投资数十亿美元，作为一项赌注，认为基于人工智能的服务将主导科技行业。该公司还支持了ChatGPT制造商OpenAI，进一步巩固了自己作为生成式AI商业化中心参与者的地位。但随着人工智能领域的尘埃落定，关于大型科技公司转向人工智能能否带来他们吹嘘的营收提振的问题日益增多。

与此同时，联邦储备委员会降息的猜测等因素导致投资者在长期对人工智能和股票价格飙升持乐观态度后开始冷静下来。特斯拉和Alphabet上周发布财报后，两公司股价急剧下跌，微软股价也从7月初的高点下跌。

微软还处在本月全球科技故障的中心，该故障导致数千架飞机停飞，并在各个行业造成数十亿美元的混乱。网络安全公司CrowdStrike通过向Windows系统推送了一次故障软件更新，导致这些系统集体崩溃。周二早些时候，微软的Azure云服务遭遇了一次规模较小、不相关的故障，导致几个国家的网络连接问题。

