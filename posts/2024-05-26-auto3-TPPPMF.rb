Leading Cybersecurity Researchers to Gather in Scottsdale, AZ September 18-21
SentinelOne (NYSE: S), a global leader in AI security, today announced the return of LABScon, the premier conference dedicated to advancing cybersecurity research for the benefit of global digital defense. The exclusive, invite-only event will take place at Mountain Shadows Resort in Scottsdale, AZ September 18-21, and researchers are encouraged to submit original findings via the conference website to secure their spot today.
“The growing complexity of cyberattacks and diversity of threat actors underscore a clear truth: no single entity can effectively shoulder the burden of defense alone,” said Juan Andres Guerrero-Saade, AVP of Research at SentinelOne. “LABScon has emerged as an indispensable forum, drawing together the security research braintrust of academia, governments, and private industry to exchange ideas, collaborate, and decisively shift the balance towards collective defense.”
Hosted by SentinelLabs, a world-class team of security researchers that uncovers critical vulnerabilities, new attack vectors, malware strains, and threat actors, LABScon 2024 will feature:
Keynotes and presentations from esteemed threat researchers and leading cybersecurity experts
Interactive breakout sessions
Ample networking opportunities
Leading global security vendors have already signed on to sponsor the event, including: Luta Security, Binarly, Cisco Talos, Dreadnode, GreyNoise, and the Alperovitch Institute.
Additional sponsorship opportunities are available. For more information, contact info@labscon.io.
Researchers who would like to be considered for participation in LABScon 2024 can submit papers here through June 21. Submissions will be reviewed by the LABScon Program Committee, which includes top threat researchers and cybersecurity experts from Google, DARPA, Netflix, Johns Hopkins University, and SentinelLabs。![图片](https://ai-techpark.com/wp-content/uploads/2024/05/LABScon-960x540.jpg){ width=60% }

