方法论
本报告中发布的数据基于对7300名来自员工人数超过10000的企业的调查，受访者来自C级高管和副总裁层，以及来自美国、加拿大、英国、德国、瑞士、法国、荷兰、新加坡和阿根廷的工程师和开发人员，覆盖八个行业（金融服务、生命科学和医疗技术、教育与商业信息服务、能源、零售和消费品、通讯、媒体和娱乐、保险、汽车和制造）。调查于2024年10月24日至2024年12月3日进行，数据收集与Censuswide合作。

