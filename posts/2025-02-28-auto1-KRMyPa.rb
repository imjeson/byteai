如果你想知道泰勒·佩里的800万美元（约合635万英镑）扩建他的录像棚的计划为何搁浅，只需在OpenAI的视频生成工具Sora中输入“山中的客厅里两个捧着书的人”。这款于周五在英国和欧洲发布的人工智能驱动的Sora的结果说明了这位美国电视和电影大亨为何暂停了他的计划。今年早些时候，佩里在观看Sora预览后表示，如果他想拍摄这样的山景，他可能不需要在拍摄地或他的摄影棚搭建场景。“我可以坐在办公室里用电脑来做这一切，这让我感到震惊，”他说。这个简单的文本提示生成的结果仅仅持续了五秒钟——你可以生成最长20秒的影像，也可以用该工具拼接更长的视频——而“演员”显示出手部的常见问题（这是AI工具常见的问题）。但宏伟的山景和温馨的室内场景让人信服，生成这个视频的时间仅为45秒。该技术也在不断进步。

为了访问Sora，用户需要购买ChatGPT的付费套餐，但这标志着视频生成技术在快速发展的AI市场中的走向。它还强调了围绕著作权展开的争夺战为何在大西洋两岸达到白热化的程度。显然，像Sora、Kling和Runway这样的工具有潜力彻底改变电影、电视和广告行业。一位参与过该工具的英国数字艺术家的Josephine Miller告诉《卫报》，它为“年轻创意者”拓展了机会，她已经用它向品牌提出广告概念。

OpenAI表示，已经可以使用Sora的创意人员和工作室（如美国的）正在利用它来制作电影和广告概念和提案。Brandtech Group的首席执行官大卫·琼斯表示，随着像Sora这样的工具的出现，广告和市场行业将面临“根本性的颠覆”。琼斯将此比作对他的行业的“柯达时刻”，提及了在数字革命中屈服的传统胶卷相机公司。

大型广告商们已经开始接受AI制作的视频。可口可乐去年制作了一则完全由AI生成的圣诞广告，而这一技术的影响在迪士尼动画系列《重力瀑布》的创作者亚历克斯·赫尔希的尖锐推特中得以体现。“趣味事实：@CocaCola是‘红色’的，因为它是用失业艺术家的血液制作的！”他写道。艺术家们因AI而失去工作的问题已成为这一技术发展的关键战场，尤其是在著作权方面。像Sora和ChatGPT这样的AI系统由大量从互联网上抽取的数据训练而成。ChatGPT也是OpenAI开发的，面临着声称未经许可使用艺术作品构成著作权侵权的诉讼。

本周，由于政府计划允许AI公司在未获许可的情况下使用受版权保护的作品，争论在英国加剧。创造领域的反击以1000名音乐人发布无声抗议专辑和包括杜阿·利帕、汤姆·斯托帕德爵士和保罗·麦卡特尼爵士等知名创意人士在内的公开信为标志，警告称，政府正处于同意“大规模放弃权利和收入给大型科技公司的边缘”。Sora及其同类产品并不是这场争论的前沿，但显然，它们对那些希望在使用其作品创建这些工具及其内容时获得报酬的艺术家构成了竞争威胁。YouTube的首席执行官去年警告称，如果OpenAI使用YouTube的内容来训练Sora的模型，这将违反该平台的服务条款。已有报告指出Sora显然对游戏内容十分熟悉。

获得诸多奖项的电影制作人和跨党派的同僚比比安·基德伦已对英国政府的计划发出警告，她在接受《卫报》采访时表示，Sora的到来为这一辩论增添了“另一层紧迫感”。泰勒·佩里并不是唯一感到担忧的创作人。
