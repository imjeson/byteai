供应链和数字转型专家为平台的咨询委员会带来全球制造业经验
FourKites®，全球领先的人工智能驱动供应链转型公司，今天宣布，前米其林集团高级副总裁 Pascal Zammit 已加入其战略咨询委员会。![图片](https://ai-techpark.com/wp-content/uploads/Pascal.jpg){ width=60% }

---
Zammit 在全球供应链运营和数字转型方面拥有丰富的经验，他将在 FourKites 的咨询委员会中提供关于产品创新和市场扩展的战略指导。
在米其林的卓越职业生涯中，Zammit 领导了全球供应链组织并推动了互联出行业务单元的启动。他的成就包括在欧洲转型米其林的供应链，实施包括人工智能、物联网和区块链在内的先进技术，以及建立全面的全球销售与运营规划（S&OP/IBP）流程。
“工业供应链必须进化——从反应式系统转变为更具预测性、更自主但也更与供应商和客户集成以及更易于操作团队理解的运营，以便提供对商业竞争力至关重要的客户和员工体验，”Zammit 说。“FourKites 开发了一种端到端的人工智能平台，不仅对事件作出反应，还积极协调复杂的操作，仅在需要时进行人力干预。这代表了供应链管理的未来：可供一线操作团队理解和使用的系统，贴近客户和供应商，能够快速有效地感知、分析和提出决策，并在需要时根据每个企业的具体供应链绩效要求自主执行。”
FourKites 的战略咨询委员会汇集了拥有数十年实践经验的成功供应链高管。该委员会代表了 FourKites 对理解供应链领导者面临的实际挑战的承诺，确保公司的解决方案满足真实的市场需求。
“Pascal 在复杂组织中推动全球转型的经验赋予了他独特的洞察力，使他能理解跨多个市场和产品线运营的制造商面临的挑战，”FourKites 创始人兼首席执行官 Mathew Elenjickal 说。“他在米其林实施先进技术并取得可衡量的商业成果的成功记录使他成为我们在不断发展平台时的理想顾问。Pascal 的战略愿景与我们的使命完美契合，即将供应链从反应式监控系统转变为能够感知、分析和执行决策并最小化人力干预的自主操作。”
Zammit 的任命是在 FourKites 进化之外的实时可视化，推出行业唯一的智能控制塔（Intelligent Control Tower™）之后。这一平台结合了供应链网络数据、数字双胞胎和可以自主根据可视化数据采取行动的数字人员，能够在干扰发生之前进行干预，并协调复杂的供应链操作。
在1997年加入米其林之前，Zammit 曾在凯捷（Cap Gemini）担任顾问，并在法国 INSA 里昂完成研究生学业。自2021年以来，他担任SaaS解决方案供应商和咨询公司的高级顾问，并加入巴黎潘特翁-阿萨大学担任供应链与物流副教授。
Zammit 将在 FourKites 峰会上分享他在数字工作者（digital workers）方面的专长，他的会议主题为“数字工作者的优势：通过人工智能实现卓越扩展”，借助他转型米其林全球供应链运营的经验，演示组织如何消除跨复杂跨国操作中的运营变异。为期两天的会议将于2025年8月26日至27日在芝加哥举行，汇聚供应链领导者，进行以人工智能为动力的转型的实操学习——注册请访问 [FourKites Summit 2025](https://www.fourkites.com/event/summit-2025/?utm_source=businesswire&utm_medium=press&utm_campaign=summit-2025)。

