云计算市场加强安全防护
Vast.ai，领先的云 GPU 平台，今天宣布成功完成了 SOC 2 Type I 审核，展示了公司致力于维护强大安全协议，以满足数据安全、可用性、处理完整性、机密性和隐私的 SOC 2 合规性要求。![图片](https://ai-techpark.com/wp-content/uploads/Vast.jpg){ width=60% }

---

SOC 2 Type I 认证是对 Vast.ai 在六年以上全球可靠计算服务交付后的安全基础设施的重要验证。该认证确认公司已实施适当的控制措施，以保护客户数据并维护系统完整性。
“这一认证里程碑反映了我们对安全的坚定承诺，同时我们继续扩展平台，”Vast.ai 首席运营官 Travis Cannell 说道。“对于依赖我们基础设施进行关键业务操作的客户来说，这为他们提供了额外的保障，确保他们的数据按照行业标准得到保护。”
SOC 2 合规性由美国注册会计师协会（AICPA）制定，评估组织的控制措施是否符合特定的信任标准，包括安全性、可用性、处理完整性和数据机密性。Type I 报告提供这些控制措施在特定时刻的快照评估。
作为持续承诺合规和安全的一部分，Vast.ai 已经在为更严格的 SOC 2 Type II 审核做好准备，该审核将评估这些控制措施在较长一段时间内的有效性。公司还在追求其他合规认证，以更好地服务于拥有严格监管要求的组织。
有兴趣获取 Vast.ai SOC 2 报告的客户可以联系 compliance@vast.ai 或通过公司网站预约咨询。

