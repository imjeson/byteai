身为艺术家的Butterbro被指探求恶搞与歧视之间的微妙平衡，并帮助让种族性侮辱成为主流文化

Verknallt in einen Talahon 是一首关于移民的歌曲，其音乐、声音和艺术作品完全由人工智能生成，这在领先的音乐市场中或许是首次。

---
这首恶搞歌曲将现代歌词（其中许多基于关于移民的种族刻板印象）与60年代的德国流行歌曲结合在一起。这首歌在德国排名第48位，是全球第四大音乐市场。在发布不到一个月后，这首歌在Spotify上获得了350万次播放，位列该流媒体平台全球热门榜第3位。创作者Josua Waghubinger，艺名Butterbro，表示他通过将自己的歌词输入到Udio这一生成式人工智能工具中制作了这首歌的合唱部分，该工具可以从简单的文本提示中生成声音和乐器编曲。他在这首合唱赢得TikTok上积极反响后使用这一音乐工具添加了一节歌词。这位IT专业人士和业余音乐家在接受德国音乐制作播客《Die Klangküche（音乐厨房）》采访时表示：“我认为这首歌还有足够的创作自由，使其成为一个创意项目。”

这首歌不仅因使用了制作技术而在德国媒体引起关注，关键还在于其歌词内容。翻译为“爱上了一个Talahon”，这首歌引用了阿拉伯表达“taeal huna”的德国化版本，意思是“过来这里”，但如今在德国常用来描述年轻有移民背景的男子群体，通常伴有贬义色彩。歌词恶搞了1960年代歌曲中经典的“好姑娘爱上坏男孩”的故事情节，比如Shangri-Las的歌曲《Leader of the Pack》。AI生成的歌手所爱的对象穿着“路易斯皮带、古驰包和Air Max运动鞋”，“闻起来像整个香水店”。当她的情人生气时，她考虑到，“他像卷心酥一样甜”——想必是试图将他与土耳其文化联系起来。Waghubinger表示，他希望制作一首歌，既取笑了明显的男子气概行为“并带着一丝嘲讽”，但他补充说，他的主要动机是制作一首能在社交媒体上走红的歌曲。“这是我给自己设定的挑战，”他告诉《Die Klangküche》。

但保守派报纸《Die Welt》的文化编辑Marie-Luise Goldmann表示，该歌曲在恶搞与歧视之间的界限上攀登：“单单是将移民青年文化与德国流行音乐结合起来，将会使一些听众感到兴奋，也会触怒另一些听众。《Die Welt》认为，这位Talahon并没有掩盖他的过时性别观念，但是否Butterbro正在贬低、美化或者攻击这种观念还存在争议。”音乐杂志《Diffus》的作家Felicia Aghaye称，这首歌曲的流行“存在双重问题”，因为“talahon”在年轻德国人和奥地利人中已经被确立为对移民的侮辱。各种类似风格的AI生成歌曲在德国社交媒体上广泛流传，它们将上世纪60年代MOR德国流行音乐的甜美声音与粗俗的性化歌词相结合。

人工智能越来越多地被音乐制作人用来生成模仿知名歌手风格的声音。2023年，披头士乐队发布了一首名为《Now and Then》的曲目，其中使用了人工智能协助来推断约翰·列侬的声音。一首具有AI生成版本图帕克·史莱克的声音的曲目在加拿大说唱歌手Drake的Instagram上上传，在此后，据报道已故说唱歌手的律师威胁要起诉，该曲目被删除。

