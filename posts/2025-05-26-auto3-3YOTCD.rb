LogicGate，这家现代企业级治理、风险与合规（GRC）解决方案提供商，今日宣布其人工智能治理解决方案获得了备受推崇的ISACA创新解决方案奖。这一行业荣誉表彰LogicGate在AI治理解决方案方面的开创性工作，旨在促进安全创新，加速业务增长，使组织能够在责任范围内应对人工智能的复杂性。
ISACA创新解决方案奖表彰那些开发或利用技术来解决重大商业或社会挑战的个人或组织。评审标准包括新技术的创新使用或开发、促进ISACA核心领域、解决方案的影响和全球覆盖范围，以及提名质量。
“LogicGate的AI治理解决方案不仅仅是GRC领域的另一种工具——这对重塑组织对AI治理的看法至关重要，”LogicGate联合创始人兼首席执行官Matt Kunkel表示。“随着AI的快速发展和广泛应用，特别是生成性AI，每个行业都面临着治理这一技术及其各种应用的挑战。我们的方法是创建一个规范而灵活的AI治理框架，企业可以轻松采用并根据其独特的AI指南和要求进行调整。”
随着AI技术日益成为商业运营的核心，建立强大的AI治理的必要性变得愈加重要。OCEG关于“GRC中AI的使用”的研究发现，虽然82%的受访者同意或强烈同意他们必须采用生成性AI，但只有12%的人报告有文件化的AI治理计划。这凸显了LogicGate的AI治理解决方案旨在解决的一个关键缺口。
LogicGate的AI治理解决方案提供了一整套工作流程，以确保AI项目的透明度、问责制和与监管标准的一致性。该解决方案包括集中式用例管理、网络风险评估、政策管理、控制合规和第三方AI风险监督，所有这些都在一个易于使用的平台上。这一整体方法弥合了GRC与AI之间日益扩大的差距，使组织能够自信地利用AI的力量，同时防范意外后果。
“LogicGate的AI治理解决方案的一个关键差异在于它与整个治理、风险、合规和网络安全程序的整合，”LogicGate联合创始人和首席产品官Jon Siegler表示。“这种整合确保了AI治理过程无缝地纳入网络风险管理、第三方风险管理、政策管理和控制管理。”
具体来说，LogicGate的AI治理解决方案使组织能够：

利用现成的工作流程提交、审查和批准AI项目和模型。
识别AI风险并自动评估、量化和减轻活动。
强化AI风险容忍度并通过自动批准和确认标准化AI政策。
利用NIST AI风险管理框架的最佳实践和控制建议。
识别第三方服务中嵌入的AI技术和风险。

LogicGate与2025年全球成就奖、章奖、认证考试最高分和2025年名人堂新成员的其他获奖者在佛罗里达州奥兰多举行的ISACA颁奖庆典上获得认可。有关获奖者的完整名单，请访问www.isaca.org/awards。

