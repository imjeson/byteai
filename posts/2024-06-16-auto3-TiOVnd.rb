这一里程碑引入了由GenAI提供支持的“智能响应”，为利用HawkSearch的企业带来参与度和收入增长。

GenAI智能响应的Athena版本现已对公众普遍可用（GA）。这通过基于搜索查询的动态对话提高了电子商务客户的转化率，引发后续问题和建议，模拟个性化的在线销售助手。这提升了参与度，帮助用户快速找到所需的内容。

现在，当用户在时尚电子商务网站上搜索类似“度假用亚麻夏季连衣裙”这样的内容时，HawkSearch智能响应将在搜索结果上方总结并比较这些连衣裙所使用的面料。这为用户提供了在快速进行时尚和自信购买所需的一瞥信息，从而提高了电子商务网站的收入。

产品与战略执行副总裁约翰·默科特(John Murcott)表示：“HawkSearch的Athena版本引入了最先进的GenAI技术，以补充我们现有的RAG（检索增强生成）电子商务搜索，为每个独特查询提供准确、个性化的答案和摘要。”

首席执行官阿里·卡恩(Ari Kahn)说：“通过Athena版本，我们正在强化对人工智能创新的承诺，提供GenAI功能，旨在帮助客户增加其网站收入。”

