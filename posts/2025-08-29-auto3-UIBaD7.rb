Gaudio Lab, 领先的AI音频技术创新者，将于今年九月在阿姆斯特丹国际广播大会（IBC）上推出其最新解决方案Gaudio Studio Pro。![图片](https://ai-techpark.com/wp-content/uploads/Gaudio.jpg){ width=60% }

---
该平台旨在作为一个AI驱动的一站式解决方案，简化全球分发内容本地化的复杂过程。

### 简化全球内容交付：本地化速度提升90%以上
Gaudio Studio Pro整合了多项本地化任务，包括音效和音乐轨道分离、配音、音乐替换和提示单生成，形成一个单一工作流程。通过整合这些流程，该平台帮助广播公司和内容提供商将制作时间缩短90%以上，同时显著降低与传统方法相比的成本。

### 符合版权要求，兼顾创意
该平台的一大亮点是其自动检测背景音乐轨道并用来自超过110,000个专业作曲曲库的许可替代曲目进行替换的能力，同时保留原作的氛围。这在消除海外销售的最大障碍之一的同时，保留了原作品的创意意图。

### 复兴旧版内容
Gaudio Studio Pro建立在Gaudio Lab屡获殊荣的AI音频分离引擎基础上，可以从单个混合母带中分离对话、音乐和效果，消除了在重新编辑时需要分开音频的麻烦。这也使得复兴只有母带文件的旧版标题成为可能，允许其重新编辑并为新市场做好准备，而无需原始音轨。

### 与顶级声音工作室Wavelab共同开发
为了确保速度和高品质，该平台与Gaudio Lab的子公司、韩国顶级电影音响工作室Wavelab共同开发。Wavelab的专业团队还提供审核和手动编辑服务，增加了额外的信任，并提供符合最高行业标准的广播级成品。

“让内容自由跨越国界”
“IBC为我们提供了一个宝贵的机会，与在内容本地化和国际交付方面面临高成本和长期时间挑战的欧洲广播公司和分销商会面，”Gaudio Lab首席执行官Henney Oh表示。“我们的使命是让内容能够自由地跨越国界，通过值得信赖的世界级音频AI技术丰富全球媒体格局。”

### 经过全球认可的技术
Gaudio Lab的技术得到了全球的认可：其文件混音和母带制作能力于2022年被采纳为ANSI/CTA标准，其AI音频分离和音乐推荐系统在2023年和2024年获得CES创新奖。

Gaudio Lab将在2025年9月12日至15日在阿姆斯特丹RAI的未来科技区展出。

