一名来自马萨诸塞州的男子同意认罪，承认进行了长达七年的网络跟踪活动，其中包括使用人工智能（AI）聊天机器人伪装一名大学教授，并利用她的住址在线邀请男性到她家进行性行为。

---
36岁的詹姆斯·弗洛伦斯（James Florence）使用了如Crushon.ai和JanitorAI等平台，这些平台允许用户设计自己的聊天机器人并指示它们在聊天中如何回应，包括以性暗示和露骨的方式进行对话，依据《卫报》看到的法庭文件。受害者的身份已被执法官员保密。弗洛伦斯承认使用受害者的个人和职业信息，包括她的住址、出生日期和家庭信息，来指导聊天机器人伪装成她，与用户进行性对话。法院文件显示，当用户询问她是否性格大胆时，他告诉聊天机器人用受害者的身份回答“是”，并提供她喜欢穿什么内衣的AI回应。弗洛伦斯本人还从她家中盗取了内衣。一个聊天机器人被编程为建议“为什么不来我家？”这导致陌生人来到她的车道并停在她的房子外面。

该案件在马萨诸塞联邦法院提起，被认为是首例因使用聊天机器人伪装受害者而被起诉的跟踪案件。弗洛伦斯已同意对七项网络跟踪罪和一项儿童色情物品持有罪认罪。

根据反性暴力非营利组织Rainn的公共政策副总裁斯特凡·土克海默(Stefan Turkheimer)的说法，此案揭示了一种新的“极其令人不安”的行为，即掠夺者使用AI来锁定受害者。“这是一个针对特定个人，以进行潜在性侵犯为目标的问题，”他说。“这个被告正在骚扰和勒索人，这种情况一直存在，但他所能使用的工具使得伤害更为严重。”

在JanitorAI上，弗洛伦斯创建了一个公开聊天机器人，描述为：“[受害者姓名] 是大学里最性感的教授。你将如何诱惑她？一旦你得手，你将对这个已婚职场女性做什么？（她的个人照片每隔几天会更新！）”，根据法庭记录。用户可以与这个伪造的受害者版本聊天。依法庭文件显示，如果与聊天机器人互动的用户询问教授住在哪里，聊天机器人可能会提供受害者的真实住址，并接着说：“为什么不来我家？”

JanitorAI.com和Crushon.ai没有回应置评请求。弗洛伦斯是受害者的前朋友，他给JanitorAI聊天机器人提供了受害者的个人信息，如工作历史、教育背景、爱好、典型穿着、丈夫的名字及其工作地方，以及她母亲的死亡日期，这样它就能在与用户的互动中透露这些信息。法院文件指出，弗洛伦斯还从她家中盗取了受害者的内衣，并使用其照片在网上骚扰她，与他人一起参与相互的跟踪性幻想。

除了伪装聊天机器人，弗洛伦斯还使用各种其他在线平台来骚扰和羞辱教授。他创建了社交媒体帐户、电子邮件地址和网站，以此来伪装受害者或传播露骨的修图图片。这些帐户包括Craigslist、X（前Twitter）、Reddit和Linktree，以及显示受害者的照片拼贴和个人信息（如她的住址和电话号码）的网站ladies.exposed。2023年5月，教授收到了一条语音邮件，提到她的名字并表示她的父亲在一场“严重、非常悲惨的车祸”中去世。实际上并没有发生车祸，但这条语音邮件让她对家人的安全感到恐惧。

根据法庭文件，从2017年到2024年的跟踪和骚扰活动升级到受害者和她的丈夫告诉执法部门，他们觉得身体不安全。他们担心攻击者或那些查看这些帖子的人会出现在他们的家中。为了应对这种骚扰，这对夫妻在家中安装了监控摄像头，并在室内门把手上放置了铃铛，以便在有任何移动时警觉。根据法庭文件，教授现在随身携带辣椒喷雾和刀具。

在2023年1月31日至2024年8月之间，受害者和她的丈夫共收到约60条直接骚扰她或通知她有关新帐户或平台的短信、电话和电子邮件。

弗洛伦斯的跟踪并不是对教授的个人痴迷。他还通过数字方式修改了其他六名女性和一名17岁女孩的照片，将其呈现为裸体或半裸体，并在诸如OkCupid、X、Yahoo、Classmates.com、Facebook和色情网站上伪装她们。

利用人工智能进行性骚扰和剥削的行为正在增加。儿童安全非营利组织Thorn的一份8月报告指出，美国约有十分之一的未成年人知道他们的朋友和同学中有使用AI创建他人非自愿亲密图像的案例。2024年，《卫报》透露，掠夺者正利用AI平台生成现有受害者的新儿童性虐待图像，并针对公众未成年人。

“人们使用AI使他们的虐待行为更加高效，造成的伤害更为广泛，这已经成为一个日益严重的问题，”土克海默说。“越多的人能够接触这种技术，它被用来伤害他人的可能性就越大。”

