随着网络安全威胁的增加，Portnox正在全球范围内帮助企业实现无密码安全解决方案，提供适应复杂性规模的云原生安全解决方案。![图片](https://ai-techpark.com/wp-content/uploads/Portnox.jpg){ width=60% }

---
Portnox是一家领先的云原生零信任访问控制解决方案提供商，今天宣布已完成3750万美元的B轮融资。此次投资由Updata Partners主导，资金将用于加速产品创新、扩大市场覆盖面，并进一步帮助资源不足的安全团队获得可接触的企业级零信任安全。Portnox提供企业所需的关键可视性和控制，确保在日益无边界的高风险世界中，对每个设备和用户的访问进行严格控制。

“企业正在用云原生零信任解决方案替代本地网络访问控制（NAC），我们估计这是一个数十亿美元的市场。我为Portnox引领这一潮流感到自豪，我们拥有业界最佳的统一访问控制（UAC）平台，”Portnox首席执行官Denny LeCompte表示。

Updata Partners的普通合伙人和共同管理人Jon Seeber已加入Portnox董事会。Updata专注于对B2B软件和技术驱动企业的投资，已经投资了超过75家软件企业，并完成了超过55次退出。Seeber自2006年起就是Updata投资团队的成员。在加入Updata之前，Seeber曾在IBM全球服务的业务发展团队工作，负责管理IBM最大业务单元的并购、剥离、投资和合作关系。

“我们看到云原生安全转型的巨大机会，而Portnox凭借其独特的统一访问控制方法在市场上脱颖而出，既强大又易于采用。公司的领导团队对市场有深刻的理解，并拥有明确的执行记录，”Seeber说。

Portnox提供统一访问控制平台——Portnox Cloud，结合了无密码认证、授权、风险缓解和合规性强制执行，涵盖企业网络、应用程序和基础设施。Portnox Cloud专为拥有复杂IT环境的分布式组织量身定制，100%云原生，提供无与伦比的易用性以及所需的强大安全能力，以抵御日益复杂的攻击。

Portnox专注于简化安全并提供业界最佳的访问控制解决方案，这一策略在市场上得到了强烈共鸣，拥有近1000个全球客户，95%的客户保留率，以及Gartner Peer Insights上的4.6星评级。在过去一年中，Portnox获得了众多行业荣誉，包括来自SC Media、Cyber Defense Magazine、Cybersecurity Breakthrough Awards和SC Awards Europe的认可。

Updata Partners的投资在Portnox于2022年由Elsewhere Partners主导的2200万美元A轮融资基础上，进一步提升了公司总投资达到5950万美元。
