这份报告呼吁医生在体检时询问屏幕使用时间。随着西班牙推进限制儿童接触技术的法律草案，这个由50名成员组成的专家委员会呼吁将未成年人接触数字设备的时间限制在13岁之前，以缓解他们所认为的公共健康问题。专家们的近250页报告被《国家报》披露，建议三岁以下的儿童不应接触数字设备，而六岁以下的儿童应仅在特殊情况下使用。对于六到十二岁的儿童，应优先使用所谓的“傻瓜手机”，即不具备互联网访问功能，仅限于通话的手机，同时应鼓励参与体育等离线活动。

报告还呼吁政府考虑在西班牙销售的数字设备上添加警告标签，告知消费者社交媒体和数字设备相关的健康风险，以及访问不当内容对儿童发展的潜在影响。当访问某些应用或平台时，类似的警告应在屏幕上弹出，详细说明健康风险的主张及建议的最大使用时间。报告呼吁政府将手机成瘾列为公共健康关注问题，这一举措将便利于制定预防措施和早期检测系统。有关屏幕使用时间和问题行为的问题应纳入各年龄段的健康咨询，同时在青少年医疗检查中定期进行“抑郁、焦虑和科技使用的筛查”。

今年早些时候，该委员会的成立是响应西班牙首相佩德罗·桑切斯所称的“儿童网络色情消费的真实流行病”。桑切斯在一月表示，“数字数据非常醒目且令人担忧”，他说，“在12岁以下的年轻人中，四分之一曾接触过或目前接触网络色情。而在15岁以下的儿童中，几乎有一半的人有类似经历。”然而，报告最终以更广泛的视角看待问题，不仅关注儿童接触色情内容，还关注他们对数字设备的使用。该报告的发布正值全球对儿童接触技术的争论日益升温，全球学校因而开始收紧课堂上手机使用的规定。

在法国，一项政府委托的研究指出，儿童应在13岁之后才能使用智能手机，并禁止他们在18岁之前接触诸如TikTok和Instagram等常规社交媒体。根据统计数据显示，在西班牙，25%的儿童在10岁时就已拥有手机，而几乎一半的儿童在11岁时已经拥有。今年6月，社会党主导的联合政府提出了保护未成年人的法律草案，建议在智能手机上默认安装家长控制，并开展国家教育活动，帮助儿童和青少年熟悉社交媒体。该草案还设定了新的数据要求，如果强制执行，将把开设社交媒体账户的最低年龄从14岁提高到16岁，同时为教师和医疗人员提供培训，以便识别那些可能面临手机成瘾的儿童。

该草案目前处于公众咨询阶段，预计将根据这份报告进行进一步细化。这份报告在整合来自数字转型欧洲协会、儿科医生和精神科医生等组织的意见后完成。报告还强调了更广泛环境在限制儿童接触方面的作用，呼吁为家庭提供培训项目，让专家回答关于儿童如何安全上网、如何限制接触和暴露的问题，并鼓励学校移除任何基于即时满足的教育应用。

