警方已展开调查，因一辆特斯拉在悉尼撞上三辆车后，从一个高架平台坠落到地面，这引发了人们对电动车制造商自动化功能的质疑。

---
周六上午10点前，紧急服务部门接到位于家庭购物中心(DFO)的报警电话，称一辆特斯拉轿车在其屋顶停车场发生事故。来自澳大利亚第九频道的画面显示，这辆特斯拉Model S首先撞上两辆车，随后以高速驶向另一辆车，并轻微碰撞。随后，这辆车从停车场的边缘驶出，坠入下方的装卸区。

特斯拉的59岁男性司机和68岁女性乘客在现场接受了急救，随后被送往医院治疗。事故中没有其他人受伤。目前尚不清楚从录像中看，司机在事件发生时是否掌控了车辆。早些时候，美国当局对特斯拉的自动驾驶功能进行调查，发现其涉及13起致命事故。卫报澳大利亚联系了特斯拉以获取评论。新南威尔士州警方在一份声明中表示，来自奥本警察指挥部的警员“在场发现一辆轿车与三辆车辆发生了碰撞，然后驶下一个高架平台，落入了一个装卸区”。“59岁男性司机和68岁女性乘客在现场接受了新南威尔士州救护车护理人员的胸痛治疗，然后被送往皇家阿尔弗雷德医院进行评估，”警方表示。“目前对于事件发生的情况的调查仍在进行之中，”警方声明称。
