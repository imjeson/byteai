Super X AI Technology Limited（NASDAQ: SUPX）（“公司”或“SuperX”）今天宣布正式推出其最新的一体化多模型服务器（“MMS”）。作为SuperX支持多个模型动态协作的首款企业级AI基础设施，该MMS以即开即用、多模型融合、深度集成应用场景为核心，向企业提供安全、高效的全栈AI解决方案，具有适用于不同规模企业的定制规格，标志着大型模型应用向多模型智能代理协作的新纪元的演进。这是SuperX为大型企业AI客户提供的集成解决方案，继2025年7月30日SuperX的XN9160-B200 AI服务器首发后，进一步扩展了SuperX的企业AI基础设施产品组合。
为了帮助企业客户在新一波生成AI浪潮中获得决定性的竞争优势，这款一体化多模型服务器预配置了高性能的大型语言模型（LLMs），包括OpenAI新发布的顶级开源模型GPT-OSS-120B和GPT-OSS-20B。基准测试结果显示，GPT-OSS-120B在某些关键测试（如大规模多任务语言理解（MMLU）和美国邀请数学考试（AIME））中，甚至超越了多个领先的闭源模型，依据OpenAI于2025年8月5日的新闻稿。这意味着SuperX的客户将能够以更高的性价比获得世界级的AI推理和知识处理能力。
对于SuperX的客户而言，这不仅是技术上的领先——更是一个效率革命。用户无需花费数月时间进行复杂的模型集成、硬件适配和性能调优。SuperX提供了一种即开即用、安全可靠、全面优化的企业级生成AI平台，让客户能够立即部署先进的生成AI应用和代理，以快速响应市场变化。这种“交钥匙”解决方案是SuperX的核心差异化因素，将设备从单纯的硬件产品转变为能够推动业务创新和智能决策的完整企业级生成AI解决方案。现在访问SuperX网站尝试最新的AI模型： https://cloud.superx.sg/chat

突破技术壁垒：多模型融合架构启用行业创新
一体化多模型服务器支持多种模型的预配置、调用、加速、管理和迭代——包括推理模型、通用模型、多模型、语音合成/识别模型、嵌入模型、重排序模型和文本-图像模型。这实现了深度融合于最终应用场景，在四个关键方面取得了功能突破：

认知飞跃：由多个智能代理协作构建的各种模型支持更复杂的业务应用场景。例如，可以直接从文本描述中定位视频剪辑，准确识别人物、动作等。
知识赋能：内置的门户助手和知识库系统支持超过60个预配置的场景化代理（例如，官方文件草拟、法律咨询、政策比较），创建一种“你想的就是你得到的”闭环业务流程。
云协调的模型缓存：通过本地与云端模型库的连接，用户可以即时体验世界最新的模型，无需等待。
一体化集成：一体化多模型服务器提供了一个统一的平台，从芯片到模型服务，处理复杂技术架构的集成和优化，因此用户无需专注于模型下方的技术栈。

企业就绪：应对企业AI部署挑战的交钥匙解决方案
MMS旨在满足现实世界企业的需求，解决AI采用中的关键障碍——数据隐私、部署复杂性和运营可扩展性：

AI数据安全（用于AI服务器）：基于NVIDIA Confidential Computing技术，采用NVIDIA Blackwell平台，MMS具有受信执行环境（TEE）。除了保护AI知识产权（IP）并支持安全、机密的AI训练、推理和联邦学习外，企业还可以保护其最大模型，同时确保性能。
成本效益的部署：通过全栈硬件-软件集成，该服务器可以在几分钟内部署，无需额外的基础设施或IT资源。它为中小型企业优化，同时通过集群可扩展到大型企业，提供高性能替代传统的MaaS API服务。
工作流效率：MMS附带预配置模板和操作指南，允许业务用户通过无代码或低代码界面快速构建智能代理，适用于广泛的企业应用场景。

