看来，Meta 下一个大模型发展的重点之一就是 Agent 了！

近日的一系列消息表明，Meta 正在快速组建一支 Agent 人才团队。

先是两个月前，原苹果首任 AI 总监 Russ Salakhutdinov 宣布加入 Meta 生成式 AI 团队，专注多模态大模型和 Agent。

