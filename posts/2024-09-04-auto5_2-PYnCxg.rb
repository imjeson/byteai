我们也许久没有听到 Russ Salakhutdinov 的名字了。Ruslan Salakhutdinov 于 2009 年从多伦多大学获得机器学习（计算机科学）博士学位，师从 Geoffrey Hinton，之后在 MIT 人工智能实验室度过了两年博士后生涯，后来加入了多伦多大学计算机科学与统计部做助理教授。

2016 年 1 月，CMU 宣布邀请 Russ Salakhutdinov 作为机器学习部门的副教授。在 CMU 任教期间，Russ Salakhutdinov 曾培养了杨植麟等多名博士生。

2016 年 10 月，Russ Salakhutdinov 从 CMU 加入苹果，成为苹果首任 AI 总监。

这也是苹果在人工智能领域最早的一些动作。在 Russ Salakhutdinov 加入之前，虽然苹果是第一家将智能助理整合进其操作系统的主流公司，却并没有专门的组织和团队进行人工智能的研究。

自离开苹果之后，这次加入 Meta 是他时隔四年重返业界。根据领英信息，Russ Salakhutdinov 在 Meta 的研究重点会放在多模态 LLM 和 AI Agent 上。

