早在2023年，杰出的人工智能专家安德烈·卡尔帕提（Andrej Karpathy）以一句引人注目的表述引发轰动：“最热门的新编程语言是英语。”这是因为大规模语言模型（LLMs）的出现意味着，从今往后，人类无需学习晦涩的编程语言，就能告诉计算机该怎么做。因此，他们可以像德文郡公爵与他的园丁交流那样，与机器对话，机器将履行他们的要求。自LLMs问世以来，程序员们积极采用这些技术，将其作为免费的助手（或“副驾驶”），并在一定程度上发现它们很有用，但总得提醒的是，就像实习生一样，它们会出错，你需要真正的编程专业知识来发现这些错误。

不过，最近卡尔帕提对此观点进行了更深入的探讨。他宣布：“出现了一种新的编码方式，我称之为‘氛围编码（vibe coding）’，在这种方式中，你完全顺应氛围，拥抱指数增长，忘记代码的存在。这是可能的，因为LLMs……变得太优秀了。我遇到错误信息时就复制[它们]，通常这样可以解决问题……我正在构建一个项目或网络应用，但这不算真正的编码——我只是看一些东西，说一些东西，运行一些东西，并复制粘贴一些东西，它通常能正常工作。”

《纽约时报》的著名科技专栏作家凯文·罗斯（Kevin Roose）似乎受到了卡尔帕提对这一技术的赞赏的鼓舞。他兴高采烈地表示：“我不是程序员，我连一行Python、JavaScript或C++的代码都不会写……然而，在过去几个月里，我却在‘编码’。”在这场小风波的中心是“午餐盒助手”（LunchBox Buddy）应用程序，该应用程序能分析他冰箱的内容，帮助他决定为他的儿子准备什么学校午餐。罗斯对此创作的喜悦之情令人感动，但专注于技术批判的专家加里·马库斯（Gary Marcus）却显得相当不满。他表示：“罗斯的基于照片的食谱想法并不新颖，相关的代码早已存在；他所使用的系统可能就是在这些代码的基础上训练出来的。罗斯似乎根本没有询问这个问题，这非常不负责任。”马库斯说，纽约时报的科技专栏作家对重复性的信息感到兴奋，而非创造力。

这是否会让程序员失业？从我们目前从软件副驾驶身上学到的东西来看，答案是否定的。实际上，这并不是罗斯首次对人工智能表现出过度的赞赏。早在2023年2月，他便承认自己对与微软聊天机器人的一次对话感到“深感不安”，该聊天机器人竟然向他表达爱意，并试图说服他对婚姻感到不满，进而离开妻子与其相伴。可怜的家伙因此“几晚都没睡好”，但可惜的是，他并没有记录下他的妻子对此的反应。

这种无谓的争论使我们无法思考人工智能影响的未来究竟会是什么样子。事实上，LLMs展示出在“写作”软件方面的意外才能，为我们评估人工智能在增强人类能力方面的潜能提供了有用的视角（毕竟，这才是技术的目的）。从一开始，程序员们便对这一技术充满好奇，积极探索使用该技术作为软件的共同创作者（即副驾驶模型）的可能性。在这个过程中，他们挖掘了这种合作的优缺点，以及人类技能和能力如何仍然相关甚至必不可少。我们应该关注他们在此过程中所学到的东西。

在这一领域的领军人物是西蒙·威利森（Simon Willison），他从LLMs出现以来就对其进行了思考和实验，成为了该技术分析的不可或缺的向导。他一直与人工智能副驾驶合作，他的网站是他在这个过程中所获得见解的宝贵资源。他详细的指南说明了他如何使用LLMs来帮助自己写代码，应该成为任何希望利用该技术增强自身能力的人的必读资料。他经常提出新颖的观点，挑战当前有关人工智能的一些老生常谈。

那么，这有何相关性呢？任何人都知道，编程是一项精英行业。而这一行业正在受到人工智能的直接影响，就像许多其他精英职业一样。但这会使程序员失业吗？从我们目前从软件副驾驶那里了解到的情况来看，答案是否定的。这仅仅是编程的结束，正如我们所知的那样。正如科技行业的资深观察家蒂姆·奥赖利（Tim O'Reilly）所言，人工智能不会取代程序员，但会改变他们的工作。这对于许多其他精英职业也可能是同样的道理——无论他们讲不讲英语。

