        Autonomous Inc.，一家致力于将未来工作设计与工程化交到创新者手中的公司，今天宣布推出Brainy，这是一款旨在加速深度学习和机器学习工作流的革命性工作站。![图片](https://ai-techpark.com/wp-content/uploads/Autonomous.jpg){ width=60% }

