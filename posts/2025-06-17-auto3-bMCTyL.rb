        Cloudera，唯一真正的混合数据、分析和人工智能平台，将在全球举办年度数据和人工智能会议系列。Cloudera的EVOLVE25会议将跨越四大洲，汇聚行业先锋、客户和合作伙伴，探讨统一混合数据平台如何推动人工智能驱动的创新，并改变各行业的客户体验。
        
        Cloudera通过结合可信数据和人工智能分析的力量，帮助企业充分利用人工智能，以推动商业价值。通过主题演讲、行业会话、互动式实验室和“与专家会面”会议，参与者将探索如何利用人工智能进行创新转型。作为亚马逊网络服务公司（AWS）的赞助商，此次活动还将包括专注于以下主题的分会：

        - 企业人工智能——组织如何扩展人工智能以转变运营、改善决策、驱动创新，并探索最新的生产力、协作、安全性和治理工具。
        - 混合云——优化混合和多云环境的策略，以支持人工智能工作负载，同时保持安全性、合规性和成本效率。
        - 现代数据架构——下一代数据架构如何支持人工智能应用和用例的独特需求。

        此外，还将设置展览区，展示一些行业内最具突破性的可扩展和安全的数据管理解决方案，使关键业务的人工智能应用和实时分析能够大规模运行。同时，Tomorrow的首席执行官迈克·沃尔什（Mike Walsh）将发表演讲，探讨颠覆性技术与商业领导力之间的交汇点，将深度技术转化为可行建议，供领导者参考。

        Cloudera的数据影响奖将在EVOLVE25上揭晓。这些著名奖项旨在表彰在各行业和全球范围内，对其组织产生重大商业影响的优秀数据驱动项目。

        在此了解EVOLVE25活动的更多信息。日程如下：

        - 新加坡，8月7日
        - 圣保罗，9月3日
        - 纽约，9月25日
        - 伦敦，10月9日
        - 华盛顿D.C.，10月22日
        - 迪拜，11月20日

        Cloudera首席执行官查尔斯·桑斯伯里（Charles Sansbury）表示：“随着人工智能和数据分析在企业中成为不可否认的必要性，展示成功用例并提供实践培训以理解技术的全面好处显得尤为重要。”他补充道：“EVOLVE25，作为全球最全面的数据和人工智能活动系列之一，为客户、合作伙伴和创新领导者提供了一个独特的机会，进行合作和交流，展望数据管理、分析和人工智能的未来。”

