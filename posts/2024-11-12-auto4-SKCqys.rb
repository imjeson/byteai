<p>字节豆包大模型又又又上新了！</p>
<p>11 月 11 日，字节跳动豆包大模型团队推出了最新图像编辑模型 SeedEdit，主打一句话轻松 P 图。</p>
<p>它是**国内首个产品化的通用图像编辑模型**，无需描边涂抹，仅使用简单的自然语言，就能换背景、转风格，或者在指定区域进行元素的增删和替换。

---
</p>
<p>比如，输入 Prompt「把老婆饼换成驴打滚」，SeedEdit 立马锁定修改目标，完成美食替换：</p>
<p>*（Prompt：把老婆饼换成驴打滚）*</p>
<p>要知道，「老婆饼」、「驴打滚」等中国美食曾「骗」倒一众大模型，但 SeedEdit 能精准理解复杂指令，不仅看得懂中英文提示词，还对成语和专有名词「门儿清」。</p>
