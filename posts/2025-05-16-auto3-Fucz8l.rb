自主科技公司（Autonomous Inc.）致力于将未来的工作设计与创新者的双手相结合，今天宣布推出Brainy，这是一款旨在加速深度学习和机器学习工作流程的革命性工作站。![图片](https://ai-techpark.com/wp-content/uploads/Autonomous.jpg){ width=60% }

---
Brainy将前所未有的AI性能带到桌面，赋予研究人员、开发人员和AI初创公司突破人工智能边界的能力。

“Brainy不仅仅是一台机器，它是创新的合作伙伴，”自主科技公司的产品经理Brody Slade表示。“我们将每秒千万亿次的AI运算能力置于触手可及之处，消除了与基于云解决方案相关的瓶颈和成本，真正改变了AI开发的方式。它使您能够不仅思考——而是与您的机器共同思考。”

释放前所未有的力量和可扩展性  
Brainy的设计旨在处理最苛刻的AI工作负载，由NVIDIA RTX 4090 GPU提供动力。该系统提供每秒超过千万亿次的AI性能，可以从2个GPU无缝扩展到8个。这使得用户能够：

- 原型设计、微调和部署多达700亿参数的大型AI模型。
- 从桌面获得高性能计算（HPC）级别的结果。
- 为训练和推理优化，支持自主差分的前向和反向传递。Brainy在微调大型语言模型（LLM）、计算机视觉任务和各种深度学习模型方面表现卓越。

Brainy的优势：速度、效率和成本效益  
自主科技公司开发Brainy是为了应对昂贵的基于云的GPU解决方案的限制。虽然云GPU提供灵活性，但按需支付的模式对于持续的大型项目可能变得不可承受。Brainy凭借几项主要优势提供了一个引人注目的替代方案：

- 每秒千万亿次的性能近在咫尺：Brainy提供专用的本地AI算力，消除了云计算的延迟和约束。
- 增强的数据隐私和安全性：通过本地处理数据，Brainy确保敏感信息仍然在您组织的控制之内，消除了与云存储相关的数据泄露和合规问题的风险。
- 成本节省：通过拥有硬件，用户可以实现与云GPU租赁相比较显著的成本节省。自主科技公司估计，Brainy可以在第一年为用户节省数千美元，相较于RunPod等服务。
- 不间断的工作流程：Brainy消除了常见的基于云的挫折，例如排队时间、临时实例关机和互联网延迟，确保纯粹、不间断的AI算力。
- 可扩展性和灵活性：用户可以从2个RTX 4090的配置开始，根据需求逐步扩展到8个GPU。当项目准备进行更广泛的部署时，可以以零重新配置直接扩展到云或数据中心。

加速AI创新：Brainy赋能AI初创公司、开发人员和研究人员，提供与NVIDIA的CUDA、cuDNN和TensorRT无缝集成的行业标准AI框架，如ONNX、PyTorch和TensorFlow。这使得AI、ML和科学计算任务的快速开发成为可能。

作为NVIDIA Inception Program的自豪成员，自主科技公司特别在Brainy的发展过程中获得了来自NVIDIA的大量宝贵资源和专业知识，支持公司在市场推出及其后各个阶段。

通过该计划，自主科技获得了免费参加NVIDIA自学课程的学分和对讲师主导的研讨会的折扣。这些培训机会使开发人员能够在生成型AI、图形和仿真等关键领域提升技能，这些领域对于优化Brainy的性能至关重要。此外，NVIDIA Inception Program还为自主科技公司提供独家优惠，使我们能够为客户提供竞争力价格的Brainy。

创业公司通过Brainy的AI应用蓬勃发展  
Brainy通过完全的数据所有权为您的AI数据训练创意提供强大的推动力。它加速了金融预测模型、医疗患者数据分析、教育个性化学习、物流路线优化等。借助顶级硬件和框架，这款超级计算机在各行各业提供高效安全的AI解决方案，直接从桌面发出。

可用性  
Brainy现已可供订购，使企业级AI性能对初创公司和创新者触手可及。有关详细规格、配置和定价，请访问 [https://www.autonomous.ai/robots/brainy](https://www.autonomous.ai/robots/brainy)。

