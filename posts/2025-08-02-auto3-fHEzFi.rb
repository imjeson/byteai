title: 'Datasite任命Matthew Steinhilber为总法律顾问'
date: 2025-08-03
author: ByteAILab

---

Datasite®，全球领先的人工智能驱动的M&A、投资和战略项目工作流程协作与自动化解决方案提供商，今天宣布任命Matthew Steinhilber为总法律顾问，以进一步增强Datasite在技术、法律和商业战略日益交织的快速发展环境中的地位。![图片](https://ai-techpark.com/wp-content/uploads/Datasite-Appoints.jpg){ width=60% }

---

在这一角色中，Matt将负责Datasite的全球法律事务，包括商业、诉讼、知识产权和公司事务，直接向Datasite总裁兼首席执行官Rusty Wiley汇报。
“我们非常高兴地欢迎Matt加入我们的领导团队，”Wiley说道。“他带来了丰富的法律专业知识和经过验证的战略顾问经验，这对我们在日益复杂的监管环境中继续发展至关重要。我相信他的指导将帮助我们继续保持最高的诚信、透明度和问责制标准。”
Matt拥有超过20年的法律专业知识和领导经验。他曾在多个组织中担任领导职务，包括ACA Group的总法律顾问，最近担任Blueflame AI™的总法律顾问，该公司是为金融服务公司提供代理人工智能解决方案的领先供应商，而Datasite最近收购了该公司。
Matt获得了穆罕默德学院的环境科学学士学位和马里兰大学法学院的法学博士学位。

