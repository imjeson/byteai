然而，热成像技术的广泛应用长期以来受到高成本、图像不清晰和集成复杂的制约。作为全球红外热成像领域的领导者，Raytron正在通过其下一代热成像核心改变这一格局，旨在提供全天候精确监控。

Raytron的红外探测器：更大的范围，更清晰的图像，更高的灵敏度  
在全球首个 6μm 和 8μm 无冷却红外探测器及内部晶圆级制造技术的突破之上，Raytron 量产的热核心分辨率从 256×192 到 1280×1024 像素，像素间距从 35μm 到 8μm。至关重要的是，Raytron 的人工智能图像处理技术，通过其专有的 Matrix IV 超分辨率算法，将 640×512 传感器输出提升到接近1920x1080的清晰度。

Raytron的热成像核心为哪些行业带来了益处  
Raytron 的高灵敏度无冷却红外探测器和热核心在各种行业中提供强大的性能：

- 工业预测性维护：检测电力变电站过热，防止能源储存系统中的电池热失控，以及及时检测无形气体泄漏。
- 汽车夜视：通过与吉利、比亚迪和广汽等 Tier-1 汽车制造商的合作，提供可靠的热夜视和行人检测功能，服务于高级驾驶辅助系统 (ADAS)。
- 安全与周界监控：通过长距离热成像相机提供关键基础设施的入侵检测，不受完全黑暗、眩光或烟雾的影响。
- 户外夜视：为野生动物保护、户外冒险和野火预防提供不间断的黑暗监控，保护生态系统。

Raytron的热成像摄像模块为OEM提供的优势  
Raytron 的超级晶圆级封装 (SWLP) 技术具有双层封装，提供强大的防尘保护，并允许在标准环境中组装。这减少了OEM对昂贵洁净室的依赖，降低了整体系统集成成本。此外，其微型热模块系列结合了紧凑大小和低功耗，非常适合无人机、手持设备和智能手机。此外，配备多种硬件接口（DVP、VOSPI、MIPI、USB）和丰富的软件开发工具包 (SDK)，OEM能够加快二次开发和算法优化。

