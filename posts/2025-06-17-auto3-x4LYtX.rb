---

Cytracom, 一家为管理服务提供商（MSP）提供安全访问服务边缘（SASE）和统一通信即服务（UCaaS）解决方案的领先企业，今天宣布收购Tentacle，一个现代平台，帮助组织管理网络安全评估，监控第三方风险，并使治理、风险和合规（GRC）程序的运作具备可操作性。![图片](https://ai-techpark.com/wp-content/uploads/Cytracom.jpg){ width=60% }

---
这次收购标志着Cytracom在安全风险管理（SRM）战略上的重大进展，进一步与公司的使命相一致，即帮助MSP超越传统IT服务，拥抱更具战略性的顾问角色，引导客户应对不断发展的风险管理领域。随着监管机构的审查加剧和网络保险公司的压力增加，MSP正被要求担任真正的风险顾问，帮助客户满足合规要求并改善整体安全态势。将Tentacle整合到Cytracom平台中，增强了应对这一需求的能力，提供结构化、可重复和协作的工具，使MSP更容易提供增值的风险和合规服务。

Cytracom通过收购Telivy进入SRM领域，引入了使MSP能够自动化风险评估、提供定期合规文档并推动咨询合作的能力。Tentacle在此基础上进一步发展，允许合作伙伴集中安全程序数据，实时与客户和第三方协作，并将内部实践与HIPAA、PCI和FTC保障等行业框架进行映射。该平台不仅减少了持续评估的行政负担，还提供了从一次性审查过渡到持续程序管理所需的结构。

“MSP越来越被要求管理风险与技术同等重要，”Cytracom首席执行官Zane Conkle说道。“这种转变带来了挑战与机遇。通过收购Tentacle，我们为合作伙伴提供了他们需要的平台，使他们能够自信地进入这一不断发展的角色——帮助客户导航合规，跟踪进展，并建立能够经受外部审查的程序，同时使MSP能够清晰地展示其服务的持续价值。”

合规服务需求的上升是MSP渠道中最紧迫的趋势之一。中小型客户现在面临着由监管框架、行业特定要求和网络保险审计所驱动的要求。Tentacle能够组织和共享评估数据，管理第三方关系，确保审计准备，使其成为Cytracom简化和扩展SRM和GRC管理的愿景中的关键组成部分。

“这是在构建一个完整平台的又一步，赋予MSP能力以安全、可靠和在不断发展的GRC框架下连接现代劳动力，”首席商业官John Tippett说道。“无论是统一通信、安全访问还是风险管理，我们的重点始终是使这些能力对每天交付这些信息的合作伙伴来说变得可访问、直观且具有影响力。”

通过将Tentacle整合到更广泛的基础设施软件平台中，Cytracom继续加大对MSP在风险和合规驱动世界中成功所需工具的投资。“我们一直在大量投资Telivy作为循环网络安全评估和合规工作流程的平台，”Cytracom首席产品官Rob McDonald补充道。“引入Tentacle的成熟技术，使我们能够将其强大的架构与现有的开发战略相结合，从而显著加速我们可以推向市场的产品。这大大推进了我们的使命，旨在为MSP提供可扩展、差异化的安全和合规工具。”

凭借跨越VoIP、SASE和SRM的解决方案，Cytracom继续为合作伙伴提供能够从单一统一平台提供安全、可扩展及合规的关键基础设施。

