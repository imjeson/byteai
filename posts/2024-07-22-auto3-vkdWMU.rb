Celonis宣布任命Grzegorz（Greg）Czajkowski为执行副总裁工程。![图片](https://ai-techpark.com/wp-content/uploads/2024/07/Celonis-A-960x540.jpg){ width=60% }

---
Czajkowski将监督公司全球工程团队，继续推进其领先市场的流程智能平台。在加入Celonis之前，Czajkowski在Snowflake担任了五年的工程与支持执行副总裁，负责监督全球工程团队，负责开发和交付Snowflake Data Cloud。

在他的新角色中，Czajkowski将利用其深厚的行业领先平台和基础设施解决方案建设经验，进一步增强Celonis的流程智能平台。他将领导工程和产品团队，引导他们在继续创新和增强平台基础的同时，确保无与伦比的规模和速度，并进一步利用人工智能获得更深入的流程见解和背景信息。

Czajkowski表示：“我非常高兴能加入团队。Celonis创建了一套高度复杂的流程智能平台，使其客户能够实现可观的价值并推动可持续的企业转型。我对帮助各行各业和地区的组织实现规模化流程优化着实着迷。这家公司的潜力确实非凡——每个人都能从Celonis的技术中受益。我期待进一步推动Celonis已经开发的全面流程优化和智能自动化，并且很高兴能与团队合作，充分实现其潜力。”

Celonis的联合创始人兼联合首席执行官Alex Rinke表示：“Greg在工程领导和创新方面的卓越经验使他成为我们的完美选择。我们非常高兴欢迎Greg加入我们的团队，并期待他在与Celonauts同事们一起推动产品开发，为我们的客户提供变革性解决方案方面带来的重要贡献。他的专业知识将在确保我们继续领先市场、为客户提供无与伦比价值方面发挥关键作用。”

Greg是分布式系统和组织扩展方面的专家。在Snowflake之前，他在Google工作了13年，担任工程副总裁，负责广泛的Google Cloud数据分析和机器学习产品，以及解决Google所有业务的数据分析需求的内部服务。他还在Sun Microsystems工作过六年，负责Java运行时环境和操作系统工作。

Greg在康奈尔大学获得计算机科学博士学位，UC伯克利分校MBA学位，并获得AGH Krakow（波兰）的本科学位。他目前拥有50多项专利。

