Exostellar，作为云资源优化领域的领先者，宣布正在与Arm合作，以推进在云中托管的芯片设计工作负载的效率和成本效益。
Exostellar与Arm和AWS紧密合作，通过利用Exostellar的基础设施优化器来优化Arm的云基础设施，该优化器利用人工智能自主将工作负载迁移到最佳实例类型，确保成本效率和运营稳定性。
“Exostellar与Arm的合作对云优化是一个颠覆性的变革，”Exostellar的首席执行官Tony Shakib说道。“我们基于人工智能的解决方案能够为复杂而关键的EDA工作负载优化云环境——这是其他人尚未实现的——这一合作使我们能够在前所未有的规模上消除资源低效，使Arm在云运营中最大化性能和成本效益。”
基础设施优化器是一个实例管理解决方案，根据选择的服务级别目标（SLO）自主分配和迁移工作负载。通过利用先进的AI算法，该平台持续预测运行工作负载的最佳位置，并实时动态迁移工作负载，无需暂停或中断。这种积极的实时迁移能力消除了停机时间和手动干预的需求，显著增强了运营效率和成本效益。
Exostellar目前支持x86处理器，但通过与Arm的合作，公司将其技术扩展到基于Arm®处理器，包括AWS Graviton和Microsoft Azure Cobalt。这一合作将使Arm充分利用这些处理器的功能，提升性能并进一步降低内部工作负载的成本。这一举措突显了Exostellar致力于推进技术并满足客户不断发展需求的承诺。
Exostellar在为客户提供显著成本节约和运营改进方面拥有令人信服的记录。客户即使在使用现货市场时，也可实现高达80%的云成本降低以及按需的可靠性，推动了云效率的新高度。
Arm的设计服务高级总监Tran Nguyen表示：“人工智能正在加速转型工作负载所需的能量需求，但也可以成为最大化性能并节省功耗的解决方案的一部分。”“通过在基于Arm的云处理器上利用Exostellar的先进优化能力，我们将在基于云的硅设计中实现新的效率，进一步降低复杂性和成本。”
