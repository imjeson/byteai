Permiso Security，领先的身份安全公司，保护人类、非人类和AI身份在混合和多云环境中的安全，今天宣布了一系列战略招聘和高管晋升，以应对对其行业领先的身份安全平台日益增长的需求。![图片](https://ai-techpark.com/wp-content/uploads/Permiso-Builds.jpg){ width=60% }

---

为了帮助更多客户采用Permiso独特的身份解决方案，Brian Crockford已加入公司担任首席收入官。Brian带来了超过20年的网络安全销售领导经验，将负责市场推广职能。在此之前，他担任Phosphorus Security的销售副总裁。

Permiso还最近宣布了两项重要的内部晋升：

Ian Ahl，领导Permiso著名的威胁研究团队P0 Labs，已晋升为首席技术官。在新职位上，Ian将监督Permiso平台的所有产品创新和技术战略。
Sanjeev Williams，自2023年起就是Permiso团队的基础成员，已晋升为产品高级副总裁。Sanjeev将领导产品管理和设计团队，继续提供帮助安全团队清点和保护其整个身份架构的创新解决方案。

“我们在Permiso建立的团队正是我们所解决的挑战的反映——复杂、快速变化且对现代企业安全至关重要，”Permiso的首席执行官兼联合创始人Paul Nguyen表示。“引入像Brian这样世界级的人才，同时提升像Ian和Sanjeev这样的证明领导者，为我们的下一个增长篇章奠定了基础。”

Permiso解决方案需求的不断增长，突显了身份网络安全攻击的增加，包括来自Scattered Spider的攻击，该组织一直瞄准领先的零售商、航空公司和保险公司，造成数百万美元的破坏。威胁行动者通过窃取身份或泄露凭证获得系统和记录的访问权限，并扰乱系统或索要赎金。随着AI技术的发展，使得冒充更难以探测，Permiso的解决方案将变得更加相关。

这些领导层的变动紧随其他几个关键人员的加入：

Austin Hall作为现场首席技术官加入Permiso，担任客户和潜在客户在应对复杂身份安全挑战时的技术顾问。
Garrett Stephens加入担任高级产品营销经理，专注于向各行业的安全领导者传达Permiso平台的价值。
Evan Davis以高级需求生成经理身份加入，运用数据驱动的方法推动品牌知名度和管道增长。

公司的增长势头进一步通过两个重要的产品里程碑得到证明：

在四月，Permiso推出了Permiso平台，这是一个用于跨云和SaaS环境的身份清单、姿态管理和身份威胁检测的统一解决方案。
在六月，Permiso宣布即将推出Permiso Discover，这是一个免费的身份清单和可见性工具，适用于人类和非人类身份。

Permiso还继续受到行业认可。就在上周，该公司在KPMG私人企业技术创新者比赛中被选为美国决赛选手，而在四月，Permiso被评选为SC Awards中最有前途的早期创业公司。
