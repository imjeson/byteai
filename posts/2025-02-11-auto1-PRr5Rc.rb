title: '英国需要在AI竞争中保护公民权利'
date: 2025-02-12
author: ByteAILab

---

随着全球领导者在巴黎的AI峰会上会晤，我们呼吁英国政府紧急制定和采取一项由公民主导的英国数字权利和原则宣言（AI竞争必须由“西方、自由、民主”国家领导，英国部长在2月10日表示）。

---
我们赞同科学、创新与技术大臣彼得·凯尔的观点：“政府确实可以影响技术的发展……我们需要利用这种影响力强化我们的民主原则、自由价值观和民主生活方式。”

我们欢迎政府通过近期的AI机会计划释放AI的经济潜力，但我们也需要确保作为英国公民在数字空间中的权利和自由得到保护。在过去两年中，全球各国已经采用了数字权利框架。我们不能落后于人。

我们要求政府：
- 邀请公众参与通过协商过程来塑造这一宣言。
- 建立国家数字权利网络，包括多元化的民间社会、法律专家和学者、科技公司及行业代表组织，以指导宣言的发展，并确保政策反映英国公民的价值观。
- 采纳这一宣言，并将数字权利纳入未来的政策决策中。

该宣言不会具有法律约束力，但将提供一致和基础性的原则来指导政策制定。通过发展这一宣言，英国可以在新兴的数字政策中建立信任，并促进负责任的创新，推动普惠增长。

