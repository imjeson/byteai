Bubblr Inc.，以道德网络人工智能（OTC: BBLR）为名，作为安全、企业级生成式人工智能的领导者，今日宣布在执行其2025战略路线图方面取得重大成就。![图片](https://ai-techpark.com/wp-content/uploads/Ethical-Web.jpg){ width=60% }

---
该公司通过亚马逊网络服务（AWS）市场成功引入了其首个AI Vault SaaS客户。

AI Vault通过AWS市场获得首个客户在最近的AWS市场首发后，道德网络人工智能的旗舰产品AI Vault已获得首个企业客户。AI Vault旨在解决企业在采用生成式人工智能时面临的关键挑战——特别是透明度、安全性和合规性。该平台拥有多个美国专利局（USPTO）专利，能够使组织全面控制人工智能的使用和数据保护。

道德网络人工智能的首席执行官兼创始人史蒂夫·莫里斯（Steve Morris）表示：“通过AWS市场获得的这一首个企业客户的成功验证了对值得信赖的企业级生成式人工智能的迫切需求。这也是我们作为AWS软件合作伙伴履行义务的重要一步。我们的目标是到本月底通过AWS市场获得五个企业客户，这一早期的进展使我们对这一目标充满信心。我们正在慢慢起步，但我们的目标是在三年内占领企业生成式人工智能市场的10%。”

以知识产权为主导的产品战略推动创新道德网络人工智能的产品理念建立在一个核心原则之上：只有在其核心能力可以申请专利的情况下，才会开发软件。每款产品都解决现有技术中的基本缺陷，并以设计为基础可维权。

莫里斯强调：“我们只开发可以合法保护的产品。我们的知识产权策略不仅仅是保护的一层，而是我们长期道路的引擎。我们邀请所有利益相关者探索我们的专利，这些专利定义并保护我们的竞争优势。”

专利和知识产权保护道德网络人工智能的产品战略建立在日益增长的可维权知识产权组合之上。所有产品开发均基于确保对每项产品的独特功能进行美国专利局（USPTO）专利保护。

专利组合亮点：

- 美国专利10,977,387 – 基于互联网的搜索机制该基础专利支持道德网络人工智能开源平台，包括16项可实施的权利主张，特别是匿名搜索和异步搜索结果。根据2022年8月Valuation Consulting（佛朗西斯科·达·克鲁兹·瓦伦塞洛斯博士）的独立评估，该专利的独立估价为47亿美元，基于全球技术公司收购该专利及相关软件的情境。随着专利期在2045年3月到期，这一估值预计会有所下降，反映出25年的法律保护期。

- 美国专利申请17/980298 – 通过动态生成的超文本链接的上下文封装增强了通过HTML输出的动态嵌入链接和多媒体的对话搜索。预计在2025年第二季度获得批准。

- 美国专利申请18/376,101 – 计算机实现的方法和系统使得通过外部API调用在生成AI提示中包括实时的、当前的数据，提高了答案的完整性，而不需重型计算需求。

- 美国专利申请19/055,968 – 在生成AI中处理敏感数据增强了AI Vault检测和编辑敏感、隐含或上下文术语的能力，确保企业数据的保护

这些专利为道德网络人工智能的核心技术形成了强大的法律护城河。每款产品都直接与特定的可实施权利主张关联，为抵御第三方模仿提供全面保护，即使仅复制部分功能。

AI Vault：为27%拒绝生成式人工智能的企业量身定制AI Vault专为当前由于隐私、安全和合规风险而拒绝生成式人工智能的27%企业而设计。这些企业包括大型金融机构和全球企业，在这些企业中，保护敏感信息是不可谈判的条件。

AI Vault提供：

- 一个安全、私密的生成式人工智能环境
- 完整的企业控制和透明度
- 通过专利编辑流程保护敏感数据
- 道德网络人工智能或第三方提供商完全无法访问客户数据

