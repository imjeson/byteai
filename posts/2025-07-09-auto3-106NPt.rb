新的首席财务官、首席产品和技术官、首席商业官及董事会顾问加入，以扩大IDnow在全球市场的信任驱动生态系统。![图片](https://ai-techpark.com/wp-content/uploads/IDnow-Announces.jpg){ width=60% }

---

IDnow，欧洲领先的身份验证平台提供商，今天宣布任命三位高级管理人员和一位新的董事会顾问，这标志着其全球扩张的重要一步。新管理团队的加入重申了IDnow将AI驱动的身份验证技术推向市场的使命——确立信任为数字世界中最有价值的资产。
新任命包括安德烈亚斯·毛罗德（Andreas Maueröder）担任首席财务官，丹尼尔·凯勒（Daniel Keller）担任首席产品和技术官，菲尔·艾伦（Phil Allen）担任首席商业官，以及卡西奥·桑帕约（Cassio Sampaio）担任董事会顾问。他们共同拥有在金融、网络安全、身份技术和业务转型方面推动增长的丰富经验，数十年的领导能力。这些任命反映了IDnow对构建可扩展、智能信任生态系统的更加专注，以支持企业在日益复杂的数字世界中导航。

执行领导团队以匹配大胆的雄心

安德烈亚斯·毛罗德（CFO）在国际金融和领导方面超过20年的经验。曾在安永（EY）、普华永道（PwC）和施尔豪博集团（Schörghuber Group）任职，2010年加入zooplus，逐步晋升为CFO，推动泛欧增长。安德烈亚斯在运营卓越、可持续价值创造以及在私募股权和公开市场环境中建立高绩效团队方面拥有丰富的经验。
丹尼尔·凯勒（CPTO）在产品、技术和业务转型交汇处拥有超过20年的经验。在微软、阿克塞尔·斯普林格（Axel Springer）、Scout24、Visable，以及最近的Onfido等公司担任过高级领导职务。作为将AI和机器学习应用于构建智能、可扩展平台的先驱，丹尼尔为加速创新和构建未来准备的组织带来了大胆的愿景。
菲尔·艾伦（CCO）在数字身份、网络安全和欺诈预防方面拥有超过25年的经验，曾在Transmit Security、CA Technologies和BMC Software等公司担任高级职务，并曾在Ping Identity担任VP，负责EMEA的增长。他在扩展以客户为中心的云原生解决方案方面拥有强大的业绩记录，加入IDnow以将信任转变为安全、可扩展增长的核心推动力。

IDnow首席执行官安德烈亚斯·博德泽克（Andreas Bodczek）评论道：“我们构建一个面向未来的数字身份生态系统的使命需要大胆而出色的领导能力。当我们超越身份验证，将智能信任驱动每一个客户旅程的步骤时，我很高兴欢迎安德烈亚斯、丹尼尔、菲尔和卡西奥。每个人都带来了独特的优势，帮助IDnow塑造数字身份的下一个篇章——一个以信任、安全和持续创新为基础的篇章。”

前Auth0（Okta）首席产品官加入董事会顾问

除了增强其执行团队外，IDnow还任命卡西奥·桑帕约（Cassio Sampaio）为董事会顾问。卡西奥是Auth0（现为Okta）的前首席产品官，曾在苹果和DigitalOcean担任领导职务，带来了在身份、产品创新和全球扩展SaaS平台方面的深厚专业知识。
“身份和信任的世界正在迅速发展，IDnow很好地定位于帮助企业导航这些变化，我非常期待与团队合作，支持未来的巨大机会，”卡西奥·桑帕约说。

数字优先世界的新愿景

今年早些时候，IDnow公布了其重新构想的愿景，旨在推动一个信任无缝嵌入每个数字交互的未来。该公司不仅仅停留于一次性的身份检查，而是帮助全球领先的企业在整个客户生命周期内建立、维护和增强信任。
随着欺诈变得越来越复杂和合规压力的加大，组织需要超越静态KYC检查的解决方案。IDnow在多个接触点提供主动、实时的风险检测，使身份成为战略资产，而不仅仅是法规的核对框。
自2014年成立以来，IDnow已成为欧洲数字身份领域的基石。其技术简化了风险管理，打破了监管复杂性，提供无缝、以用户为中心的体验。如今，随着公司将其更新的愿景变为现实，IDnow正在塑造下一代身份验证——帮助企业应对当今全球数字经济中复杂和不断变化的风险。

