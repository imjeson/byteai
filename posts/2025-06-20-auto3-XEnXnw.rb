Duro，一家领先的硬件工程PLM平台，今天重新推出其产品为Duro Design，这是一款从零开始重建的云端和AI原生PLM，旨在帮助制造商以三倍的速度推出产品。

预计到2032年，PLM市场将达到460亿美元，但仍被1980年代构建的传统工具所主导——这些工具僵化、难以采用且不适合现代工作流程。构建硬件仍然不必要地困难：数据孤岛、工具之间缺乏沟通，价值实现可能需要数月甚至数年。这些过时的系统通常需要昂贵的顾问，导致团队之间出现摩擦，而不是促进速度和协作。

当今的硬件工程师，在敏捷工作流程中经验丰富，期望PLM工具灵活、用户友好，并与他们日常使用的软件一样连接。人工智能不再是可有可无的选项，而是企业SaaS的必要条件，提升生产力并简化用户体验（UX）。正如GitHub重新定义了开发者构建软件的方式，Duro正在重新定义硬件的产品生命周期管理。

“当今公司需要超越数据存储的解决方案，以增强决策、简化供应链管理并加速创新，”Duro董事会成员、前GrabCAD副总裁Jon Stevenson说。“Duro正在提供一个可访问的AI原生PLM，帮助制造商优化产品生命周期的每个阶段——从概念到生产。”

### 硬件工程的新标准

Duro Design通过统一的数字线程连接设计、供应链和制造数据。该平台是基于客户和行业反馈构建的，取代了僵化的传统PLM，成为一个快速部署、易于配置且避免供应商锁定的云端和AI原生系统。

“控制数字线程是制造业的下一个竞争优势，”Duro首席执行官兼联合创始人Michael Corr说。“通过Duro Design，我们正在帮助硬件团队超越传统工具，通过在一个AI原生平台中统一设计、采购和制造。”

Duro Design的主要功能包括：

- 企业级功能与直观用户体验：Duro使用户能够快速找到所需内容，而无需点击数十个菜单。团队希望现在就能使用PLM，因为他们可以登录、注销并继续设计硬件。

- 内置AI用于验证、影响分析、元数据和采购：通过直接向工程师提供采购数据，提高准确性，加快设计进程并降低成本。

- 通过UI向导、YAML编辑器或GraphQL API的多级可配置性：无需顾问。用户可以使用点击或代码配置零件编号方案、变更管理工作流程、自定义字段和验证。

#### 其他开箱即用的功能

- Altium 365、SolidWorks、Onshape和NX的CAD插件
- 最佳实践的新产品介绍（NPI）工作流程
- 可扩展的变更订单和变更请求工作流程
- 与Netsuite、SAP、Ion、Epsilon3、Boltline等的集成

