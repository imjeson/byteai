Polyguard正在重新定义AI时代的反欺诈技术，为用户提供全面控制他们的个人和关键通信的能力。

AI驱动的欺诈正在激增，预计到2027年，美国金融行业的损失将达到400亿美元，高于2023年的123亿美元。深度假冒诈骗已经成为私人银行客户的主要担忧，因为传统的欺诈检测无法防止伪装攻击，从而导致账户接管、未授权交易和催付欺诈。

目前的深度假冒检测工具基本上是低效的，在攻击已经开始后才进行防守，存在15-30秒的延迟，主要关注音频而忽视视频和消息，并依赖AI与AI之间的对抗，而非主动验证身份。更糟糕的是，这些系统无意中训练生成对抗网络（GAN），使得更具说服力的深度假冒得以创造，推动了诈骗者占据主动的军备竞赛。

Polyguard采取预防措施，实时阻止潜在威胁，具备业内首个实时的出入境号码拦截、来电显示伪造保护、身份验证加密通话通道以及桌面和移动设备上的安全视频会议。通过与呼叫中心软件和Zoom等会议平台的集成，Polyguard确保全球安全的虚拟通信。其防篡改的来电身份验证系统防止了伪造，而3D人脸识别和硬件认证则有效抵御数字伪造和设备篡改。遵循隐私优先原则，所有通话均保持机密，敏感数据仍然在设备上存储。

“电话诈骗是400亿美元犯罪浪潮的核心，我们致力于从技术源头上制止这一现象，” Polyguard联合创始人兼首席执行官Joshua McKenty表示。“从银行家到高中生，每个人都面临着深度假冒驱动的诈骗，从语音伪造到视频伪装。仅仅检测并不足够——在你意识到信任的来电显示是伪造的时候，损害已经发生。我们在诈骗发生之前就将其阻断，实时拦截伪造电话，恢复通信信任。”

“和许多人一样，我认为我们的银行和经纪合作伙伴尚未充分认识到这一问题的规模——但我宁愿不让我的家族办公室账户成为他们代价高昂的警钟，” Tiny, Ltd. 首席执行官Andrew Wilkinson说道。Wilkinson的家族办公室是Polyguard的设计合作伙伴。

McKenty及其联合创始人Khadem Badiyan（首席技术官）在安全、人工智能和云基础设施方面拥有深厚的专业知识。McKenty曾任NASA首席云架构师，是OpenStack的共同创始人，曾担任Netscape 8.0的技术负责人，并在Pivotal担任现场CTO，后来成立的Piston被Cisco收购。Badiyan是一位人工智能创业公司创始人和数学家，对社交媒体图像的语义分析进行了开创性研究，并在p-adic分析和密码学领域进行了博士研究。

Polyguard现已向金融机构、家族办公室、专业顾问及希望增强金融安全和重新掌控通信的个人提供服务，弥补了新身份基础设施的“最后一公里”。

