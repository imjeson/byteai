Arm（纳斯达克股票代码：ARM）今天宣布任命新董事会成员Young Sohn。Young Sohn是HARMAN董事会主席，三星电子高级顾问，Cadence董事会成员以及Walden Catalyst的创始管理合伙人。Young在半导体行业拥有深厚的经验，涵盖业务拓展、投资战略和可持续发展，这将有助于Arm在继续推动关键市场增长并为人工智能时代提供基础计算平台的同时，拓展业务多样性。
Arm首席执行官雷内·哈斯（Rene Haas）表示：“Young带来的丰富经验将对Arm非常宝贵，特别是在我们继续实现业务多样化并努力应对人工智能时代面临的非常真实和复杂的计算挑战时。Young在一系列世界重要半导体公司担任领导职位的漫长职业生涯中取得了出色的业绩。我期待与他密切合作，共同定义计算的未来。”
Young Sohn表示：“曾在Arm董事会任职的经历，让我对Arm技术对依赖它的世界有多么重要有了独特的视角。Arm计算平台将成为未来创新的核心，特别是在人工智能方面，我期待成为推动计算未来的团队的一部分。”
此外，Tony Fadell将继续支持Arm，我们将正式任命他为公司的战略顾问，使我们能够直接受益于他丰富的行业知识和经验。

