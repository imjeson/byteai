今天，该公司宣布了一系列战略招聘和高管晋升，以响应对其行业领先的身份安全平台日益增长的需求。

为帮助更多客户采用Permiso独特的身份解决方案，Brian Crockford已加入公司担任首席营收官。他拥有二十多年网络安全销售领导经验，将负责市场推广职能。Brian最近担任Phosphorus Security的销售副总裁。

Permiso还宣布了两项重大内部晋升：

Ian Ahl，领先Permiso知名的威胁研究团队P0 Labs，已晋升为首席技术官。在新角色中，Ian将负责监督Permiso平台的所有产品创新和技术战略。

Sanjeev Williams，自2023年以来的Permiso核心成员，已晋升为产品高级副总裁。Sanjeev将领导产品管理和设计团队，继续提供突破性解决方案，帮助安全团队清点和保护整个身份生态。

“我们在Permiso构建的团队反映了我们所解决的挑战——复杂、快速变化且对现代企业安全至关重要，”Permiso的共同首席执行官兼共同创始人Paul Nguyen表示。“引入像Brian这样的世界级人才，同时提升像Ian和Sanjeev这样的经验丰富的领导者，为我们下一个增长阶段奠定了基础。”

Permiso解决方案的需求得到不断增加的身份网络安全攻击的支持，包括Scattered Spider的攻击，该组织持续针对主要零售商、航空公司和保险公司，造成数百万美元的损失。威胁参与者通过被盗身份或被泄露的凭据进入系统和记录，进而干扰系统或索要赎金。随着人工智能的应用，使得身份伪装更加困难可以被发现，Permiso的解决方案将变得更加相关。

这些领导班子的变动是其他关键人员加入团队后的进一步发展：

Austin Hall加入Permiso担任现场首席技术官，担任技术顾问，协助客户和潜在客户解决复杂的身份安全挑战。

Garrett Stephens担任高级产品营销经理，专注于向各行业的安全领导者传达Permiso平台的价值。

Evan Davis担任高级需求生成经理，带来数据驱动的方法以推动意识和管线增长。

该公司的势头进一步通过两项重大产品里程碑得以强调：

在4月，Permiso推出了Permiso平台，这是一种统一的身份清点、状态管理和身份威胁检测解决方案，适用于云和SaaS环境。

在6月，Permiso宣布即将推出Permiso Discover，这是一个免费的身份清点和可视化工具，适用于人类和非人类身份。

Permiso继续获得行业认可。就在上周，该公司被选为KPMG私人企业科技创新者比赛的美国决赛选手，并在4月获得了SC Awards的最具前景早期创业公司奖。

