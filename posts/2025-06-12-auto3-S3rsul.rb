title: 'Dolbey推出环境临床智能解决方案：Fusion Narrate ACI'
date: 2025-06-13
author: ByteAILab

---

Dolbey已宣布推出Fusion Narrate ACI™，这是Fusion Narrate®的一项扩展，受到nVoq™的支持，为医疗提供者提供环境文档记录。![图片](https://ai-techpark.com/wp-content/uploads/Dolbey-Launches.jpg){ width=60% }

---
Fusion Narrate ACI为每次患者就诊生成一份转录和草稿报告。

借助Fusion Narrate ACI，患者就诊可以通过符合HIPAA的移动Fusion Narrate Recorder智能手机应用程序或直接通过桌面工作站的Fusion Narrate进行安全记录。录音后，Fusion Narrate ACI从患者就诊转录中生成草稿报告，该报告可以根据部分进行缩短或扩展，然后直接插入到电子健康记录（EHR）中。

“我喜欢Fusion Narrate ACI。它减少了点击疲劳，让我能够回归更自然的患者接触，”早期采用Fusion Narrate ACI的Jamie Taweel博士表示。“它在不同的EMR服务之间无缝流动，因此您可以在不同的办公室或服务中使用它。现在我有了Fusion Narrate ACI，我无法想象再回到过去。”

“环境临床智能是文档记录和语音识别技术的一个重要里程碑，这将彻底改变提供者完成患者记录和与患者互动的方式。为提供者提供机会与他们的患者一对一会面，并在会谈结束时准备完整的报告和转录，这听起来像是一场梦，但现在已成为现实，”Dolbey的高级副总裁兼总经理John Dolbey表示。“Fusion Narrate ACI是一个真正尖端且强大的解决方案，其灵活性更来自于Fusion Narrate中已经存在的自定义、自动化和应用程序兼容性。”

Fusion Narrate ACI为医疗提供者提供了一种新的方式，以熟悉和直观的Fusion Narrate界面捕捉患者就诊的细节。

Dolbey的Fusion Narrate ACI现已上市。Fusion Narrate ACI提供按使用付费定价，无使用限制。了解更多信息，请访问dolbeyspeech.com/aci。

