Zebra Technologies Corporation（纳斯达克代码：ZBRA）是一家全球领先的数字化和自动化前线工作流程的公司，今天宣布已与Elo Touch Solutions, Inc.达成正式协议，以13亿美元的现金收购该公司。Elo是一家在零售、酒店、快速服务餐厅（QSR）、医疗保健和工业市场中，致力于客户互动、自助服务和加速自动化的解决方案创新者。

Zebra与Elo的产品组合互补且市场推广策略相似，双方将共同提供全面的产品组合，以满足客户不断变化的需求，并与领先的独立软件供应商（ISV）、支付解决方案提供商（PSP）、增值分销商（VAR）和分销商紧密合作。

扩展产品组合以加速各行业连接的前线体验
Zebra在前线工作人员的硬件、软件和服务的领导地位将通过Elo的一整套面向消费者的自助服务终端、边缘计算、支付和触摸屏解决方案得到增强，为前线提供更全面的体验。

“此次收购代表了我们加速连接前线的下一步，这是我们增长战略的关键要素，”Zebra Technologies首席执行官Bill Burns表示。“对自助服务和面向消费者的工作流程的更加关注将使我们的可寻址市场扩大约80亿美元，并创建出领导性的解决方案组合，数字化和自动化业务前线。我们期待着在完成收购后欢迎Elo团队加入Zebra，并共同追求新的增长机会。”

各行业客户越来越多地采用由自助服务终端和互动触摸显示器驱动的新解决方案。Elo提供一系列适合行业的解决方案，以现代化销售点（POS）、简化自助服务和支付体验、自动化厨房和工业工作流程，并优化生产和流程管理。这笔收购将在自助服务应用中加强Zebra的产品，并补充其最近推出的自助服务终端解决方案。

利用现代商店中的关键客户趋势
与Elo携手，Zebra将能够充分利用影响零售及其他领域的趋势。合并后的公司将使零售商和QSR能够在AI驱动的现代商店中提升消费者体验。Elo产品组合的计划增加将为Zebra的客户和合作伙伴提供更多选择，并随着时间的推移采用更全面的方法来应对他们新出现的用例。零售媒体网络的持续增长以及新AI代理在前线的部署是Zebra和Elo可以共同更成功地追求的新机会的例子。

根据Zebra的第17届年度全球购物者研究，78%的购物者表示，自助结账选项改善了他们的购物体验。此外，领先分析师指出，传统的POS技术正超越店内交易服务，支持由新数据流驱动的体验导向统一商业战略。Zebra和Elo在未来将处于一个越来越重要的位置，以推动POS和自助结账的转变。

通过互补解决方案和全球覆盖提高增长
Zebra的全球覆盖、广泛的服务能力和深厚的客户关系将加速Elo在新市场和地区的扩展。

“将Zebra市场领先的移动性、可视化和自动化解决方案与我们在面向消费者的工作流程中的专业知识结合，将为我们的客户和合作伙伴创造显著价值，”Elo首席执行官Craig Witsoe表示。“我们对加入Zebra并为其增长战略贡献力量的机会感到兴奋。”

交易细节
Zebra预计将用现金储备与其信贷设施融资的组合来资助13亿美元的购买价格。购买价格将受到常规关闭调整的影响。该交易需遵从常规关闭条件，包括监管审批，且预计将在2025年完成。

Elo的年销售额约为4亿美元，年销售增长率（在一个周期内）与Zebra相似（5-7%），以及与Zebra的EBITDA利润率特征类似。预计交易在关闭后立即对收益产生积极影响，并通过协同效应在第三年产生额外2500万美元的年度EBITDA。

摩根士丹利和公司作为Zebra财务顾问，柯克伦和艾利斯律师事务所作为其法律顾问。Moelis & Company LLC担任Elo的财务顾问，吉布森、邓和克鲁彻律师事务所担任其法律顾问。Crestview Partners自2018年以来一直是Elo的主要投资者。

