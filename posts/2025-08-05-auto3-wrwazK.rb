在今天的另一份新闻稿中，Zebra将公布其第二季度业绩。该公司将在8月5日上午8:30（东部时间）召开网络广播以讨论业绩、前景以及计划收购Elo的事宜。网络广播可通过Zebra的投资者关系网站访问，网址为investors.zebra.com。

