拥有近三十年经验的网络安全专家，将领导数字风险保护公司的市场策略，进一步加强其独特的分析师驱动方法
GroupSense，一家数字风险保护服务公司，今天宣布任命网络安全专家 Taylor Banks 为其全球销售与市场副总裁。![图片](https://ai-techpark.com/wp-content/uploads/GroupSense-1.jpg){ width=60% }

---
在这一角色中，Banks 将负责通过发展高绩效的销售和客户成功团队来推动收入增长，扩大公司通过各类思想领导活动的市场影响力，以及进一步支持公司独特的分析师驱动的威胁情报和数字风险保护方法。

“Taylor 是我们领导团队的受欢迎的新成员，他在网络安全和公共服务方面的丰富背景带来了独特的专业知识结合，”GroupSense 首席执行官 Kurtis Minder 说。“我相信他对营销、销售和产品开发的整合方法将帮助 GroupSense 获取更多市场份额，同时推动公司进入下一个增长阶段。”

Banks 是一位资深的网络安全领导者，拥有近三十年的实践、教学、指导和高管经验。他在业界广为人知，已培训超过 10,000 名网络安全专业人员，并在 DEF CON 和 ShmooCon 等世界著名会议上发表演讲。他还曾建议从初创公司到中央情报局（CIA）、美国国防部（DoD）、联邦调查局（FBI）、国家安全局（NSA）和联合国等多种机构。作为 DC404 的创始人之一，这个当地网络安全社区已运行多年，同时作为志愿消防员、急救员和搜索与救援团队成员，Taylor 将深厚的技术专长与公共服务的承诺结合在一起。

“GroupSense 的务实、分析师驱动的威胁情报方法，通过专注于对每个客户真正重要的事项，提供了真正的价值，”Banks 说。“我的热情一直在于帮助他人成功，无论是帮助网络安全专业人员在行业中立足，赋能团队穿越噪声，还是作为志愿者为我的社区做贡献。我迫不及待想通过扩大我们的影响力，帮助更多组织受益于我们的情报解决方案，这不仅仅是数据——它们是帮助我们的客户做出真正、具有影响力决策的工具。”

Banks 的任命紧随 GroupSense 最近宣布其新的 ransomware 执行桌面演练（TTX）服务的可用性。该服务基于成功谈判数百个高档次勒索软件案件的第一手经验，旨在改善组织的安全态势，增强事件响应能力，并确保响应团队在勒索攻击期间能够快速有效地采取行动。

