尼日利亚已提起诉讼，要求Binance支付795亿美元的经济损失，理由是该国政府表示Binance的运营导致了这些损失，以及20亿美元的拖欠税款，法庭文件显示。

---
政府当局指责全球最大的加密货币交易所Binance造成了尼日利亚货币困境，并在2024年逮捕了该公司两名高管，因为加密网站已成为交易当地货币奈拉的主要平台之一。Binance并未立即对置评请求做出回应。该公司此前表示，正与尼日利亚联邦税务局(FIRS)合作，解决潜在的历史税务责任。税务局声称，Binance在尼日利亚拥有“显著的经济存在”，因此应对公司所得税负责。它正在寻求法庭宣布Binance为2022年和2023年支付所得税，以及对未支付金额征收10%的年罚款。税务局还要求对未支付税款按尼日利亚中央银行的贷款利率收取26.75%的利息。Binance在尼日利亚面临四项逃税指控，此前政府去年对该行业进行了打击。指控包括未支付增值税、公司所得税、未提交税务申报及通过其平台协助客户逃税。Binance正在对这些指控提出异议，并在去年3月宣布停止所有奈拉的交易和交易。该公司还面临反贪污机构的洗钱指控，但它对此予以否认。

