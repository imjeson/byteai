Solace是一家数字健康平台，通过连接患者与专业卫生保健倡导者来提供个性化的卫生保健支持，由医保支付。![图片](https://ai-techpark.com/wp-content/uploads/2024/08/Solace-Raises-960x540.jpg){ width=60% }

