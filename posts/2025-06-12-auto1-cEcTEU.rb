---

埃隆·马斯克对上周他关于唐纳德·特朗普的一些推文表示懊悔，似乎在努力缓和一场威胁到特斯拉老板商业利益的激烈冲突。

---
马斯克是特朗普总统竞选的最大捐助者，但上周两人之间的紧张关系公开爆发，迅速升级，世界首富在一系列帖子中呼吁对总统进行弹劾，并嘲讽他与性犯罪者杰弗里·爱泼斯坦的关系。

周三，马斯克在他拥有的社交网络X上发布了一条帖子：“我对上周关于总统@realDonaldTrump的一些帖子感到遗憾。他们走得太远了。”据《纽约时报》首次报道，马斯克在周一晚上私下致电特朗普，三位熟悉此事的人士透露。马斯克在周一晚上给特朗普打电话之前，曾在周五与副总统JD·万斯和白宫幕僚长苏西·怀尔斯进行了对话。《纽约时报》报道称，三人在通话中讨论了公开争吵问题。

据知情人士告诉CNN，万斯在周五询问特朗普他希望如何公开处理这场争斗。万斯的提问是在他安排与保守派播客主持人西奥·冯进行的采访之前提出的。特朗普告诉万斯要保持外交，CNN援引该消息来源称。在周六播出的播客采访中，万斯说：“老实说，我认为他去攻击总统是一个巨大错误……我实际上认为如果埃隆稍微冷静一下，一切都会好起来。”

关于马斯克的明显道歉，总统在接受《纽约邮报》采访时表示：“我觉得他这样做很不错。”这段评论出现在该报发布的一段事先录制的采访中，特朗普表示“我想我可以”与马斯克和解。总统称在马斯克上周发起的攻击时“并不是一个快乐的露营者”，但声称自己“没有怨恨”，补充道：“我认为他对自己说过的事情感到非常难过。”

这种实质上虽然表面化的和解可能受到投资者的欢迎。特斯拉的股价在盘前交易中上涨了2.6%。不可阻挡的力量在与不可动摇的对象的斗争中败下阵来：埃隆向特朗普低头。

这场公开争吵是两者关系中最非同寻常的转变之一。在总统竞选期间，他们曾声称是意识形态上的盟友，马斯克还曾短暂担任特朗普政府的“政府效率部门”负责人，这一努力旨在削减政府项目，被人戏称为“狗狗”（Doge），以纪念网络迷因。在马斯克公开批评特朗普的“美丽大型法案”，称其将导致美国政府借款增加2.4万亿美元，并称其为“令人厌恶的可憎之物”之后，两者的关系恶化。

特朗普回应马斯克的猛烈批评时表示，科技亿万富翁“变得疯狂”。他还直接提到了马斯克的公司，强调了这场争吵可能带来的财政风险。在被问及特斯拉时，特朗普在他的社交网络Truth Social上写道：“我取消了他的电动汽车强制令，迫使每个人购买没有人想要的电动汽车。”分析人士称，电动汽车先锋特斯拉在一些市场的销量下降，部分原因是马斯克与特朗普的关系。

马斯克与美国总统的结盟曾促使特斯拉的市场价值激增，投资者希望白宫会对该公司的自动驾驶技术表示更为积极的态度。在即将发布“机器人出租车”服务的前一天，马斯克从争执中退却，特斯拉的“机器人出租车”服务将在德克萨斯州奥斯汀市推出，此次推出被视为该公司证明其作为全球最有价值汽车制造商地位的关键，即使它面临产品阵容老化的问题。

特朗普还威胁到了马斯克的另一项关键业务——火箭公司SpaceX。特朗普写道：“节省我们预算的最简单方法，节省数十亿美元，就是终止埃隆的政府补贴和合同。”实际上，美国政府不太可能取消SpaceX的合同，因为该公司执行的战略重要卫星发射的数量超过了全球其他任何公司。

马斯克最初威胁要退役SpaceX的龙飞船，这是一种将NASA宇航员送往国际空间站的关键飞行器，但随后又撤回了这一威胁。

