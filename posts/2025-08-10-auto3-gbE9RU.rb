Curve Dental®，领先的全方位云端牙科实践管理软件提供商，今天宣布推出Curve FLO AI，这是一套深度嵌入的人工智能能力模块，旨在提升生产力并恢复牙科的“人性化”一面。![图片](https://ai-techpark.com/wp-content/uploads/Curve.jpg){ width=60% }

