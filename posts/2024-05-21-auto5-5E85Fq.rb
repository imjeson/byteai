那些 AI 换脸惹出的乱子
对于电影、电视剧制作方来说，AI 换脸绝对是美事一桩。
原因很简单，内娱明星动不动「塌房」，为了避免血本无归，制作方只能换人重新拍，而如今有了 AI 换脸技术，只要上传一张照片就能轻松换角，不仅省时省力，还节省了大把的银子。
不过，AI 换脸自诞生起也伴随着巨大的争议，甚至搞出了不少乱子。
例如，利用 AI 换脸搞「黄色」，侵犯他人隐私权和肖像权。
今年年初时，一组 AI 换脸生成的霉霉（泰勒・斯威夫特）不雅照在社交平台上疯传，甚至惊动了美国白宫，发言人让・皮埃尔（Karine Jean-Pierre）表示：「我们对此类虚假图像的流传感到担忧。」
再如，AI 换脸技术让社交媒体充斥假新闻。
前几天的 Met Gala 红毯上，凯蒂・佩里和蕾哈娜明明未现身，但 AI 生成的红毯照满天飞，如果不细看很难找出破绽。（以下照片均为 AI 生成）
还有「失踪」几个月的凯特王妃突然现身，「真假难辨」的图片和视频引得全世界瓜民搬着板凳来「找茬」。
更有甚者，有些不法分子还拿 AI 换脸技术搞诈骗。最典型的例子就是一群尼日利亚非洲小哥假扮「落魄王子」诈骗欧美富人。
在 AI 技术出来之前，他们主要用雅虎邮箱忽悠人，骗人话术基本就是「尼日利亚落魄王子，急需爱心人士赞助夺回王位，事成之后共享国库」等，手法相当粗糙。
随着 AI 换脸技术的普及，他们也「马枪换炮」，用自己的声音和深度伪造的面孔与受害者互动，建立在线浪漫关系，从而更好的操控对方。
AI 诈骗猖狂到什么程度？就连巴菲特都在 2024 年度股东大会上表示，「基于深度伪造技朦率碛挛︽那悼蛔强『不断增长的行业』」。
AI 换脸技术带来诸多乱象，我们该何去何从呢？
其实，OpenAI 等 AI 巨头们早就有所布局。
