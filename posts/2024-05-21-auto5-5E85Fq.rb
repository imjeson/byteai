Faceswap 是一款付费且需要魔法的应用，官方提供多种付费计划来满足不同用户需求。
免费计划：每位用户每月可免费换脸 50 张照片、10 个视频，不过上传的视频时长不超过 15 秒且生成的作品有水印等。
初级计划：每月 9.99 美元，每月可生成生成 500 张图片、100 个视频、视频时长最长 30 秒且无水印；
高级计划：每月 29.99 美元，可生成 2000 张图片、200 个视频、视频时长最长为 60 秒，画质也更为清晰。

上手体验：一键生成，效果丝滑
这款 Faceswap 上线了网页版，用户只负责上传图片，系统即可一键生成个性化、搞笑的视频和照片，它既可以实现单人换脸，还可以实现多人换脸，效果丝滑。
Faceswap 官网链接：
https://faceswap.so/zh-cn
单人照或多人照换脸
首先，打开 Faceswap 官网，点击「立即换脸」按钮，再上传一张待换脸的照片，然后点击「添加面孔」，继续上传一张照片，最后点击「交换面孔」，换脸完成。
为保证生成质量，上传的照片尽量清晰，脸部五官无遮挡且为正面照。
例如，我们先后上传了一张「大嘴姑婆」安吉丽娜・朱莉（左上图）和「法兰西玫瑰」苏菲・玛索（右上图）的照片，最终换脸效果（下图）如下：
眉眼、鼻子以及口红色号巧妙地换成了苏菲・玛索，不过仍保留着安吉丽娜・朱莉的发型、服饰以及那张标志性大嘴。
我们再生成一张朱莉和马斯克的换脸照片，其换脸效果更加明显，眉眼和嘴巴都有马斯克的影子。（女版马斯克还挺风情万种～）
