今天，在SIGGRAPH 2025大会上，Autodesk, Inc.（NASDAQ: ADSK）推出了更具可承受性的Autodesk Flow Studio（前称Wonder Studio）的定价，包括首个免费层和Lite计划的价格减半——从20美元下降到10美元——这一重大举措使得该工具集更加易于获取。通过去除前期费用，艺术家可以使用先进的人工智能（AI）驱动的3D工具，创作曾经只属于好莱坞的动画和VFX，Autodesk正在宣告一个以创作者为中心的媒体与娱乐新纪元。这次发布标志着Autodesk收购Wonder Dynamics一年后的重要里程碑，该公司创建了这一技术。

Autodesk Flow Studio利用AI自动化复杂的VFX任务，如动作捕捉、相机跟踪和角色动画，因此创作者可以专注于讲故事，而不是技术设置。只需一段视频剪辑和几次点击，用户就可以将计算机生成的角色插入实景场景并进行渲染，使得3D动画和VFX对所有人开放。

用户还可以将AI生成的资产（例如干净背景、3D场景、alpha蒙版、相机跟踪）导出到Maya、Blender或Unreal Engine等工具中进行编辑和微调。这使得创作者能够加速复杂的动画和VFX工作流程，从而为更多的创意叙述腾出时间。通过免费提供这些功能，Autodesk降低了高质量内容创作的门槛，使更多的艺术家、营销人员和数字内容创作者能够在自己的条件下进行实验、学习并实现他们的创意。

“让Autodesk Flow Studio对每个人更加可获取，反映了我们对创作者优先未来的信念——AI赋予更多艺术表现的能力，而不是取代它，”Autodesk媒体与娱乐执行副总裁Diana Colella说道。“通过降低成本门槛，更多的讲故事者现在能够探索新的AI驱动工具如何帮助他们实现创意，无论他们的创作旅程处于何种阶段。”

此次扩展推出了一种分层模型，旨在与创作者共同成长——从首次用户到专业工作室。新的免费和标准层加入了现有的Lite、Pro和Enterprise产品，为用户提供了更多灵活的选择，以满足他们的创作需求与抱负。免费层包含核心工具，如AI动作捕捉和现场动作，而诸如Wonder Animation和Wonder Tools等高级功能则仍然仅限于付费层。对于更大的团队和工作室，企业层提供可扩展的AI驱动工作流程访问权限，配备增强支持和数据控制——帮助他们在保持专业制作所定义的创作流程和角色的同时，采用新技术。

“在规模上运行AI模型对科技初创企业来说一直非常具有挑战性，”Wonder Dynamics的联合创始人Nikola Todorovic表示，他现在是Autodesk的一部分。“我们最初的使命始终是让我们的技术以尽可能低的价格免费提供给更多的创意者，但作为初创公司，我们的资源有限。现在，作为Autodesk的一部分，我们能够将Flow Studio作为一个免费的产品提供，依托强大的基础设施和云合作伙伴——使更多的艺术家能够讲述更多的故事，无需工作室场地或数百万美元的预算。”

免费的推出和对全球受众的灵活定价模式的发布，也正式完成了Autodesk Flow Studio与Autodesk生态系统的整合。这确保了与Autodesk的标准产品、政策和流程的一致性，为用户提供了更统一、一致和可扩展的体验。此外，Autodesk还在其媒体与娱乐集合中包含了Flow Studio的Pro层，无需额外费用——为现有客户提供了低风险的方式来探索AI驱动的工作流程。这种方法反映了Autodesk致力于帮助客户在媒体和娱乐的AI不断发展环境中，利用可访问、可信赖和能够随着他们的创作抱负而扩展的工具。


