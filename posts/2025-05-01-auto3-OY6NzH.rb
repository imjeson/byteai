title: 'Forward Networks连续第三年获得全球信息安全大奖'
date: 2025-05-02
author: ByteAILab

---

Forward Networks在2025年RSAC上被授予“开拓者合规奖”，荣获Cyber Defense Magazine第13届全球信息安全大奖。![图片](https://ai-techpark.com/wp-content/uploads/Forward-Networks.jpg){ width=60% }

---
该奖项的获得标志着Forward连续第三年获颁全球信息安全奖，2024年荣获“漏洞评估、修复与管理市场领导者”奖，2023年获得“云安全热门公司”奖。

Forward Enterprise是唯一的网络数字双胞胎，能够提供整个网络始终准确的合规状态，并附带优先级修复计划，针对在数据收集过程中发现的任何问题进行处理。Forward Enterprise从网络中的所有L2-L7设备（无论是本地还是云）收集配置和状态数据。这些数据形成了准确的网络清单和拓扑，成为其开创性合规功能的基础，包括持续网络安全合规监测、CVE（共同漏洞和暴露）修复以及零信任态势验证。

“随着攻击面不断演变和新威胁的出现，安全合规变得日益重要。在Forward Enterprise，我们的目标是通过提供准确、最新的合规视图和在需要时保护网络的明确、可操作的见解，来减少不确定性，”Forward Networks产品与用户体验高级副总裁Chiara Regale表示。“我们专注于帮助安全团队摆脱耗时的手动流程，同时提供前所未有的安全态势洞察。连续三年被认可为全球信息安全奖的获奖者，我们感到非常荣幸，这也是我们团队奉献精神的体现。”

“我们在全球范围内寻找能够产生巨大差异的网络安全创新者，并希望能够帮助扭转网络犯罪不断增长的趋势。Forward Networks绝对值得获得这一备受推崇的奖项，并考虑在您的环境中进行部署，”Cyber Defense Magazine全球编辑Yan Ross表示。

