HopeAI，一家梅奥诊所平台加速公司，正在推动癌症临床试验的设计和执行。![图片](https://ai-techpark.com/wp-content/uploads/HopeAI-Unveils.jpg){ width=60% }

---
在2025年美国临床肿瘤学会（ASCO）年会上，HopeAI展示了新的结果，证明AI生成的合成个体患者数据（SynthIPD）能够加速监管级证据的生成——无需真实患者级数据。

药物开发面临一个长期挑战：关键的肿瘤学试验通常需要数年的患者随访来评估生存结果，这导致监管决策和患者获得治疗的延迟。验证替代终点（例如，微小残留病（MRD）阴性）提供了一条加速批准的有效路径，但传统的验证方法依赖于耗时的荟萃分析和多机构数据共享，通常需要1-2年的时间才能完成。

ASCO演示的关键亮点

- **AI驱动的替代验证**：HopeAI的自主AI工作流程结合专家统计策划，使最新证据可以快速进行试验级和患者级替代分析。
- **合成个体患者数据（SynthIPD）**：通过数字化已发表研究中的Kaplan-Meier图，SynthIPD以高统计信度重建个体患者的时间事件和协变量数据。
- **对多发性骨髓瘤的影响显著**：分析确认在最近的骨髓瘤试验中，MRD阴性与无进展生存期（PFS）之间存在中等的替代关系，支持其作为监管终点的有效性。
- **速度和效率**：与传统的汇总分析动用多达24个月相比，HopeAI的AI加专家辅助手段在不到两周内完成了整个过程。

市场意义

- **缩短上市时间**：在几周内启动替代终点验证可以加速药物批准，提高管道价值并降低后期流失风险。
- **符合监管要求的证据**：AI驱动的工作流程提供符合监管期望的分析，支持NDA/BLA提交及标签扩展。
- **商业验证**：HopeAI的平台已在全球顶尖制药公司和多家生物科技创新公司中使用。
- **战略差异化**：获得梅奥诊所的真实世界数据和临床专业知识，在证据质量和洞察时间方面提供竞争优势。

