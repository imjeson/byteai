---

Homecare HomebaseSM（HCHB），家居护理领域领先的软件提供商，宣布推出其机构欢迎通知自动文本类型，这是HCHB患者参与产品套件的一部分，旨在建立强大的患者关系，同时优化运营效率。![图片](https://ai-techpark.com/wp-content/uploads/Homecare.jpg){ width=60% }

---

居家健康机构在确保转诊、与患者互动和保持清晰沟通方面面临挑战。竞争对手的干扰导致护理连续性受损，因此患者常常不知道何时开始接受护理。沟通缺口阻碍了重要信息的传递，降低了信心，削弱了转诊转化率。
一旦转诊被保存，患者便会收到“欢迎加入机构”的短信，包含链接到服务重要信息的可自定义URL。这种主动联系旨在增强转诊转化率，减少不入院情况，并建立即时品牌信任。
“通过自动化初始患者参与，机构可以简化运营，减少冗余数据录入，为患者提供无摩擦的入院体验，” Homecare Homebase首席临床官Brandy Sparkman Beierle表示。“通过机构欢迎通知，患者从一开始就感到被告知、得到支持并与提供者保持联系，建立起对护理提供者的信任和信心。”
HCHB患者参与套件中的其他值得注意的解决方案包括临床医生预计到达时间通知，向患者或看护人发送预填文本，告知预计到达窗口，以及患者访问提醒，利用自动访问提醒将临床医生与患者保持连接。这些解决方案旨在改善沟通并节省时间。机构欢迎通知特别旨在创建：

更强的患者联系：增强转诊转化，确保患者一开始就认识到您机构在其护理旅程中的角色。
运营效率：自动化初始参与，消除对未入院患者的冗余数据录入，为您的团队节省宝贵时间和资源。
无缝集成：HCHB患者参与文本产品完全集成在HCHB的电子病历中，无需额外集成，确保顺畅、无障碍的实施。

“转诊过程面临挑战，” Homecare Homebase产品管理副总裁Lynn Carroll表示。“机构欢迎通知帮助机构更早与患者联系，加强他们的护理计划，并提高转诊留存率，最终改善患者和运营结果。”
通过提供及时的沟通，机构欢迎通知确保患者意识到他们的护理已经开始，并增强他们对提供者的信心。机构可以利用这个工具弥补沟通缺口，简化入院过程，最终提升患者参与度，提高满意度，创造更顺畅的护理过渡。

