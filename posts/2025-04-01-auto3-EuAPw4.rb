Belay Diagnostics宣布与精准医学软件公司GenomOncology (GO)建立合作关系，旨在提供数据驱动的工具和资源，以改善癌症护理。![图片](https://ai-techpark.com/wp-content/uploads/Belay-Diagnost.jpg){ width=60% }

---
为简化其Summit™测试的三级分析，以及其综合DNA检测面板，Belay Diagnostics将GO Pathology Workbench无缝地集成到其变异解读和分析工作流程中。凭借定制的临床应用，GO Pathology Workbench赋能Belay Diagnostics团队高效分析每月成千上万的样本。这一定制化解决方案使团队能够做出知情的变异解读和治疗建议。

Belay Diagnostics提供的脑癌测试Summit™是一种液体活检检测，旨在帮助指导原发性和继发性中枢神经系统 (CNS) 恶性肿瘤的诊断和管理。Summit检测来自与CNS肿瘤相关的基因的DNA中的单核苷酸变异、多核苷酸变异、插入和缺失以及染色体臂水平的丢失/获得。第二种新型检测Vantage™ MGMT启动子甲基化，评估脑脊液 (CSF) 中的MGMT甲基化状态。这两种检测可以同时使用仅6毫升的CSF样本进行检测。Summit和Vantage现已在他们位于芝加哥的先进实验室提供。

Belay Diagnostics的高级副总裁兼医学主任Honey Reddi博士强调了准确检测CSF以指导CNS癌症的治疗和管理的重要性。“我们的尖端CSF液体活检检测旨在为现有选项提供一种创新的补充，例如细胞学、影像扫描或脑活检。通过与GenomOncology的合作，我们能够有效减少实验室开销，并优化临床样本的审查和分析。这一简化过程确保患者能更快获得建议，提升我们的诊断效率。”为深入教育和提升对该数据分析平台的认识，Reddi博士将于4月9日中午12点举行题为“Belay Summit™ + GenomOncology – 最大化临床效用以指导CNS肿瘤诊断”的网络研讨会，请访问Belay或GenomOncology的网站以获取网络研讨会的描述及链接。

GenomOncology的首席商务官Garreth Hippe强调了GO Pathology Workbench的适应性。“GO Pathology Workbench的灵活性和可定制性使我们能够根据Belay Diagnostics的检测和工作流程的独特需求量身定制解决方案。丰富的知识库增强了变异解读，改善了从样本审查到治疗方案报告的周转时间，最终为脑和脊髓肿瘤患者提供了更快速和高效的治疗路径洞察。”

GenomOncology的Pathology Workbench是一种多功能且全面的三级分析软件解决方案，简化了变异分析、审查和报告生成的整个过程。该平台注释检测到的变异，突出匹配的潜在临床试验和治疗，并生成可以无缝集成回实验室系统工作流程或电子病历平台的可定制输出报告。GO Pathology Workbench整合了多种注释集和经过策划的内容，以支持各种规模的检测面板。

