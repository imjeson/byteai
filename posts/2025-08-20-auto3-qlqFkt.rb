Series B桥接融资使总融资超过1.5亿美元，旨在扩展文档智能，推动在受监管行业的采用，并进行全球扩展，得到KDB、亚马逊和AMD的支持。![图片](https://ai-techpark.com/wp-content/uploads/Upstage-Completes.jpg){ width=60% }

---


Upstage是一家专注于企业级生成式AI解决方案的关键AI公司，今天宣布完成4500万美元的B轮桥接融资，此次融资由韩国开发银行（KDB）、亚马逊和AMD支持。这笔资金的总额已达到1.57亿美元，将加速其下一代Solar语言模型的开发，提升文档AI产品，并在美国和亚太地区进行市场扩展。

新资金还将推动研发工作，以增强Solar的能力并扩大企业用例，包括文档解析、代理式任务执行和工作流自动化。Upstage最近宣布与亚马逊网络服务（AWS）达成战略合作，这是其首选的云服务提供商，以推动其Solar语言模型的进步和扩大对生成式AI技术的访问。在AWS的合作下，Upstage将利用AWS构建和部署基础模型，使用AWS的机器学习基础设施，包括SageMaker和AWS设计的硅芯片，如AWS Trainium和Inferentia。

这些增强的AI能力被战略性地部署以解决目标行业中存在的重大低效，特别是保险行业。近期对美国保险领域的分析发现，索赔裁决成本已达257亿美元，其中180亿美元与推翻拒赔和不必要的审核周期有关——大多数是由于手动的、纸质的操作和过时的、分散的系统所驱动。Upstage正面临这一挑战，保险行业成为其首个主要全球垂直市场，在索赔处理和经纪人提交等资源密集型领域尤其适合进行自动化。

该战略的核心是文档智能套件，其中包含文档解析——将非结构化文档转换为结构化、准备好供LLM使用的格式——和信息提取，从原始文本中提取关键数据点。无论是处理保单、损失记录、财务报表、发票还是电子邮件，该平台以超过95％的准确率处理高变异文档包，消除手动数据输入，减少处理时间，并实现更快速、更自动化的决策。

与传统的OCR工具不同，后者提取静态字段而不理解上下文，Upstage的文档智能全面解释文档，理解布局、含义和目的，以提供为实际工作流程准备的结构化输出。

“这是一个关键的时刻，不仅对Upstage来说，也是对企业生成式AI未来的影响。”Upstage的联合创始人兼CEO Sung Kim表示。“AI的下一个阶段是关于性能、精确度和应对现实复杂性的准备。这正是我们用我们的套件所构建的。我们与AWS的合作带来了规模、信誉和深度技术对齐—这些都是我们在商业和公共部门保险等高风险领域从第一天开始提供可生产AI的关键。”

“许多企业团队仍然被文档混乱所困扰，”Upstage美国首席执行官Kasey Roh表示。“我们为这一混乱带来结构和速度，帮助承保人和经纪人更快地行动，降低成本，并自信地为客户服务。”

Solar LLM是Upstage的一系列专有小型LLM，利用亚马逊SageMaker进行训练，并在亚马逊Bedrock市场中提供，支撑Upstage更广泛的生成式AI堆栈。公司还通过AWS AI代理和工具市场提供其通用信息提取器——一个用于文档层面理解的AI代理。

Upstage的模型已经被富国500强公司使用，包括三星，并广泛被韩国保险公司使用。它也在多个公共部门和政府机构中部署，展示了Upstage从概念到生产的能力。其在美国保险及其他高度结构化行业的采用正在增长。

这轮B轮桥接融资将支持销售和营销人员的扩充以及持续的国际扩张。

Upstage最近被评选为CB Insights AI 100名单中的一员，从数千名全球申请者中脱颖而出——这一里程碑巩固了Upstage在全球市场的影响力。这一桥接轮融资在2024年公司宣布的7200万美元B轮基础上进行，后者由包括SK Networks和新汉银行投资等韩国投资者主导。

