Leader in security, compliance and integrity software technologies provides support for the CompTIA Cybersecurity Trustmark
SureShield, Inc.,一家在安全、合规和完整性技术解决方案领域的领先公司，今天宣布为CompTIA网络安全信任标记计划提供重要支持。![图片](https://ai-techpark.com/wp-content/uploads/2024/05/SureShield-960x540.jpg){ width=60% }

---

与SureShield合作的托管服务提供商（MSPs）现在可以访问信任标记的保障措施。该信任标记计划旨在帮助MSPs在其整体网络安全文化方面实现积极变革，并对其风险姿态产生积极影响。
“网络安全信任标记是在保护敏感数据、防范损害隐私和财务安全的网络威胁的复杂斗争中的一个重要标准。我们很高兴与CompTIA合作，为全球MSP社区提供一个强大的基础设施，帮助实现网络安全信任标记认证。”SureShield的首席执行官Sanjaya Kumar表示。
“随着我们将网络安全信任标记扩展到全球其他意识方面，我们欢迎SureShield加入我们不断增长的平台合作伙伴名单，他们致力于帮助我们扩大努力。”CompTIA网络安全项目副总裁Wayne Selk表示。

来自37个国家的超过1250家MSPs已经注册了CompTIA网络安全信任标记计划，大约有200家目前正在通过认证过程。完成该过程的公司将获得一份认证证书，表明他们达到或超过业界公认的最佳网络安全实践。

