量子计算机可能在几年内而非几十年内建成，微软透露了一项突破，称这可能为更快的发展铺平道路。

---
这家科技公司开发了一种芯片，它表示，这种芯片的出现与使现代智能手机、计算机及电子设备得以小型化和提升处理能力的半导体发明相呼应。该芯片由世界首个拓扑导体（topoconductor）驱动，拓扑导体可以创造一种既不是固态、液态或气态的新物质状态——这使得能够设计出适合放置在握在手掌中的单一芯片的量子系统，并创造出更可靠的硬件，《自然》期刊的一篇经过同行评审的论文报告指出。

萨里大学物理学教授保罗·史蒂文森表示，如果微软能够成功在该研究基础上继续推进，那么该公司可能在构建首个可靠的量子计算机的竞争中成为“非常严肃的竞争者”。“这些新论文是一个重要的步骤，但与量子计算中许多有前景的工作一样，接下来的步骤将非常困难，在这些步骤实现之前，过于乐观是不合适的，”他说。

拓扑导体使得能够设计出适合放置在握在手掌中的单一芯片的量子系统。乔治·布斯，伦敦国王学院的理论物理学教授，表示该研究代表了一项“令人印象深刻的技术成就”，尽管其价值可能只有在后续才能清楚。“微软所声称的‘几年’（而非几十年）是否准确还需要拭目以待，”他说。微软声称，拓扑导体为开发可扩展至一百万量子比特的量子系统提供了一条途径，这些量子比特是构建量子计算机的微妙构件，可以与当前计算机使用的二进制数（1和0）类比。

这可能最终产生出基于量子力学而非经典物理学的最强大计算机，能够解决高度复杂的工业和社会问题。这可能包括将微塑料分解为无害副产品；为建筑、制造或医疗行业发明自愈材料；解决复杂的物流供应链问题；或破解加密代码。

本月早些时候，美国国防高级研究计划局（Darpa）选择微软的拓扑导体作为其探索量子计算的两个路径之一，作为其一个旨在确定是否能在2033年前构建出工业可用的量子计算机的项目，这比大多数预测快得多。另一种方法是由PsiQuantum创建的，该公司使用基于光子学的硅技术，即基于光波的技术，以创建基于光子量子比特的量子计算机。

微软之前声称已经在一篇论文中开发了拓扑量子比特，但该论文因科学缺陷被撤回。布斯表示，尽管微软的进展相较于其他一些公司较慢，但它却专注于长期目标，致力于开发一种固有更具抗干扰能力的系统，而不是其他竞争者几年来所构建的脆弱的基本量子计算机。“这些拓扑量子比特通过使用一种新型生成粒子——马约拉纳费米子（Majorana fermion）的特性来保护其携带的信息，这使得信息在处理过程中的丢失变得更加困难。然而，与竞争架构相比，构建这些量子比特时增加了复杂度，”他说。

这些马约拉纳粒子此前从未被观察或制造过。微软表示，它们必须借助磁场和超导体“引导”产生，这也是为什么大多数量子计算研究集中在其他方法上的原因。布斯表示，该研究代表了朝着一种非常不同的平台迈出了一步，这可能与像谷歌那样的成熟技术竞争，尽管他补充道，在证明该技术能够扩展之前，还有很长的路要走。

