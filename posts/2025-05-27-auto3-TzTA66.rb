79% 的全球B2B买家表示，AI搜索改变了他们的研究方式
G2 今天发布了其 2025 年买家行为报告，该报告基于对B2B软件买家的全球年度调查，并通过专有的G2数据验证关键发现和见解。![图片](https://ai-techpark.com/wp-content/uploads/G2-Report.jpg){ width=60% }

---
该报告标题为“AI现在意味着始终包含”，探讨了AI如何颠覆软件购买旅程，以及市场推广团队如何调整以保持领先。

今年的报告发现，AI已经从实验阶段转变为购买过程研究和评估阶段的核心参与者，并深植于企业战略性改善运营的方式中。尽管软件和技术支出持续加速，2025年买家行为报告揭示了购买模式的基本变化，交易规模变小，购买委员会更集中，AI在买家旅程的每个阶段都在重塑——从AI搜索颠覆研究阶段的传统搜索开始。

“AI不再是炒作——它已经融入我们的工作流程，深刻影响我们的商业战略，”G2研究洞察副总裁Tim Sanders表示。“对于软件卖家而言，这一新现实要求采用新的市场推广策略。去年的做法在现在可能行不通，因为AI颠覆了购买过程的每一个部分。”

G2 2025 年买家行为报告中观察到的一些主要趋势包括：
AI资金从实验性转向运营性
企业正在将AI投资从临时的探索性预算转移到核心运营支出。这一转变标志着AI采用的成熟，使其成为基础设施而非可选创新。

57%的买家预计，其组织在未来一年内将增加对技术和软件的支出——年同比增加8个百分点——这主要是由于AI在商业转型中日益扩大的作用。
超过一半（55%）的AI解决方案购买通过中央IT预算资助，这标志着与过去两年对受限的R&D预算、自由支出或专门发起的AI举措的转变。

买家角色和委员会正在演变
最终决策权不仅限于传统的C级高管，技术专家和最终用户也开始参与。向更小、更敏捷的购买委员会的趋势表明，希望加速技术采用，同时保持适当的监督。

主导的5-8人购买委员会正在缩小，越来越多的3-4人小组变得流行。
IT参与几乎在所有采购决策中都有提及，2025年呈现的主题是“AI管弦乐”。
近三分之二的公司在评估AI驱动的软件时施加更严格的要求，与非AI解决方案相比。

生成性AI正在创造更直接的买家旅程
买家利用大型语言模型（LLMs）显著压缩研究和评估过程。这一缩短的旅程反映了买家对使用AI聊天机器人和代理进行购买决策的信心、信任和熟练度的提高。

近8成受访者表示AI搜索改变了他们的研究方式，29%的人指出他们更常通过LLMs开始研究，而非Google。这与主要依赖SEO提供商的研究形成鲜明对比，后者主要衡量推荐流量，并声称AI搜索的采用微不足道。
几乎三分之二的买家更喜欢在旅程的后期阶段与供应商销售人员接触，与2024年相比，显著上升（+17个百分点）。
生成性AI聊天机器人（17%）和软件评论网站（15%）是影响供应商候选名单的两个主要来源，超过供应商网站、市场研究公司和独立内容等其他来源。

交易规模和定价模式正在变化
虽然软件支出的热情仍然强劲，受到AI推动的势头，但宏观经济逆风和对投资回报率（ROI）的增加审查正在改变交易规模。同时，市场正在从大型预付款承诺转向基于实际价值交付的灵活消费模型。这一根本性转变更强调证明持续的投资回报率，而不是销售大规模的初始合同。

企业目前将采购集中在10万美元到15万美元的范围内，回撤自零利率时代的庞大预算。
39%的受访者更倾向于选择按需付费定价模型，这一向基于使用的定价模式的转变促成了初始交易的缩小，而在产品与客户契合良好的情况下则不断扩大。

