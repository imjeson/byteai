方法论
G2 2025 年买家行为报告基于2025年4月G2对1169名B2B决策者进行的在线调查结果。全球受访者来自北美、EMEA和亚太地区，包括负责或影响多个部门、运营单位或整个业务的采购决策的工作人员。

生成性AI被广泛应用于定义研究的重点领域、优化调查设计和分析结果。数据可视化使用推理模型生成，用于发现见解并提供创造性原型，加速生产过程。G2数据被用来验证各种发现并生成该报告的见解。

