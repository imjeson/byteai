---

Menlo Ventures的资金推动Goodfire解码AI模型的神经元，重塑对其理解和设计的方式。![图片](https://ai-techpark.com/wp-content/uploads/Goodfire-Raises.jpg){ width=60% }

---

今天，领先的AI可解释性研究公司Goodfire宣布完成一轮由Menlo Ventures领投、Lightspeed Venture Partners、Anthropic、B Capital、Work-Bench、Wing、South Park Commons等知名投资者参与的5000万美元A轮融资。这笔资金将在Goodfire成立不到一年的时间内，支持其研究计划的扩展以及主要可解释性平台Ember的开发，与客户合作。
“AI模型以其非确定性的黑箱著称，”Menlo Ventures的投资人Deedy Das表示。“Goodfire的世界级团队——来自OpenAI和Google DeepMind——正在破解这个黑箱，帮助企业真正理解、引导和控制他们的AI系统。”
尽管AI取得了显著的进展，但即使是领先的研究人员也对神经网络的实际功能知之甚少。这一知识差距使得神经网络的工程设计变得困难，容易出现不可预测的故障，并且随着这些强大系统的复杂性加剧，它们的部署风险也在增加。
“没有人理解AI模型失败的机制，因此没有人知道如何修复它们，”Goodfire的联合创始人兼首席执行官Eric Ho表示。“我们的愿景是构建能够从内而外让神经网络易于理解、设计和修复的工具。这项技术对于构建下一代安全而强大的基础模型至关重要。”
为了解决这一关键问题，Goodfire正大力投资于机制可解释性研究——这一相对新兴的科学，旨在逆向工程神经网络并将这些见解转化为通用的、模型无关的平台。Goodfire的这一平台Ember解码AI模型内的神经元，从而直接、可编程地访问其内部思维。通过超越黑箱的输入和输出，Ember解锁了全新的应用、训练和对齐AI模型的方法——让用户能够发现模型中隐藏的新知识，精确塑造其行为，并提高其性能。
“随着AI能力的提升，我们对这些系统的理解能力也必须跟上。我们对Goodfire的投资反映了我们相信，机制可解释性是帮助我们将黑箱神经网络转变为可理解、可引导系统的最佳选择之一——这是强大AI负责任发展的关键基础，”Anthropic的首席执行官兼联合创始人Dario Amodei表示。
展望未来，Goodfire通过与前沿模型开发者的定向合作，加快其可解释性研究进程。通过与业界创新者的紧密合作，Goodfire将迅速增强和巩固可解释性研究的应用。“与Goodfire的合作对于解锁我们DNA基础模型Evo 2的更深层次见解至关重要，”Arc Institute的联合创始人Patrick Hsu表示，他是Goodfire的早期合作伙伴之一。“他们的可解释性工具使我们能够提取新的生物学概念，加速我们的科学发现过程。”
该公司还计划发布更多研究预览， highlighting state-of-the-art 解释技术，涵盖图像处理、先进推理语言模型和科学建模等多个领域。这些努力承诺揭示新的科学洞察，并从根本上重塑我们如何与AI模型互动和利用它们的理解。
Goodfire团队汇集了顶级AI可解释性研究人员和来自OpenAI和Google DeepMind等组织的经验丰富的初创企业运营者。Goodfire的研究者们帮助创立了机制可解释性领域，撰写了三篇引用最多的论文，并开创了如稀疏自编码器（SAEs）用于特征发现、自动可解释性框架等多项进展，以及揭秘AI模型中的隐含知识。

