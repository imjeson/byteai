Hexagon宣布成立专门的机器人事业部，以加速下一代自主技术，利用其在人工智能、空间智能和测量技术方面的专业知识。![图片](https://ai-techpark.com/wp-content/uploads/Hexagon.jpg){ width=60% }

---
该机器人事业部旨在满足关键行业对自主解决方案日益增长的需求，以应对劳动力挑战。

今天，Hexagon推出了一个新的机器人事业部，利用其在测量技术、人工智能和自主系统方面的行业领先专业知识，推动人形机器人发展，并帮助客户实现真正的自主。人工智能、仿真和强化学习的进步正在释放机器人系统的潜力，使其能够更具自主性，导航复杂环境并完成复杂任务。作为导航复杂环境所需测量技术和传感器领域的市场领导者，Hexagon处于推动进一步进展的有利地位，通过提供空间智能和数字双胞胎，使机器人系统能够学习、与周围环境互动并采取行动。

“机器人一直是Hexagon的核心创新支柱，”Hexagon临时总裁兼首席执行官Norbert Hanke表示。“从用于建筑的全站仪和无人机，到制造业的激光跟踪器，再到矿业和农业的越野自主系统，我们不断开创准确的自主系统，利用机器人技术。今天，我们通过建立专门的机器人事业部，迈出了下一步，以利用我们过去几年的投资，开发自主解决方案，以补充逐渐缩小、老龄化和变化的劳动力。”

Hexagon的机器人事业部将专注于工业应用。通过利用其在制造、汽车、航空、能源、仓储和物流市场的成功经验，Hexagon独特地处于应对高度成功和多功能机器人日益增长的需求的有利位置。

为了领导这个新事业部，Hexagon任命Arnaud Robert为Hexagon机器人事业部总裁。这位全球战略领导者拥有25年的经验，深厚的人工智能背景，成功领导了六个行业的业务转型，推出了全球产品，并将多个业务从创立发展为显著的利润中心。他拥有洛桑联邦理工学院（EPFL）的计算机科学博士学位，持有十多项专利，还获得了多个行业奖项。

“Hexagon是测量技术和定位解决方案的世界领导者，这将在这个快速增长的市场中成为关键的差异化因素，”Arnaud Robert表示。“我很荣幸能够领导这个开创性的事业部，与团队一起构建自主机器人解决方案，以利用机器人和人工智能的最新进展，重新定义各个行业的效率、智能和自动化。我们将继续推动创新，帮助客户实现其全部潜力，并为可持续未来做出贡献。”

作为总裁，Arnaud将领导研发、产品、商业、运营、合作伙伴关系和支持功能，并推动Hexagon机器人创新的下一个阶段。Arnaud将向Norbert Hanke汇报，并成为执行管理团队的成员。

