6月25日，商汤科技宣布推出大模型0元购计划，即日起成为商汤“日日新SenseNova”新注册企业用户，将获得涉及调用、迁徙、训练等多项免费服务大礼包，0元GO！

“日日新SenseNova”平台覆盖商量大语言模型、商量图文多模态大模型、秒画文生图大模型、语音大模型、向量模型等多类模型API接口，满足企业用户不同需求。商汤始终坚持AI原创，技术安全且自主可控，并自有商汤大装置SenseCore这类高效率、低成本、规模化的新一代AI基础设施，算力有保障。

---


商汤将为“日日新SenseNova”新注册企业用户赠送5000万Token包，并且派出专属搬家顾问，提供从OpenAI 到“日日新SenseNova”的迁移系列培训，让新用户入驻新家舒适顺利。

据OpenAI近日推送邮件信息显示，自7月9日起，OpenAI将采取额外措施，阻止来自不在OpenAI支持的国家和地区列表中的API流量。如果希望继续使用OpenAI的服务，必须在其支持的国家或地区内访问。据了解，OpenAI API支持的国家和地区列表中不包括中国。

