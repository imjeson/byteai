title: '5C集团成立，Hypertec Cloud收购5C数据中心'
date: 2025-04-12
author: ByteAILab

---

在一项标志性举措中，领先的人工智能云平台Hypertec Cloud收购了5C数据中心，这是美国一家拥有超过2吉瓦（GW）路线图容量的顶级托管与数据中心提供商。![图片](https://ai-techpark.com/wp-content/uploads/5C-Group.jpg){ width=60% }

---
这项战略收购标志着Hypertec Cloud从其母公司Hypertec Group的分拆，以及5C集团的成立——这一新的独立实体专注于构建下一代大规模人工智能基础设施。

新成立的5C集团任命了Jonathan Ahdoot为首席执行官，该集团结合了Hypertec Cloud的高性能云计算能力与5C数据中心在美国各地的广泛布局。两者合并后，共同成为北美最大、最灵活的人工智能数字基础设施提供商之一，致力于构建将驱动行业未来的下一代人工智能工厂。

此次收购的驱动力来自三个关键战略目标：
1. 提供全面的人工智能数字基础设施解决方案：通过结合Hypertec Cloud与5C数据中心，5C集团将提供综合的数据中心与人工智能基础设施，旨在满足人工智能前沿实验室、人工智能原生企业与大型企业客户的需求。
2. 加速在受限市场中的部署速度：随着对人工智能部署的需求增长，而大规模数据中心的容量仍然受到限制，联合平台将使超大规模人工智能集群更快地推向市场。集成的计算和数据中心设计将显著减少部署时间，实现同步基础设施的推出。
3. 驱动性能和成本优化：凭借Hypertec在硬件和基础设施设计、白手套支持和可持续工程方面的数十年经验，新实体将采用先进的冷却技术——如直对芯片液体冷却和浸没冷却——以支持更高的机架密度和能量节约。

“今天人工智能基础设施面临的最大挑战是对齐——即人工智能计算所需的与物理托管环境所能提供的之间的对齐。我们通过将两者集成在一起解决了这个问题，”5C集团首席执行官Jonathan Ahdoot表示。“通过这次收购，我们将优化过的人工智能计算基础设施的深厚专业知识与世界级数据中心校园的设计和运营结合在一起。这一组合使我们能够加速部署，推动密度的进一步提升，并在未来几年为最大、最具需求的人工智能用户提供无与伦比的效率。”

“Hypertec Cloud的分拆标志着Hypertec Group的一个重要时刻，”Hypertec Group首席执行官Simon Ahdoot补充道。通过对5C数据中心的收购，新成立的5C集团有望提供为未来行业的规模、速度和复杂性而设计的人工智能数字基础设施。我们将作为合作伙伴，继续推进计算、存储和数据中心技术，为推动人工智能创新的未来提供真正的价值。”

5C集团在美国的人工智能优化数据中心校园组合包括超过600兆瓦（MW）的可用容量，作为其总计2吉瓦（GW）的一部分，这些容量将在未来6到18个月内可供新的和现有的人工智能数据中心托管和计算客户部署。该平台专为满足人工智能工作负载的极端密度、功率和冷却需求而设计，为未来人工智能创新奠定基础。

