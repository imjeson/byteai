加入微软的 Copilot 生态系统，在舞台上展示 AI 可达性
‘Dot Vista’ 演示通过利用 Windows AI API 和 Copilot+ PC 进行视觉内容的触觉转换
将 PowerPoint 重新构想为视觉障碍人士的触觉界面

Dot Inc. 应邀于微软年度开发者大会“@Build 2025”上，于5月20日在西雅图会议中心揭开了其可达性解决方案 Dot Vista 的面纱，该解决方案结合了微软的 Copilot+ PC 和 Windows AI Foundry 技术。![图片](https://ai-techpark.com/wp-content/uploads/Dot-Inc.jpg){ width=60% }

---
 
Dot Vista 由 Dot Inc. 的 AI 团队与汉阳大学的游永在教授 AI 组合作开发，是全球首个使用 Windows AI API 和 Dot Pad 触觉显示屏的 Windows 可达性应用程序，能够将 PowerPoint 幻灯片文本和视觉内容（如图表和图形）转换为语音和立体图形，帮助视觉障碍用户更好地跟随演示。

此次展示是微软 Windows AI API 团队主导的一场会议的一部分，Dot 在一台集成了三项关键 Windows AI API 的 Windows Copilot+ PC 上现场演示了 Dot Vista：

- Phi Silica API：快速总结文本密集的 PowerPoint 幻灯片。
- 图像描述 API：提供幻灯片图像的上下文描述。
- 文本 OCR API：提取并解释图表和图形中的数据。

Dot 的首席执行官 Eric Ju-Yoon Kim 表示：“我们选择 Windows AI API 是因为其轻量级架构，使得快速总结和关键信息提取成为可能，而无需依赖复杂的 LLM 服务器。” 
Dot 的旗舰产品 Dot Pad 打破了传统的单行盲文显示限制，能够同时显示多达七行，帮助用户快速浏览大量信息。它还能够直观地呈现复杂内容，如数学公式、扩展代码片段和表格，提升学习效率和工作生产力。

RNIB 可达性专家 Dave Williams 强调了 Dot Pad 在促进包容性方面的变革潜力：“Dot Pad 为从地图到音乐的图像提供触觉访问，使盲人与视力正常的用户之间能够进行协作。作为一名盲人家长，我终于能够通过触摸体验我儿子的画作。随着 AI 的进步，这仅仅是一个开始。”

牛津大学著名的触觉学习研究专家 Maziar Zarrehparvar 及全球大学倡议负责人表示：“我对 Dot 团队如何利用 AI 使我们的内容更加互动令人印象深刻。我们相信，通过 Dot 的先进机器学习模型，为视觉障碍学生量身定制的个性化学习的未来将会蓬勃发展，牛津大学也全力支持 Dot 基于 AI 的个性化教育方法。”

Dot Inc. 已与牛津大学和波士顿大学等领先机构合作开发“触觉课程”。它还扩大了与图书馆及英国皇家盲人协会 (RNIB) 等组织的合作，树立了可达教育和数字公共服务的新基准。

这些全球努力使 Dot 成为韩国公共和教育领域，以及北美、中美和南美地区的重要可达性合作伙伴。Dot 的突破性创新和社会影响受到了国际赞誉，包括 CES 创新奖和 SXSW 创新奖。

