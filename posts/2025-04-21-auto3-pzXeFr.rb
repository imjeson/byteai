title: 'Goodfire 完成 5000 万美元的 A 轮融资，推动 AI 可解释性研究'
date: 2025-04-22
author: ByteAILab

---

Goodfire, 领先的 AI 可解释性研究公司，今日宣布完成由 Menlo Ventures 主导的 5000 万美元 A 轮融资，参与者包括 Lightspeed Venture Partners、Anthropic、B Capital、Work-Bench、Wing、South Park Commons 及其他知名投资者。![图片](https://ai-techpark.com/wp-content/uploads/Goodfire-Raises.jpg){ width=60% }

---
这笔融资是在其成立不到一年的时间内获得的，旨在支持 Goodfire 扩展其研究计划和开发公司旗舰可解释性平台 Ember，与客户合作推进。

“AI 模型通常是不可预测的黑匣子，”Menlo Ventures 的投资者 Deedy Das 说道。“Goodfire 的世界级团队——来自 OpenAI 和 Google DeepMind，正在打开这个黑匣子，帮助企业真正理解、引导和控制他们的 AI 系统。”

尽管 AI 取得了显著的进展，甚至领先的研究人员也对神经网络的真正运作仍然知之甚少。这一知识缺口使得神经网络难以工程化，容易出现不可预测的故障，并随着这些强大系统变得越来越难以引导和理解而承担风险。

“没有人了解 AI 模型失败的机制，因此没有人知道如何修复它们，”Goodfire 的联合创始人兼 CEO Eric Ho 表示。“我们的愿景是构建工具，使神经网络从内部容易理解、设计和修复。这项技术对于构建下一代安全而强大的基础模型至关重要。”

为了应对这一关键问题，Goodfire 正在大力投资于机制可解释性研究——这一相对新兴的科学探索反向工程神经网络并将这些洞见转化为一个通用的，与模型无关的平台。Goodfire 的 Ember 平台解码 AI 模型内部的神经元，为其内部思维提供直接的、可编程的访问权限。通过超越黑匣子的输入和输出，Ember 解锁了全新的应用、训练和协同 AI 模型的方式，使用户能够发现模型中隐藏的新知识，精确塑造其行为并提高性能。

“随着 AI 能力的提升，我们对这些系统的理解能力也必须跟上。我们对 Goodfire 的投资反映了我们相信机制可解释性是帮助我们将黑匣子神经网络转变为可理解、可引导系统的最佳选择之一，这是负责任地开发强大 AI 的关键基础，”Anthropic 的 CEO 兼联合创始人 Dario Amodei 说道。

展望未来，Goodfire 正在通过与前沿模型开发者的针对性项目加速其可解释性研究。通过与行业创新者紧密合作，Goodfire 将迅速增强和巩固可解释性研究的应用。“与 Goodfire 的合作在解锁 Evo 2 这一基因组基础模型的更深层次洞见方面发挥了重要作用，”Arc Institute 的联合创始人 Patrick Hsu 表示，是 Goodfire 的早期合作伙伴之一。“他们的可解释性工具使我们能够提取新颖的生物学概念，加速我们的科学发现过程。”

该公司还计划发布更多研究预览，突出展示各个领域（如图像处理、高级推理语言模型和科学建模）的最先进的可解释性技术。这些努力有望揭示新的科学洞见，并从根本上改变我们如何与 AI 模型互动和利用它们的理解。

Goodfire 团队汇集了来自 OpenAI 和 Google DeepMind 的顶级 AI 可解释性研究人员和经验丰富的创业运作专家。Goodfire 的研究人员帮助创建了机制可解释性领域，撰写了三篇被引量最高的论文，并在特征发现、自动可解释性框架以及揭示 AI 模型中的隐含知识等方面取得了开创性进展。


