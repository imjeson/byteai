这位科技亿万富翁、对法西斯敬礼情有独钟的人，以及美国的事实首脑，不仅把自己搬进了政府大楼——似乎还选择了一些他的孩子一起入住。在过去几周，迷你穆斯克们频繁出现在高调的政治活动中，产生了源源不断的网络迷因、头条新闻和分析。例如，上周四，马斯克的三个年轻孩子在与印度总理纳伦德拉·莫迪的一次会议上出现。马斯克与莫迪的会面到底是为了什么？好问题。连特朗普似乎也不知道，但他告诉记者，他猜测马斯克“想在印度做生意”。考虑到马斯克在美国政府中已经深入扎根，这听起来有点像是利益冲突。但我们不妨先不关注这个问题，而是看看马斯克的父亲身份！别问任何棘手的问题，只需看那些在网上广泛传播的可爱图片——例如莫迪给马斯克的孩子们送礼物。太可爱了。

马斯克四岁的儿子X Æ A-Xii（通常被称为“X”）现在算是个老谋深算的政治家。就在与莫迪会面前几天，X还和马斯克以及特朗普一起参加了白宫的新闻发布会。当马斯克喋喋不休地谈论民主，并为一个关于向加沙提供五千万美元安全套的可憎谎言做解释时，X似乎只是想看《汪汪队立大功》。他在某个时刻似乎对特朗普说——也许是想对特朗普说——“我希望你闭嘴”。（我在想，他从哪里听到的这个？）在另一个时刻，X（马斯克曾形容他为“情感支持人类”）则抡起手指挖鼻子，然后把结果擦在了特朗普的桌子上。这种挖鼻子是小孩子的正常行为。但在美国总统身边站立，而你的父亲似乎认为自己是世界的国王，并做出夸张的声明？这可没那么简单。

马斯克最近这种婚内育儿的方式引发了公众的热议。他的追随者似乎认为这非常可爱，是亿万富翁不仅是美国和人类文明的救世主，还是全球最佳爸爸，勇敢地将他的生育主张付诸实践。其他人（正常人）则似乎认为，这是一个阴险且剥削性的公关策略，旨在使马斯克看起来更加人性化，并转移对他干预民主进程的关注。毕竟，肩上扛着孩子，似乎让你看起来不那么像一个有奇怪生育癖好的强盗大亨，而更像一个有趣的爸爸。

我想大家都猜到了我属于哪个阵营：我认为马斯克把孩子们作为道具在镜头前展示没有任何可爱之处。为了在忙碌的日程中与孩子们多待些时光，带孩子上班是可以理解的。但像马斯克那样把他们带来作为拍照的道具，显然就是另一回事了。

需要明确的是：我不是说政治家就该总是把孩子藏起来。公共场合的领导人作为父母的形象可以传达强有力的信息。例如，2018年，新西兰前总理雅辛达·阿德恩成为首位带着婴儿参加联合国大会的世界领导人。阿德恩因这一举动广受赞扬，表明了女性可以兼任母亲与领导者。

然而，马斯克所做的，显然有所不同。尤其是因为与他有三个孩子的格林斯（Grimes）曾多次表示，她的小儿子“不应该如此在公共场合出现”（或相似的主题）。格林斯还声称，在她和马斯克进行抚养权斗争期间，她有五个月没有见到其中一个孩子，并表示她自己“在Instagram上的帖子和模特生涯”被武器化，成为她无法获得孩子抚养权的理由。去年，格林斯的母亲也指责马斯克扣留了她孙子的护照，以致他们无法探访病重的曾祖母。关于马斯克在这里所做的事情，似乎可以用很多词来形容，唯独“年度最佳父亲”不在其中。

好吧，我得结束这篇文章，因为我今天也带着孩子上班。这并不是为了公关宣传，归根结底，因为我在客厅工作，而孩子今天放假。她倒没有在我的桌子上擦鼻子，但确实把橡皮泥放进了我的袜子里。

