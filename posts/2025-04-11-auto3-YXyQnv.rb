title: '5C集团成立，Hypertec Cloud收购5C数据中心'
date: 2025-04-12
author: ByteAILab

---

在一项反映人工智能和数据中心基础设施迅速融合的标志性举措中，领先的人工智能云平台Hypertec Cloud收购了5C数据中心，这是一家在美国拥有超过2吉瓦（GW）计划容量的优质托管和数据中心提供商。![图片](https://ai-techpark.com/wp-content/uploads/5C-Group.jpg){ width=60% }

---
这一战略收购标志着Hypertec Cloud从其母公司Hypertec Group的分拆，以及5C集团的成立——一个专注于在规模上构建下一代人工智能基础设施的新独立实体。

Jonathan Ahdoot被任命为新成立的5C集团的首席执行官，该集团结合了Hypertec Cloud的高性能云计算能力与5C数据中心在美国的广泛布局。两者结合形成了北美最大和最灵活的人工智能数字基础设施提供商之一，旨在构建将推动行业未来的下一代人工智能工厂。

此次收购的驱动力有三个主要战略目标：
1. 提供全面的人工智能数字基础设施解决方案：通过结合Hypertec Cloud和5C数据中心，5C集团将提供旨在满足人工智能前沿实验室、人工智能原生企业和大规模企业客户需求的集成数据中心和人工智能基础设施。
2. 在受限市场中加速部署速度：随着人工智能部署需求的增长，而大规模数据中心容量仍然有限，结合的平台将使得超大人工智能集群的市场时间更快。集成的计算和数据中心设计将显著减少部署时间，并实现基础设施的同步推出。
3. 驱动性能和成本优化：借助Hypertec在硬件和基础设施设计、白手套支持以及可持续工程方面的数十年经验，新实体将结合先进的冷却技术——如直接到芯片的液冷和浸没冷却——以支持更高的机架密度和节能。

“今天人工智能基础设施面临的最大挑战是对齐——在人工智能计算所需和物理托管环境可以提供之间的对齐。我们通过将这两个方面整合在一起解决了这个问题，”5C集团首席执行官Jonathan Ahdoot说道。“通过这次收购，我们将性能优化的人工智能计算基础设施和大型、世界级数据中心校园的设计及运营深厚专业知识结合在一起。这种结合使我们能够加速部署，推进密度，满足大型和最苛刻的人工智能用户的无与伦比的效率，持续多年。”

“Hypertec Cloud的分拆标志着Hypertec Group的一个重要时刻，”Hypertec Group首席执行官Simon Ahdoot补充道。随着5C数据中心的收购，新成立的5C集团有望提供面向未来的人工智能数字基础设施，专为明天行业的规模、速度和复杂性而设计。我们将继续作为合作伙伴推进计算、存储和数据中心技术，交付实际价值，以推动人工智能创新的未来。”

5C集团在美国的AI优化数据中心校园的组合包括超过600兆瓦（MW）的容量——作为其总计2吉瓦（GW）的一部分，将在未来6到18个月内提供给新的和现有的人工智能数据中心托管和计算客户。该平台是为满足人工智能工作负载的极端密度、功率和冷却需求而专门构建的，为人工智能创新的未来奠定基础。

