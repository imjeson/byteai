云计算市场强化安全态势  
Vast.ai，这个领先的云GPU平台，专注于人工智能和机器学习，今天宣布成功完成SOC 2 Type I审计，展示了公司在遵循数据安全、可用性、处理完整性、保密性和隐私等SOC 2合规要求方面，始终如一的强大安全协议承诺。![图片](https://ai-techpark.com/wp-content/uploads/Vast.jpg){ width=60% }

---
  
SOC 2 Type I认证是对Vast.ai在全球可靠计算服务交付超过六年后安全基础设施的重要验证。此认证确认公司已实施适当的控制措施，旨在保护客户数据并维护系统完整性。  
“这一认证里程碑反映了我们在扩大平台规模过程中对安全的坚定承诺，”Vast.ai首席运营官Travis Cannell表示。“对于依赖我们基础设施进行关键任务操作的客户，这进一步增强了他们数据受到行业标准保护的信心。”  
SOC 2合规性是由美国注册会计师协会（AICPA）制定的，评估一个组织的控制措施是否符合特定信任标准，包括安全性、可用性、处理完整性和数据保密性。Type I报告提供了对这些控制措施在特定时刻的快照评估。  
作为其持续致力于合规性和安全的承诺的一部分，Vast.ai已在准备更严格的SOC 2 Type II审计，该审计将评估这些控制措施在较长时间内的有效性。公司还在追求额外的合规认证，以更好地服务于具有严格监管要求的组织。  
有意获取Vast.ai SOC 2报告的客户可以联系compliance@vast.ai或通过公司网站预约咨询。

