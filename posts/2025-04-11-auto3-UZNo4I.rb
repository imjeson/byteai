地位巩固了其在AI基础设施解决方案领域的领导地位
ePlus公司（纳斯达克：PLUS）今天宣布已获得NVIDIA DGX SuperPOD专业合作伙伴资格。![图片](https://ai-techpark.com/wp-content/uploads/ePlus.jpg){ width=60% }

---
将这一证书加入ePlus的AI专业知识投资组合，验证了其提供全面的NVIDIA DGX SuperPOD服务的能力，确保AI基础设施能够为企业AI工作负载设计、部署和优化。

作为NVIDIA合作伙伴网络（NPN）中的精英合作伙伴和DGX-Ready托管服务合作伙伴，ePlus展示了其通过众多资源团队成功提供专业知识的能力，为客户提供NVIDIA DGX SuperPOD解决方案，包括：

- 设计与架构 – 设计和部署用于AI训练、推理和研究的NVIDIA DGX SuperPOD配置
- 部署与网络 – 整合NVIDIA DGX系统、NVIDIA Quantum InfiniBand网络解决方案和高速存储
- AI软件与优化 – 实施NVIDIA AI Enterprise、NVIDIA NeMo和NVIDIA Base Command，确保性能、可扩展性和操作效率达到最佳
- 先进支持服务 – 提供监控、操作支持和AI工作负载的持续优化

“AI在各个行业的快速崛起使许多组织迫切希望采用这项技术，但却没有必要的基础设施、资源或可扩展性来支持它，” ePlus首席运营官Darren Raiguel表示。“在ePlus努力帮助客户为成功做好准备的过程中，我们推进了我们的专业知识，通过获得NVIDIA DGX SuperPOD专业合作伙伴资格脱颖而出，这证明了我们的承诺。我们为指导客户成功采用AI感到自豪，并提供可扩展、优化和可靠的解决方案，以推动真实成果。”

“AI正在改变各个行业，企业正在寻求值得信赖的供应商来帮助他们构建可扩展的高性能基础设施，以应对这些复杂工作负载，”NVIDIA美洲合作伙伴组织副总裁Craig Weinstein表示。“通过获得NVIDIA DGX SuperPOD专业合作伙伴资格，ePlus带来了经过验证的专业知识和端到端解决方案，帮助组织加速其AI驱动的创新。”

NVIDIA DGX SuperPOD是一种可扩展的高性能AI基础设施平台，旨在应对企业AI和高性能计算的挑战。集成了NVIDIA DGX系统、NVIDIA Quantum InfiniBand网络解决方案和AI软件，有助于消除瓶颈，加速工作负载并简化部署。

通过其AI Ignite投资组合，ePlus提供一套全面的AI专注解决方案，包括研讨会和评估、旅程支持、优化基础设施和先进服务。客户还可以探索ePlus AI体验中心或利用最近推出的ePlus Secure GenAI加速器，这是一个有指导和托管的概念验证产品，帮助组织探索和精炼想法，通过指标发现关键洞察，并自信地以速度和安全性测试生成AI用例。

