---

经验丰富的科技领导者将推动以客户为中心的增长和云转型的卓越
CompQsoft Digital，全球微软解决方案和服务提供商，今天宣布任命 Andrew Jeffers 为首席客户官 (CCO)。![图片](https://ai-techpark.com/wp-content/uploads/Andrew-Jeffers-Join.jpg){ width=60% }

---
Andrew 将在这一战略角色中领导客户成功倡议，监督企业规模的数字化转型，并最大化公司在云和人工智能转型产品中的客户价值。
拥有超过 25 年提供微软聚焦的企业解决方案的经验，Andrew 在推动客户参与、加速商业成果和提供高投资回报率方面建立了良好的声誉。他的专长在于设计可扩展的、以结果为导向的战略，以确保客户成功和长期合作关系。
在加入 CompQsoft Digital 之前，Andrew 在一些全球领先的科技公司担任高层领导职务，包括 Deloitte、Avanade、IBM/Neudesic、TTEC 和 DXC Technology。他通过扩大售后参与、增强客户健康和促进创新驱动的服务交付，始终交付成果。
“Andrew 带来了丰富的经验和培养增长、文化与创新的良好记录，”CompQsoft Digital 的总裁兼董事会成员 Greg Moser 说道。“他的领导正是我们增强以客户为中心的关注所需。Andrew 对于变革客户旅程的热情与我们的愿景相符，我相信他将提升我们在每一个接触点向客户、团队成员和合作伙伴提供的体验。”
“我很高兴加入 CompQsoft Digital，打造一个以客户结果为中心的世界级微软合作伙伴组织，”Andrew Jeffers 说道。“我们的使命很明确：确保客户健康、推动客户留存并提供卓越价值——这一切都根植于创新和卓越的文化之中。”

