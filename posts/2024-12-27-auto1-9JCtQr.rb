他是背后有30亿次播放量、一系列畅销艺术家的男人——也是Wetherspoon最新推出的龙舌兰品牌的创始人。

---
问迈克尔·阿德克斯如何在28岁时取得这样的成就，他会将部分功劳归于他“不可动摇”的意志，促使事情发生。但这位在曼彻斯特成长起来的娱乐巨头，最初作为说唱歌手Aitch的经纪人尝到成功后，创办了人才代理机构、唱片公司和全球音乐出版业务，他深知，无论愿景多么强大，想法多么优秀，初创公司都需要资金才能生存。

英国希望成为“深科技”的领导者，这一创新领域包括人工智能、量子计算和区块链等先进技术。但从光明的想法到商业成功的道路往往充满曲折，尤其是对于黑人创业者来说。

数字催化剂（Digital Catapult）的研究发现，英国总投资中只有0.2%流向黑人创办的公司。阿德克斯，去年被《福布斯》杂志评选为“30位30岁以下”青年，是为了改变这一现状，担任数字催化剂黑人创始人项目的代言人，支持10家公司在其商业旅程中取得成功。曾入选的公司已与大企业建立合作关系，并吸引了天使投资者的兴趣。

这为阿德克斯忙碌的交易年画上了句号。Wetherspoon在800多家酒吧中上架了他创办的MODA龙舌兰，而他与Aitch共同创办的瓶装水品牌Sypz也被冰岛超市挑选。阿德克斯还将其娱乐代理公司NQ与哥伦比亚唱片公司（Columbia Records UK）结成了合作关系，旨在为该唱片公司提供北英格兰新兴音乐明星的接入。

阿德克斯表示：“即使拥有雄心壮志……如果你想独立达到某些水平，你需要资源来进行成长。这就是像[黑人创始人]这样的项目非常重要的原因——为了让人们了解如何筹集资金以及表达他们的愿景。”

我们在数字催化剂选出的黑人创始人项目中可以看到深科技进一步推动英国创意产业成功的潜力。每家公司都将沉浸式技术应用于音乐制作、游戏、叙事和教育等领域。它们包括TwoShot，该公司使用声音检测技术解决与AI生成音乐相关的版权问题；Mismatch Studios，创作虚拟世界的数字服装，为设计师提供了一种可持续的设计原型方式；以及Immersely，该公司利用生物识别技术和人工智能创建“超个性化”的游戏。

阿德克斯自己在音乐产业的成功之路——NQ的制作人曾与Central Cee、ArrDee、Tion Wayne、Bryson Tiller、Mist、Blanco和Anne-Marie等艺术家合作——始于他在学校时。“这很自然，”他说。“我有很多联系人和朋友，家人在伦敦，每年夏天我都会去那里建立联系。从那时起，我开始建立关系，和艺术家们闲逛。”

“在曼彻斯特周围有大量的才能激励我想要让他们出名——因为我去伦敦，看到了很多机会，而在曼彻斯特却缺乏。”阿德克斯补充道：“展示黑人创业者可以参与的事情是重要的。我从来不想让任何人觉得他们应该限制自己。”

“我一直意志坚定，从未因为我来自哪里、我的背景或我的外貌而在想我无法做某件事。但回想起六七年后的现在，看到我所取得的成就，我几乎对这一切感到更加不敢相信。”

谈到黑人创始人计划，数字催化剂的首席政策和战略官Jessica Rushworth表示：“打破代表性不足的创业者成功扩展的壁垒”是该计划的“核心”，并补充说：“新一届成员的创新解决方案无疑会开启增长英国创意经济的新机会。”

