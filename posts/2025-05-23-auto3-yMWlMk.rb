title: 'Discern Security任命Evgeniy Kharam为首席战略官'
date: 2025-05-24
author: ByteAILab

---

Kharam将引导Discern的全球扩展，并重新定义组织构建和扩展网络安全项目的方式
Discern Security，一家在基于AI的安全管理领域中的先驱，已任命Evgeniy Kharam为新的首席战略官（CSO），这一战略举措旨在加速公司的增长和创新努力。![图片](https://ai-techpark.com/wp-content/uploads/Discern-Security.jpg){ width=60% }

---
Evgeniy拥有超过二十年的经验，涵盖实操安全架构、执行战略和网络安全倡导等领域。作为CSO，他将领导公司的战略路线图，推动市场扩展，并提升Discern在全球扩展过程中的思想领导力。

“Evgeniy带来了深厚的技术背景、真实的客户洞察力和富有远见的领导能力，”Discern Security的CEO Sai Venkataraman表示。“他善于将复杂的挑战简化，与业务成果对齐，并赢得行业信任，这正是我们在Discern定义网络安全优化新类别时所需要的。”

除了企业领导力外，Evgeniy还创立了EK Cyber and Media Consulting，在那里他为网络安全厂商和MSSP提供市场战略、产品集成和平台定位方面的咨询。他的见解帮助支持投资尽职调查和并购评估，形塑了新兴安全企业的增长。作为网络安全社区的受人尊敬的声音，他共同主持了包括《安全架构播客》和《网络启示》在内的领先行业播客。他对指导和交流的热情在他的书籍《成功架构：技术销售中的软技能艺术》中得以体现。Kharam还共同创立了滑雪和 snowboard 网络安全大会，并且在帮助北美和欧洲的大型企业关闭控制漏洞和增强安全态势方面拥有悠久的历史。

“在帮助MSSP和VAR扩展服务、为厂商提供功能战略建议以及推出行业会议和播客多年后，我之所以加入Discern，是因为这是我见过的第一支真正提供我倡导了十多年的实时、数据驱动的清晰度的团队，”Evgeniy Kharam表示。“Discern将猜测转化为可操作的洞察——揭示资产覆盖、控制有效性和配置健康。”

在Kharam担任首席战略官的带领下，Discern Security准备加速其战略愿景，拓展新垂直市场，并深化全球市场执行。他的领导将增强公司的MSSP和VAR生态系统，通过主题演讲和媒体参与提升其公共形象，并增强对产品采用的关注——特别是在AI驱动的洞察、董事会级别的报告和合理化控制管理方面。

