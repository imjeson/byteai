Penske交付了一种新水平的AI驱动车队智能——在范围、规模和可操作性洞察方面无与伦比。
Catalyst AI是在内部开发的，应用了专有模型，将复杂性转化为竞争优势。
Catalyst AI从车队层面的可视性推进到微观洞察，能够实现实时基准比较和诊断，现在可在单个车辆和中心级别进行。
93%的领导者认为AI可以提高韧性，但84%的人表示该行业滞后，表明了采纳的紧迫性。

今天，Penske Truck Leasing推出了第二代车队智能，最新发布的Catalyst AI™，其专有人工智能引擎和行业领先的AI平台重新定义了车队管理。Catalyst AI专为商业运输而构建，应用机器学习的先进方法——将行业中最丰富的车辆数据集转化为更快、更具战略性的运营决策。
在这里体验完整的互动多渠道新闻发布：https://www.multivu.com/penske-truck-leasing/9335551-en-penske-truck-leasing-catalyst-ai-fleet-intelligence
Catalyst AI旨在满足现代运营中的基本需求：如何从正确的数据中采取正确的行动。此次更新引入了四个核心增强功能，以实现这一目标：

- 幻想车队（Fantasy Fleet）——提供一个新的比较集，由与用户车队中每辆车最相似的表现最佳的车辆组成，帮助他们找出差距，提高性能。
- 车辆级别比较——使用户能够比较单个车辆，确定性能差异，帮助识别出可以推动更好结果的目标调整所在。
- 中心级别比较——允许用户按位置评估运营，发现地方层面提高效率的机会。
- 影响指标——赋予用户专注于与其业务最相关的特定指标的能力——燃油效率、维护成本、利用率等。

“这次发布标志着Catalyst AI的新篇章——将复杂数据变得更可用、更具规模性和可操作性，”Penske Truck Leasing总裁Art Vallely说道。“我们增强了该平台提供趋势、车辆级别基准和识别车队与设施之间重要异常值的能力。该平台在内部构建，旨在帮助我们的客户花更少的时间追逐数据，更多地关注提升表现。”
Catalyst AI随着现代车队运营的复杂性不断演变，提供更快的洞察、更清晰的基准和旨在使企业保持领先的智能。该平台反映了当今车队专业人士所需的：驱动行动的可信洞察。
这一需求在Penske 2025运输领导者调查中得以体现：93%的运输和物流行业高级商业决策者一致认为AI将改善他们组织的韧性和适应突变的能力。超过一半（54%）的人表示，能够将他们的车队性能与同一市场的其他车队进行比较将改善效率和运营决策。当他们特别计划应对当前持续的经济不确定性时，车队领导者提到他们最迫切的数据需求包括：维护成本可视性（44%）、燃油价格趋势（41%）和车队利用率（36%）。
已经采纳AI的组织正在收获好处，使用AI工具的40%受访者表示，他们至少在燃油使用、成本降低或通过更智能的路线规划和优化减少行驶距离方面看到超过50%的改善。然而，84%的运输领导者认为该行业在AI采纳方面滞后，36%的人只感到部分准备应对持续的供应链中断和经济波动。这表明迫切需要不仅提供数据，而且能将数据转化为驱动成果的具体行动的工具，例如降低运营成本和提高效率。
“我们在设计Catalyst AI时考虑了客户的需求，”Penske Transportation Solutions的战略与市场部执行副总裁Sherry Sanger表示。“Catalyst AI代表了车队使用数据的战略 leap。它不仅仅是关于可视性——而是关于赋能更智能的决策，贯穿整个网络的技术在现实世界中有效。”
自Catalyst AI于2024年推出以来，该平台已扩大到支持成千上万的用户，帮助车队专业人士从滞后指标转向领先决策。该平台现在是Penske网络内部如何报告、基准和规划的基础部分——涵盖车辆、地点和系统。
“Catalyst AI现在每年分析超过1000亿个数据点，”Penske数字和客户数据副总裁Tim Haynes表示。“我们构建了一个可以实时运行300多个模型的系统——提供比较逻辑、趋势检测和评分，这些都具有可扩展性和即时性。这次发布反映了帮助车队更聪明地基准比较和更快响应所需的技术基础。”
Catalyst AI嵌入到Fleet Insight®数字平台内的Comparative Insights功能中。比较洞察功能以车队级别为基础，仍向Penske客户免费提供。新的Comparative Insights Premium计划解锁了对车辆和中心级比较及幻想车队功能的访问，并提供30天的免费试用。在此了解更多信息，请访问penskecatalyst.ai。

