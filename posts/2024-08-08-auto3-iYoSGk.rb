Keeper Security，领先的零信任和零知识网络安全软件提供商，高兴地宣布任命Paul Aronhime为联邦部门高级副总裁。Aronhime在国防、联邦文职和私营部门拥有卓越的履历和丰富的专业知识，使他成为Keeper团队的关键补充。

Aronhime最近担任IntelliBridge的战略增长高级副总裁，在那里他领导了高效团队，并推动了显著的销售和战略增长。凭借超过20年的经验，他的职业生涯包括在通用动力信息技术（GDIT）、IBM、科学应用国际公司（SAIC）和Idemia等公司担任关键职务，在国防部、第四代机构、作战指挥部（COCOMS）和国际市场的关键倡议中发挥了重要作用。作为一名具有防空情报背景的美国陆军退伍军人，Aronhime将独特的战略眼光和运营专业知识带到Keeper。

Keeper Security首席执行官兼联合创始人Darren Guccione表示：“我们非常高兴欢迎Paul加入Keeper。他的行业经验和客户了解正是我们推动团队和产品提升到新高度所需要的。在Paul的领导下，我们有信心将继续超越我们雄心勃勃的增长目标，并巩固我们在联邦部门网络安全领域的显著和创新领导地位。”

Aronhime的任命发生在Keeper增长和创新的重要时刻。Keeper Security Government Cloud密码管理器和零信任特权访问管理器（PAM）提供了PAM的基本组件，而不具备传统解决方案的复杂性或成本。这包括特权账户凭证管理、密钥管理、单点登录（SSO）集成、凭证保管和访问控制，以及特权会话管理、监视和录制。Keeper Security Government Cloud可以无缝地配置和整合到任何IT环境和身份堆栈中，提供无与伦比的安全性和效率。

Aronhime表示：“在网络安全威胁不断演变的情况下，对先进零信任解决方案的需求从未如此迫切。”他说：“我很高兴加入Keeper，领导我们努力为联邦机构和国防组织提供所需的尖端能力，以保护我国最敏感的信息。”

随着网络安全威胁不断演变，强大和创新的安全解决方案的必要性变得愈发迫切。Aronhime的领导将对推动Keeper Security的使命——为联邦机构提供尖端保护——发挥关键作用。了解更多关于Keeper Security Government Cloud FedRAMP授权密码和特权访问管理网络安全平台的信息。

