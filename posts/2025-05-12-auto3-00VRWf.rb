---

新的服务让NVIDIA云合作伙伴和GPU云服务提供商能够快速在Rafay驱动的基础设施上推出高利润的人工智能服务，从而加速上市时间并最大化投资回报
今天，Rafay Systems，一家领先的云原生和人工智能基础设施编排与管理公司，宣布其无服务器推理服务的正式上线，这是一个用于运行开源和私有训练或调优的大型语言模型的代币计量API。![图片](https://ai-techpark.com/wp-content/uploads/Rafay-Launches.jpg){ width=60% }

---
许多NVIDIA云服务提供商（NCPs）和GPU云服务已经在利用Rafay平台为其客户提供多租户的按需服务体验，配备了计算和人工智能应用的自服务消费。这些NCPs和GPU云可以以即插即用的方式提供无服务器推理服务，无需额外费用，帮助他们的客户快速构建和扩展 AI 应用，而无需处理构建GPU基础设施的自动化、治理和控制的成本和复杂性。

全球AI推理市场预计将在2025年增长至1060亿美元，2030年将达到2540亿美元。Rafay的无服务器推理使GPU云服务提供商（GPU Clouds）和NCPs能够利用蓬勃发展的生成人工智能市场，消除关键的采用障碍——自动化复杂基础设施的配置和分段、开发者自服务、快速将新的生成人工智能模型作为服务推出、生成按需使用的计费数据等等。

“在过去一年中，许多企业已经开始致力于构建增强和提升他们商业产品的代理型人工智能应用。通过推理端点快速消费生成人工智能模型的能力是加速生成人工智能能力开发的关键。这正是Rafay的NCP和GPU云合作伙伴的显著优势所在。” Rafay Systems的首席执行官兼联合创始人Haseeb Budhani表示。

“凭借我们新的无服务器推理服务，该服务对NCPs和GPU云提供商免费开放，我们的客户和合作伙伴现在可以向他们的客户提供类似于Amazon Bedrock的服务，以可扩展、安全和具成本效益的方式访问最新的生成人工智能模型。开发者和企业现在可以在几分钟内将生成人工智能工作流集成到他们的应用中，而不必经历基础设施管理的痛苦。该服务推动了我们公司的愿景，帮助NCP和GPU云从运营GPU即服务转变为人工智能即服务。”

Rafay引领从GPU即服务到人工智能即服务的转变
通过将无服务器推理作为按需功能提供给下游客户，Rafay帮助NCP和GPU云填补了市场的关键空白。Rafay的无服务器推理服务为NCP和GPU云提供了以下关键能力：

无缝的开发者集成：OpenAI兼容的API无需对现有应用进行代码迁移，提供安全的RESTful和流式准备端点，极大地加速了最终客户的价值实现时间。

智能基础设施管理：具备自动扩展GPU节点和正确模型分配能力的功能，可以动态优化多租户和专用隔离选项的资源，消除过度配置，同时保持严格的性能SLA。

内置计量和计费：输入和输出的代币和时间基础使用跟踪提供详细的消费分析，同时通过综合计量API与现有计费平台集成，启用透明的基于消费的定价模型。

企业级安全和治理：通过仅限HTTPS的API端点、轮换的承载令牌认证、详细的访问日志记录以及每个团队、业务单元或应用程序可配置的令牌配额，提供全面的保护，满足企业合规要求。

可观测性、存储和性能监控：通过存储在提供商自有存储命名空间中的日志和指标提供端到端的可见性，支持如MinIO（一种高性能的、兼容AWS S3的对象存储系统）和Weka（一种高性能、人工智能原生的数据平台）等后端，并提供集中凭证管理，以确保基础设施和模型性能的完全透明。

可用性
Rafay的无服务器推理服务今天已向所有使用Rafay平台提供多租户、基于GPU和CPU基础设施的客户和合作伙伴开放。公司还计划在不久的将来推出微调功能。这些新功能旨在帮助NCP和GPU云快速提供高利润、生产就绪的人工智能服务，消除复杂性。

