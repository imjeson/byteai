对于那些想知道为什么埃隆·马斯克，这位因其生硬的敬礼而闻名的新兴富豪，在这个月突然对英国展现出强烈兴趣的人来说，答案可能在于一项正在逐步实施的网络媒体立法。

---
这个月初，马斯克在其平台X上进行了猛烈的推特风暴，描绘英国为一个由“专制政府”统治的反乌托邦“警察国家”，在这里，年轻的工人阶级女性频繁被移民 gangs绑架。他进一步指责首相基尔·斯塔默与工党领导人“在交换选票中深为参与大规模强奸，并称内阁成员杰斯·菲利普斯，负责保护年轻女孩免受伤害的部长，是一位‘强奸种族灭绝辩护者’。”《金融时报》对马斯克在一月第一周推特内容的分析发现，在那段时间内，马斯克616条推文与转发中，有225条涉及英国政治。这一轮攻势似乎并没有受到任何新政治事件的刺激。马斯克的焦点显然集中在十多年前有关巴基斯坦裔男性的强奸案件上，这些案件一直未得到妥善处理。西汉姆市市长萨迪克·汗警告西方民主正面临“复兴的法西斯主义”威胁，恰好是在特朗普就职前夕。马斯克解释了他对英国的关注，提到了他出生在英国的祖母科拉·阿米莉亚·罗宾逊，他在孩提时代与她关系密切。他表示，他的“奶奶”是“曾经在今天的英国中没有人保护的贫穷工人阶级女孩之一”。

曾是极右翼改革党发言人和战略家的戈温·陶勒认为，马斯克确实对英国感到一种真实的情感。他指出，马斯克的第二任妻子塔露拉·莱莉是英国人，他的几个孩子也有英国血统。陶勒说：“马斯克将英国视为一个遥远的故乡。我认为他视英国为美国的雅典，而他对此感到担忧。”他认为，马斯克在英国政治中的戏剧性介入纯属巧合——他偶然看到了涉及北英格兰“ grooming gangs”的审判记录，这些团伙在数十年间对数百名年轻女孩进行性虐待，而警方和司法系统的行动失败正是导致如此惨痛结果的原因。基尔·斯塔默在2008年至2013年期间担任过公共检察官，他的办公室在任期初因对一名主要证人和受害者的可信度产生疑虑而未能采取及时行动，但在任期后期，他果断采取行动，确保了关键定罪并打击了这些团伙。

政治和媒体分析家认为，马斯克的政治攻击并非偶然，而是意在施压于英国当局，这些当局正努力落实网络安全法（Online Safety Act，OSA），一项旨在规范在线平台的努力。OSA于2023年成为法律，但尚未在实践中实施，其具体实施方式将在春季决定。前工党顾问、现居华盛顿并运营数字仇恨中心的伊姆兰·艾哈迈德表示：“这完全是为了掩饰他真实目的的烟幕弹，他的 agenda纯粹是经济性质的。”艾哈迈德在倡导监管X的内容时曾多次受到马斯克的攻击。

英国拉夫堡大学政治传播教授安德鲁·查德威克表示，马斯克的此番关注不仅出于经济考虑，还有政治动机。“监管环境威胁到马斯克的影响力及其在全球议程中的运作，因此，任何能削弱其作为政治行为者合法性的威胁，都是他的问题。”OSA旨在向大型社交媒体平台施加压力，以防止儿童接触潜在有害内容，如色情和促进自残、自杀及饮食失调的材料。为此，平台需要设置年龄验证系统，成年用户必须能够选择不接触此类内容，该法律还要求公司确保其平台上不托管非法材料，并执行一致的行为规范。

马斯克可能并非为了盈利而收购Twitter，但这可能会给他带来超过他预期的财务损失。DSA允许对违反规则的数字公司处以全球年营业额的最高6%罚款。而OSA的罚款可能高达公司全球贸易的10%，甚至还有对高管的监禁惩罚。“我认为，马斯克可能已经扩展得过于冒进了，”查德威克说，指的是他对斯塔默和其他人的猛烈人身攻击。“我认为，他使得英国政府通过Ofcom在制定这些新规范时，更容易实施《网络安全法》。这项法律的实施拖延已久，还有一些路要走，但我感觉过去几天中出现了一些变化。”

然而，在马斯克面前，英国和欧洲将面临一个庞然大物，他将其数百亿的财富与世界最强大国家的国家机器结合在一起。他的影响力远超以前的媒体巨头，如鲁珀特·穆尔多克。“我认为他真的感受到了自己的权力，”梅里兰大学信息学院的计算机科学家兼教授詹·戈尔贝克说。“这种财富和权力的集中，加上很多科技首席执行官和硅谷人士普遍存在的观念——他们认为自己是例外，可以自由行动，而不受国家法律的约束，”戈尔贝克说。“显然，马斯克正是持有这种世界观的人，而如今他有了足够的金钱和政治影响力，因此，他在利益受威胁时，会感到与日俱增的压力。我认为这足以让他真正进入任何事物的领域。”

