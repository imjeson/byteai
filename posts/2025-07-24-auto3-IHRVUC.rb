---

在2025年，Cognizant的AI实验室获得了两项新的美国专利，开源了一个关键的人工智能开发平台，并在GECCO（遗传与进化计算大会）上获得了金奖。![图片](https://ai-techpark.com/wp-content/uploads/Cognizant.jpg){ width=60% }

---
Cognizant（纳斯达克代码：CTSH）今天宣布，其AI实验室已获批两项新美国专利，并在本月的西班牙马拉加GECCO上发表了一篇关于“通过人工智能实现人类专业知识”的研究论文，获得金奖。

Cognizant人工智能首席技术官Babak Hodjat表示：“在2025年上半年获得的两项新美国专利，使我们的AI实验室美国专利总数达到59项，另有23项正在申请中，这彰显了我们持续创新的步伐。”他补充道：“加上在GECCO的金奖，这些里程碑反映了我们在推动变革性AI技术方面的深刻承诺，将前沿思想转化为实际影响。”

这两项最新专利突出了Cognizant在AI研究中的关键创新：

美国专利号12,282,845（于2025年4月22日发布）涉及一种多目标共演化深度神经网络架构的方法，旨在提高模型性能和资源效率。该方法的应用范围从医学图像分类到自然语言处理。

美国专利号12,292,944（于2025年5月6日发布）则概述了一种通过泰勒级数展开优化损失函数的方法，旨在提高训练效率，并增强模型的鲁棒性，特别是在数据有限的情况下。

这些创新由Cognizant的研究人员开发，包括Dr. Jason Liang、Dr. Elliot Meyerson和Risto Miikkulainen教授，强化了Cognizant在推动AI和机器学习领域的领导地位。

为了使人工智能的承诺更具实用性并广泛可及，今年早些时候，Cognizant的AI实验室开源了其Neuro AI多智能体加速器。Neuro AI帮助企业加速人工智能代理的开发和应用，转变其业务流程，实现自适应运营、实时决策和根据客户定义目标和监督量身定制的个性化客户体验。

Cognizant AI实验室于2024年3月在旧金山开设了旗舰AI研究设施，还因其在2025年GECCO上在人类竞争性结果方面的突破性工作RHEA（通过人工智能实现人类专业知识）获得了金奖。RHEA由Dr. Elliott Meyerson和Risto Miikkulainen教授开发，使用进化AI来提炼和重组人类专家团队生成的数百个模型——尤其是那些提交给XPRIZE疫情响应挑战赛的模型——形成精炼的决策策略。在严格评估中，RHEA超越了单个专家提交的表现，充分利用了即使在未完整开发的人类想法中的潜在可能性，提供了创新的疫情政策建议。这个里程碑凸显了Cognizant在利用全球人类专业知识驱动下一代AI解决方案方面的领导地位。

Risto Miikkulainen是德克萨斯大学奥斯汀分校计算机科学的研究副总裁和教授，他表示：“RHEA是进化AI如何增强全球人类智慧的强大示例——不仅通过匹配专家解决方案，而是超越它们发现新颖而高影响力的策略。这一来自Humies奖委员会的认可强化了基于人群的AI作为解决世界最复杂挑战基础的承诺。”

