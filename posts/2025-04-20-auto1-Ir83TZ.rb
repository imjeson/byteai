澳大利亚人平均每月支付84美元用于全国宽带网络（NBN）的互联网接入，而在生活成本危机中，人们对为什么没有更便宜的互联网可供低收入人群使用提出了质疑。可以采取什么措施来降低NBN的定价计划？我们能从国外学到什么？

人们为家庭互联网支付多少钱？根据最新报告，在能够连接到NBN的1250万处住宅中，大约860万人已经在使用该服务。消费者倡导组织澳大利亚通信消费者行动网络（Accan）最近与Essential联合进行的一项调查显示，1065名受访者的平均家庭互联网费用为每月84美元，其中31%的人支付在81至100美元之间，30%的人支付在61至80美元之间，20%的人支付超过100美元，而只有13%的人支付60美元或以下（其余不确定）。调查还发现，74%每月使用少于50GB流量的人仍然支付超过60美元，每月账单平均为71美元。这些数字不仅包括NBN连接，还包括与NBN竞争的其他服务，如TPG的服务，以及以低于人们NBN支付价格的费率提供的5G家庭互联网。

难道这些人支付更高费用仅仅是为了获得更快的下载速度吗？调查发现，18%的人愿意为更高的速度支付更多费用，而68%的人表示所支付价格与他们所获得的速度是适当的。14%的受访者表示，接受较低速度的前提是价格降低。

NBN的30亿澳元光纤改造是个好消息，但难道现在澳大利亚人更在意价格而不是更高的速度吗？

为什么NBN这么贵？NBN是一家由政府拥有的公司，过去15年投资了数十亿用于铺设网络。历届政府均主张NBN应实现对纳税人投资的回报，因此批发价格设定水平也被要求以达到一定的收益率。这种定价模式，再加上零售商成本，导致价格，尤其是高速度计划的价格，比一些人认为的要高。

收入较低的人是否应支付更少的NBN费用？在调查中，69%的受访者支持提供优惠的宽带服务，其中34%强烈支持，35%较为支持。“太多澳大利亚人被迫在确保互联网连接和支付食物或房租等基本开支之间选择。这种现象是不可接受的。让我们让澳大利亚成为数字包容的领袖，确保没有澳大利亚人被排除在外。”Accan首席执行官卡罗尔·本内特表示。

现在有没有任何优惠计划？联邦政府为全国约23,000个有学龄儿童的家庭支付了互联网费用，该计划已扩展到2028年，最多可提供30,000个名额。目前，工党政府抵制降低NBN定价的呼声，政府的重点是在许多地方升级到全光纤到户连接，以扭转前任协同政府推出的多技术混合模型，该模型使得家庭的下载速度不尽如人意。

工党还没有宣布任何关于NBN的政策，除最近指出工党政府不会私有化这家政府拥有的网络。

其他国家是否曾尝试过宽带收费优惠？美国曾推行过一项提供每月最高30美元的可负担宽带计划，该计划于2024年结束，因为国会未批准额外的资金。一个无线互联网公司委托的研究表明，该项目的成本通过医疗保健、教育和员工生产力的收益得到抵消。加拿大的连接家庭倡议为基本宽带提供每月10加元或20加元的费用，下载速度最高为50Mbps，每月提供200GB的数据。

本内特表示，如果澳大利亚跟随国际成功故事，将不会迈出新的一步。“可负担宽带的好处很明显——更好的健康、更好的教育、更好的工作参与和更强的社区。我们呼吁政府抓住这一机会。我们将跟随国际成功的例子。”“我们的呼声跟随国际日益增加的证据，这表明可负担的宽带不仅是良好的政策，还是良好的经济。”

