title: 'SAFE被Liminal评为第三方风险管理领域的领导者'
date: 2025-04-06
author: ByteAILab

---

SAFE, 作为AI驱动的持续网络风险管理领域的领导者，今天宣布其在2025年Liminal网络安全第三方风险管理（TPRM）链接指数中被评选为“领导者”。![图片](https://ai-techpark.com/wp-content/uploads/SAFE-Named.jpg){ width=60% }

---
Liminal是一家技术公司，为企业提供可行的市场和竞争情报。该报告在产品能力方面将SAFE评为所有供应商中最高，同时在服务交付、战略对齐和AI驱动的自动化方面也获得了“卓越”的评价。

SAFE首席执行官Saket Modi表示：“我们很荣幸能够被Liminal认可为第三方风险管理领域的领导者。这增强了我们对TPRM未来的信念，即以自主的AI为驱动，它不仅仅是提供协助，而是积极推动行动。它旨在无缝扩展，将人工劳动降至零，并推动实时决策，以推动业务向前发展。”

Liminal链接指数是一个备受尊重的行业基准，评估领先的网络安全供应商在产品性能、用户满意度和战略执行方面的表现。SAFE因其提供的自主、端到端的第三方风险管理而备受瞩目，这一解决方案是由Agentic AI驱动的，利用专门的AI代理自动化供应商的 onboard、评估、证据收集、监控和执行报告。

报告指出：“在战略上，SAFE的安全性与从业者的需求相一致，专注于自动化、零信任原则和AI驱动的能力。”

SAFE在链接指数报告中的主要亮点包括：

- 产品能力 – 在所有领先供应商中获得最高分
- 从业者满意度 – 超过领先供应商类别的中位数8%
- 在战略、产品深度和市场存在上获得“优秀”评级

报告提到SAFE的：

- 高效的供应商onboarding和在几分钟内完成的自动外部风险扫描
- 帮助优先处理修复的“假设”模拟和控制建议
- 可扩展的基于使用的定价模型，避免按供应商收费
- 在数据隐私、准确性和客户支持方面用户满意度高

通过SAFE TPRM，组织可以受益于：

- 100%自动化的风险评估
- 100倍更快的供应商onboarding
- 100%风险优先的供应商管理

SAFE的自主AI代理在第三方生命周期的每个阶段运营，帮助组织降低风险、满足合规要求，并在没有人工努力或复杂性的情况下自信扩展。

