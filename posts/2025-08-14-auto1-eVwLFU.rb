澳大利亚应该采用美国式版权法，允许人工智能吸收所有创意内容，否则将损害澳大利亚的行业投资，来自Atlassian创始人斯科特·法夸尔（Scott Farquhar）如是说。法夸尔在ABC的《7.30》节目中表示：“根据澳大利亚法律，所有的AI数据挖掘或搜索都是不合法的，我认为这会对这些公司在澳大利亚的投资造成很大伤害。”他还提到，这一问题的根源在于澳大利亚的版权法中没有像美国那样的“合理使用”豁免条款。

法夸尔的主张忽视了在美国这一问题并未完全确立，且可能对创意产业造成毁灭性影响。包括Atlassian、谷歌和Meta在内的AI开发公司希望在版权法中加入文本和数据挖掘豁免，使AI能够在不支付费用的情况下永远训练所有的创作作品。

法夸尔认为，除非AI用于“直接复制某位艺术家的作品”，否则它并不是对他人作品的偷窃。“我确实认为人们会觉得，如果人们要与数字助手、AI音乐创作工具一起坐下来共同创作出新的作品，这可能是合理使用。”法夸尔表示，大型语言模型所带来的好处超越了AI免费利用他人作品进行训练所引发的问题。

他的观点依赖于AI是否能够创造出“新颖且独特”的作品，而这一点在版权中被称为转化性——即创作出新内容。他表示，如果有人使用他创建的作品，并且它是“转化性”的，他不会对此有任何异议。“如果有人利用我的知识产权与我竞争，这肯定是个问题。如果他们利用世界上所有软件的知识产权来帮助人们更好地编写软件，我认为这是合理使用。”

然而，美国的法律在AI训练是否属于合理使用的问题上尚未确立。美国版权局在其5月份的关于生成AI训练的预报告中指出，正在挑战AI公司利用合理使用作为借口在未支付费用的情况下对受版权保护作品进行训练的几起诉讼。

在美国，判断某种用法是否为合理使用时要考虑多个因素，包括：

- 是否为商业用途。
- 受版权保护作品的性质。
- 使用的受版权保护作品的数量。
- 此用法对受版权保护作品市场或价值的影响。

美国案例法中，创造出的作品的转化特性是合理使用中首要考虑的因素，澳大利亚的律师事务所Gilbert + Tobin在5月的一份出版物中指出，但这并不是唯一需要考虑的因素，受版权作品市场的影响才是关键。美国最高法院曾两度将其称之为“无疑是合理使用中最重要的要素”，根据美国版权局的报告称：“用于AI训练的复制可能对受版权保护作品的市场或价值造成重大潜在伤害。”

“当一个模型能够生成与训练数据中的作品基本相似的输出时，这可能会导致销售损失。”“即使模型的输出与任何特定的受版权保护作品并不完全相似，它也可能稀释与其训练数据类似作品的市场，包括生成在风格上与这些作品相似的材料。”版权局并没有推荐美国的立法干预，指出一些AI公司已经进行自愿授权，允许授权将使AI创新得以持续“推进而不损害知识产权”。

法夸尔的观点是，如果能确保所有使用都是转化性的，并不会影响它们所借用的市场，那么生成AI使用受版权作品的方式就会是合理的。然而，在许多行业中，AI可能会造成毁灭性的影响。例如，在新闻行业，Google搜索中的AI摘要已导致人们更少通过点击浏览故事以获取信息，并且AI聊天机器人对此页面的抓取次数与推荐次数的比例更为低劣。

因此，主张以美国法律为基础的AI合理使用是澳大利亚应当追求的目标，忽视了这一点几乎是未解决的法律问题，且在法庭上正在激烈争论。匆忙为了一个新行业的创新而给予科技公司其所需的东西，可能会牺牲许多其他行业。

